                <button class="faq-question" onclick="toggleFaq(this)"><span>What is DPIIT recognition?</span><span class="faq-icon">+</span></button>
                <div class="faq-answer"><div class="faq-answer-inner">DPIIT recognition is a certificate from the Department for Promotion of Industry and Internal Trade that officially recognises an entity as a startup under the Startup India scheme. It is applied for free on the Startup India portal and, once granted, becomes the gateway to a range of benefits such as the Section 80-IAC income tax holiday, self-certification under labour and environmental laws, intellectual property rebates and access to government procurement. Recognition itself does not automatically grant the tax exemption, which is a separate application.</div></div>
