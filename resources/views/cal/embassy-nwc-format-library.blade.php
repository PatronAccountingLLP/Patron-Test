            <h2>Country-Specific Notes</h2>
            <p>The 12 elements are common to all destinations, but each consulate has its own emphasis. Always confirm against the official source before finalising:</p>
            <ul>
                <li><strong>USA</strong> — the net worth certificate supports the financial-capacity and ties-to-India assessment; consult the US Department of State guidance at <a href="https://travel.state.gov/" target="_blank" rel="noopener">travel.state.gov</a>. Present INR + USD.</li>
                <li><strong>UK</strong> — UKVI actively verifies UDIN; check current financial-evidence rules on <a href="https://www.gov.uk/browse/visas-immigration" target="_blank" rel="noopener">gov.uk</a>. Present INR + GBP.</li>
                <li><strong>Canada</strong> — IRCC expects clear proof of funds and verifies UDIN; see <a href="https://www.canada.ca/en/immigration-refugees-citizenship.html" target="_blank" rel="noopener">canada.ca</a> (IRCC). Present INR + CAD, and for study permits pair with GIC/loan proof.</li>
                <li><strong>Australia</strong> — for student and visitor visas, refer to the Department of Home Affairs at <a href="https://immi.homeaffairs.gov.au/" target="_blank" rel="noopener">immi.homeaffairs.gov.au</a>. Present INR + AUD.</li>
                <li><strong>Schengen</strong> — consulates assess sufficient means of subsistence; the certificate supports (not replaces) bank statements. Present INR + EUR.</li>
            </ul>
