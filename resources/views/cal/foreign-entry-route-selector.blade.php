        <section class="content-section" id="routes">
            <h2>The Six Entry Routes Explained</h2>
            <h3>Wholly-Owned Subsidiary (Private Limited)</h3>
            <p>A separate Indian legal entity, up to 100% foreign-owned, that can contract, hire, invoice, raise funding and grant ESOPs. It gives the parent full control, carries the lowest corporate tax among revenue-generating entities (~25%), and is formed with the Registrar of Companies — no RBI permission needed to incorporate, subject to sector FDI rules. The default long-term vehicle for MNCs and Global Capability Centres.</p>
            <h3>LLP</h3>
            <p>Allowed for FDI under the automatic route only in sectors with 100% FDI and no performance-linked conditions. Lower compliance, fits advisory/consulting/professional-services, but limited capital-market and exit optionality and no shares/ESOPs — not a universal foreign-entry vehicle.</p>
            <h3>Branch Office</h3>
            <p>An extension of the foreign parent (not a separate entity, so the parent stays fully liable) that <strong>can earn income</strong> from permitted activities — import/export, consultancy, research, technical support — but cannot manufacture (except in an SEZ). Needs RBI approval under FEMA via an AD bank; the parent must usually show a 5-year profit track record and net worth ≥ USD 100,000.</p>
            <h3>Liaison / Representative Office</h3>
            <p>A communication bridge only. It <strong>cannot earn income</strong> or do any commercial activity; all expenses are funded by the parent through inward remittance. RBI approval required. Ideal for exploring the market before committing.</p>
            <h3>Project Office</h3>
            <p>A temporary setup to execute a specific contract awarded by an Indian company. It can do commercial activity related to that project (the key difference from a liaison office) and is wound up on completion. Registered with the RBI and RoC.</p>
            <h3>Employer of Record (EOR)</h3>
            <p>No entity needed — the EOR is the legal employer while the team reports to you, cutting time-to-team from ~60 days to under 2 weeks. Common as a fast start while a subsidiary is incorporated in parallel. See Patron's <a href="/eor-india-services">EOR India services</a>.</p>
