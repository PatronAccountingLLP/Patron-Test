<li><strong>Mutual funds at invested amount</strong> — must be at latest NAV</li>
<li><strong>Missing accrued FD interest</strong> — interest accrued since last credit must be added</li>
<li><strong>Wrong FX rate or date</strong> — must match SBI TT rate on certificate date</li>
<li><strong>CA membership not verifiable</strong> — embassy checks ICAI website; expired or fake membership rejects</li>
<li><strong>Format mismatch with embassy template</strong> — particularly common for US Consulate Mumbai</li>
