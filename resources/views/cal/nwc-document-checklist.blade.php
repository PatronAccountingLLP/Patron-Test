function generate(silent){
    const sections = [];

    // Universal
    sections.push({type: 'universal', title: '1. Universal Identity Documents', intro: 'Required for every NWC regardless of use case.', items: UNIVERSAL_DOCS});

    // Use case
    if(USECASE_DOCS[currentUseCase] && USECASE_DOCS[currentUseCase].length){
        const ucTitle = {visa: 'Visa-Specific', loan: 'Bank Loan-Specific', tender: 'Tender-Specific', director: 'Director Appointment-Specific', other: 'Use Case-Specific'}[currentUseCase];
        sections.push({type: 'usecase', title: '2. ' + ucTitle + ' Documents', intro: 'Required for ' + currentUseCase + ' use case.', items: USECASE_DOCS[currentUseCase]});
    }

    // Country note (only for visa)
    if(currentUseCase === 'visa'){
        sections.push({type: 'country', title: 'Embassy Format Note — ' + currentCountry, items: [], note: COUNTRY_NOTES[currentCountry]});
    }

    // Assets
    let sectionNum = currentUseCase === 'visa' ? 3 : 3;
    let assetSubSection = sectionNum;
    selectedAssets.forEach((a, idx) => {
        if(ASSET_DOCS[a]){
            const data = ASSET_DOCS[a];
            sections.push({type: 'asset', title: assetSubSection + '.' + (idx+1) + ' ' + data.title, items: data.items});
        }
    });

    // Liabilities
    if(selectedLiabs.length){
        sectionNum = currentUseCase === 'visa' ? 4 : 4;
        selectedLiabs.forEach((l, idx) => {
            if(LIAB_DOCS[l]){
                const data = LIAB_DOCS[l];
                sections.push({type: 'liab', title: sectionNum + '.' + (idx+1) + ' ' + data.title, items: data.items});
            }
        });
    }

    // FX
    if(currentFx === 'yes'){
        sections.push({type: 'fx', title: '5. Foreign Currency Conversion', items: FX_DOCS});
    }

    // Submission tips
    sections.push({type: 'tips', title: '6. Submission Tips', items: SUBMISSION_TIPS.map(t => ({title: t, hint: ''}))});

    // Count total
    let total = 0;
    sections.forEach(s => total += s.items.length);
    let sectionCount = sections.length;

    const ucDisplay = {visa: 'Visa (' + currentCountry + ')', loan: 'Bank Loan', tender: 'Government Tender', director: 'Director Appointment', other: 'Generic / Personal'}[currentUseCase];
    document.getElementById('resHeadline').textContent = total;
    document.getElementById('resHeadlineMeta').textContent = ucDisplay + ' · ' + selectedAssets.length + ' asset categor' + (selectedAssets.length === 1 ? 'y' : 'ies') + ' · ' + selectedLiabs.length + ' liability categor' + (selectedLiabs.length === 1 ? 'y' : 'ies') + ' · FX: ' + (currentFx === 'yes' ? 'Yes' : 'No');
    document.getElementById('resTotal').textContent = total;
    document.getElementById('resSections').textContent = sectionCount;
    document.getElementById('checklistTitle').textContent = 'NWC Document Checklist — ' + ucDisplay;
    const today = new Date();
    const dateStr = today.getDate() + ' ' + ['January','February','March','April','May','June','July','August','September','October','November','December'][today.getMonth()] + ' ' + today.getFullYear();
    document.getElementById('checklistSub').textContent = 'Generated on ' + dateStr + ' · ' + total + ' documents across ' + sectionCount + ' sections';

    const body = document.getElementById('checklistBody');
    body.innerHTML = '';
    sections.forEach(s => {
        const block = document.createElement('div');
        block.className = 'checklist-block';
        if(s.type === 'country') block.className += ' country';
        if(s.type === 'tips') block.className += ' tips';
        let html = '<h5' + (s.type === 'country' ? ' class="country-note"' : '') + '>';
        html += '<span>' + s.title + '</span>';
        if(s.items.length > 0) html += '<span class="count">' + s.items.length + (s.items.length === 1 ? ' item' : ' items') + '</span>';
        html += '</h5>';
        if(s.intro) html += '<div class="block-intro">' + s.intro + '</div>';
        if(s.note) html += '<div style="font-size:13px;color:var(--primary-dark);line-height:1.65;">' + s.note + '</div>';
        if(s.items.length > 0){
            html += '<ul>';
            s.items.forEach(item => {
                html += '<li><strong>' + item.title + '</strong>';
                if(item.hint) html += '<span class="doc-hint">' + item.hint + '</span>';
                html += '</li>';
            });
            html += '</ul>';
        }
        block.innerHTML = html;
        body.appendChild(block);
    });

    const section = document.getElementById('resultSection');
    section.classList.add('visible');
    if(!silent) requestAnimationFrame(function(){requestAnimationFrame(function(){section.scrollIntoView({behavior:'smooth',block:'start'});});});
}

