        <section class="content-section" id="roadmap">
            <h2>Beyond the First 180 Days — the First-Year Roadmap</h2>
            <p>The tasks above get you legally operational, but the first year keeps going. Once the immediate filings are done, the company settles into an annual rhythm administered by the <a href="https://www.mca.gov.in/" target="_blank" rel="noopener">MCA</a>: a minimum of four board meetings spaced no more than 120 days apart, the first annual general meeting, and the year-end filings of AOC-4 (financial statements) and MGT-7 or MGT-7A (annual return) after the AGM. Alongside the MCA cycle sits the income-tax calendar run by the <a href="https://www.incometax.gov.in/" target="_blank" rel="noopener">income-tax department</a> — advance tax, TDS returns where applicable, and the company's own ITR — and the year-end statutory audit, which the first auditor you appointed will conduct under standards issued by the <a href="https://www.icai.org/" target="_blank" rel="noopener">ICAI</a>.</p>
            <p>If you indicated you'll hire, the labour registrations feed their own ongoing returns: Provident Fund and ESI filings administered through the <a href="https://www.epfindia.gov.in/" target="_blank" rel="noopener">EPFO</a> become monthly obligations once you cross the thresholds. And if your business has an innovative or scalable angle, this is also the natural moment to consider <a href="https://www.dpiit.gov.in/" target="_blank" rel="noopener">DPIIT</a> recognition through the <a href="https://www.startupindia.gov.in/" target="_blank" rel="noopener">Startup India</a> portal, since the benefits compound from early in the entity's life.</p>
            <p>The practical takeaway is that the checklist above is the on-ramp, not the whole journey — building a simple compliance calendar in the first month, while the filings are fresh, is the single best way to avoid the per-day penalties that accrue silently when a deadline is missed.</p>
