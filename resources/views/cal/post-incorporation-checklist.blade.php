            <h2>Frequently Asked Questions</h2>

            <div class="faq-item">
                <button class="faq-question" onclick="toggleFaq(this)"><span>What are the first compliances after incorporating a company?</span><span class="faq-icon">+</span></button>
                <div class="faq-answer"><div class="faq-answer-inner">For a private limited company the immediate compliances are holding the first board meeting within thirty days of incorporation, appointing the first auditor within thirty days and filing Form ADT-1 within fifteen days of that appointment, opening a company bank account and depositing the subscription money, issuing share certificates within sixty days, and filing the commencement of business declaration in Form INC-20A within one hundred eighty days. Statutory registers must be maintained from day one, and applicable tax and labour registrations taken as needed.</div></div>
            </div>

            <div class="faq-item">
                <button class="faq-question" onclick="toggleFaq(this)"><span>What is Form INC-20A and when is it due?</span><span class="faq-icon">+</span></button>
                <div class="faq-answer"><div class="faq-answer-inner">Form INC-20A is the declaration for commencement of business that a company with share capital, incorporated on or after 2 November 2018, must file within one hundred eighty days of incorporation. It confirms that every subscriber has paid the value of the shares agreed to be taken and that the registered office is verified, so the subscription money must be in the company bank account first. Until it is filed the company cannot legally commence business or exercise borrowing powers.</div></div>
            </div>

            <div class="faq-item">
                <button class="faq-question" onclick="toggleFaq(this)"><span>What happens if I miss the INC-20A deadline?</span><span class="faq-icon">+</span></button>
                <div class="faq-answer"><div class="faq-answer-inner">Missing the one hundred eighty day deadline for Form INC-20A carries a penalty of fifty thousand rupees on the company and one thousand rupees per day on every officer in default, up to a maximum of one lakh rupees. More seriously, the Registrar of Companies can initiate action to strike the company's name off the register under section 248. Because of these consequences it is best to file INC-20A as soon as the subscription money is deposited rather than waiting until the deadline approaches.</div></div>
            </div>

            <div class="faq-item">
                <button class="faq-question" onclick="toggleFaq(this)"><span>When must the first auditor be appointed?</span><span class="faq-icon">+</span></button>
                <div class="faq-answer"><div class="faq-answer-inner">Under section 139(6) of the Companies Act, the board of directors must appoint the first statutory auditor within thirty days of incorporation, usually at the first board meeting. Form ADT-1 intimating the appointment to the Registrar should then be filed within fifteen days of the appointment. The first auditor holds office until the conclusion of the first annual general meeting. Appointing the auditor promptly is important because the auditor is needed for the year-end statutory audit and several downstream filings.</div></div>
            </div>

            <div class="faq-item">
                <button class="faq-question" onclick="toggleFaq(this)"><span>Is post-incorporation compliance different for an LLP?</span><span class="faq-icon">+</span></button>
                <div class="faq-answer"><div class="faq-answer-inner">Yes, considerably. An LLP does not file INC-20A, does not appoint a statutory auditor at the outset unless thresholds are crossed, does not hold board meetings and does not issue share certificates. Its key first task is filing the LLP agreement in Form 3 with the Registrar within thirty days of incorporation. Beyond that it opens a bank account, obtains PAN and TAN, takes applicable registrations, and then follows the annual cycle of Form 11 and Form 8. The compliance load is lighter than a company's.</div></div>
            </div>

            <div class="faq-item">
                <button class="faq-question" onclick="toggleFaq(this)"><span>Do I need to open a company bank account before filing INC-20A?</span><span class="faq-icon">+</span></button>
                <div class="faq-answer"><div class="faq-answer-inner">Yes. Form INC-20A requires proof that the subscription money has been received, so the company must first open a current account in its own name and have the subscribers deposit the value of the shares they agreed to take. You will typically need the Certificate of Incorporation, the company PAN, a board resolution authorising the account, and KYC of the directors. Some banks open the account but keep it transaction-restricted until the INC-20A filing proof is provided, so sequence these two steps together early.</div></div>
            </div>

            <div class="faq-item">
                <button class="faq-question" onclick="toggleFaq(this)"><span>Which tax and labour registrations do I need after incorporation?</span><span class="faq-icon">+</span></button>
                <div class="faq-answer"><div class="faq-answer-inner">It depends on your activity, turnover and headcount. GST registration is required once you cross the turnover threshold or make inter-state or e-commerce supplies, and many businesses register voluntarily. Professional Tax registration is required in states that levy it, often within thirty days of hiring. Provident Fund and ESI registrations become mandatory once you cross the prescribed employee counts, and a Shops and Establishment registration is commonly needed locally. The checklist flags these when you indicate you will hire employees or need GST.</div></div>
            </div>

            <div class="faq-item">
                <button class="faq-question" onclick="toggleFaq(this)"><span>What is DIR-3 KYC and when is it due?</span><span class="faq-icon">+</span></button>
                <div class="faq-answer"><div class="faq-answer-inner">DIR-3 KYC is an annual know-your-customer filing that every person holding a Director Identification Number must complete to keep the DIN active, and the due date is the thirtieth of September each year. It is not strictly a one-time post-incorporation step, but new directors should be aware of it from the start because a missed KYC deactivates the DIN and attracts a five thousand rupee reactivation fee. Maintaining DIR-3 KYC on time is part of keeping the company and its directors in good standing.</div></div>
            </div>

            <div class="faq-item">
                <button class="faq-question" onclick="toggleFaq(this)"><span>What statutory registers must a company maintain?</span><span class="faq-icon">+</span></button>
                <div class="faq-answer"><div class="faq-answer-inner">From the date of incorporation a company must maintain statutory registers such as the register of members, the register of directors and key managerial personnel, the register of charges and the register of contracts, along with minutes books of board and general meetings. Directors must also disclose their interests in Form MBP-1 and give a non-disqualification declaration in Form DIR-8 at the first board meeting of each financial year. These records are often overlooked but are a legal requirement and are examined in due diligence and audits.</div></div>
