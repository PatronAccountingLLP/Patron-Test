        <section class="content-section" id="penalties">
            <h2>What It Costs to Miss These</h2>
            <p>These aren't soft deadlines — several carry heavy penalties and director-level exposure:</p>
            <ul>
                <li><strong>INC-20A:</strong> ₹50,000 on the company + ₹1,000/day per officer in default (max ₹1 lakh), and the ROC can strike the company off under Section 248.</li>
                <li><strong>Annual filings (AOC-4 / MGT-7):</strong> ₹100 per day per form, with no upper cap — and directors risk disqualification after three years of non-filing.</li>
                <li><strong>DIR-3 KYC:</strong> a missed KYC deactivates the DIN and costs ₹5,000 to reactivate.</li>
            </ul>
            <div class="callout warn">
                <p><strong>Note:</strong> Deadlines and penalties are set by the Companies Act / LLP Act and change by notification; some steps depend on your specific facts. This is an educational planning aid — confirm your exact obligations with a CA or CS.</p>
