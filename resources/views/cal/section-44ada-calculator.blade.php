                [800000, 0.05],
                [1200000, 0.10],
                [1600000, 0.15],
                [2000000, 0.20],
                [2400000, 0.25],
                [Infinity, 0.30]
            ];
            let prev = 0;
            for (const [limit, rate] of slabs) {
                if (income <= limit) {
                    tax += (income - prev) * rate;
                    break;
                }
                tax += (limit - prev) * rate;
                prev = limit;
            }
        } else {
