        <section class="content-section" id="how-it-works">
            <h2>How Section 44ADA Works</h2>
            <p>Section 44ADA is the presumptive taxation scheme designed exclusively for professionals. Instead of calculating actual profit by tracking every expense, you simply declare 50% of your gross professional receipts as taxable income. The remaining 50% is deemed to cover all your business costs — staff salary, rent, utilities, depreciation, conveyance, professional development, software subscriptions, everything. Originally enacted by the Finance Act 2016 and refined by Budget 2023's enhanced ₹75 lakh threshold for digital professionals (announced by the <a href="https://www.finmin.nic.in/" target="_blank" rel="noopener">Ministry of Finance</a> and notified via <a href="https://www.pib.gov.in/" target="_blank" rel="noopener">CBDT press releases</a>), the scheme is administered by the <a href="https://www.incometaxindia.gov.in/" target="_blank" rel="noopener">Income Tax Department</a> and applied per the standards prescribed by the <a href="https://www.icai.org/" target="_blank" rel="noopener">Institute of Chartered Accountants of India</a>.</p>

            <h3>The Core Formula</h3>
            <div class="formula-box">
                <span class="label">Presumptive Income</span> = 50% × Gross Professional Receipts<br>
                <span class="label">Taxable Income</span> = Presumptive Income + Other Income − Chapter VI-A Deductions<br>
                <span class="label">Tax</span> = Tax on Taxable Income (at applicable slab) + 4% Cess
            </div>

            <p>Three things make Section 44ADA distinctive: (1) you don't maintain books of accounts, (2) you don't undergo tax audit, (3) you pay 100% advance tax in one shot by 15 March instead of four quarterly installments. The trade-off: you cannot claim any actual expenses beyond the 50% deemed amount.</p>
        </section>

        <section class="content-section" id="eligibility">
            <h2>Who Can Use Section 44ADA</h2>
            <p>Eligibility requires meeting all four conditions simultaneously. Failing any one disqualifies you for that financial year.</p>

            <h3>1. Specified Profession Under Section 44AA(1)</h3>
            <ul>
                <li><strong>Legal:</strong> Advocates, lawyers in practice</li>
                <li><strong>Medical:</strong> Doctors, surgeons, dentists, physiotherapists in private practice</li>
                <li><strong>Engineering:</strong> Practising engineers offering professional services</li>
                <li><strong>Architectural:</strong> Architects in private or partnership practice</li>
                <li><strong>Accountancy:</strong> CAs, CMAs, CSs in practice (not employed)</li>
                <li><strong>Technical Consultancy:</strong> Standalone technical advisory practice</li>
                <li><strong>Interior Decoration</strong></li>
                <li><strong>Film Artists:</strong> Actors, directors, producers, music directors, singers, dance directors, art directors, costume designers, lyricists, story writers, cameramen, editors, screenplay/dialogue writers</li>
                <li><strong>Information Technology Professionals:</strong> Software development, IT consulting (in private practice)</li>
                <li><strong>Authorised Representatives:</strong> Tax practitioners other than those in employment, who appear before tribunals/authorities for fee</li>
            </ul>
            <p><strong>Excluded:</strong> Brokerage and commission agents, real estate agents, insurance agents, general consultancy not falling under the specified list, retail traders, manufacturers, contractors. These may consider Section 44AD instead (for businesses, with 6%/8% presumptive rate up to ₹3 crore turnover).</p>

            <h3>2. Resident Individual or Partnership Firm</h3>
            <p>Only resident individuals and partnership firms qualify. <strong>LLPs are explicitly excluded</strong> — Section 44ADA does not apply to Limited Liability Partnerships even if all partners are professionals. <strong>HUFs are also excluded</strong>, as are companies of any kind. Non-residents cannot use Section 44ADA regardless of profession.</p>

            <h3>3. Gross Receipts Within Threshold</h3>
            <p>Receipts up to ₹50 lakh standard, or ₹75 lakh if cash receipts are ≤ 5% of total. See the next section for details on this calculation.</p>

            <h3>4. No Brokerage or Commission Income</h3>
            <p>If your professional income includes brokerage or commission, that portion is specifically excluded. A doctor who also earns insurance commission cannot include the commission in 44ADA receipts — only the medical practice receipts qualify.</p>

            <div class="callout warn">
                <p><strong>Common error:</strong> Many CAs/lawyers in employment mistakenly claim Section 44ADA on their salary. The scheme applies only to <em>professional</em> income from independent practice, not employment salary. If you have both salary and side consulting, only the consulting receipts qualify.</p>
            </div>
