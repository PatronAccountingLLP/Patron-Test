        <section class="content-section" id="audit">
            <h2>When Audit and Books Are Required</h2>
            <p>Section 44ADA's biggest benefit is freedom from books and audit. But this is conditional. Audit becomes mandatory under Section 44AB if any of these triggers apply:</p>

            <h3>Audit Trigger 1 — Declaring Less Than 50%</h3>
            <p>If you opt out of 44ADA (declare less than 50% of receipts as profit) AND your total income exceeds the basic exemption limit, books under Section 44AA and audit under Section 44AB are mandatory. You must file ITR-3 with auditor's report (Form 3CA/3CB-3CD or Form 26 from FY 2026-27).</p>

            <h3>Audit Trigger 2 — Receipts Exceed Threshold</h3>
            <p>If gross receipts exceed ₹75 lakh (or ₹50 lakh where cash &gt; 5%), Section 44ADA is unavailable and regular taxation applies. Audit kicks in if professional gross receipts exceed ₹75 lakh under Section 44AB(b) — even if you would have qualified earlier.</p>

            <h3>Audit Trigger 3 — Ineligible Entity</h3>
            <p>LLPs, companies, HUFs cannot use Section 44ADA. If they were operating under 44ADA in a prior year (incorrectly), the assessment officer will reassess with audit requirement going back up to 6 years under Section 148.</p>

            <h3>What the Audit Involves</h3>
            <ul>
                <li>Maintain books of accounts (cash book, journal, ledger, sales register)</li>
                <li>Maintain receipts for all expenses claimed</li>
                <li>Engage a CA in practice for the audit</li>
                <li>File audit report on the e-filing portal by 30 September of the assessment year</li>
                <li>File ITR-3 (not ITR-4) by 31 October instead of 31 July</li>
            </ul>
            <p>Audit fees typically range from ₹15,000 to ₹50,000 depending on practice complexity. Patron handles 44ADA filings under both presumptive and regular methods — see <a href="/itr-filing-for-freelancers-professionals">ITR Filing for Freelancers &amp; Professionals</a>.</p>

            <div class="callout warn">
                <p><strong>Form numbering change effective 1 April 2026:</strong> Under the Income Tax Act 2025 and Income Tax Rules 2026, Forms 3CA, 3CB, and 3CD will be replaced by Form 26 for tax audit reports. ITR-4 and ITR-3 retain their numbering for now. Refer to the <a href="https://www.indiacode.nic.in/" target="_blank" rel="noopener">Income Tax Act 2025</a> for full mapping. Confirm current form numbers with your CA before filing.</p>
