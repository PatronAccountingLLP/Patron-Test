    function checkAudit(isEligible, gross, expenses, presumptive) {
        const w = document.getElementById('auditWarning');
        const r = document.getElementById('auditReason');
        let trigger = null;

        if (gross > 7500000) {
            trigger = 'Gross professional receipts exceed ₹75 lakh — Section 44AB(b) tax audit is mandatory regardless of method chosen.';
        } else if (isFinite(expenses) && expenses > presumptive) {
            // declared income < 50% AND total income > basic exemption → audit
            const netProfit = gross - expenses;
            const basicExempt = (regime === 'new') ? 400000 : (ageGroup === 'supersenior' ? 500000 : (ageGroup === 'senior' ? 300000 : 250000));
            if (netProfit > basicExempt && isEligible) {
                trigger = 'Declaring profit below 50% (' + fmtINR(gross - expenses) + ' actual vs ' + fmtINR(presumptive) + ' presumptive) AND total income exceeds basic exemption of ' + fmtINR(basicExempt) + ' → books of accounts and tax audit required under Sections 44AA and 44AB.';
            }
        }

        if (trigger) {
            r.textContent = trigger;
            w.classList.add('visible');
        } else {
            w.classList.remove('visible');
        }
    }

