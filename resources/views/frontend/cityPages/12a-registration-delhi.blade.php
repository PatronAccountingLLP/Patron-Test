        <div class="text-content">
            <h2 class="section-title">Legal and Compliance Framework for 12A Registration</h2>
            <div class="content-text">
                <p><strong>Income Tax Act, 1961 - Section 12A:</strong> Original provision requiring registration of trusts and institutions for claiming exemption under Sections 11 and 12. Application filed in Form 10A with the Principal Commissioner / Commissioner of Income Tax (Exemption).</p>

                <p><strong>Section 12AB (Finance Act 2020):</strong> Replaced Section 12AA for new registrations from 1 April 2021. Introduces time-bound registration: provisional for 3 years, regular for 5 years. Mandatory re-registration for all existing 12A/12AA entities. Form 10AB for renewal and re-registration.</p>

                <p><strong>Section 11 - Income Exemption:</strong> Income of a registered charitable or religious trust is exempt from tax to the extent it is applied to charitable purposes. Accumulation up to 15% permitted.</p>

                <p><strong>Section 12 - Donations as Income:</strong> Voluntary contributions (donations) received by a registered trust/institution are treated as income of the trust. Corpus donations (with specific direction) are not treated as income and are exempt from the 85% application rule.</p>

                <p><strong>Section 2(15) - Charitable Purpose:</strong> Defines charitable purpose as relief of the poor, education, yoga, medical relief, preservation of environment, preservation of monuments/places/objects of artistic or historic interest, and advancement of any other object of general public utility.</p>

                <p><strong>Rule 17A - Form 10A/10AB:</strong> Prescribes the form, manner and documents for 12A application. Form 10A for fresh/provisional. Form 10AB for renewal, re-registration and conversion from provisional to regular.</p>

                <p><strong>Form 10B/10BB - Annual Audit Report:</strong> Mandatory annual filing. Form 10B for trusts with total income below Rs 5 crore. Form 10BB for institutions with income above Rs 5 crore. Must be filed before ITR-7 due date. Read more about <a href="/statutory-audit">Statutory Audit Services</a>.</p>
