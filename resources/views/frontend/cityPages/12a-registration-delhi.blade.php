            <div class="content-text">
                <p>12A registration is the process of obtaining income tax exemption for a charitable or religious organisation under Section 12A of the Income Tax Act, 1961, by filing Form 10A with the Principal Commissioner or Commissioner of Income Tax (Exemption).</p>

