document.addEventListener('DOMContentLoaded', function() {
    const form = document.querySelector('form[action*="submit-payroll-enquiry"]');
    const submitBtn = document.getElementById('submitBtn');
    if (form && submitBtn) {
        form.addEventListener('submit', function(e) {
            submitBtn.disabled = true;
            submitBtn.textContent = 'Submitting...';
            setTimeout(function() {
                if (submitBtn.disabled) {
                    submitBtn.disabled = false;
                    submitBtn.textContent = 'Find My Payroll Plan →';
                }
            }, 3000);
        });
        const phoneInput = document.getElementById('phone');
        if (phoneInput) {
            phoneInput.addEventListener('input', function(e) {
                let value = e.target.value.replace(/\D/g, '');
                if (value.length > 15) { value = value.slice(0, 15); }
                e.target.value = value;
                const isValid = /^[0-9]{7,15}$/.test(value);
                if (value.length > 0 && !isValid) {
                    e.target.setCustomValidity('Please enter a valid phone number (7-15 digits)');
                } else {
                    e.target.setCustomValidity('');
                }
            });
        }
    }
});

