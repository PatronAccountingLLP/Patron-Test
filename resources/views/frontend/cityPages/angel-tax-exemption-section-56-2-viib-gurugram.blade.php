                <h2 class="section-title">What Is Angel Tax Exemption Under Section 56(2)(viib)</h2>
                <div class="content-text what-is-definition">
                    
                    <p><strong>Angel tax exemption refers to relief from the tax under Section 56(2)(viib) of the Income Tax Act 1961, which treated share premium above fair market value as taxable income for unlisted Indian companies. The Finance (No. 2) Act 2024 abolished this provision from 1 April 2025.</strong></p>
                    <p>Until abolition, DPIIT-recognised startups could claim exemption by filing Form 2 declaration under DPIIT Notification GSR 127(E) dated 19 February 2019, subject to a cap of INR 25 crore aggregate paid-up share capital plus share premium after the proposed issue. Fair market value was determined under Rule 11UA of the Income Tax Rules 1962, using the Net Asset Value method or a merchant banker discounted cash flow valuation.</p>
                    <p>Foreign investors were originally outside the section, brought within scope by the Finance Act 2023 with effect from 1 April 2024, and excluded again by the Finance (No. 2) Act 2024 with effect from 1 April 2025. CBDT Notification dated 24 May 2023 had earlier exempted entities resident in 21 specified countries including the United States, United Kingdom, Japan and Australia.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Angel Tax Exemption:</strong></p>
                    <p><strong>Angel Tax:</strong> informal name for Section 56(2)(viib) charge on share premium above FMV when an unlisted company issues shares.</p>
                    <p><strong>DPIIT:</strong> Department for Promotion of Industry and Internal Trade; recognises eligible startups under GSR 127(E) dated 19 February 2019.</p>
                    <p><strong>Form 2:</strong> declaration filed by DPIIT-recognised startups to claim angel tax exemption for the relevant assessment year.</p>
                    <p><strong>Rule 11UA:</strong> Income Tax Rules 1962 rule prescribing valuation methods (NAV and DCF) for unquoted equity shares.</p>
                    <p><strong>FMV:</strong> Fair Market Value of unquoted equity shares determined under Rule 11UA on the date of issue.</p>

