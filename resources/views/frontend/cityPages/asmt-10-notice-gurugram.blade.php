                <h2 class="section-title">What Is an ASMT-10 Notice?</h2>
                <div class="content-text what-is-definition">
                    
                    <p>An <strong>ASMT-10 notice</strong> is a discrepancy intimation issued under <strong>Section 61 of the CGST Act, 2017 read with Rule 99 of the CGST Rules, 2017</strong> (and parallel provisions of the Haryana GST Act, 2017) when the proper officer scrutinises your filed GST returns and identifies inconsistencies. It <strong>seeks an explanation - not payment.</strong></p>
<p>The notice quantifies the alleged discrepancy in tax, interest, and other amounts (where possible) and gives the taxpayer <strong>up to 30 days</strong> to either explain via <strong>Form ASMT-11</strong> or accept and pay via <strong>Form DRC-03.</strong> A satisfactory reply leads to closure via <strong>Form ASMT-12.</strong> An unsatisfactory or absent reply can trigger Section 65 audit, Section 67 inspection, or a Section 73 / 74 / 74A demand SCN.</p>
<p>Critically, an ASMT-10 cannot itself create a demand. The <strong>Andhra Pradesh High Court in Devi Traders v. State of Andhra Pradesh</strong> [2023] 152 taxmann.com 22 held that Section 74 SCN cannot be issued without first invoking Section 61 scrutiny - making the ASMT-10 a mandatory procedural gateway in many cases.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for ASMT-10 Notice Gurugram:</strong></p>
                    <p><strong>ASMT-10 (Discrepancy Notice):</strong> Form under Rule 99(1) listing each discrepancy found by the proper officer with quantified tax/interest amounts where possible.</p>
<p><strong>ASMT-11 (Taxpayer Reply):</strong> Form under Rule 99(2) for either accepting the discrepancy (with DRC-03 payment) or furnishing explanation with documentary evidence.</p>
<p><strong>ASMT-12 (Closure Order):</strong> Form under Rule 99(3) issued by the proper officer when the ASMT-11 reply is found acceptable - ending the scrutiny.</p>
<p><strong>DRC-03 (Voluntary Payment):</strong> Challan-cum-acknowledgement form to pay tax, interest, and any penalty before, during, or after an ASMT-10.</p>
<p><strong>DGARM:</strong> Directorate General of Analytics and Risk Management - the CBIC body that uses risk analytics to select GSTINs for scrutiny.</p>
<p><strong>ACES-GST:</strong> Automation of Central Excise and Service Tax (now GST) - the CBIC backend platform. The Automated Return Scrutiny Module sits on top of it.</p>
<p><strong>Section 62 Best Judgment:</strong> Where a return has not been filed at all, scrutiny under Section 61 cannot apply; Section 62 best-judgment assessment is invoked instead.</p>

