            <h2 class="section-title">Who Needs Pune ASMT-10 Notice Handling?</h2>
            <div class="content-text">
                
                <p>Pune ASMT-10 notice handling is required when one or more of the following apply:</p>
<ul>
    <li>You hold a Maharashtra GSTIN registered in Pune and have received <strong>Form GST ASMT-10</strong></li>
    <li>Your ASMT-10 alleges <strong>GSTR-1 vs GSTR-3B</strong> outward supplies mismatch</li>
    <li>Your ASMT-10 alleges <strong>GSTR-2A / GSTR-2B vs GSTR-3B ITC mismatch</strong> (the most common Pune pattern - especially Hinjewadi/Kharadi IT)</li>
    <li>Your ASMT-10 alleges <strong>GSTR-3B vs GSTR-9</strong> annual return reconciliation gap</li>
    <li>Your ASMT-10 alleges <strong>e-way bill vs GSTR-1</strong> supply value or HSN mismatch (Talegaon-Chakan auto)</li>
    <li>Your ASMT-10 alleges <strong>Section 9(3) RCM under-reporting</strong> (advocate fees, security services, director sitting fees)</li>
    <li>Your ASMT-10 alleges HSN / SAC classification or rate-of-tax discrepancy</li>
    <li>Your ASMT-10 alleges <strong>ITC reversal under Rule 42/43</strong> or Section 17(5) tracking issues</li>
    <li>You are within the 30-day ASMT-11 reply window from ASMT-10 service - critical deadline</li>
    <li>Your ASMT-10 has been served by <strong>Pune-I CGST or Pune-II CGST Commissionerate</strong></li>
    <li>Your ASMT-10 has been served by Maharashtra State Tax Pune Circle office (state-component)</li>
    <li>You are operating in Hinjewadi / Kharadi / Wakad / Aundh / Baner / Hadapsar / Camp / Koregaon Park / Viman Nagar</li>
    <li>You are operating in PCMC (Pimpri-Chinchwad-Bhosari) / Talegaon / Chakan / Ranjangaon</li>
</ul>
<h3 style="margin-top:24px;">Statutory Deadline Snapshot</h3>
<ul>
    <li><strong>Reply to Form GST ASMT-10 in Form ASMT-11:</strong> 30 days from ASMT-10 service (Rule 99(2))</li>
    <li><strong>Extension of reply time:</strong> Officer may extend on application for sufficient cause</li>
    <li><strong>Officer's ASMT-12 acceptance:</strong> No fixed statutory time limit; practice is 60 to 120 days after ASMT-11 filing</li>
    <li><strong>Escalation to Section 73 SCN (Form DRC-01):</strong> Within Section 73(2) SCN time limit (2 years 9 months from GSTR-9 due date, subject to Section 168A extensions)</li>
    <li><strong>Escalation to Section 74 SCN if fraud alleged:</strong> Within Section 74(2) SCN time limit (4 years 6 months from GSTR-9 due date)</li>
    <li><strong>Voluntary payment under Section 73(5) via Form DRC-03:</strong> At any time before SCN - NIL penalty</li>
