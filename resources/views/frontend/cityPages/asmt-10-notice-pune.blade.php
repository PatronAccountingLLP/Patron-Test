                <h2 class="section-title">What Is Form GST ASMT-10?</h2>
                <div class="content-text what-is-definition">
                    
                    <p><strong>Form GST ASMT-10</strong> is the scrutiny notice issued by the proper officer under Section 61(2) of the CGST Act 2017 read with Rule 99(1) of the CGST Rules 2017. It is issued when the officer notices a discrepancy in a registered person's returns through scrutiny - typically via DGARM system-driven flagging.</p>
<p>The notice <strong>seeks explanation - it does NOT quantify tax demand.</strong> The taxpayer's reply is in <strong>Form GST ASMT-11</strong> with explanation and supporting evidence within 30 days of ASMT-10 service (extendable for sufficient cause via the GST portal).</p>
<p>The strategic significance lies in what happens next. If the ASMT-11 explanation is acceptable, the officer issues <strong>Form GST ASMT-12</strong> - an order of acceptance that effectively closes the scrutiny without any tax demand. If the explanation is NOT satisfactory, or if no reply is filed, the officer may proceed under Section 65 (audit), Section 66 (special audit), Section 67 (inspection/search) or - most commonly - Section 73 / 74 by issuing <strong>Form GST DRC-01 SCN.</strong></p>
<p>A well-drafted ASMT-11 with rigorous reconciliation and supporting evidence often achieves ASMT-12 closure - saving the Pune assessee from Section 73 SCN penalty exposure, multiple hearings, and Section 107 / 112 appeal cycles.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for ASMT-10 Notice Pune:</strong></p>
                    <p><strong>Section 61 CGST Act 2017:</strong> Scrutiny of returns - empowers proper officer to scrutinise returns to verify correctness. Section 61(1) power; Section 61(2) ASMT-10 issuance; Section 61(3) ASMT-12 acceptance or escalation to Section 65/66/67/73/74.</p>
<p><strong>Rule 99 CGST Rules 2017:</strong> Procedure for scrutiny - officer issues ASMT-10; taxpayer reply in ASMT-11 within 30 days; officer issues ASMT-12 or proceeds further.</p>
<p><strong>Form GST ASMT-10:</strong> Notice for intimating discrepancies - issued by Pune CGST or Maharashtra State Tax Pune Circle proper officer; specifies discrepancy with return periods and amount.</p>
<p><strong>Form GST ASMT-11:</strong> Reply by taxpayer to ASMT-10 - filed on GST portal; explanation of each discrepancy with supporting evidence; the highest-value document in the ASMT-10 lifecycle.</p>
<p><strong>Form GST ASMT-12:</strong> Order of acceptance of explanation by proper officer - the closure outcome; terminates scrutiny without tax demand.</p>
<p><strong>DGARM:</strong> Directorate General of Analytics and Risk Management - CBIC body running data analytics infrastructure that generates most ASMT-10 flags.</p>
<p><strong>Pune-I and Pune-II CGST Commissionerates:</strong> Pune CGST Zone under CBIC - issue Form GST ASMT-10 for Pune-registered GSTINs based on jurisdictional allocation.</p>
<p><strong>Maharashtra State GST Pune Circle:</strong> Pune Circle offices under Maharashtra State Tax Department (HQ Mazgaon Mumbai) - issue state-component Form GST ASMT-10 under Maharashtra GST Act 2017 Section 61.</p>
<p><strong>GSTR-2A vs GSTR-3B Pune Trend:</strong> The single highest-volume ASMT-10 category in Pune - especially for Hinjewadi/Kharadi IT firms with multi-state vendor base and complex ITC eligibility under Section 16(2)(c) and Rule 36(4).</p>

