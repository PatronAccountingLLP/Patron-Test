                <div class="content-text what-is-definition">
                    
                    
                    <p><strong>A branch office</strong> is a place of business through which a foreign company operates in India in its own name after Reserve Bank permission under <strong>FEMA 22(R)/2016-RB</strong>. It earns income only from permitted activities, is taxed in India as a permanent establishment, and is registered as a foreign company under Sections 380-386 of the Companies Act, 2013.</p>
                    <p>Permission comes from RBI under Section 6(6) of FEMA, 1999 and Notification FEMA 22(R)/2016-RB; the application travels in <strong>Form FNC</strong> through an AD Category-I bank, which obtains a Unique Identification Number.</p>
                    <p>The branch is an extension of the parent: <strong>liabilities sit with the foreign company</strong>, and profits, net of Indian taxes, are freely remittable. The parent invoices in its own name through the branch; the branch cannot manufacture (except in an SEZ) or trade in retail.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Branch Office Setup:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>Form FNC:</strong> the application for opening a branch, liaison or project office, filed with the AD bank.</li>
                        <li><strong>AD Category-I bank:</strong> the authorised dealer that processes the FNC, holds the branch account and reports to RBI.</li>
                        <li><strong>UIN:</strong> the Unique Identification Number RBI allots to each approved office.</li>
                        <li><strong>AAC:</strong> the Annual Activity Certificate a CA issues, due by 30 September each year, to the AD bank and DGIT (International Taxation).</li>
                        <li><strong>FC-1:</strong> Companies Act foreign company registration filing within 30 days of establishment; goes to RoC Delhi.</li>
                        <li><strong>FC-3:</strong> audited annual accounts of the foreign company and its branches in India.</li>
                        <li><strong>FC-4:</strong> annual return of the foreign company filed within 60 days from FY-end.</li>
                        <li><strong>Form 3CEB:</strong> transfer pricing report where dealings with the parent exist (Sections 92 to 92F).</li>
                        <li><strong>PE:</strong> Permanent Establishment - the branch is taxed as a PE of the foreign company in India.</li>
                    </ul>
                    

