                    <li>Export or import of goods.</li>
                    <li>Rendering professional or consultancy services.</li>
                    <li>Carrying out research work in which the parent company is engaged.</li>
                    <li>Promoting technical or financial collaborations between Indian companies and the parent or overseas group.</li>
                    <li>Representing the parent in India and acting as a buying or selling agency.</li>
                    <li>Rendering services in information technology and development of software in India.</li>
                    <li>Rendering technical support to the products supplied by the parent company.</li>
                    <li>Foreign airline or shipping company operations.</li>
                </ul>
                <p style="margin-top:16px;"><strong>Prohibited:</strong> Retail trading of any nature and manufacturing or processing, except a branch in a <strong>Special Economic Zone (SEZ)</strong>.</p>
