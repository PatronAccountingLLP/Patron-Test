                    <li><strong>Net worth of USD 100,000 or more</strong> evidenced by latest audited balance sheet (or a CA's net worth certificate / banker's report).</li>
                    <li><strong>Parent letter of comfort</strong> can bridge a shortfall in either criterion (subject to RBI satisfaction).</li>
