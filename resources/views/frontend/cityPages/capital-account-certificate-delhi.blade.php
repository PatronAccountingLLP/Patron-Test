                        <li><strong>Capital account transaction:</strong> one that alters cross-border assets or liabilities (FDI, ECB, transfers, ODI).</li>
                        <li><strong>FC-GPR:</strong> Foreign Currency Gross Provisional Return - filed within 30 days of allotting shares to a foreign investor.</li>
                        <li><strong>FC-TRS:</strong> the form for transfer of capital instruments between residents and non-residents.</li>
                        <li><strong>ECB:</strong> External Commercial Borrowing - a foreign-currency loan, reported monthly via ECB-2.</li>
                        <li><strong>FIRMS portal:</strong> the RBI's online portal for FEMA reporting through the AD bank.</li>
