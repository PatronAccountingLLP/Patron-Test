                    <div class="faq-expanded__item" id="faq-3">
                        <h3 class="faq-expanded__q">When must FC-GPR be filed?</h3>
                        <div class="faq-expanded__a"><p>Form FC-GPR must be filed on the RBI FIRMS portal within 30 days of allotting shares to a foreign investor. Late filing is a compoundable FEMA contravention, so timely certification and filing matter to avoid penalties.</p></div>
                    </div>
