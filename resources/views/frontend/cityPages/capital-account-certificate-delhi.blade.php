                        <h3 class="faq-expanded__q">What is a Capital Account Certificate?</h3>
                        <div class="faq-expanded__a"><p>It is a Chartered Accountant's certificate attesting the details and FEMA compliance of a capital account transaction, used to support the statutory reporting - FC-GPR for FDI, FC-TRS for transfers, and ECB reporting. It is not a single RBI form by that name but the CA-certification layer accompanying these filings.</p></div>
