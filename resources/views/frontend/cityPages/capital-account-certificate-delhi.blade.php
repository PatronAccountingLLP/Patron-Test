                <div class="content-text what-is-definition">
                    
                    <p>A Capital Account Certificate is a certificate from a practising Chartered Accountant attesting the details of a capital account transaction under FEMA - the amount, the parties, the pricing, and its compliance with the applicable RBI regulations - to support reporting to the RBI.</p>
<p>It is not a single RBI form by that exact name; rather, it is the CA-certification layer that accompanies the statutory filings - FC-GPR when shares are issued to a foreign investor, FC-TRS when capital instruments are transferred, and the reporting around an ECB drawdown. The certificate carries a UDIN for verification.</p>

