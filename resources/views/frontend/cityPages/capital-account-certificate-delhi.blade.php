                        <h3 class="faq-expanded__q">What is a capital account transaction under FEMA?</h3>
                        <div class="faq-expanded__a"><p>It is a transaction that alters assets or liabilities outside India for a resident, or inside India for a non-resident - such as foreign direct investment, External Commercial Borrowings, transfer of shares between residents and non-residents, and overseas direct investment. These are restricted unless permitted under FEMA.</p></div>
                    </div>
