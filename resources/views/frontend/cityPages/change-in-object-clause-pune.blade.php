                <h2 class="section-title">What Is Change in Object Clause</h2>
                <div class="content-text what-is-definition">
                    
                    <p>The object clause is Clause III of the Memorandum of Association (MOA), which defines the scope and nature of business activities a company is legally permitted to undertake. Changing the object clause means amending Clause III to add new business activities, delete existing ones, or modify the description of current activities, as governed by <strong>Section 13 of the Companies Act, 2013</strong>.</p>
<p><strong>The Ultra Vires Doctrine:</strong> A company cannot carry on any business activity that is not covered by its object clause. Any act beyond the stated objects is ultra vires (Latin for 'beyond the powers') and is considered void. Contracts entered into for activities outside the object clause may not be legally enforceable, and directors who authorise such activities can be held personally liable.</p>
<p>For Pune companies, the process is handled through the <strong>MCA V3 portal</strong> (mca.gov.in) with the <strong>ROC Pune</strong> at PMT Building, Shivaji Nagar being the jurisdictional registrar. Section 8 companies (non-profit) changing objects may need Central Government approval. Public companies require newspaper publication of the resolution.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Change in Object Clause in Pune:</strong></p>
                    <p><strong>Object Clause (Clause III):</strong> Defines main objects and other objects of the company in the MOA. Any activity not covered is ultra vires.</p>
<p><strong>Ultra Vires:</strong> Latin for 'beyond the powers'. Acts outside the object clause are void and unenforceable. Directors can be personally liable.</p>
<p><strong>Special Resolution (75%):</strong> Required for object clause change. At least 75% of votes cast at EGM must be in favour.</p>
<p><strong>Form MGT-14:</strong> Filed with ROC within 30 days of the special resolution. Registers the resolution and amended MOA with ROC Pune.</p>
<p><strong>Fresh Certificate of Incorporation:</strong> Issued by ROC after MGT-14 approval. The alteration is legally effective only upon issuance of this certificate.</p>

