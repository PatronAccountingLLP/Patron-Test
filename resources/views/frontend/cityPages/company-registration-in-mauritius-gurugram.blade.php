            <div class="column-content">
                <h2 class="section-title">What Is Company Registration in Mauritius</h2>
                <div class="content-text what-is-definition">
                    
                    <p><strong>Company registration in Mauritius is the process of incorporating a legal entity under the Companies Act 2001 with the Registrar of Companies, with Global Business Company (GBC) variants additionally licensed by the Financial Services Commission under the Financial Services Act 2007 for activities conducted mainly outside Mauritius.</strong></p>
                    <p>From 1 January 2019 onwards, the Category 1 Global Business Company became the Global Business Company (GBC) and the Category 2 Global Business Company was replaced by the Authorised Company (AC). The GBC is tax-resident in Mauritius, can obtain a Tax Residency Certificate from the Mauritius Revenue Authority and access the treaty network. The AC is tax-non-resident in Mauritius, cannot avail DTAA benefits and is used where Mauritius-flag presence is needed without treaty access.</p>
                    <p>For Indian residents and entities, funding either vehicle is a regulated outbound investment under the Foreign Exchange Management (Overseas Investment) Rules 2022. Form FC must be filed via AD Bank within 30 days of remittance and Annual Performance Reports submitted by 31 December each year. Treaty benefits on the India side require an annual TRC from the MRA plus Form 10F on the Indian tax return and adherence to the Limitation of Benefits clause and the 2024 Protocol PPT.</p>
                    <div class="highlight-box" style="margin-top:20px;background:var(--orange-lighter);border-left:4px solid var(--orange);">
                        <p style="margin-bottom:0;"><strong>Editorial note (post-2024 Protocol reality):</strong> The India-Mauritius DTAA continues to exist with capital gains grandfathering for pre-1 April 2017 investments per CBDT Circular 1/2025, but the Principal Purpose Test now applies to post-2017 investments. Substance, TRC and PPT defence are front and centre - this is the responsible CA-firm posture, not aggressive tax planning.</p>
                    </div>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Mauritius Company Registration:</strong></p>
                    <p><strong>Global Business Company (GBC):</strong> Mauritius company licensed by FSC under Financial Services Act 2007, tax-resident in Mauritius, treaty-eligible subject to substance.</p>
                    <p><strong>Authorised Company (AC):</strong> Mauritius company registered for activities outside Mauritius, tax-non-resident in Mauritius, no DTAA access.</p>
                    <p><strong>FSC:</strong> Financial Services Commission of Mauritius - regulator that issues GBC licences and supervises non-bank financial services.</p>
                    <p><strong>MRA:</strong> Mauritius Revenue Authority - the tax authority that issues the Tax Residency Certificate to GBCs.</p>
                    <p><strong>TRC:</strong> Tax Residency Certificate issued by MRA confirming Mauritius residency for DTAA purposes; required annually by Indian taxpayer under Section 90(4) of the Income Tax Act 1961 read with Form 10F.</p>
                    <p><strong>Partial Exemption Regime:</strong> Mauritius regime that exempts 80 percent of qualifying foreign-source income at the GBC level, bringing the effective rate to approximately 3 percent for qualifying activities.</p>
                    <p><strong>PPT:</strong> Principal Purpose Test under the 2024 Protocol to the India-Mauritius DTAA, denying treaty benefits where one of the principal purposes was to obtain a tax advantage.</p>
                    <p><strong>LOB:</strong> Limitation of Benefits clause introduced by the 2016 Protocol with conditional treaty access during the transition period.</p>
                    <p><strong>ODI:</strong> Overseas Direct Investment under OI Rules 2022 by an Indian entity in a foreign entity (control or 10 percent or more stake in a listed entity).</p>

