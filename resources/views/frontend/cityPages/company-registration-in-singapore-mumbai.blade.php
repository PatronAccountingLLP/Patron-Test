                <h2 class="section-title">What Singapore Company Registration Actually Means</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p><strong>Company Registration in Singapore</strong> is the process of incorporating a legal entity, most commonly a Private Limited Company (Pte Ltd), with the <strong>Accounting and Corporate Regulatory Authority (ACRA)</strong> through the <strong>BizFile+ portal</strong> under the Singapore Companies Act (Cap. 50).</p>
                    <p>Indian residents can own <strong>100% of the share capital</strong>. The only structural mandate is at least one director ordinarily resident in Singapore (citizen, PR, EP or EntrePass holder) - typically met through a nominee director until the Indian promoter secures an Employment Pass.</p>
                    <p>For an Indian founder, the Singapore filing is the easy part. The hard parts are India-side FEMA ODI compliance, bank account KYC depth at DBS/OCBC/UOB, and avoiding the <strong>POEM trap</strong> under Section 6(3) of the Indian Income-tax Act, 1961.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Singapore Company Registration:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>ACRA:</strong> Accounting and Corporate Regulatory Authority - Singapore's company registrar.</li>
                        <li><strong>BizFile+:</strong> ACRA's online incorporation and filing portal.</li>
                        <li><strong>Pte Ltd:</strong> Private Limited Company - the standard limited-liability vehicle in Singapore.</li>
                        <li><strong>Resident Director:</strong> A director who is a Singapore Citizen, Permanent Resident, Employment Pass or EntrePass holder. At least one is mandatory.</li>
                        <li><strong>ODI:</strong> Overseas Direct Investment by an Indian resident, governed by FEM (OI) Rules 2022.</li>
                        <li><strong>Form FC / Form OI:</strong> RBI reporting forms filed through an Authorised Dealer (AD) bank for ODI transactions and annual performance.</li>
                        <li><strong>SUTE:</strong> Start-Up Tax Exemption under Section 43(6A) of the Singapore Income Tax Act - 75% on first S$100,000 + 50% on next S$100,000 for first 3 Years of Assessment.</li>
                        <li><strong>DTAA:</strong> India-Singapore Double Taxation Avoidance Agreement governing dividends, royalties, interest and capital gains.</li>
