        <div class="two-column">
            <div class="column-content">
                <h2 class="section-title">What Is Conversion of Proprietorship to LLP</h2>
                <div class="content-text what-is-definition">
                    
                    <p><strong>Conversion of proprietorship to LLP is the operational migration of a sole proprietary business into a newly incorporated Limited Liability Partnership. It is NOT a statutory conversion under the LLP Act 2008, since the Second Schedule of that Act covers only partnership firms, the Third Schedule covers private companies and the Fourth Schedule covers unlisted public companies.</strong></p>
                    <p>In practical terms, conversion means: (a) incorporating a new LLP under the LLP Act 2008 via Form FiLLiP at the MCA portal with at least two partners and at least two designated partners; (b) transferring the proprietorship's business, assets, liabilities, contracts, employees, goodwill and licenses to the LLP through a Business Transfer Agreement or Slump Sale Agreement; (c) migrating GST registration via Form GST ITC-02 (which transfers unutilised input tax credit), applying for fresh PAN and TAN for the LLP, updating bank accounts in the LLP name and notifying customers, suppliers and regulators.</p>
                    <p>Tax treatment is the key area of professional attention. Section 47(xiv) of the Income Tax Act 1961 grants capital gains exemption for transfer of capital assets by a sole proprietary concern to a company on incorporation, subject to conditions. There is no corresponding provision for transfer to an LLP. So the transfer is a taxable event under Section 45, and capital gains are computed under Section 50B (slump sale) if the entire undertaking is transferred as a going concern for a lump sum, or on itemised basis (where individual asset values are assigned) under regular capital gains provisions. Long-term capital gains on slump sale are taxed at 12.5 percent under Finance Act 2024 if the undertaking has been held for more than 36 months.</p>
                    <div class="highlight-box" style="margin-top:20px;background:var(--orange-lighter);border-left:4px solid var(--orange);">
                        <p style="margin-bottom:0;"><strong>Honest framing matters here:</strong> a lot of market content suggests proprietorship to LLP is a tax-neutral statutory conversion. It is not. There is no LLP Act schedule for it and no Section 47(xiv) shield. Patron's job is to structure the migration so the unavoidable tax cost is computed correctly, defended with a Rule 11UAE valuation, and paid through proper advance tax instalments - not to pretend it does not exist.</p>
                    </div>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Convert Proprietorship to LLP:</strong></p>
                    <p><strong>LLP Act 2008:</strong> Limited Liability Partnership Act 2008 - the statutory framework for LLP incorporation and operation in India.</p>
                    <p><strong>FiLLiP:</strong> Form for Incorporation of LLP - the e-form filed with the MCA portal for LLP incorporation (replaces the older Form 2 and integrates DIN / DPIN issuance).</p>
                    <p><strong>Designated Partner:</strong> a partner formally responsible for compliance under the LLP Act 2008; minimum two required; at least one must be resident in India for at least 120 days in the financial year.</p>
                    <p><strong>Slump Sale:</strong> transfer of one or more undertakings as a going concern for a lump sum consideration without assigning separate values to individual assets and liabilities, defined under Section 2(42C) of the Income Tax Act 1961.</p>
                    <p><strong>Section 50B:</strong> provision computing capital gains on slump sale; net worth of the undertaking serves as cost of acquisition; LTCG taxed at 12.5 percent post Finance Act 2024 if held over 36 months.</p>
                    <p><strong>Rule 11UAE:</strong> prescribed FMV computation methodology for slump sale undertakings under the Income Tax Rules 1962.</p>
                    <p><strong>Business Transfer Agreement (BTA):</strong> contractual instrument transferring specified assets and liabilities of the proprietorship to the LLP on either lump sum or itemised basis.</p>
                    <p><strong>Form GST ITC-02:</strong> GST form filed by the transferor (proprietorship) to transfer unutilised input tax credit to the transferee (LLP) when business is transferred as a going concern.</p>
                    <p><strong>Going Concern:</strong> business that is operational and expected to continue in the foreseeable future; transfer as a going concern is NIL rated under GST per Notification 12/2017-Central Tax (Rate) Sl. No. 2.</p>
                    <p><strong>Section 47(xiv):</strong> Income Tax Act provision providing capital gains exemption for transfer of capital assets by sole proprietary concern to a COMPANY (NOT LLP) subject to conditions; commonly misunderstood as extending to LLP.</p>

