            <div class="content-text">
                
                <div class="highlight-box" style="margin-bottom:20px;">
                    <p style="margin-bottom:0;"><strong>Any sole proprietor running an existing business in India is eligible to move to an LLP structure. The eligibility constraints sit on the LLP side and on the tax-planning side.</strong></p>
                </div>
                <ul>
                    <li><strong>Minimum Partners for LLP:</strong> at least two partners required under Section 6 of LLP Act 2008. The proprietor will be one designated partner; the second can be a family member (spouse, parent, adult child), trusted business associate or a corporate body.</li>
                    <li><strong>Designated Partner Residency:</strong> at least one designated partner must be resident in India for at least 120 days in the financial year under Section 7(1) of LLP Act 2008.</li>
                    <li><strong>Profit Sharing Ratio:</strong> the LLP Agreement must clearly set out the capital contribution and profit / loss sharing ratio. The proprietor typically retains majority share post conversion if family or token partner is added.</li>
                    <li><strong>Outstanding Liabilities and Litigation:</strong> outstanding loans, secured creditor relationships, pending tax notices and litigation must be disclosed in the BTA; secured creditor NoCs typically obtained before transfer.</li>
                    <li><strong>GST Registration Status:</strong> if the proprietor is GST-registered, the LLP must obtain fresh GST registration and the proprietor's GSTIN should be cancelled after ITC transfer via Form GST ITC-02 to avoid parallel-registration scrutiny.</li>
                    <li><strong>Business Licenses:</strong> Shop and Establishment under Haryana Shops and Commercial Establishments Act 1958, FSSAI, MSME / Udyam, IEC, Professional Tax and factory license (if applicable) need fresh applications in the LLP name.</li>
                </ul>
                <div class="highlight-box" style="margin-top:20px;background:var(--blue-lighter);border-left:4px solid var(--blue);">
                    <p style="margin-bottom:0;"><strong>Statutory Triggers:</strong> practitioners typically recommend moving from proprietorship to LLP when annual turnover crosses INR 50 lakh to INR 2 crore (audit threshold considerations), when the proprietor wants to onboard family or partners for succession planning, when limited liability becomes important due to vendor or customer scale, or when bank or institutional credit underwriting demands a corporate-form borrower.</p>
