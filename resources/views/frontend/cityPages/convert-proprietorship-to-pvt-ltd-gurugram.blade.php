        <div class="two-column">
            <div class="column-content">
                <h2 class="section-title">What Is Conversion of Proprietorship to Private Limited Company</h2>
                <div class="content-text what-is-definition">
                    
                    <p><strong>Conversion of proprietorship to Private Limited Company is the operational migration of a sole proprietary business into a newly incorporated company under the Companies Act 2013, structured to qualify for the capital gains exemption under Section 47(xiv) of the Income Tax Act 1961.</strong></p>
                    <p>It is NOT a statutory conversion under Section 366 of the Companies Act 2013 - that provision applies to registered entities (firms, LLPs, societies). A proprietorship is not a registered entity; it has no separate legal personality. Therefore the Pvt Ltd is incorporated as a fresh company via the SPICe+ integrated form on the MCA portal (Part A for name reservation, Part B for incorporation with AGILE-PRO-S for GSTIN, EPFO, ESIC, bank account and Professional Tax). The proprietorship business is then transferred to the newly incorporated company under a Business Transfer Agreement structured to satisfy Section 47(xiv).</p>
                    <p>Section 47(xiv) of the Income Tax Act 1961 provides that where a sole proprietary concern is succeeded by a company in the business carried on by it as a result of which the sole proprietary concern sells or otherwise transfers any capital asset or intangible asset to the company, the transfer is exempt from capital gains tax under Section 45 - subject to four cumulative conditions. The Pvt Ltd inherits depreciation history, accumulated losses (subject to Section 79) and goodwill. If any condition is breached within the 5-year lock period, Section 47A(3) deems the previously exempt gain to be income of the company chargeable in the year of breach. This is the key compliance discipline Patron embeds into the structuring memo.</p>
                    <div class="highlight-box" style="margin-top:20px;background:var(--orange-lighter);border-left:4px solid var(--orange);">
                        <p style="margin-bottom:0;"><strong>The four Section 47(xiv) conditions in plain language:</strong> (a) ALL business assets and liabilities transfer to the company; (b) the proprietor must hold AT LEAST 50 percent of total voting power post conversion; (c) this shareholding must be maintained for 5 YEARS from succession; (d) the proprietor receives ONLY shares as consideration (no cash, no debentures, no loan repayment, no benefits). Miss any one - even in year 4 - and Section 47A(3) charges the earlier exempt gain in the year of breach.</p>
                    </div>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Convert Proprietorship to Pvt Ltd:</strong></p>
                    <p><strong>Section 47(xiv):</strong> provision in the Income Tax Act 1961 granting capital gains exemption on transfer of business from sole proprietary concern to a company, subject to four conditions.</p>
                    <p><strong>Section 47A(3):</strong> provision triggering retrospective taxation if any Section 47(xiv) condition is breached during the 5-year lock period.</p>
                    <p><strong>SPICe+:</strong> Simplified Proforma for Incorporating Company Electronically Plus - the integrated MCA form bundling name reservation (Part A), incorporation (Part B), GSTIN, EPFO, ESIC, bank account and Professional Tax (via AGILE-PRO-S).</p>
                    <p><strong>AGILE-PRO-S:</strong> Application for GSTIN, EPFO, ESIC, Bank Account and Professional Tax via SPICe+ - completes registrations in one filing.</p>
                    <p><strong>Business Transfer Agreement (BTA):</strong> contractual instrument transferring the proprietorship business to the company, structured to satisfy Section 47(xiv) by exchanging the business for shares only.</p>
                    <p><strong>Form GST ITC-02:</strong> GST form filed by the proprietor to transfer unutilised input tax credit to the company when business is transferred as a going concern.</p>
                    <p><strong>Going Concern:</strong> business that is operational; transfer as a going concern is NIL rated under GST per Notification 12/2017-Central Tax (Rate) Sl. No. 2.</p>
                    <p><strong>50 Percent Voting Power Test:</strong> Section 47(xiv) requires the sole proprietor's shareholding in the company to be at least 50 percent of total voting power at the time of succession and continued for 5 years.</p>
                    <p><strong>5-Year Lock:</strong> the proprietor's 50 percent or higher shareholding must be maintained for 5 years from the date of succession; any dilution below 50 percent triggers Section 47A(3).</p>
                    <p><strong>Section 79:</strong> Income Tax Act 1961 limitation on carry-forward of losses where there is a change in beneficial shareholding of more than 49 percent in a private company.</p>

