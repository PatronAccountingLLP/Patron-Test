                        <div class="faq-expanded__a"><p>The fundamental difference is tax treatment. Proprietorship to Pvt Ltd benefits from Section 47(xiv) of Income Tax Act 1961 - capital gains can be exempt subject to the 4 conditions including the 5-year, 50 percent shareholding lock. Proprietorship to LLP has NO equivalent exemption - capital gains are taxable under Section 50B (slump sale) or itemised basis. Other differences: Pvt Ltd has 2 shareholders plus 2 directors and supports ESOPs and equity funding; LLP has 2 partners with light compliance but cannot issue shares. Patron fee for Pvt Ltd is INR 19,999 starting; LLP is INR 14,999 starting.</p></div>
