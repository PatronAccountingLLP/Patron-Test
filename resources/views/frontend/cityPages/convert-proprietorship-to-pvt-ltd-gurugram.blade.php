                    <div class="faq-expanded__item" id="faq-1">
                        <h3 class="faq-expanded__q">Can a proprietorship be converted to a Private Limited Company tax-free?</h3>
                        <div class="faq-expanded__a"><p>Yes, subject to conditions. Section 47(xiv) of the Income Tax Act 1961 grants full capital gains exemption on transfer of a sole proprietary concern's business to a company. Four conditions must be satisfied cumulatively: (a) all assets and liabilities relating to the business transfer to the company; (b) the proprietor holds at least 50 percent of total voting power in the company; (c) this shareholding is maintained for 5 years from succession; (d) consideration is only by way of shares - no cash or other benefit. Breach of any condition during the 5-year lock triggers Section 47A(3) retrospective taxation.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-2">
                        <h3 class="faq-expanded__q">Is proprietorship to Pvt Ltd a Section 366 conversion?</h3>
                        <div class="faq-expanded__a"><p>No. Section 366 of the Companies Act 2013 applies to registered entities (firms, LLPs, societies) seeking to register as companies. A proprietorship is not a registered entity - it has no separate legal personality. Therefore the Pvt Ltd is incorporated as a fresh company via the SPICe+ form (Part A for name reservation, Part B for incorporation with AGILE-PRO-S for GSTIN, EPFO, ESIC, bank account and Professional Tax). The proprietorship business is transferred separately to the new company through a Business Transfer Agreement structured to satisfy Section 47(xiv).</p></div>
                    </div>
