            <h2 class="section-title">Applicability and Eligibility</h2>
            <div class="content-text">
                
                <div class="highlight-box" style="margin-bottom:20px;">
                    <p style="margin-bottom:0;"><strong>Any sole proprietor running an existing business in India is eligible to move to a Pvt Ltd structure. The eligibility envelope is shaped by the Section 47(xiv) conditions and the Companies Act 2013 minimums.</strong></p>
                </div>
                <ul>
                    <li><strong>Minimum Shareholders for Pvt Ltd:</strong> at least 2 shareholders under Section 3(1)(b) Companies Act 2013. The proprietor will be one; the second can be a spouse, family member, adult child or a trusted business associate. To qualify under Section 47(xiv), the proprietor must hold at least 50 percent of total voting power - so the second shareholder takes at most 49 percent.</li>
                    <li><strong>Minimum Directors:</strong> 2 directors under Section 149(1) Companies Act 2013. At least one director must satisfy the Indian residency test of 120 days in the preceding financial year under Section 149(3).</li>
                    <li><strong>Section 47(xiv) Condition (a):</strong> all assets and liabilities of the proprietorship relating to the business must transfer to the company immediately before succession. Carving out specific assets (e.g. immovable property to avoid stamp duty) risks breaching this condition.</li>
                    <li><strong>Section 47(xiv) Condition (b):</strong> proprietor's shareholding in the company shall not be less than 50 percent of the total voting power. Combine the proprietor's individual holding plus any family entity holdings carefully to meet this threshold.</li>
                    <li><strong>Section 47(xiv) Condition (c):</strong> the 50 percent voting power must continue for 5 years from succession. ESOP grants, external fundraising, or share transfers in the first 5 years risk breach.</li>
                    <li><strong>Section 47(xiv) Condition (d):</strong> consideration only by way of shares in the company. Cash payouts, debentures, loan repayments, or other benefits to the proprietor in any form trigger condition failure.</li>
                    <li><strong>Restricted Profiles:</strong> disqualified directors under Section 164; persons under ED, CBI, or SFIO investigation; loan defaulters with bank NPA classification cannot serve as directors.</li>
                </ul>
