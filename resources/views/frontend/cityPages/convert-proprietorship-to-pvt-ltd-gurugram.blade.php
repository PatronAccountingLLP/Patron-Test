                    <div class="faq-expanded__item" id="faq-4">
                        <h3 class="faq-expanded__q">What happens if I dilute below 50 percent shareholding in the 5-year lock period?</h3>
                        <div class="faq-expanded__a"><p>Section 47A(3) of the Income Tax Act 1961 triggers. The amount of profits or gains that were exempt earlier under Section 47(xiv) become deemed income of the company chargeable to tax in the year of the breach. Interest and penalty under Sections 234B and 234C may also apply. Common scenarios that cause breach include ESOP grants that dilute the founder, external funding rounds, voluntary share transfers and inheritance events. Patron builds a 5-year compliance calendar at the conversion stage to flag any planned dilution against the threshold.</p></div>
