                <h2 class="section-title">What Is Conversion of Public Limited Company to Private Limited Company</h2>
                <div class="content-text what-is-definition">
                    
                    <p><strong>Conversion of Public Limited Company to Private Limited Company is the alteration of the company's Articles of Association under Section 14 of the Companies Act 2013 to incorporate the three Section 2(68) restrictions that define a Private Limited Company - restriction on transfer of shares, limit of 200 members and prohibition on inviting the public to subscribe.</strong></p>
                    <p>The conversion is supervised by the Regional Director under Rule 41 of the Companies (Incorporation) Rules 2014 (inserted by the Fourth Amendment Rules 2018 effective 18 December 2018, with the power simultaneously delegated to the Regional Director via Notification S.O. 6225(E) dated 18 December 2018 read with the Companies (Amendment) Second Ordinance 2019 effective 2 November 2018). Earlier the National Company Law Tribunal had jurisdiction; the shift was made to reduce timelines and ease the operational burden for smaller unlisted public companies.</p>
                    <p>Listed Public Limited Companies cannot use this RD route directly. Listed shares must first be delisted under the SEBI (Delisting of Equity Shares) Regulations 2021 - which involves a reverse book-building process, exit price discovery and stock exchange approval - before the company qualifies for the unlisted public-to-private conversion. Patron handles both legs where required.</p>
                    <p>Post conversion, the company's name changes from "&lt;Company Name&gt; Limited" to "&lt;Company Name&gt; Private Limited". The Memorandum of Association name clause is altered accordingly. The Articles of Association are amended to embed Section 2(68) restrictions. The PAN, TAN and GSTIN of the company are unaffected - the legal entity is the same; only its character changes from public to private. CIN format updates from "U/L....PLC" to "U/L....PTC" reflecting the private company classification.</p>
                    <div class="highlight-box" style="margin-top:20px;background:var(--orange-lighter);border-left:4px solid var(--orange);">
                        <p style="margin-bottom:0;"><strong>The 2018 jurisdictional shift in plain terms:</strong> before 18 December 2018, this conversion took 9 to 18 months via NCLT. After the delegation to the Regional Director, the same conversion clears in 75 to 120 days - but the catch is a strict 21-day pre-filing newspaper objection window plus individual notices to every creditor and debenture holder. Miss the timing by even a day and the RD-1 gets rejected.</p>
                    </div>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Convert Public Ltd to Pvt Ltd:</strong></p>
                    <p><strong>Section 14:</strong> provision in the Companies Act 2013 governing alteration of Articles of Association; sub-section (1) proviso and sub-section (2) deal specifically with conversion of a public company into a private company.</p>
                    <p><strong>Section 2(68):</strong> defines Private Limited Company - restriction on transfer of shares, maximum 200 members (excluding employees and ex-employee shareholders) and prohibition on inviting the public to subscribe to securities.</p>
                    <p><strong>Rule 41:</strong> Rule 41 of the Companies (Incorporation) Rules 2014 (inserted by Fourth Amendment Rules 2018) prescribes the operational procedure for conversion of public to private company.</p>
                    <p><strong>Regional Director:</strong> officer of the Ministry of Corporate Affairs supervising regional ROC offices; for Gurugram-registered companies, the Northern Region RD based in New Delhi has jurisdiction.</p>
                    <p><strong>Form RD-1:</strong> e-form filed at the MCA portal applying to the Regional Director for approval of conversion. Filed within 60 days of the special resolution.</p>
                    <p><strong>Form MGT-14:</strong> e-form filed with the Registrar of Companies under Section 117, attaching the special resolution and altered Articles. Filed within 30 days of the special resolution.</p>
                    <p><strong>Form INC-28:</strong> e-form filed with the Registrar of Companies notifying the RD order. Filed within 15 days of the RD order.</p>
                    <p><strong>SEBI Delisting Regulations 2021:</strong> SEBI (Delisting of Equity Shares) Regulations 2021 - replaced the 2009 Regulations; prescribes the reverse book-building process for delisting equity shares from stock exchanges before any private-status conversion can proceed.</p>
                    <p><strong>Special Resolution:</strong> resolution passed by 75 percent affirmative vote of members present and voting at a general meeting (Section 114(2)).</p>
                    <p><strong>Reverse Book Building:</strong> SEBI-prescribed price discovery mechanism for delisting where public shareholders bid the price at which they would be willing to tender their shares to the promoter.</p>

