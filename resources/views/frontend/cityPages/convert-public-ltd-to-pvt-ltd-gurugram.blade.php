            <h2 class="section-title">Applicability and Eligibility</h2>
            <div class="content-text">
                
                <div class="highlight-box" style="margin-bottom:20px;">
                    <p style="margin-bottom:0;"><strong>Any unlisted Public Limited Company registered under the Companies Act 2013 (or the predecessor Companies Act 1956) is eligible for conversion to a Private Limited Company under Section 14 read with Rule 41, subject to several disqualifying conditions.</strong></p>
                </div>
                <ul>
                    <li><strong>Listed Status Bar:</strong> companies whose shares are listed on a recognised stock exchange must first delist under SEBI (Delisting of Equity Shares) Regulations 2021. Delisting involves reverse book-building, exit price discovery and stock exchange in-principle and final approvals.</li>
                    <li><strong>Maximum Members Test:</strong> at the time of RD-1 filing and post conversion, the company must have or be able to limit to a maximum of 200 members (excluding present employees and former employees who hold shares acquired during employment) under Section 2(68).</li>
                    <li><strong>No Pending Default:</strong> the company must have no unsettled default in repayment of deposits or interest on deposits (Section 73 to 76A); no inability to satisfy creditors as evidenced by the list of creditors filed with RD-1.</li>
                    <li><strong>No Ongoing Investigation:</strong> no inspection, inquiry or investigation under Sections 206, 207, 208, 210, 213 or 219 to 229 of the Companies Act 2013 is pending at the time of RD-1 filing.</li>
                    <li><strong>No IBC Proceedings:</strong> no pending insolvency or bankruptcy proceedings under the Insolvency and Bankruptcy Code 2016.</li>
                    <li><strong>No Section 73-76A, 177, 178, 185, 186, 188 Default:</strong> the affidavit attached to RD-1 must declare no non-compliance under these key provisions covering deposits, audit committee, nomination and remuneration committee, related party loans, related party investments and related party transactions.</li>
                    <li><strong>Members and Directors Bandwidth:</strong> at minimum 2 members and 2 directors post conversion (Section 3(1)(b) and Section 149(1)). A Public Limited Company with 7+ members and 3+ directors typically needs to reduce excess members through share purchase or transfer before or after conversion.</li>
                </ul>
                <div class="highlight-box" style="margin-top:20px;background:var(--blue-lighter);border-left:4px solid var(--blue);">
                    <p style="margin-bottom:0;"><strong>Statutory Triggers:</strong> practitioners typically recommend this conversion when the IPO plan has been shelved, when the promoter family wants to consolidate ownership and remove free transferability, when listing-grade compliance overhead (Section 177 audit committee, Section 178 nomination and remuneration committee, Section 188 related party rigour, AGM mechanics) is disproportionate to the business scale, or when the company prepares for a strategic sale that benefits from a simpler corporate structure.</p>
