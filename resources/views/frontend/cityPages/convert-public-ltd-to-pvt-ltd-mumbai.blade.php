                <h2 class="section-title">What Public Ltd to Pvt Ltd Conversion Actually Means</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p><strong>Public Limited to Private Limited conversion</strong> is the statutory process under Section 14 of the Companies Act, 2013 by which a Public Limited Company alters its Articles of Association (and Memorandum where required) to assume the restrictions of a Private Limited Company, after Special Resolution of members and approval from the Regional Director.</p>
                    <p>The company's <strong>legal identity (CIN, PAN, TAN, contracts and assets) continues</strong>; only the class of company changes. The name updates from ending in "Limited" to ending in "Private Limited".</p>
                    <p>The restrictions added to AoA: share transfer restriction, maximum 200 members, prohibition on public invitation for shares or debentures. These are the Section 2(68) clauses that distinguish a Private Limited from a Public Limited.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Convert Public Ltd to Pvt Ltd:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>Section 14:</strong> Companies Act 2013 provision permitting alteration of Articles by special resolution; conversion of public to private requires Central Government (delegated to RD) approval.</li>
                        <li><strong>Rule 41:</strong> Companies (Incorporation) Rules, 2014 - prescribes the procedure, attachments and forms for the conversion application. Inserted by the Fourth Amendment Rules, 2018.</li>
                        <li><strong>Regional Director (RD):</strong> MCA officer empowered to approve public-to-private conversions since 18 December 2018. For Mumbai-registered companies, the RD (Western Region), Mumbai office is jurisdictional.</li>
                        <li><strong>Form MGT-14:</strong> Filing of resolutions and agreements with RoC; must be filed within 30 days of passing the Special Resolution.</li>
                        <li><strong>Form INC-25A:</strong> Public notice (advertisement) in two newspapers - one English, one vernacular (Marathi in Mumbai) - at least 21 days BEFORE filing RD-1.</li>
                        <li><strong>Form RD-1:</strong> Application to the Regional Director; must be filed within 60 days of passing the Special Resolution.</li>
                        <li><strong>Form INC-27:</strong> Notice of conversion filed with RoC within 15 days of RD order, accompanied by altered e-MoA and e-AoA.</li>
                        <li><strong>Form INC-28:</strong> Intimation of RD order to RoC; filed alongside INC-27.</li>
                        <li><strong>Section 2(68):</strong> Companies Act 2013 - defines Private Company: restricts share transfer, limits members to 200, prohibits public invitation for shares or debentures.</li>
