            <div class="column-content">
                <h2 class="section-title">What Is Conversion of Private Limited Company to Public Limited Company</h2>
                <div class="content-text what-is-definition">
                    
                    <p><strong>Conversion of Private Limited Company to Public Limited Company is the alteration of the company's Articles of Association under Section 14 of the Companies Act 2013 to remove the three Section 2(68) restrictions that define a Private Limited Company - the restriction on transfer of shares, the 200-member cap and the prohibition on inviting public subscription.</strong></p>
<p>The procedural framework is Section 13 of the Companies Act 2013 (for altering the name clause of the Memorandum of Association from Private Limited to Limited), Section 14 (for altering the Articles), and Section 18 (which authorises conversion from one class of company to another), read with Rule 33 of the Companies (Incorporation) Rules 2014. Critically, this direction does NOT require Regional Director or NCLT approval - the Registrar of Companies has direct jurisdiction. Form MGT-14 is filed within 30 days of the special resolution and Form INC-27 is the conversion application filed with the ROC.</p>
<p>Post conversion, the company must satisfy three structural minimums - at least 7 members (Section 3(1)(a)), at least 3 directors (Section 149(1)(a)) and at least one director resident in India for at least 120 days in the preceding financial year (Section 149(3)). The name changes from "&lt;Company Name&gt; Private Limited" to "&lt;Company Name&gt; Limited". The CIN classification updates from PTC (Private Limited Company) to PLC (Public Limited Company). The PAN remains unchanged - the legal entity is the same, only its character escalates from private to public. GSTIN amendment via Form GST REG-14 reflects the name change.</p>
<p>The real cost of conversion is the governance escalation that follows. Audit Committee under Section 177 becomes mandatory above the threshold (paid-up capital INR 10 crore or turnover INR 100 crore or outstanding debt INR 50 crore). Nomination and Remuneration Committee under Section 178 follows the same threshold. Vigil Mechanism under Section 177(9). Section 204 Secretarial Audit kicks in above INR 50 crore paid-up capital or INR 250 crore turnover. Section 135 CSR Committee applies above net worth INR 500 crore or turnover INR 1000 crore or net profit INR 5 crore.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Pvt Ltd to Public Ltd Conversion:</strong></p>
                    <ul>
<li><strong>Section 13:</strong> provision governing alteration of Memorandum of Association including the name clause change.</li>
<li><strong>Section 14:</strong> provision governing alteration of Articles of Association including conversion of private to public (and vice versa).</li>
<li><strong>Section 18:</strong> provision authorising conversion of a company from one class to another.</li>
<li><strong>Rule 33:</strong> Rule 33 of the Companies (Incorporation) Rules 2014 prescribing Form INC-27 for both directions of private-public conversion.</li>
<li><strong>Form MGT-14:</strong> e-form filed under Section 117 attaching the special resolution and altered MoA and AoA; within 30 days of special resolution.</li>
<li><strong>Form INC-27:</strong> e-form filed with the Registrar of Companies as the conversion application under Rule 33.</li>
<li><strong>Audit Committee:</strong> Section 177 Companies Act 2013 - minimum 3 directors with majority independent; reviews financial statements, internal controls and auditor engagement.</li>
<li><strong>Nomination and Remuneration Committee (NRC):</strong> Section 178 - identifies directors and KMP, recommends remuneration policy.</li>
<li><strong>Vigil Mechanism:</strong> Section 177(9) - whistleblower channel for directors and employees.</li>
<li><strong>SEBI ICDR Regulations 2018:</strong> SEBI (Issue of Capital and Disclosure Requirements) Regulations 2018 - prescribes the IPO process for listing on a stock exchange; separate from the Section 14 conversion.</li>
</ul>

