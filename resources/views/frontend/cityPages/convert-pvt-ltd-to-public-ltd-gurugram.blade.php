        <div class="text-content">
            <h2 class="section-title">Applicability and Eligibility for Pvt Ltd to Public Ltd Conversion</h2>
            <div class="content-text">
                
                <p><strong>Any Private Limited Company registered under the Companies Act 2013 (or the predecessor Companies Act 1956) is eligible for conversion to a Public Limited Company under Section 14 read with Section 18. There are no statutory bars in the Companies Act for this direction - the constraints are practical and structural.</strong></p>
<ul>
<li><strong>Minimum 7 Members:</strong> at the time of INC-27 filing, the company must have or be able to attain 7 or more members under Section 3(1)(a). If the company has fewer than 7 members, it must allot fresh shares (via Form PAS-3) or transfer existing shares (via share transfer forms) before INC-27 filing.</li>
<li><strong>Minimum 3 Directors:</strong> at the time of INC-27 filing, the Board must comprise at least 3 directors under Section 149(1)(a). If the company has only 2 directors, appoint a third director via Form DIR-12 before INC-27.</li>
<li><strong>Resident Director:</strong> at least one director must satisfy Section 149(3) - resident in India for at least 120 days in the preceding financial year.</li>
<li><strong>Compliance Current:</strong> all annual returns (Form MGT-7) and financial statements (Form AOC-4) must be filed with the ROC; income tax returns and GST returns up to date.</li>
<li><strong>Authorised Capital Sufficiency:</strong> if the conversion is in anticipation of IPO or institutional fundraise, the authorised share capital may need an increase via Form SH-7 under Section 61; budget for Haryana stamp duty on capital increase.</li>
<li><strong>No Director Disqualification:</strong> all directors should be free from disqualification under Section 164 - which includes non-filing of returns by other companies they direct, conviction or default in deposit repayment.</li>
</ul>
<p>Patron Accounting runs a pre-conversion readiness audit before any filing - we verify member count, director count, ROC compliance status, Section 164 disqualification flags and authorised capital headroom. If any gap is found, we sequence the top-up filings (PAS-3 for shares, DIR-12 for directors, SH-7 for authorised capital, MGT-7/AOC-4 for backlog) BEFORE the EGM so the special resolution itself is valid on day one.</p>

