<section class="content-section">
    <div class="content-container">
        <div class="text-content">
            <h2 class="section-title">Legal Framework for Literary Work Copyright</h2>
            <div class="content-text">
                
                <p><strong>Section 2(o) - Literary Work:</strong></p>
                <ul><li>Includes computer programmes, tables, compilations including databases. Broadest category. No literary merit required.</li></ul>
                <p style="margin-top:16px;"><strong>Section 14(a) vs 14(b) - Rights:</strong></p>
                <ul><li><strong>S.14(a):</strong> Non-software: reproduce, issue copies, perform, communicate, make film/recording, translate, adapt.</li>
                    <li><strong>S.14(b):</strong> Software: ALL S.14(a) + commercial rental restriction. Cannot rent software without permission.</li></ul>
                <p style="margin-top:16px;"><strong>Section 17 - Ownership:</strong></p>
                <ul><li>Author (general). Employer S.17(c) for service works. Publisher S.17(a) for periodicals. Freelancer retains unless assigned. Computer-generated: person causing creation.</li></ul>
                <p style="margin-top:16px;"><strong>Section 22 - Duration:</strong> Lifetime + 60 years. Computer-generated: 60 years from creation.</p>
