                    <p><strong>Key Terms for Ecommerce Business Registration:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>Electronic Commerce Operator (ECO):</strong> Section 2(45) of the CGST Act, 2017 - any person who owns, operates or manages a digital or electronic facility or platform for electronic commerce (Amazon, Flipkart, Meesho, Zomato, Swiggy, Nykaa, Myntra are ECOs).</li>
