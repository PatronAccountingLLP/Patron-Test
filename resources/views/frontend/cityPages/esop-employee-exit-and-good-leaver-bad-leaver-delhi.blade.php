                <div class="content-text what-is-definition">
                    
                    <p>Almost every ESOP scheme run by a Delhi company splits departing employees into two categories, and the category decides what happens to their options. For a Nehru Place IT manager moving firms or a Connaught Place finance professional exiting on mutual terms, the line is drawn by how, and why, the person leaves.</p>
