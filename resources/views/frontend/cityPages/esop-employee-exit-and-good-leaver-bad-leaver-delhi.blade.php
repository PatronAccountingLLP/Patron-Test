            <h2 class="section-title">How an Exit Exercise Is Taxed in Delhi</h2>
            <div class="content-text">
                
                <ul>
                    <li><strong>At vesting:</strong> no tax; vesting alone is not a taxable event.</li>
                    <li><strong>On exercise:</strong> the spread between fair market value and the exercise price is taxed as a salary perquisite.</li>
                    <li><strong>On sale or buyback:</strong> the gain over the value taxed at exercise is taxed as capital gains.</li>
                    <li><strong>The cash crunch:</strong> the perquisite tax falls due on exercise, often before any liquidity, which is the core exit problem.</li>
                </ul>
                <p><strong>Delhi example.</strong> A long-serving manager exiting a Connaught Place-headquartered private company on mutual separation may hold vested options under a scheme drafted years ago that is silent on the exercise window. On exercise the FMV-minus-strike spread is taxed as a perquisite at once, and without a clear window or a buyback the manager can lose options they have earned. Because Delhi schemes are often older and governance-led, we first fix the leaver definition and window, then sequence the exercise, buyback and perquisite tax so the exit is both fair and defensible.</p>
