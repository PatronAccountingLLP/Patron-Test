<section class="content-section" id="what-section">
    <div class="content-container">
        <div class="two-column">
            <div class="column-content">
                <h2 class="section-title">What Makes Deeptech ESOPs Different</h2>
                <div class="content-text what-is-definition">
                    
                    <p>Mumbai sits unusually close to the machinery that governs equity - SEBI's headquarters is at BKC, and the city's deeptech grows out of an IIT-Bombay research base in Powai alongside a fintech-and-finance core in BKC and Lower Parel. That proximity raises the bar on how defensibly an ESOP is structured. Four things separate a deeptech scheme from an ordinary tech one.</p>
                    <p><strong>Begin with valuation discipline.</strong> Because pre-revenue research companies must justify FMV to investors and counsel who know SEBI norms cold, the <a href="/esop-valuation-services/mumbai">Rule 11UA methodology choice</a> matters more in Mumbai than almost anywhere - DCF defensibility is scrutinised, not assumed.</p>
                    <p><strong>Then the extended clock.</strong> Foundation models, drug candidates, semiconductor designs and quantum systems run 3 to 7 years lab-to-product, pushing vesting to 5 or 6 years versus the usual 4.</p>
                    <p><strong>Next, the milestones themselves.</strong> Papers accepted at NeurIPS, patents granted by USPTO, model benchmarks crossed (loss thresholds, eval scores) and tapeouts completed replace revenue and DAU as the vesting triggers, while IP-creating inventors and principal investigators receive a separate carve-out reflecting their patent contribution.</p>
                    <p><strong>Finally, the recruiting maths.</strong> An Andheri or Powai deeptech firm competes with BKC fintech salaries, so the ESOP plus <a href="/esop-startup-tds-deferral-section-192-1c/mumbai">Section 80-IAC tax deferral</a> - not cash - has to carry the offer.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Deeptech ESOP Design:</strong></p>
                    <p><strong>Research Milestone Vesting:</strong> Hybrid vesting schedule combining time-based and milestone-based components. Milestones include papers accepted at peer-reviewed venues, patents granted, model benchmarks crossed, prototype demonstrations and tapeouts completed.</p>
                    <p><strong>IP-Creator Pool:</strong> Separate equity carve-out (2 to 5 percent) for inventors, principal investigators and PhD researchers. Eligibility tied to patent filings, paper authorship, grant principal investigator status or lead designer role on tapeouts.</p>
                    <p><strong>Section 80-IAC Deferral:</strong> Income Tax Act 1961 provision allowing DPIIT-recognised and IMB-certified eligible startups to defer <a href="/esop-perquisite-tax-section-17-2-vi/mumbai">perquisite tax at ESOP exercise</a> for up to 48 months (60 months under ITA 2025 from 1 April 2026).</p>
