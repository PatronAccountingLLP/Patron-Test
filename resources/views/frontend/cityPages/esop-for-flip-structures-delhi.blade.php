                <h2 class="section-title">Forward Flip vs Reverse Flip - Decision Framework</h2>
                <div class="content-text what-is-definition">
                    
                    <p>Delhi runs the flip from the policy end of the table. The MCA is headquartered here, RoC Delhi sits in the same ecosystem, and the NCLT Principal Bench - where many reverse-flip schemes are heard - is in the capital. So when a Connaught Place product company or a Nehru Place trading-and-tech business weighs a flip, the conversation is as much about where the file will be approved as where the cheque comes from. There is also a Delhi-specific wrinkle: a deep NRI investor base, which changes the <a href="/esop-fema-and-rbi-firms-reporting/delhi">FEMA and residency analysis</a> on both legs. Forward flip and reverse flip are two distinct ESOP problems, and Delhi founders tend to meet both because they sit close to the regulator that signs off.</p>
                    <p><strong>Forward flip, and why (2018-2022):</strong> the growth round was led by a US fund that contracts on Delaware paper; SAFEs and convertible notes are well-settled under US law; a NASDAQ or NYSE exit is cleaner from a Delaware parent; global customers want a US entity to sign with; US-resident founders pursue Section 1202 QSBS; and US acquirers read a Delaware cap table at a glance.</p>
                    <p><strong>Reverse flip, and why (2023-2026):</strong> BSE and NSE have opened to new-economy issuers; Finance Act 2024 scrapped the Section 56(2)(viib) angel tax; Section 80-IAC benefits persist; with the MCA, DPIIT, IMB and FEMA OI Rules 2022 all administered from the capital, the home framework has matured; domestic funds now write US-sized cheques; and US 409A plus quarterly US board reporting outlives its usefulness at scale.</p>
                    <p><strong>The precedents that anchor the decision:</strong> PhonePe reverse-flipped from Singapore in 2023; Groww completed Delaware-to-India in 2024 and paid Rs 1,340 crore in tax; Razorpay, Pixxel, Meesho and Eruditus are underway. For a Delhi company with <a href="/esop-for-nri-and-non-resident-employees/delhi">NRI shareholders</a> on the register, those cases are studied closely because the residency mix can swing the tax answer.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>The terms a Delhi founder should walk into the MCA-side conversation already knowing:</strong></p>
                    <p><strong>Forward Flip (externalisation):</strong> the Indian-origin company moves its holdco abroad - Delaware C-Corp for US-VC-backed product and tech, Singapore Pte Ltd for APAC distribution, Cayman for funds and IP holdcos - reducing the Delhi company to a wholly-owned services subsidiary.</p>
                    <p><strong>Reverse Flip (re-domiciliation to India):</strong> the foreign parent migrates home through an NCLT-approved scheme of arrangement under Sections 230-232 plus Section 234 of the Companies Act 2013 - often heard at the NCLT Principal Bench - with a Delhi NewCo or the existing sub becoming the new parent.</p>
                    <p><strong>Mirror Grant:</strong> options the foreign parent grants directly to Delhi-sub employees; the instrument is foreign parent equity, not an Indian Section 62(1)(b) ESOP.</p>
                    <p><strong>FEMA OI Rules 2022:</strong> the Overseas Investment Rules of 22 August 2022 governing resident holdings of foreign equity - OPI up to 10 percent of parent equity per individual under Rule 7, ODI above under Rule 9. For NRI-heavy Delhi cap tables, residency status decides who this even applies to.</p>
                    <p><strong>LRS cap of USD 250,000:</strong> the Liberalised Remittance Scheme limit per financial year per resident individual under Section 5 of FEMA 1999, relevant to <a href="/esop-exercise-and-cap-table-management/delhi">cash-exercise remittances</a>.</p>
                    <p><strong>Section 47 pathway:</strong> the Income Tax Act 1961 <a href="/esop-capital-gains-tax-on-sale/delhi">capital-gains exemption route</a> on reverse flip - 47(via) for foreign amalgamating-company shares, 47(vii) for the in-scheme share transfer, 47(vicc) for the merger of a foreign holding into the Indian subsidiary, with 49(2AA) preserving the original cost basis.</p>

