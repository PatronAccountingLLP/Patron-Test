            <h2 class="section-title">Section 47 Tax Treatment - Reverse Flip Framework</h2>
            <div class="content-text">
                
                <p>Section 47 of the Income Tax Act 1961 carries the reverse flip, and for a Delhi company it carries an extra layer: a mixed register of resident and NRI shareholders. The section can render the share cancellation a non-transfer for founders, employees and investors - but only where the conditions are met, and the residency of each holder feeds directly into that test. Groww's 2024 migration is the number to remember: Rs 1,340 crore in tax, because particular shareholder positions fell outside Section 47. Patron runs every Delhi shareholder class, resident and non-resident alike, against the section before drafting so the exposure is sized before anything reaches the Principal Bench. The class-by-class position follows.</p>
                <ul>
