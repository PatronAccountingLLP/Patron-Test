                <h2 class="section-title">What Makes This the Hardest ESOP Scenario</h2>
                <div class="content-text what-is-definition">
                    
                    <p>Delhi sits at the centre of India's corporate machinery - the MCA headquarters and RoC Delhi are here, and the city's consumer-tech and product subsidiaries file under their watch. It is also home to a large NRI investor base whose residency status complicates Schedule FA in ways a pure-employee population never sees. A domestic ESOP is one company, one statute. A Saket consumer-tech subsidiary vesting RSUs from a US-listed parent must instead manage five technical layers at once.</p>
                    <p><strong>One - the perquisite arises in the Delhi entity.</strong> The parent issues the shares; Section 17(2)(vi) still places the taxable perquisite in the subsidiary's payroll.</p>
                    <p><strong>Two - the Delhi subsidiary is the deductor.</strong> Section 192(1) makes the Indian employer withhold on a non-cash, foreign-currency benefit.</p>
                    <p><strong>Three - four schedules in the ITR.</strong> Schedule FA, FSI, TR and Form 67 each have to be reconciled - and residency status (RNOR vs ROR) decides how much of this even applies, a live question for Delhi's NRI-returnee workforce.</p>
                    <p><strong>Four - DTAA and Foreign Tax Credit.</strong> Sections 90/91 read with Rule 128 govern recovery of foreign withholding.</p>
                    <p><strong>Five - the transfer pricing recharge.</strong> Section 92 requires the parent's equity cost to be recharged to the Delhi entity and documented.</p>
                    <p>Country overlays (US 409A, UK CSOP/EMI, Singapore IRAS, Israel Section 102 trustee route) sit above all five, and for Connaught Place trading and finance parents the capital-gains and dividend articles of each treaty get worked hard.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Foreign Parent ESOP and RSU:</strong></p>
                    <p><strong>RSU (Restricted Stock Unit):</strong> Grant of shares that vests over time; no exercise price; full FMV is the perquisite at vesting. Standard for mature MNCs like Microsoft, Adobe, Salesforce, Cisco, Google.</p>
                    <p><strong>ESOP (Employee Stock Option Plan):</strong> Right to buy shares at a predetermined exercise price; perquisite is FMV minus exercise price at exercise. More common at growth-stage and pre-IPO MNCs.</p>
                    <p><strong>ESPP (Employee Stock Purchase Plan):</strong> Periodic purchases of parent stock at discount (typically 5 to 15 percent); discount is taxable perquisite at the purchase date.</p>
                    <p><strong>SAR (Stock Appreciation Right):</strong> Cash-settled right paying the appreciation in parent share price; treated as a cash bonus, fully taxable as salary at payment.</p>
                    <p><strong>Sell-to-Cover:</strong> Default mechanism at vesting where a portion of vested shares is automatically sold by the broker to fund the TDS liability deposited by the Indian subsidiary.</p>
                    <p><strong>Transfer Pricing Recharge:</strong> Cross-border invoice from the foreign parent to the Indian subsidiary for the equity compensation cost attributable to Indian employees; required under Section 92 read with OECD transfer pricing principles.</p>
                    <p><strong>W-8BEN:</strong> US tax form filed by Indian residents with the US broker (e.g. E-Trade, Fidelity, Morgan Stanley StockPlan Connect) to claim DTAA-reduced withholding rates on US dividends and avoid US capital gains withholding.</p>
                    <p><strong>FTC (Foreign Tax Credit):</strong> Credit claimed in Delhi under Section 90 read with Rule 128 for taxes paid in the foreign country on the same income; claimed via Form 67 plus Schedule TR in ITR.</p>

