            <div class="content-text">
                
                <p>A grant held by a Nehru Place product manager or a returning NRI in a Saket consumer-tech firm resolves into three Indian tax events once mapped to the calendar - each with a statutory anchor, a withholding mechanic and a reporting obligation. In Delhi the residency question (RNOR vs ROR) comes first, because it decides whether Schedule FA even bites; after that, the two recurring failures are Schedule FA non-disclosure (Black Money Act exposure) and Form 67 filed late (Foreign Tax Credit denial). The three-stage cycle below is what we wire into payroll and the ITR.</p>
                <ul>
                    <li><strong>Stage 1 - At Grant:</strong> No Indian tax event. No perquisite arises until vesting. RSU/ESOP grant date documented in employee records and HR systems. FEMA OI classification (OPI/ODI) determined based on the percentage of parent equity held.</li>
                    <li><strong>Stage 2 - At Vesting (RSU) or Exercise (ESOP):</strong> Section 17(2)(vi) perquisite arises. FMV computed under Rule 3(8)(ii) as the average of opening and closing price on vesting date, converted to INR at SBI TTBR. Indian subsidiary deducts TDS under Section 192(1). Sell-to-cover broker mechanism funds the TDS. Form 16 and Form 12BA disclose the perquisite to the employee.</li>
                    <li><strong>Stage 3 - At Sale of Shares:</strong> Capital gains under Section 112 (post Finance Act 2024). Cost of acquisition under Section 49(2AA) equals FMV taxed at vesting. STCG at slab rate if shares held under 24 months; LTCG at 12.5 percent if held over 24 months. Foreign country withholding (if any) reported via Schedule FSI and Form 67 for Foreign Tax Credit claim.</li>
                    <li><strong>Schedule FA Disclosure (Annual):</strong> Mandatory for Resident and Ordinarily Resident (ROR) employees. Foreign equity holdings reported in Table A3 with initial value (FMV at vesting), peak value during FY and closing balance. Non-disclosure attracts Black Money Act penalties.</li>
                    <li><strong>FEMA OI Rules 2022 Reporting (Annual):</strong> Form FLA filing by 15 July for ODI holdings; OPI reporting for holdings up to 10 percent of parent equity. RBI compounding penalties for default range Rs 50,000 to Rs 5,00,000 per instance per employee.</li>
