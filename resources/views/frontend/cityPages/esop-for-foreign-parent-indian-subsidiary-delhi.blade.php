            <h2 class="section-title">Worked Example - US RSU to Indian Employee</h2>
            <div class="content-text">
                
                <p>Take a product manager at a Saket consumer-tech subsidiary of a US-listed parent - a common Delhi profile, often a returning NRI. The working below follows one RSU grant from vest to sale (assuming the employee is ROR for the year). Figures are illustrative; the Rule 3(8)(ii) method applies whatever the parent jurisdiction.</p>
                <ul>
                    <li><strong>Grant Facts:</strong> 100 RSUs granted on 1 April 2024. Vesting schedule: 25 RSUs per year over 4 years on grant anniversary. First vesting date: 1 April 2025 (25 RSUs).</li>
                    <li><strong>Stage 1 - At Grant:</strong> No Indian tax event. Grant date documented. FEMA OI classification deferred until vesting.</li>
                    <li><strong>Stage 2 - At Vesting:</strong> Parent listed price on vesting date - open USD 200, close USD 210. FMV per share under Rule 3(8)(ii) = USD 205 (average). SBI TTBR USD/INR on vesting date = Rs 85.00. FMV per share in INR = Rs 17,425. Total perquisite (25 RSUs) = Rs 4,35,625. Added to salary in payroll cycle.</li>
                    <li><strong>TDS Computation:</strong> Employee in 30 percent slab plus 4 percent cess = 31.2 percent effective rate. TDS under Section 192 = Rs 4,35,625 multiplied by 31.2 percent = Rs 1,35,915.</li>
                    <li><strong>Sell-to-Cover Mechanism:</strong> Broker sells 8 shares (rounded up from 7.80 shares calculated as Rs 1,35,915 divided by Rs 17,425 per share). Indian subsidiary deposits TDS. Employee retains 17 shares.</li>
                    <li><strong>Stage 3 - At Sale (1 May 2026, 13 months hold = STCG):</strong> Sale price USD 250 multiplied by Rs 86 TTBR = Rs 21,500 per share. Sale value 17 shares = Rs 3,65,500. Cost of acquisition under Section 49(2AA) = 17 multiplied by Rs 17,425 = Rs 2,96,225. STCG = Rs 69,275. Tax at 30 percent slab plus cess = Rs 21,614.</li>
                    <li><strong>Schedule FA Disclosure (assuming ROR):</strong> Initial value Rs 2,96,225 (FMV at vesting for 17 shares). Peak value during FY = highest INR-converted value during the year. Reported in Schedule FA Table A3.</li>
                    <li><strong>Form 67 FTC Considerations:</strong> W-8BEN filed by employee with US broker. No US federal tax withheld on capital gains. No FTC claim required on this transaction. Had US tax been withheld, Form 67 would be filed before ITR submission with proof of US tax paid; FTC capped at lower of US tax paid or Indian tax payable on the same gain.</li>
