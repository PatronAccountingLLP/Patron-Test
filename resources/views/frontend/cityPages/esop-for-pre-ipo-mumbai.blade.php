                <h2 class="section-title">SEBI SBEB 2021 for Mumbai Pre-IPO Issuers - The Listed Entity Framework</h2>
                <div class="content-text what-is-definition">
                    
                    <p>For a Mumbai issuer, SBEB 2021 is the code SEBI will hold your DRHP against - and it is enforced from the regulator's own headquarters in the Bandra Kurla Complex. Notified on 13 August 2021, it replaced the 2014 Regulations and folded the entire listed-entity ESOP regime into one rulebook that binds every listed company and every company proposing to list.</p>
                    <p>The conversion itself has to be done before the DRHP is filed. SBEB 2021 recognises six instrument types - ESOS, ESPS, RSU, SAR, GEBS and RBS - and the mix that turns up on a Mumbai cap table is distinctive: the BKC and Lower Parel finance houses lean on RSU and SAR structures, while the Andheri-Powai SaaS and IIT-Bombay deep-tech belt around Goregaon-Vikhroli carries large option pools from <a href="/esop-at-funding-round-series-a-b-c/mumbai">successive funding rounds</a>. Where the issuer is an NBFC, fintech or other BFSI name - a common profile in this city - Independent Director eligibility needs a second screen, because the RBI prohibition on ESOPs for Independent Directors of Middle, Upper and Top Layer NBFCs overrides the otherwise-permissive SBEB position.</p>
                    <p>Sitting above the scheme, SEBI ICDR Regulations 2018 add a lock-in on pre-issue capital plus the specific Regulation 167 trigger for ESOP-exercise shares. Once the DRHP is filed the cleanup window shuts - Regulation 18 of SBEB 2021 bars any variation detrimental to grantees from that point on.

