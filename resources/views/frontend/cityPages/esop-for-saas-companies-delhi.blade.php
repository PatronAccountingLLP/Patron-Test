                    <p>Delhi has a particular advantage on ESOP work: the Ministry of Corporate Affairs is headquartered here, and the Registrar of Companies, Delhi sits at the centre of a product, consumer-tech and trading ecosystem that has long blended Indian and NRI capital. For a recurring-revenue startup, though, proximity to the regulator does not substitute for a scheme that actually fits SaaS economics. The issuance power is the familiar Section 62(1)(b) of Companies Act 2013 read with Rule 12 of Companies (Share Capital and Debentures) Rules 2014, but a B2B SaaS design adds the parts a generic page skips: ARR-linked performance vesting at $1M, $5M and $10M, Account Executive grants with quota-linked acceleration, Customer Success Manager grants tied to NRR and GRR, late co-founder backfill grants under the Rule 12 DPIIT 10-year founder exemption, Rule 11UA methodology selection to contain revenue-multiple FMV inflation, and US Delaware parent plus India subsidiary (Delaware flip) mirror grants under FEMA Overseas Investment Rules 2022 - the last especially relevant where an NRI-backed cap table points toward a US holding company.</p>
                    <p>For a Delhi SaaS team, B2B ESOP design diverges from generic tech ESOPs in four ways. First, role compensation differs - the Nehru Place and Saket sales orgs are quota-driven with OTE plus equity, CSMs are retention-driven with NRR linkage, and engineering follows standard time-based vesting. Second, ARR-based milestones at $1M, $5M and $10M are natural acceleration triggers. Third, SaaS revenue-multiple valuations create FMV swings that materially impact <a href="/esop-perquisite-tax-section-17-2-vi/delhi">perquisite tax at exercise</a> - which is why Delhi founders lean hard on the DPIIT and Section 80-IAC deferral available through the local IMB. Fourth, the Delaware flip is the dominant structure for Delhi-built SaaS selling to global enterprises, requiring mirror grant design under FEMA Overseas Investment Rules 2022 with US 409A coordination, LRS tracking and Section 92 transfer pricing.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for SaaS ESOP:</strong></p>
                    <p><strong>ARR (Annual Recurring Revenue):</strong> Annualised value of all subscription contracts in force; the primary SaaS valuation metric.</p>
                    <p><strong>NRR (Net Revenue Retention):</strong> Revenue from existing customers in the current period as a percent of the same customers' revenue 12 months earlier, including expansion, contraction and churn. NRR above 100 percent indicates net account growth.</p>
                    <p><strong>GRR (Gross Revenue Retention):</strong> Same as NRR but excludes upsells; floor measure of retention. Above 90 percent considered healthy; above 95 percent excellent.</p>
                    <p><strong>OTE (On-Target Earnings):</strong> Total expected compensation for a sales role assuming 100 percent quota attainment; combines base salary plus variable commission.</p>
                    <p><strong>Quota Acceleration:</strong> Vesting acceleration triggered by sales rep hitting a defined quota percentage (e.g. 150 percent, 200 percent of annual quota).</p>
                    <p><strong>ARR-Linked Vesting:</strong> Vesting milestones tied to ARR thresholds rather than time alone; legally permitted under Rule 12 with measurable conditions documented in grant letter.</p>
                    <p><strong>Founder Backfill Grant:</strong> Grants given to senior leadership who join after Series A close, with equity packages comparable to founding team members; permitted under DPIIT 10-year founder exemption.</p>
                    <p><strong>Delaware Flip / US Holdco:</strong> A structure where a US-incorporated parent (typically Delaware C-Corp) owns the Indian operating entity as a wholly-owned subsidiary; common for Indian B2B SaaS selling globally.</p>
                    <p><strong>Mirror Grant:</strong> ESOPs issued by the foreign parent to Indian subsidiary employees on the parent's stock; classified under FEMA Overseas Investment Rules 2022 as OPI if 10 percent or less of parent equity, ODI otherwise.</p>
                    <p><strong>Section 62(1)(b) Companies Act 2013:</strong> Statutory framework for issuing ESOPs by private and public unlisted companies; read with Rule 12 of Companies (Share Capital and Debentures) Rules 2014.</p>
                    <p><strong>Rule 12(6)(a):</strong> Minimum 1-year cliff between grant date and first vesting date; mandatory under Companies (Share Capital and Debentures) Rules 2014.</p>
                    <p><strong>DPIIT 10-Year Founder Exemption (Rule 12 Explanation):</strong> DPIIT-recognised startups (Private Limited or LLP, 10 years from incorporation, turnover under Rs 100 crore) can grant ESOPs to founders and 10 percent-plus directors for 10 years - key for late co-founder backfill grants.</p>
                    <p><strong>Rule 11UA (Income Tax Rules 1962):</strong> FMV methodology for perquisite tax - DCF (Discounted Cash Flow), NAV (Net Asset Value), CCA (Comparable Companies Approach); for SaaS, DCF or NAV usually defensible and more tax-efficient than revenue-multiple CCA.</p>
                    <p><strong>Section 17(2)(vi) Income Tax Act 1961:</strong> Perquisite tax at exercise computed as FMV minus exercise price, multiplied by options exercised, taxed at employee's slab rate.</p>
                    <p><strong>Section 80-IAC + Section 192(2C):</strong> DPIIT plus IMB certified startups - 48-month perquisite tax deferral at exercise (60 months under Income Tax Act 2025 Section 392(3) read with 289(3) from 1 April 2026).</p>
                    <p><strong>FEMA Overseas Investment Rules 2022:</strong> Governs mirror grants from foreign parent to Indian employees; OPI classification if individual beneficial ownership at or below 10 percent of parent equity, ODI otherwise.</p>
                    <p><strong>LRS (Liberalised Remittance Scheme) Section 5 FEMA 1999:</strong> USD 250,000 per FY per individual remittance limit for exercise consideration to foreign parent under Delaware flip mirror grant exercise.</p>
                    <p><strong>Section 92 Income Tax Act 1961:</strong> Transfer pricing arm's-length pricing on India subsidiary providing engineering, product and Customer Success services to US parent; cost-plus 10 to 15 percent markup industry-standard.</p>
                    <p><strong>US Internal Revenue Code Section 409A:</strong> Parent stock FMV valuation methodology (US-side); refreshed every 12 months or earlier on material events.</p>

