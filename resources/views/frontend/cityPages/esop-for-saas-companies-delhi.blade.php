            <h2 class="section-title">Legal and Compliance Framework</h2>
            <div class="content-text">
                <p>A Delhi SaaS ESOP rests on a fixed statutory stack administered, in part, from the MCA's headquarters in the capital, with corporate actions filed through the Registrar of Companies, Delhi. The provisions below are the ones we work to on each engagement - from the core Section 62(1)(b) issuance power through the FEMA, LRS and US-side rules that matter to flipped or NRI-heavy companies.</p>
                <ul>
                    <li><strong>Section 62(1)(b), Companies Act 2013</strong> - statutory framework for issuing ESOPs by private and public unlisted companies. <a href="https://www.mca.gov.in" target="_blank" rel="noopener">Ministry of Corporate Affairs portal</a>.</li>
                    <li><strong>Rule 12, Companies (Share Capital and Debentures) Rules 2014</strong> - operational provisions for ESOP - minimum cliff, vesting, exercise, scheme adoption procedures.</li>
                    <li><strong>Rule 12(6)(a)</strong> - minimum 1-year cliff between grant date and first vesting date; mandatory.</li>
                    <li><strong>Rule 12 Explanation - DPIIT 10-Year Founder Exemption</strong> - DPIIT-recognised startups (Private Limited or LLP, 10 years from incorporation, turnover under Rs 100 crore) can grant ESOPs to founders and 10 percent-plus directors for 10 years. Key for late co-founder backfill grants.</li>
                    <li><strong>Section 117(2), Companies Act 2013</strong> - MGT-14 filing within 30 days of scheme adoption and pool top-up special resolutions; Rs 100 per day default.</li>
                    <li><strong>Section 39(4), Companies Act 2013 read with Rule 12</strong> - PAS-3 within 30 days of share allotment on exercise.</li>
                    <li><strong>Section 80-IAC + Section 192(2C), Income Tax Act 1961</strong> - DPIIT plus IMB certified startups - 48-month perquisite tax deferral at exercise (60 months under Income Tax Act 2025 Section 392(3) read with 289(3) from 1 April 2026).</li>
                    <li><strong>Section 17(2)(vi), Income Tax Act 1961</strong> - perquisite tax at exercise computed as FMV minus exercise price multiplied by options exercised; taxed at employee slab rate.</li>
                    <li><strong>Section 192(1), Income Tax Act 1961</strong> - employer (or India subsidiary in Delaware flip mirror grants) acts as TDS deductor on perquisite at exercise.</li>
                    <li><strong>Rule 11UA, Income Tax Rules 1962</strong> - FMV methodology including DCF (Discounted Cash Flow), NAV (Net Asset Value), CCA (Comparable Companies Approach). For SaaS, DCF or NAV usually defensible and tax-efficient over revenue-multiple CCA.</li>
                    <li><strong>FEMA Overseas Investment Rules 2022</strong> - foreign parent ESOPs to Indian employees; OPI classification if individual beneficial ownership at or below 10 percent of parent equity; ODI otherwise. <a href="https://www.rbi.org.in" target="_blank" rel="noopener">RBI portal</a>.</li>
                    <li><strong>Rule 21, FEMA Non-Debt Instruments Rules 2019</strong> - cross-border share issuance pricing for India subsidiary.</li>
                    <li><strong>LRS (Liberalised Remittance Scheme), Section 5 FEMA 1999</strong> - USD 250,000 per FY per individual remittance limit for exercise consideration to foreign parent.</li>
                    <li><strong>Section 92, Income Tax Act 1961 + Rule 10D</strong> - transfer pricing arm's-length pricing on India subsidiary providing engineering, product and Customer Success services to US parent; cost-plus 10 to 15 percent markup industry-standard.</li>
                    <li><strong>DTAA Article 22 (India-US Double Taxation Avoidance Agreement)</strong> - Foreign Tax Credit on US capital gains paid on sale of parent stock; avoids double taxation.</li>
                    <li><strong>US Internal Revenue Code Section 409A</strong> - parent stock FMV valuation methodology (US-side); refreshed every 12 months or earlier on material events.</li>
                    <li><strong>DPIIT Notification GSR 127(E) 2019</strong> - startup recognition criteria (Private Limited or LLP, 10 years from incorporation, turnover under Rs 100 crore).</li>
                    <li><strong>Ind AS 102 / ICAI Guidance Note 2020</strong> - share-based payment expense recognition under Indian Accounting Standards; revenue-multiple valuations may inflate grant-date fair value; group SBP rules apply to Delaware flip mirror grants where India subsidiary recognises expense for parent-issued equity.</li>
                    <li><strong>SEBI (Share Based Employee Benefits and Sweat Equity) Regulations 2021 (SBEB)</strong> - applicable on listing transition; pre-IPO SaaS startups must align with SBEB Regulations for IPO readiness.</li>
                    <li><strong>Section 280G (US Internal Revenue Code)</strong> - excess parachute payment considerations on US M&amp;A exit for Delaware flip structures.</li>
                    <li><strong>Section 68, Companies Act 2013</strong> - buyback authority for India-incorporated SaaS exit via secondary; key for India IPO-not-yet-feasible scenarios.</li>
                    <li><strong>Income Tax Act, 2025</strong> - applies to Tax Year 2026-27 onwards (FY 2026-27 income from 1 April 2026); Section 392(3) read with 289(3) extends Section 80-IAC perquisite tax deferral from 48 to 60 months.</li>
