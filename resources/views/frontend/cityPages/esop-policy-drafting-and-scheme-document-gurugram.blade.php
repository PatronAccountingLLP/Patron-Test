                        <h3 class="faq-expanded__q">What is an ESOP scheme document?</h3>
                        <div class="faq-expanded__a"><p>It is the formal, legally binding document that governs a company's employee stock option plan. Approved by the board and by shareholders through a special resolution under Section 62(1)(b) of the Companies Act, it sets out the option pool, who is eligible, the vesting and exercise terms, the exercise price, lapse and leaver provisions, and the valuation and accounting approach. Every option the company grants is governed by this scheme, which is why it must be drafted carefully and compliantly.</p></div>
