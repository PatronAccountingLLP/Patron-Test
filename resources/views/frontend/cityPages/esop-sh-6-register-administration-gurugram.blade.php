                    <p>This is where Gurugram companies most often trip up over jurisdiction. Sitting in Haryana, a Cyber City or Golf Course Road company files its e-forms, the <a href="/esop-corporate-filings/gurugram">MGT-14 for the ESOP special resolution and the PAS-3 on allotment</a>, with the Registrar of Companies, Delhi. The SH-6 register, by contrast, is never uploaded to the MCA portal at all. It lives at the registered office, is authenticated entry by entry by the Company Secretary or an authorised person, and is produced only when an inspector, auditor or acquirer asks to see it.</p>

