            <h2 class="section-title">How They Are Taxed</h2>
            <div class="content-text">
                
                <p>For Mumbai finance and SaaS teams weighing the two, tax is often where the decision lands. The rule is blunt: an ESOP triggers tax twice over its life, phantom stock only once, and at a later point.</p>
                <ul>
                    <li><strong>Phantom, at payout only:</strong> the full cash amount is taxed as salary income at the employee's slab rate, with the employer deducting TDS, and there is no capital-gains event because no share was ever held.</li>
                    <li><strong>ESOP, at exercise:</strong> the difference between FMV and the exercise price is taxed as a salary perquisite.</li>
                    <li><strong>ESOP, at sale:</strong> any gain above the exercise FMV is taxed as capital gains.</li>
                </ul>
                <div class="highlight-box" style="margin-top:16px;">
                    <p><strong>Why phantom stock keeps the tax simple</strong></p>
                    <p>Nothing is taxed at grant or at vesting, and there is no capital-gains leg to track. A Powai or BKC employee is taxed a single time, as salary, only when the cash actually lands, so no one is paying tax on paper gains before any money has moved.</p>
                </div>
                <p style="margin-top:16px;"><strong>Note on SAR:</strong> appreciation-only phantom stock sits very close to a stock appreciation right; the two overlap heavily and we treat them together in our <a href="/esop-vs-sar/mumbai">SAR comparison</a>.</p>
                <p><strong>Where phantom stock wins for a Mumbai company:</strong> protecting a tightly held BKC promoter cap table, bridging an ESOP pool that ran dry between funding rounds, keeping founder control intact, and a lighter compliance load with no Section 62 process at all. Where the ESOP still wins: at seed and Series A, where Mumbai investors expect an ESOP pool and real ownership motivates an early Powai team more than a cash promise.</p>

