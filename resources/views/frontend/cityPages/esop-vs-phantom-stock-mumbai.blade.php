        <div class="text-content">
            <h2 class="section-title">Legal and Tax Framework</h2>
            <div class="content-text">

                <p>For a Mumbai company the rules sit in three places at once, the Companies Act and RoC Mumbai, the Income-tax Act, and, for anyone listed or listing-track near the SEBI head office in BKC, the SEBI regulations. Here is how each instrument lands across them.</p>
                <p><strong>The ESOP route:</strong> the option is granted under Section 62(1)(b) of the Companies Act; on exercise the perquisite is taxed under Section 17(2)(vi), and any later sale of the shares attracts capital gains. This is the route that runs through RoC Mumbai filings.</p>
                <p><strong>The phantom route:</strong> no share is issued and Section 62 simply does not apply, so for an unlisted BKC or Powai firm the plan stands or falls on a board-approved policy, with the Companies Act largely silent.</p>
                <p><strong>How the cash is taxed:</strong> the phantom payout is salary income in the year it is paid, at the employee's slab rate, with employer TDS; nothing is taxed at grant or vesting, and there is no capital-gains leg at all.</p>
