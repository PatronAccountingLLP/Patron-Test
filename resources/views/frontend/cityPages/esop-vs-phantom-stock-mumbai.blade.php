            <h2 class="section-title">What Is Phantom Stock</h2>
            <div class="content-text">
                
                <p>Now flip the structure. A closely held Lower Parel finance group wants to reward its CFO for three years of valuation growth but will not let go of a single share. Phantom stock answers that exactly: the company writes a promise to pay cash, an amount pegged to the value of a notional block of shares (or only to their appreciation), payable when an agreed trigger event arrives. No allotment happens, and no new face shows up on the register.</p>
                <p>Because nothing is issued, the CFO never becomes a shareholder, gains no vote and no dividend, and the cap table the promoters and investors signed off stays frozen in place. The reward is shaped one of two ways:</p>
                <ul>
                    <li><strong>Full-value phantom stock:</strong> settles the entire share value in cash.</li>
                    <li><strong>Appreciation-only phantom stock:</strong> settles only the gain above the grant-date value.</li>
                </ul>
                <p>With no security changing hands, the Companies Act has little to say and there is nothing to file at RoC Mumbai. The plan lives entirely inside a board-approved policy or agreement that the company drafts for itself, which is precisely why Mumbai groups guarding control reach for it.</p>

