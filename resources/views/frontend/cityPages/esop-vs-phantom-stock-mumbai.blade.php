                    <p><strong>&#128204; TL;DR - ESOP vs Phantom Stock Services at a Glance</strong></p>
                    <p>An ESOP issues real shares on exercise and dilutes ownership; phantom stock pays cash linked to share value with no share issue and no dilution. Phantom is contractual and taxed only at payout as salary.</p>
                </div>
                <p>Walk through Mumbai's working day and you meet both answers to this question. A fintech off BKC's G Block, a media or AMC group in Lower Parel, and a deep-tech spin-out from IIT-Bombay in Powai each reward their best people differently, because each carries a different cap table. This free guide is built for that mix: it sets out, in plain terms, how an ESOP and phantom stock differ on dilution, RoC Mumbai filings and tax, and which one a given Mumbai company should reach for.</p>
                <p>Two instruments, one decision rule. An ESOP hands over real shares under Section 62 and permanently moves your shareholding; phantom stock pays cash that merely tracks share value, so the register never changes. In a promoter-anchored Lower Parel or BKC group, that single difference is usually decisive, equity stays where the founders and PE investors put it. In a leaner Goregaon-Vikhroli or Andheri-Powai startup, the same difference cuts the other way, because investors and engineers want the upside that only real ownership delivers.</p>
                <p>What makes Mumbai distinct is proximity to the regulator. The <strong>SEBI head office is at BKC</strong> and companies here register with <strong>RoC Mumbai</strong>, so listed groups and anyone on a listing track watch the SEBI share-based rules far more closely than peers elsewhere. A purely cash-settled phantom plan, by contrast, transacts in no shares at all and stays clear of that perimeter entirely.</p>
