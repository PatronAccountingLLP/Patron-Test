                        <h3 class="faq-expanded__q">What is a family business and how do I register one in India?</h3>
                        <div class="faq-expanded__a"><p>A family business is one owned and run primarily by members of the same family. There is no single 'family business registration' form in India - it is a strategic structuring exercise. The available vehicles are HUF (for Hindu / Buddhist / Jain / Sikh families), Sole Proprietorship, Partnership Firm under the Indian Partnership Act 1932, LLP under the LLP Act 2008, Private Limited Company under the Companies Act 2013, and Private Trust under the Indian Trusts Act 1882. Most Mumbai families end up with two layers - an operating Pvt Ltd plus an HUF or Trust for passive holdings - with a Family Constitution, Shareholders Agreement and Wills as the governance overlay.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-2">
                        <h3 class="faq-expanded__q">Is HUF still tax-efficient under the new tax regime in FY 2026-27?</h3>
                        <div class="faq-expanded__a"><p>Yes, but less so than under the old regime. The HUF retains its status as a separate 'person' under Section 2(31) of the Income-tax Act, 1961 with its own PAN and basic exemption slab - INR 4 lakh under the new regime and INR 2.5 lakh under the old regime for FY 2026-27. Most Section 80 deductions (80C, 80D, etc.) are NOT available under the new regime - so the tax-saving edge of HUF is reduced. The HUF is still useful for income-splitting and as a separate basic exemption pool, but the case is stronger for families that opt for the old regime.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-3">
                        <h3 class="faq-expanded__q">Can daughters be coparceners in a HUF after the 2005 Amendment?</h3>
                        <div class="faq-expanded__a"><p>Yes. The Hindu Succession (Amendment) Act, 2005 effective 9 September 2005 extended coparcenary rights to daughters - they now have the same rights as sons by birth in joint Hindu family property, including the right to seek partition and to dispose of their share by will. This is retroactive in effect - daughters who were alive on 9 September 2005 became coparceners by birth in pre-existing HUFs. Many older HUF deeds drafted before 2005 need amendment to acknowledge these rights.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-4">
                        <h3 class="faq-expanded__q">What is the difference between a HUF, a family Pvt Ltd and a family trust?</h3>
                        <div class="faq-expanded__a"><p>HUF is a tax-recognised joint Hindu family entity - good for passive holdings and a separate tax slab. Family Pvt Ltd is a Companies Act, 2013 entity - good for operating business with limited liability and growth horizon. Family Trust under the Indian Trusts Act, 1882 is a fiduciary arrangement - good for succession, wealth preservation and multi-generation horizon. Most HNI Mumbai families use a combination - operating business in Pvt Ltd, passive investments in HUF, multi-generation assets in Trust. Each vehicle is optimised for a different role.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-5">
                        <h3 class="faq-expanded__q">How does Section 64 clubbing affect a family business structure?</h3>
                        <div class="faq-expanded__a"><p>Section 64 of the Income-tax Act, 1961 clubs certain income with the transferor where assets are transferred to family members without adequate consideration. Section 64(1) covers income from assets transferred to a spouse, minor child or daughter-in-law without adequate consideration. Section 64(2) covers the specific HUF case - if a member converts self-acquired property into HUF property without adequate consideration, income from that property is taxed in the member's hands, not the HUF's. These provisions are the most common surprise in DIY family structures. Patron designs initial capital contributions to avoid the clubbing exposure.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-6">
                        <h3 class="faq-expanded__q">Can a Muslim or Parsi family form an HUF?</h3>
                        <div class="faq-expanded__a"><p>No. HUF under Indian tax law is restricted to families governed by Hindu personal law - which covers Hindus, Buddhists, Jains and Sikhs. Muslims, Christians and Parsis are governed by separate personal laws and cannot form an HUF. For these families the structure is typically a combination of Partnership Firm or Pvt Ltd Company plus Private Trust plus a Will - delivering similar tax efficiency and succession outcomes by different routes. Muslim families follow Muslim Personal Law (Shariat) Application Act, 1937 for inheritance; Christians and Parsis follow the Indian Succession Act, 1925.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-7">
                        <h3 class="faq-expanded__q">What is a Family Constitution and why does it matter?</h3>
                        <div class="faq-expanded__a"><p>A Family Constitution is a non-statutory governance document that sets out a family's values, roles, dividend policy, family-employment rules, succession plan and dispute-resolution mechanism. It is not codified in any Indian statute and is not directly binding in court. Its power comes from buy-in by all adult family members and from being operationalised through binding derivative documents - the Shareholders Agreement of a family Pvt Ltd, employment contracts of family members, board resolutions on dividend policy, Wills aligned with the succession plan. Most family disputes that reach court could have been prevented by a Family Constitution drafted in time.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-8">
                        <h3 class="faq-expanded__q">How is HUF partition done under Section 171 of the Income-tax Act?</h3>
                        <div class="faq-expanded__a"><p>Section 171 of the Income-tax Act, 1961 governs HUF partition for tax purposes. Critically, only a TOTAL partition is recognised - the entire HUF property must be divided among all coparceners and the HUF must cease to exist as a tax entity. PARTIAL partition (dividing only some assets or excluding some members) is NOT recognised since Finance Act 1980 - the post-partition income from such partial partition continues to be taxed in the HUF's hands. A total partition requires (a) physical division of all assets, (b) execution of a partition deed, and (c) an order under Section 171 from the Assessing Officer recognising the partition.</p></div>
