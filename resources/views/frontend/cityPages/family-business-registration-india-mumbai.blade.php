                <h2 class="section-title">What Family Business Registration Actually Means</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p><strong>Family Business Registration</strong> is the coordinated setup of a tax and legal structure that a family will use to operate, hold and pass down its business, together with the governance documents that prevent disputes across generations.</p>
                    <p>The work has two parts. <strong>First, the legal vehicle</strong> - HUF for tax-efficient passive holdings, Sole Proprietorship for very simple operations, Partnership Firm or LLP for two or more working family members, Pvt Ltd Company for businesses that need limited liability and growth optionality, and Private Trust for succession-focused holdings.</p>
                    <p><strong>Second, the governance and succession layer</strong> - Family Constitution, Shareholders Agreement, Wills under the Indian Succession Act, 1925 and Family Settlement Deeds where existing assets need re-alignment. Patron runs both layers.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Family Business Registration:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>HUF (Hindu Undivided Family):</strong> A unique Indian tax entity recognised under Section 2(31) of the Income-tax Act, 1961. Consists of persons lineally descended from a common ancestor plus their spouses and children. Has its own PAN and basic exemption slab.</li>
                        <li><strong>Karta:</strong> The head of the HUF - traditionally the eldest male, now can also be the eldest female coparcener post the 2005 amendment. Manages HUF affairs.</li>
                        <li><strong>Coparcener:</strong> Members who have a birthright to a share in joint Hindu family property. Earlier limited to males; the Hindu Succession (Amendment) Act, 2005 extended this to daughters.</li>
                        <li><strong>Section 64(2):</strong> Income-tax Act clubbing provision - if a member converts self-acquired property into HUF property without adequate consideration, income from that property is taxed in the member's hands, not the HUF's.</li>
                        <li><strong>Section 56(2)(x):</strong> Gifts received by a person from "relatives" (defined term) are exempt from income tax. Gifts from non-relatives above INR 50,000 in a financial year are taxable as Income from Other Sources.</li>
                        <li><strong>Section 171:</strong> Income-tax Act provision governing HUF partition - only TOTAL partition is recognised for tax purposes. Partial partition is not recognised since Finance Act 1980.</li>
                        <li><strong>Family Constitution:</strong> A non-statutory governance document that sets out family values, roles, succession plans, dividend policy, employment of family members and dispute-resolution mechanisms. Enforced through derivative documents (SHA, employment contracts, board resolutions).</li>
                        <li><strong>Shareholders Agreement (SHA):</strong> A contract among shareholders of a family Pvt Ltd that addresses share transfer restrictions, pre-emption rights, dividend policy, board nomination, deadlock resolution and exit mechanisms.</li>
                        <li><strong>Family Settlement Deed:</strong> A registered deed under which existing family assets are re-allocated among family members to align ownership with the desired structure. Stamped under the Maharashtra Stamp Act, 1958 at a concessional rate when among family members.</li>
                    </ul>
                    

