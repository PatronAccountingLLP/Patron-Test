                    <p>An FCRA certificate is valid for five years from the date of grant. To keep receiving and using foreign contribution, an association must renew it by filing <strong>Form FC-3C</strong> online on <strong>fcraonline.nic.in</strong> within six months before expiry (Section 16(1) FCRA 2010 read with Rule 12 FCRR 2011). The Ministry of Home Affairs advises applying at least four months in advance. The standard government fee is <strong>Rs. 5,000</strong>; a delayed application after expiry carries an enhanced fee of <strong>Rs. 10,000</strong> and can generally be filed within one year of expiry. A valid DARPAN ID is mandatory, pending Form FC-4 annual returns must be up to date, and the Central Government ordinarily processes renewal within ninety days. If no valid application is filed, the certificate is deemed to have ceased on completion of five years - after which fresh foreign contribution cannot be received and existing foreign contribution cannot be utilised.</p>
                </div>
                <p>The five-year validity cycle is central to FCRA compliance. Many otherwise well-run NGOs, charitable trusts, societies, and Section 8 companies lose their ability to receive foreign donations simply because the renewal window is missed. The <a href="https://fcraonline.nic.in" target="_blank" rel="noopener">FCRA services portal (fcraonline.nic.in)</a> is where the FC-3C form, fee payment, and status tracking are all handled electronically, so timely and complete online filing is the whole game.</p>
                <p>Renewal is not merely a form-filling exercise. It is granted only to compliant associations, which means the organisation's statutory house must be in order - pending <a href="/fcra-fc-4-annual-return">FC-4 annual returns</a> with audited accounts filed, quarterly disclosures made, and office-bearer affidavits prepared in the prescribed proforma. Patron helps NGOs sequence this correctly so the FC-3C is filed clean, well inside the window, and with the least risk of query or rejection.</p>
