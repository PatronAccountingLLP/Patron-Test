// Form validation and submission
document.addEventListener('DOMContentLoaded', function() {
    const form = document.querySelector('form[action*="submit-certificate-enquiry"]');
    const submitBtn = document.getElementById('submitBtn');

    if (form && submitBtn) {
        form.addEventListener('submit', function(e) {
            submitBtn.disabled = true;
            submitBtn.textContent = 'Submitting...';
            setTimeout(function() {
                if (submitBtn.disabled) {
                    submitBtn.disabled = false;
                    submitBtn.textContent = 'Find My Certificate →';
                }
            }, 3000);
        });

        const phoneInput = document.getElementById('phone');
        if (phoneInput) {
            phoneInput.addEventListener('input', function(e) {
                let value = e.target.value.replace(/\D/g, '');
                if (value.length > 15) { value = value.slice(0, 15); }
                e.target.value = value;
                const isValid = /^[0-9]{7,15}$/.test(value);
                if (value.length > 0 && !isValid) {
                    e.target.setCustomValidity('Please enter a valid phone number (7-15 digits)');
                } else {
                    e.target.setCustomValidity('');
                }
            });
        }
    }
});

// Sticky WhatsApp Bar - scroll trigger + sessionStorage dismissal
(function() {
    var waBar = document.getElementById('waBar');
    var waClose = document.getElementById('waBarClose');
    if (!waBar || !waClose) return;
    var dismissed = false;

    waBar.classList.add('hidden');

    window.addEventListener('scroll', function() {
        if (dismissed) return;
        if (window.scrollY > 300) {
            waBar.classList.remove('hidden');
        } else {
            waBar.classList.add('hidden');
        }
    });

    waClose.addEventListener('click', function() {
        dismissed = true;
        waBar.classList.add('hidden');
        try { sessionStorage.setItem('wa_bar_dismissed', '1'); } catch(e) {}
    });

    try {
        if (sessionStorage.getItem('wa_bar_dismissed') === '1') {
            dismissed = true;
            waBar.classList.add('hidden');
            waBar.style.display = 'none';
        }
    } catch(e) {}
})();
</script>

