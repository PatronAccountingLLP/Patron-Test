<section class="content-section" id="legal-section">
    <div class="content-container">
        <div class="text-content">
            <h2 class="section-title">Legal Framework for Financial Statements in India</h2>
            <div class="content-text">
                <p><strong>Companies Act 2013, Section 129:</strong> Calls on every company to prepare financial statements that give a true and fair view and that comply with the accounting standards notified under the Act.</p>
