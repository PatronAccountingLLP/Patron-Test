    <tr><td>Vehicle</td><td>Private Limited Company under Companies Act 2013 (Wholly Owned Subsidiary if 100 percent foreign-held)</td></tr>
    <tr><td>Subsidiary Definition</td><td>Section 2(87) Companies Act 2013 - more than 50 percent voting power or Board control by holding company</td></tr>
    <tr><td>Primary Regulator (FDI)</td><td>RBI through Authorised Dealer (AD) Category-I Banks</td></tr>
