                <h2 class="section-title">What Is Foreign Subsidiary Registration in India</h2>
                <div class="content-text what-is-definition">
                    
                    
<p><strong>Foreign Subsidiary Registration is the incorporation of a Private Limited Company in India whose holding company is incorporated outside India. The Indian subsidiary qualifies as a "subsidiary" under Section 2(87) of the Companies Act 2013 when the foreign parent controls more than 50 percent of the total voting power OR controls the composition of the Board of Directors.</strong></p>
<p>If the foreign parent holds 100 percent of the Indian entity's equity (subject to FDI sectoral caps), the entity is a "Wholly Owned Subsidiary" or WOS. The Indian Pvt Ltd subsidiary is a separate legal entity from the foreign parent and is treated as an Indian RESIDENT COMPANY for tax purposes under Section 6 of the <a href="https://www.incometax.gov.in" target="_blank" rel="noopener">Income Tax Act 1961</a>. It is subject to full Indian corporate compliance - Companies Act, Income Tax, GST, labour laws, sector-specific regulations - and ALSO to FEMA-mandated foreign investment reporting through its AD Category-I Bank.</p>
<p>The foreign investment is governed by the Foreign Exchange Management Act 1999 read with the <a href="https://www.rbi.org.in" target="_blank" rel="noopener">FEMA (Non-Debt Instruments) Rules 2019</a> notified by the Central Government (Ministry of Finance). Schedule I of the Rules sets out sectoral caps and routes - most sectors permit 100 percent FDI under the AUTOMATIC ROUTE (no prior government approval needed; only post-investment reporting). The APPROVAL ROUTE applies to restricted sectors (defence above 74 percent, print media 26 percent, broadcasting content 49 percent, satellite services, banking private above 49 percent and a few others). PROHIBITED sectors are listed exhaustively - lottery business, gambling and betting (including casinos), chit funds, Nidhi companies, real estate (excluding construction-development), tobacco products, atomic energy and railway operations.</p>
<p>The defining compliance event post-incorporation is Form FC-GPR (Foreign Currency - Gross Provisional Return) - filed with RBI through the AD Bank on the <a href="https://firms.rbi.org.in" target="_blank" rel="noopener">FIRMS portal</a> within 30 days of share allotment to the foreign parent. A Foreign Inward Remittance Certificate (FIRC) from the AD Bank confirms the inward remittance of the share subscription amount. The Indian subsidiary then needs to track ongoing compliance: Form FC-TRS for any share transfer between resident and non-resident (within 60 days), the annual Foreign Liabilities and Assets (FLA) Return by 15 July, Form 3CEB Transfer Pricing certification by 31 October if related party transactions cross thresholds, and standard ROC and Income Tax filings.</p>
<p>Patron's role spans the full inbound stack - sector and route check at scoping, apostille document curation from the foreign parent, SPICe+ incorporation with foreign subscriber and Indian resident director composition, FIRC arrangement coordination with the AD Bank, Form FC-GPR filing within 30 days, and ongoing annual compliance support.</p>


                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Foreign Subsidiary Registration:</strong></p>
                    
<div class="key-term"><strong>Foreign Subsidiary:</strong> Indian company whose holding company is incorporated outside India and holds more than 50 percent shareholding or Board control (Section 2(87) Companies Act 2013).</div>
<div class="key-term"><strong>Wholly Owned Subsidiary (WOS):</strong> foreign subsidiary in which the foreign parent holds 100 percent of the share capital.</div>
<div class="key-term"><strong>FEMA:</strong> Foreign Exchange Management Act 1999 - the primary statute governing foreign investment and outward remittances; succeeded the Foreign Exchange Regulation Act 1973.</div>
<div class="key-term"><strong>FEMA (Non-Debt Instruments) Rules 2019:</strong> rules notified by the Central Government in October 2019 replacing FEMA 20(R) 2017; consolidates inbound FDI compliance in equity and equity-linked instruments.</div>
<div class="key-term"><strong>DPIIT:</strong> Department for Promotion of Industry and Internal Trade (Ministry of Commerce and Industry) - the nodal body for the Consolidated FDI Policy.</div>
<div class="key-term"><strong>AD Category-I Bank:</strong> an Authorised Dealer Category-I Bank under RBI's foreign exchange framework - the operational interface between the Indian subsidiary and RBI for foreign exchange reporting.</div>
<div class="key-term"><strong>FIRC:</strong> Foreign Inward Remittance Certificate issued by the AD Bank confirming receipt of foreign currency for share subscription.</div>
<div class="key-term"><strong>Form FC-GPR:</strong> Foreign Currency - Gross Provisional Return - filed within 30 days of share allotment to a non-resident; documents the inward FDI on the FIRMS portal.</div>
<div class="key-term"><strong>Form FC-TRS:</strong> Foreign Currency - Transfer of Shares - filed within 60 days of share transfer between resident and non-resident.</div>
<div class="key-term"><strong>FLA Return:</strong> Foreign Liabilities and Assets Return - filed annually by 15 July via FLAIR portal by Indian companies with FDI or ODI.</div>
<div class="key-term"><strong>Apostille:</strong> certification of foreign documents under the Hague Convention 1961 (India acceded in 2005); replaces consularisation for Convention-member countries.</div>
<div class="key-term"><strong>Section 6 Tax Residency:</strong> Indian subsidiary incorporated in India is automatically an Indian resident for tax purposes; no POEM test required for incorporated entities.</div>
<div class="key-term"><strong>Section 92 Transfer Pricing:</strong> arm's length pricing requirement for International Transactions between Associated Enterprises; Form 3CEB certification mandatory.</div>
<div class="key-term"><strong>DTAA:</strong> Double Taxation Avoidance Agreement; India has DTAAs with 90+ countries; offers reduced withholding tax rates on dividends, interest and royalties.</div>


