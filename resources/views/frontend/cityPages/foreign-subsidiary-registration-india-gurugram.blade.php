<p style="margin-bottom:16px;"><strong>Foreign Subsidiary Registration in India is the incorporation of a Private Limited Company under the Companies Act 2013 with a foreign parent holding more than 50 percent shareholding (Section 2(87)).</strong> It is governed by FEMA 1999 plus FEMA (Non-Debt Instruments) Rules 2019. Most sectors permit 100 percent FDI under automatic route. Form FC-GPR filed with RBI within 30 days of share allotment. Patron Gurugram fee starts INR 49,999 (Exl GST and Govt. Charges).</p>
