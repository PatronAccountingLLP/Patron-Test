            <div class="column-content">
                <h2 class="section-title">What Foreign Subsidiary Registration Actually Means</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p><strong>Foreign Subsidiary Registration</strong> is the incorporation of an Indian company in which the foreign parent holds more than 50% of paid-up equity (or controls the board composition), together with FEMA reporting and operational set-up.</p>
                    <p>The Indian subsidiary is a separate legal person under Indian law - it can contract, employ, own assets, sue and be sued. For tax purposes it is a domestic Indian company. The foreign parent's rights are exercised through shareholding, board nomination and any inter-company contracts.</p>
                    <p>The Wholly-Owned Subsidiary (WOS) is the most common form - 100% foreign-owned, available in any sector where 100% FDI is permitted under the automatic route.</p>
                    <p>The Joint Venture (JV) Subsidiary brings in an Indian shareholder, typically used where the foreign parent wants local market expertise or where the FDI sectoral cap requires Indian participation.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Foreign Subsidiary Registration:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>WOS (Wholly-Owned Subsidiary):</strong> Indian Pvt Ltd in which the foreign parent holds 100% of paid-up equity. Permitted in any sector where 100% FDI is allowed under the automatic route.</li>
                        <li><strong>Partly-Owned Subsidiary:</strong> Indian Pvt Ltd in which the foreign parent holds more than 50% but less than 100%. Common in sectors with FDI sectoral caps below 100%.</li>
                        <li><strong>Joint Venture (JV) Subsidiary:</strong> Indian Pvt Ltd with both foreign and Indian shareholders. Shareholders' Agreement (SHA) is critical to govern board nomination, ROFR, deadlock and exit.</li>
                        <li><strong>Branch Office (BO):</strong> Indian establishment of a foreign company (not a separate Indian company). Can do most activities except retail trading and manufacturing (outside SEZ). Requires RBI approval via Form FNC.</li>
                        <li><strong>Liaison Office (LO):</strong> Indian communication office of a foreign parent. Cannot earn income from India; expenses funded by inward remittance from parent. Requires RBI approval via Form FNC.</li>
                        <li><strong>Project Office (PO):</strong> Temporary Indian office to execute a specific project. Permitted under specific conditions (project funded by inward remittance, bilateral or multilateral funding, or specific government clearance). Requires RBI approval via Form FNC.</li>
                        <li><strong>Automatic Route:</strong> FDI route where no prior government approval is required; the foreign investor invests and reports via FC-GPR.</li>
                        <li><strong>Government Route:</strong> FDI route requiring prior approval from the concerned ministry through the Foreign Investment Facilitation Portal (FIFP).</li>
                        <li><strong>FC-GPR:</strong> Foreign Currency Gross Provisional Return - report of foreign capital received by issuing shares; filed within 30 days of share allotment on the RBI FIRMS portal.</li>
                        <li><strong>FLA Return:</strong> Foreign Liabilities and Assets Return - annual reporting to RBI by 15 July of each year by all Indian companies with FDI or ODI.</li>
                        <li><strong>AD Cat-I Bank:</strong> Authorised Dealer Category-I Bank - a bank authorised by RBI to handle FDI inward remittances; issues the FIRC (Foreign Inward Remittance Certificate).</li>
                    </ul>
                    

