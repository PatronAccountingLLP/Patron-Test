                <p>From AY 2023-24 both forms are common to 12AB and 10(23C) entities. The choice turns entirely on three tests. The table summarises how each factor decides the form.</p>
                <div class="table-responsive-wrapper">
                <table>
                    <thead><tr><th>Factor</th><th>Form 10BB (shorter report)</th><th>Form 10B (larger report)</th></tr></thead>
                    <tbody>
                        <tr>
                            <td>Total income before exemption</td>
                            <td>Rs 5 crore or less (computed without sections 11, 12 and specified 10(23C) sub-clauses)</td>
                            <td>Exceeds Rs 5 crore</td>
                        </tr>
                        <tr>
                            <td>Foreign contribution during the year</td>
                            <td>None received</td>
                            <td>Any foreign contribution received (Form 10B applies irrespective of income)</td>
                        </tr>
                        <tr>
                            <td>Income applied outside India</td>
                            <td>No income applied outside India</td>
                            <td>Any income applied outside India (Form 10B applies irrespective of income)</td>
                        </tr>
                        <tr>
                            <td>Condition logic</td>
                            <td>All three conditions must be satisfied together</td>
                            <td>Any one condition crossed triggers Form 10B</td>
                        </tr>
                        <tr>
                            <td>Applicable entities</td>
                            <td>12AB and 10(23C) funds, trusts, educational and medical institutions meeting the conditions</td>
                            <td>Same entity universe, once any threshold is crossed</td>
                        </tr>
                        <tr>
                            <td>Auditor</td>
                            <td>Chartered accountant</td>
                            <td>Chartered accountant</td>
                        </tr>
                        <tr>
                            <td>Specified date</td>
                            <td>One month before the section 139(1) due date (generally 30 September)</td>
                            <td>One month before the section 139(1) due date (generally 30 September)</td>
                        </tr>
                    </tbody>
                </table>
                </div>
                <div class="highlight-box" style="margin-top:16px;">
                    <p><strong>Worked example:</strong> A charitable trust has total income of Rs 3.2 crore before exemption, received no foreign contribution, and applied all its income within India. All three conditions are met, so the audit report is Form 10BB. If the same trust had received even a small foreign donation during the year, it would move to Form 10B despite being well under Rs 5 crore.</p>
                </div>
                <p>For entities that fall on the Form 10B side of the line, see our companion page on the <a href="/form-10b-audit-report-section-12a-trust">Form 10B audit report for a section 12A trust</a>. If you are unsure which form applies in a borderline year, a CA review of income, foreign contribution and outbound application is advisable each year.</p>
