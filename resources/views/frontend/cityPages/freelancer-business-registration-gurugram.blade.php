                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Freelancer Business Registration:</strong></p>
                    
<div class="key-term"><strong>Sole Proprietorship:</strong> business owned and operated by a single individual; NOT a separate legal entity; PAN of the individual is used; unlimited personal liability.</div>
<div class="key-term"><strong>Trade Name (DBA):</strong> name under which the proprietor does business; declared on Shop license, bank account and invoices; NOT a registered trademark unless filed separately.</div>
<div class="key-term"><strong>One Person Company (OPC):</strong> introduced under Section 2(62) of Companies Act 2013; separate legal entity with limited liability; one shareholder; one director; mandatory conversion threshold INR 50 lakh paid-up capital or INR 2 crore turnover.</div>
<div class="key-term"><strong>Udyam Registration:</strong> MSME Registration under MSMED Act 2006 via udyamregistration.gov.in; instant; free; classifies as Micro, Small or Medium; unlocks government schemes, priority sector lending and 45-day payment protection.</div>
<div class="key-term"><strong>Section 22 GST Threshold:</strong> INR 20 lakh service receipts (INR 10 lakh special category states) for compulsory GST registration.</div>
<div class="key-term"><strong>Section 24(i) Inter-State Trigger:</strong> mandatory GST registration from first sale for inter-state supply of goods (with exceptions for services under specific notifications).</div>
<div class="key-term"><strong>Section 44ADA Presumptive Taxation:</strong> 50 percent of gross receipts deemed as taxable income for specified professionals (Section 44AA(1)) up to INR 50 lakh standard / INR 75 lakh for 95 percent+ digital receipts; no books required; ITR-4 filing.</div>
<div class="key-term"><strong>Section 44AD Presumptive Taxation:</strong> 6 percent digital / 8 percent cash deemed income for eligible business; up to INR 2 crore standard / INR 3 crore for 95 percent+ digital receipts; available to resident individuals, HUFs and partnership firms.</div>
<div class="key-term"><strong>Section 44AA Books of Accounts:</strong> mandatory bookkeeping for specified professionals; presumptive taxation under Section 44ADA exempts from this requirement.</div>
<div class="key-term"><strong>ITR-4 (Sugam):</strong> income tax return form for presumptive taxation under Section 44ADA or 44AD; simplified disclosure.</div>
<div class="key-term"><strong>Section 194M TDS:</strong> TDS by individual or HUF paying any resident contractor or professional above INR 50 lakh annual; 5 percent rate; applies when freelancer pays subcontractors.</div>
<div class="key-term"><strong>Section 43B(h):</strong> Income Tax Act provision effective AY 2024-25 - buyer's expense disallowed if payment to MSME exceeds 45 days at financial year-end; gives Udyam-registered freelancers a recovery lever.</div>
<div class="key-term"><strong>FIRC:</strong> Foreign Inward Remittance Certificate from AD Bank confirming receipt of foreign currency; needed for freelancers receiving from foreign clients.</div>
<div class="key-term"><strong>LUT under GST:</strong> Letter of Undertaking for export of services without payment of IGST; alternative to refund route.</div>


