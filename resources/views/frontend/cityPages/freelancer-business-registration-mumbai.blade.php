                <h2 class="section-title">What Freelancer Business Registration Actually Means</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p><strong>Freelancer Business Registration</strong> is shorthand for the bundle of identity, MSME and (modularly) GST / IEC set-ups that move a freelancer from an "informal individual earning income" to a "formally registered service supplier".</p>
                    <p>Sole Proprietorship is the default - the freelancer is the business; there is no separate legal entity. The proprietorship operates on the freelancer's PAN.</p>
                    <p>Udyam Registration with the Ministry of MSME is free and gives the freelancer formal MSME status.</p>
                    <p>The optional layer - GST (only if turnover or inter-state supply triggers it), IEC (only if foreign clients), Maharashtra Shops Act (only if commercial premise) - is added based on the freelancer's actual activity profile, not assumed by default.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Freelancer Business Registration:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>Sole Proprietorship:</strong> Business structure where the proprietor and the business are the same legal person. No separate legal identity; income is taxed in the individual's ITR. Unlimited liability.</li>
                        <li><strong>Udyam Registration:</strong> Free online registration with the Ministry of MSME; gives the freelancer formal MSME status and certificate. Unlocks MSME credit schemes, government tender eligibility and Section 43B(h) protection.</li>
                        <li><strong>Section 43B(h):</strong> Income-tax Act provision (effective AY 2024-25) requiring buyers to pay MSME suppliers within 45 days of invoice; buyers who delay payment beyond this period lose the income-tax deduction for that expense in the year of accrual.</li>
                        <li><strong>Section 44ADA:</strong> Presumptive taxation scheme for specified professionals; 50% of gross receipts deemed as income; threshold INR 50L (INR 75L if cash receipts under 5%); ITR-4 Sugam; no books, no audit.</li>
                        <li><strong>Section 44AD:</strong> Presumptive taxation scheme for small NON-professional businesses; 8% deemed income for cash receipts and 6% for digital receipts; turnover threshold INR 2 crore (INR 3 crore if cash under 5%).</li>
                        <li><strong>Section 24(i) CGST:</strong> Mandatory GST registration from Rs 0 turnover for any person making inter-state taxable supplies of services or goods. THE FREELANCER'S BIGGEST INVISIBLE TRAP.</li>
                        <li><strong>LUT (Letter of Undertaking):</strong> Annual filing on the GST portal that allows export of services (and goods) without payment of IGST. Avoids the pay-then-claim-refund cycle for zero-rated supplies.</li>
                        <li><strong>IEC (Import Export Code):</strong> 10-digit code issued by the DGFT (Director General of Foreign Trade) needed for receiving payments from foreign clients (most banks insist on it). Free issuance online.</li>
                        <li><strong>FIRC / BRC:</strong> Foreign Inward Remittance Certificate / Bank Realisation Certificate - issued by the AD bank receiving the foreign payment; needed for GST refund of zero-rated exports.</li>
                        <li><strong>ITR-4 Sugam:</strong> Simplified income-tax return form for individuals and HUFs opting for presumptive taxation under Section 44ADA or Section 44AD.</li>
