                <h2 class="section-title">What Is GeM Government e-Marketplace Registration</h2>
                <div class="content-text what-is-definition">
                    
                    <p>GeM Government e-Marketplace registration is the onboarding process by which an Indian business creates a verified seller account at gem.gov.in to supply goods and services to government departments, ministries, PSUs and autonomous bodies. Rule 149 of the General Financial Rules 2017 mandates that all eligible government procurement be routed through GeM, making the portal the single largest public-sector sales channel in India.</p>

<p>Sellers register against a PAN (business) or Aadhaar (individual), link a valid GSTIN, validate bank details and where required undergo Vendor Assessment for Q1 and Q2 product categories. The portal then activates the profile, after which the seller can list catalogues, receive bids and fulfil orders.</p>

<p>Delhi has one of the highest concentrations of central government buyers in India - line ministries, central PSUs, the Department of Commerce itself, defence procurement bodies and central regulatory authorities. A Delhi-based seller benefits from physical proximity to central buyers, faster delivery on Delhi-NCR procurement contracts and easier on-site Vendor Assessment scheduling. Patron's Delhi office coordinates the onboarding from one engagement.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for GeM Registration:</strong></p>
                    <p><strong>GeM SPV:</strong> Government e-Marketplace Special Purpose Vehicle, a Section 8 company that operates the portal under the Department of Commerce.</p>
<p><strong>Vendor Assessment:</strong> Mandatory third-party assessment for sellers in Q1/Q2 categories and non-OEM resellers - INR 11,200 plus GST per assessment.</p>
<p><strong>EMD (Earnest Money Deposit):</strong> Bid security required for certain tenders; MSMEs are exempt under Rule 170 of the GFR 2017.</p>
<p><strong>OEM (Original Equipment Manufacturer):</strong> Seller status confirming the entity manufactures the product offered; enables direct listing without reseller route.</p>
<p><strong>GFR Rule 144(xi):</strong> Restriction on bidders from countries sharing a land border with India; requires prior DPIIT registration.</p>
<p><strong>Caution Money:</strong> Earlier a seller-side security deposit on GeM tied to turnover (INR 5,000 to INR 25,000). GeM Revenue Policy v1.6 (2026) reportedly removed this for new sellers - verify the live policy at registration.</p>
<p><strong>L1 Matching:</strong> MSME benefit allowing Udyam-linked sellers to match the lowest bid (L1) and supply up to 25% of procurement quantity even when not the original L1 bidder.</p>

