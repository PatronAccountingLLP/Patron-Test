<div class="key-term"><strong>EMD (Earnest Money Deposit):</strong> refundable deposit paid by bidders as security; MSMEs and DPIIT startups are exempt.</div>
<div class="key-term"><strong>Performance Security:</strong> financial guarantee for contract performance; reduced for MSMEs and DPIIT startups.</div>
<div class="key-term"><strong>Bid Document:</strong> tender document published by the government buyer specifying requirements, quantity, evaluation criteria.</div>
