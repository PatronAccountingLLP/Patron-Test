                <h2 class="section-title">What Is GeM Government e-Marketplace Registration</h2>
                <div class="content-text what-is-definition">
                    
                    
<p><strong>GeM (Government e-Marketplace) is the Government of India's centralised online procurement portal at <a href="https://gem.gov.in" target="_blank" rel="noopener">gem.gov.in</a>. It allows registered sellers to offer goods and services to government departments, ministries, central PSUs, autonomous bodies and other government organisations through a transparent bid and direct-purchase mechanism.</strong></p>
<p>GeM was launched on 9 August 2016 by the Ministry of Commerce and Industry through a Special Purpose Vehicle (GeM SPV) - not a regulator but a procurement facilitator. The mandatory force of GeM comes from Rule 149 of the General Financial Rules (GFR) 2017, which provides that procurement of common-use goods and services by government departments must be done through GeM where the item is available. This effectively makes GeM the default procurement channel for the central government, with state governments also progressively integrating.</p>
<p>GeM has four primary seller categories: (a) Goods Seller - manufactures or trades goods; (b) Service Provider - offers services such as IT services, consultancy, manpower, security, housekeeping; (c) OEM (Original Equipment Manufacturer) - manufacturer of products with brand ownership; (d) Authorised Channel Partner or Reseller - authorised by an OEM to sell branded products. The right category affects bid eligibility, pricing visibility and category-specific approvals.</p>
<p>Registration on GeM is FREE - there is no government fee at any stage. Patron's role is purely professional handling - end-to-end onboarding with PAN verification, GSTIN verification, Aadhaar e-KYC for the authorised signatory, business address validation, bank account linking, product or service catalogue setup, MSME and DPIIT recognition linkage for benefits, and Vendor Assessment coordination where required for high-value categories. Most standard registrations complete in 3 to 7 working days. Vendor Assessment through third-party agencies (RXIL, Dun and Bradstreet, CRISIL) adds 7 to 15 working days but is necessary for tenders above certain value thresholds.</p>
<p>Two compliance frameworks dominate post-registration: Rule 149 of GFR 2017 (binding government buyers to procure through GeM); and the Public Procurement Policy 2012 (<a href="https://msme.gov.in" target="_blank" rel="noopener">Ministry of MSME</a> notification mandating 25 percent procurement from MSEs, with sub-targets of 4 percent for SC/ST-owned MSEs and 3 percent for women-owned MSEs). DPIIT-recognised startups receive prior experience and turnover criteria relaxations in public procurement under the <a href="https://www.startupindia.gov.in" target="_blank" rel="noopener">Startup India Initiative</a>. These three layers - GeM as the platform, GFR 2017 as the buyer mandate, Public Procurement Policy 2012 as the seller preference - make GeM the most material government procurement channel for any Indian MSME or startup.</p>


