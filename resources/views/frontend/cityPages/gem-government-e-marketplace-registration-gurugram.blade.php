<div class="key-term"><strong>GeM (Government e-Marketplace):</strong> the Government of India's centralised online procurement portal at gem.gov.in.</div>
<div class="key-term"><strong>GeM SPV:</strong> Special Purpose Vehicle company operating the GeM platform; under the Ministry of Commerce and Industry.</div>
<div class="key-term"><strong>Rule 149 of GFR 2017:</strong> General Financial Rules 2017 provision that mandates government departments, ministries, central PSUs and autonomous bodies to procure through GeM where the item is available.</div>
<div class="key-term"><strong>Public Procurement Policy 2012:</strong> notification by Ministry of MSME mandating 25 percent procurement reservation for Micro and Small Enterprises (MSEs); 4 percent sub-target SC/ST owned; 3 percent sub-target women owned.</div>
<div class="key-term"><strong>Goods Seller:</strong> GeM seller category for businesses manufacturing or trading goods.</div>
<div class="key-term"><strong>Service Provider:</strong> GeM seller category for businesses offering services such as IT, consultancy, manpower, security, housekeeping.</div>
<div class="key-term"><strong>OEM:</strong> Original Equipment Manufacturer - GeM seller category for businesses manufacturing branded products.</div>
<div class="key-term"><strong>Authorised Channel Partner:</strong> GeM seller category for businesses authorised by an OEM to sell branded products.</div>
