            <div class="column-content">
                <h2 class="section-title">What GeM Registration Actually Means</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p><strong>GeM Registration</strong> is the process of creating a seller account on the Government e-Marketplace (gem.gov.in), completing the seller profile to 100%, paying the Caution Money Deposit (where applicable), undergoing Vendor Assessment if required by category, and listing the product or service catalogue with correct UNSPSC classification and BIS / IS certification compliance.</p>
                    <p>The portal is operated by GeM Special Purpose Vehicle under the Ministry of Commerce and Industry, Government of India.</p>
                    <p>Once registered and catalogue-listed, the seller becomes eligible to receive Direct Purchase orders (below thresholds), to compete in Bid invitations, and to participate in Reverse Auctions (RA).</p>
                    <p>Payments to sellers flow through PFMS (Public Financial Management System) and are typically credited within 10 days of order acceptance.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for GeM Registration:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>GeM:</strong> Government e-Marketplace - the Government of India online procurement portal at gem.gov.in.</li>
                        <li><strong>OEM:</strong> Original Equipment Manufacturer - entity that manufactures the product under its own brand. Eligible for direct brand listing on GeM.</li>
                        <li><strong>Deemed OEM:</strong> Entity that does not manufacture directly but has contractual rights from the OEM to be deemed as an OEM on GeM.</li>
                        <li><strong>Reseller:</strong> Non-OEM seller listing OEM products with a Brand Authorisation Letter from the OEM. The OEM must itself be registered on GeM.</li>
                        <li><strong>Service Provider:</strong> Seller offering services (manpower, IT, housekeeping, transport, consulting). Service categories have their own listing and pricing rules.</li>
                        <li><strong>Caution Money Deposit (CMD):</strong> Refundable security deposit based on annual turnover. Udyam-registered MSMEs are exempt per current GeM policy.</li>
                        <li><strong>Vendor Assessment (VA):</strong> Process by which RITES Limited (Rail India Technical and Economic Service) - which replaced QCI as VA agency in 2024 - assesses the seller / manufacturer's production capability. Required for OEMs and certain technical / safety categories. Desktop or Physical Inspection.</li>
                        <li><strong>EMD:</strong> Earnest Money Deposit - the bid security paid by a seller participating in a tender. Udyam-registered MSEs are exempt under MSE Public Procurement Policy.</li>
                        <li><strong>L1:</strong> Lowest qualifying bidder - the seller with the lowest price in a tender. MSEs have L1 price-matching privilege within 15% in many categories.</li>
                        <li><strong>UNSPSC:</strong> United Nations Standard Products and Services Code - the catalogue classification system used on GeM.</li>
                        <li><strong>BIS / IS:</strong> Bureau of Indian Standards / Indian Standard - mandatory certification for products in specified categories before they can be listed on GeM in those categories.</li>
                        <li><strong>PFMS:</strong> Public Financial Management System - the central government payment system that processes GeM order payments to sellers.</li>
