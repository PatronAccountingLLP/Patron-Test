                    <p>A GST notice for a restaurant is any of the following served on a Gurugram-registered food-and-beverage GSTIN by CGST Gurugram Commissionerate, the GST Audit Commissionerate Gurugram, or Haryana SGST DETC - ASMT-10 (scrutiny under Section 61), ADT-01/ADT-02 (Section 65 audit), DRC-01A (pre-SCN intimation under Rule 142(1A)), DRC-01 (Section 73 or 74 SCN), or DRC-07 (final demand order). The notice may cover one FY or multiple FYs.</p>
<p>Gurugram's restaurant-specific notices revolve around the three-rate structure - 5% no-ITC for standalone restaurants under Notification 11/2017 CT(R); 18% with-ITC for restaurants in specified premises (hotel-restaurants with any room tariff Rs 7,500 or higher - DLF Cyber Hub, Golf Course Road luxury hotels, Sohna Road premium resorts); and 5% paid by the ECO under Section 9(5) for supplies through Zomato, Swiggy, and similar platforms effective 1 January 2022.</p>
<p><strong>Key compliance pivot:</strong> Section 9(5) supplies are reported in GSTR-3B Table 3.1.1(ii) by the restaurant (informational only - tax already paid by ECO) and Table 3.1.1(i) by the ECO. They must NEVER be included in Table 3.1(a) - doing so creates a double-tax payment and triggers the most common Gurugram restaurant notice pattern.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for GST Notice Restaurants Gurugram:</strong></p>
                    <p><strong>Section 9(5) of CGST Act -</strong> Notifies categories where the e-commerce operator (ECO) pays GST as if it were the supplier. Restaurant services through ECO notified under Notification 17/2017 CT(R) as amended by 17/2021 - effective 1 January 2022.</p>
<p><strong>Specified Premises (5% vs 18% pivot) -</strong> A restaurant located in a hotel where any unit of accommodation has a declared room tariff of Rs 7,500 or more per unit per day. Such restaurants charge 18% with full ITC. Gurugram cluster: DLF Cyber Hub luxury hotel-restaurants, Golf Course Road premium hotels, Sohna Road resorts, MG Road business hotels.</p>
<p><strong>GSTR-3B Table 3.1.1(i) -</strong> Where ECOs (Zomato, Swiggy) report Section 9(5) supplies and pay GST in cash (no ITC permitted).</p>
<p><strong>GSTR-3B Table 3.1.1(ii) -</strong> Where the restaurant reports Section 9(5) supplies on which ECO has paid GST - purely informational, no tax payable here.</p>
<p><strong>Composition Scheme (Section 10) -</strong> 5% flat GST for restaurants with turnover up to Rs 1.5 crore. No ITC, simplified compliance via CMP-08.</p>
<p><strong>Cloud Kitchen -</strong> Delivery-only kitchen with no dine-in. Gurugram cloud kitchens concentrate in Udyog Vihar, Sector 49-50, parts of Sohna Road, and Manesar. Falls under standalone 5% no-ITC; if via ECO, Section 9(5) applies.</p>
<p><strong>GSTAT Haryana State Bench (Gurugram) -</strong> The state appellate tribunal bench for Haryana is located at Gurugram itself - covering Gurugram, Charkhi Dadri, Faridabad, Jhajjar, Mahendragarh, Nuh, Palwal, Panipat, Rewari, Rohtak, and Sonepat districts. Per Notification S.O. 3048(E) dated 31 July 2024. Local advantage for Gurugram appellants.</p>

