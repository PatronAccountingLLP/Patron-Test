                    <div class="faq-expanded__item" id="faq-1">
                        <h3 class="faq-expanded__q">Is interest on bank deposits or loans taxable under GST?</h3>
                        <div class="faq-expanded__a"><p>No. Under Entry 27 of Notification 12/2017-Central Tax (Rate) dated 28 June 2017, services by way of extending deposits, loans, or advances in so far as the consideration is represented by interest or discount are exempt. The only exception is interest involved in credit card services, which is taxable. Inter-bank and authorised dealer foreign currency transactions are also exempt under the same entry.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-2">
                        <h3 class="faq-expanded__q">What is the Section 17(4) option for banks and NBFCs?</h3>
                        <div class="faq-expanded__a"><p>Section 17(4) of the CGST Act gives banking companies, financial institutions, and NBFCs engaged in supply of services by way of accepting deposits or extending loans / advances the option to either follow Section 17(2) proportionate ITC reversal under Rule 42/43, or avail 50% of eligible monthly ITC on inputs, capital goods, and input services under Rule 38, with the balance lapsing. Once exercised in a FY, the option cannot be withdrawn that year.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-3">
                        <h3 class="faq-expanded__q">What does Rule 38 require?</h3>
                        <div class="faq-expanded__a"><p>Rule 38 of the CGST Rules sets the procedure for the 50% option. The bank or NBFC first excludes ITC on inputs and input services used for non-business purposes; ITC blocked under Section 17(5); and ITC ineligible due to place of supply rules. From the remaining balance, 50% is claimed and the other 50% is reversed in GSTR-3B Table 4(B)(1). For intra-PAN distinct person transactions, 100% ITC is available under the second proviso to Section 17(4).</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-4">
                        <h3 class="faq-expanded__q">Is penal interest on EMI default taxable under GST?</h3>
                        <div class="faq-expanded__a"><p>No, when charged by a bank or NBFC. Per CBIC Circular 102/21/2019-GST dated 28 June 2019, penal interest charged by a lender for default in EMI payment is in the nature of additional interest on the underlying lending service and is exempt under Entry 27. Only penal interest charged by a seller for delayed payment of consideration on supply of goods or services is taxable (treated as additional consideration).</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-5">
                        <h3 class="faq-expanded__q">Are securities subject to GST?</h3>
                        <div class="faq-expanded__a"><p>No. Section 2(101) of the CGST Act defines securities by reference to the Securities Contracts (Regulation) Act 1956. Section 2(52) excludes securities from the goods definition, and Section 2(102) excludes securities from the services definition. Therefore, sale or purchase of securities (shares, debentures, mutual fund units, bonds, derivatives) is outside the scope of GST. However, brokerage charged on transactions in securities is a taxable service at 18%.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-6">
                        <h3 class="faq-expanded__q">How is GST applied on foreign exchange transactions?</h3>
                        <div class="faq-expanded__a"><p>Rule 32(2) of the CGST Rules provides two options. Option 1 - slab-based: 1% of gross currency exchanged subject to minimum INR 250 for the first INR 1 lakh; 0.5% for INR 1 lakh to INR 10 lakh (minimum INR 1,000); 0.1% above INR 10 lakh (minimum INR 5,500, maximum INR 60,000). Option 2 - 1% of the gross amount of currency exchanged. The option once chosen cannot be changed in the same FY.</p></div>
