                    <p><strong>&#128204; TL;DR - GST Returns for Financial Services Services at a Glance</strong></p>
                    <p>Banks, NBFC, and financial institutions engaged in deposit/lending have a Section 17(4) option: (a) Rule 42/43 proportionate reversal, OR (b) Rule 38 - claim 50% of eligible monthly ITC, rest lapses. 100% ITC on intra-PAN distinct person transactions per the second proviso. Once chosen, irrevocable for the FY. Interest on deposits/loans/advances is exempt under Entry 27 (except credit card). Securities are outside GST (Section 2(52)/(101)/(102)). Penal interest on EMI is exempt per Circular 102/21/2019-GST. FX is valued under Rule 32(2). Insurance agent and MF distributor commissions are under RCM. Patron Accounting starts from INR 1,999 per month.</p>
                </div>
                <p>Financial services GST is the most rule-specialised regime under CGST - because interest on deposits, loans, and advances is exempt under Entry 27 of Notification 12/2017-CTR (except credit-card interest), and securities are statutorily outside the supply definition under Sections 2(52), 2(101), and 2(102) of the CGST Act, the dominant revenue stream of a bank or NBFC sits outside the GST base. The taxable revenue - processing fees, foreclosure charges, demat fees, locker rent, advisory fees, brokerage, fund management fees, insurance premiums - forms a minority of total revenue but generates the entire output tax liability.</p>
                <p>This asymmetry pushed Parliament to draft Section 17(4) CGST, giving banks, financial institutions, and NBFCs engaged in deposit / lending services the option to either follow proportionate reversal under Rule 42/43, or claim a flat 50% of eligible monthly ITC under Rule 38 (with the rest lapsing). The second proviso to Section 17(4) creates a 100% credit pass-through on intra-PAN distinct person transactions. Notification 19/2022-CT effective 1 October 2022 modernised Rule 38 to remove obsolete GSTR-2 references and route balance reversal through GSTR-3B Table 4(B)(1). Patron Accounting LLP runs monthly financial services GST compliance starting from INR 1,999 per month - covering Rule 38 vs Rule 42/43 economics, interest classification, securities and FX valuation, insurance / MF distributor RCM, and DRC-01C mismatch defence.</p>
