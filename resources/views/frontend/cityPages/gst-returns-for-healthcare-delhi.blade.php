                        <div class="faq-expanded__a"><p>No, provided the food is supplied as part of treatment and per the advice of the treating doctor or nutritionist. CBIC Circular 32/06/2018-GST FAQ treats such food as part of the composite healthcare supply, fully exempt. Food supplied in canteens to visitors or outsourced caterers not linked to dietary orders is taxable at the applicable catering rate; outsourced caterers also do not pass on ITC.</p></div>
