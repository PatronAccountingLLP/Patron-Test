                        <div class="faq-expanded__a"><p>No. Per CBIC Circular 32/06/2018-GST dated 12 February 2018, services provided by senior doctors, consultants, and technicians hired by a hospital - whether as employees or independent contractors - are healthcare services and exempt under Entry 74. The amount retained by the hospital from patient billing is part of the composite supply and equally exempt.</p></div>
