                        <h3 class="faq-expanded__q">Is GST payable on hospital room rent?</h3>
                        <div class="faq-expanded__a"><p>Hospital room rent up to INR 5,000 per day per patient is part of the composite healthcare supply and exempt. Notification 4/2022-Central Tax (Rate) dated 13 July 2022, effective 18 July 2022, made room rent exceeding INR 5,000 per day taxable at 5% without input tax credit. Rooms in ICU, CCU, ICCU, or NICU remain exempt regardless of the charge level.</p></div>
