                        <h3 class="faq-expanded__q">Are healthcare services exempt from GST?</h3>
                        <div class="faq-expanded__a"><p>Yes. Entry 74 of Notification 12/2017-Central Tax (Rate) dated 28 June 2017 exempts healthcare services provided by a clinical establishment, an authorised medical practitioner, or paramedics under Heading 9993. The IGST exemption is provided under Entry 77 of Notification 9/2017-IT(R). Section 11 of the CGST Act read with the notification gives statutory authority for the exemption.</p></div>
