            <div class="column-content">
                <h2 class="section-title">What Is GST Returns for Travel and Logistics?</h2>
                <div class="content-text what-is-definition">
                    
                    <p>GST returns for travel and logistics is the monthly compliance workflow for tour operators, travel agencies, GTAs, freight forwarders, and customs brokers covering scheme classification (5% / 12% / 18%), supply categorisation under Heading 9985 (tour operator vs ancillary), Heading 9964 (passenger transport), Heading 9965 / 9966 (goods transport), Heading 9967 (auxiliary transport), Section 12 / 13 IGST place-of-supply determination, Rule 33 pure-agent valuation, and full GSTR-1 / GSTR-3B filing.</p>
                    <p>A complete monthly close also addresses GTA RCM tracking and Annexure V FCM management, ITC restrictions under the tour operator scheme, the ocean freight post-Mohit Minerals position, export of services under LUT (Section 16 IGST), import of services RCM (Section 5(3) IGST), and DRC-01C mismatch defence. The objective is correct rate selection per supply, complete ITC discipline, and audit-ready documentation across multi-state transport flows.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for GST Returns for Travel and Logistics:</strong></p>
                    <ul>
                        <li><strong>Tour Operator:</strong> Explanation under Heading 9985(i) Notification 11/2017-CTR - any person planning, scheduling, organising, or arranging tours (including accommodation, sightseeing, or similar services) by any mode of transport. Includes the business of operating tours.</li>
                        <li><strong>GTA (Goods Transport Agency):</strong> A person who provides the service of transportation of goods by road and issues a consignment note. Without a consignment note, the service is mere goods transport.</li>
                        <li><strong>Consignment Note:</strong> A document issued by a GTA containing prescribed details - serial number, name of consignor / consignee, registration number of carriage, vehicle details, place of origin / destination, and person liable for tax.</li>
                        <li><strong>Annexure V:</strong> The declaration filed by a GTA on or before 15 March of the preceding FY to opt for forward charge (5% or 12%) for the next FY. Notification 3/2022-CT(R).</li>
                        <li><strong>Freight Forwarder:</strong> A person providing logistics support services including booking, documentation, customs clearance, and end-to-end coordination - taxable at 18% under Heading 9967.</li>
                        <li><strong>Pure Agent:</strong> Rule 33 CGST Rules - a person who incurs expenditure on behalf of the recipient and is reimbursed separately. Pure-agent reimbursements are excluded from supply value, subject to conditions.</li>
                    </ul>

