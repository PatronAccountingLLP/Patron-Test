                    <div class="faq-expanded__item" id="faq-1">
                        <h3 class="faq-expanded__q">What is the GST rate on tour operator services?</h3>
                        <div class="faq-expanded__a"><p>Under Heading 9985(i) of Notification 11/2017-Central Tax (Rate) dated 28 June 2017, tour operator services attract 5% GST (2.5% CGST + 2.5% SGST) without input tax credit, except ITC on services procured from another tour operator in the same line of business. The invoice must show the gross amount inclusive of accommodation and transportation. There is no option to charge 18% with full ITC on tour operator services.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-2">
                        <h3 class="faq-expanded__q">Can a tour operator charge 18% with full ITC instead?</h3>
                        <div class="faq-expanded__a"><p>No. Heading 9985(iii) at 18% applies only to ancillary support services that do not qualify as tour operator services. The Crown Tours AAR Rajasthan (2020) and TUI India Delhi AAR (2019) confirm that supply remains tour operator service at 5% even if the supplier voluntarily charges 18%. Where a separate ancillary service is provided (porter, guide, theme park access), that distinct service can be billed at 18% under 9985(iii) with full ITC.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-3">
                        <h3 class="faq-expanded__q">What is the GST rate for a Goods Transport Agency (GTA)?</h3>
                        <div class="faq-expanded__a"><p>Post 18 July 2022 per Notification 5/2022-Central Tax (Rate), a GTA has three options: (a) 5% under reverse charge by default (recipient pays, no GTA ITC); (b) 5% under forward charge by Annexure V (GTA pays, no GTA ITC); or (c) 12% under forward charge by Annexure V (GTA pays, full GTA ITC). Recipient under RCM can claim ITC subject to Section 16 conditions.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-4">
                        <h3 class="faq-expanded__q">What is Annexure V and when must it be filed?</h3>
                        <div class="faq-expanded__a"><p>Annexure V is the declaration filed by a GTA on or before 15 March of the preceding financial year to opt for forward charge mechanism (5% or 12%) for the next FY. Format notified vide Notification 3/2022-CT(R) dated 13 July 2022. Once filed, the option cannot be withdrawn for that FY. Missing the deadline means default RCM for the next FY.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-5">
                        <h3 class="faq-expanded__q">What is the GST rate for freight forwarders and customs house agents?</h3>
                        <div class="faq-expanded__a"><p>Freight forwarders, customs house agents (CHA), and auxiliary transport service providers attract 18% GST with full ITC under Heading 9967 of Notification 11/2017-Central Tax (Rate). Where the freight forwarder also acts as a pure agent for certain reimbursements (statutory deposits, third-party invoices), Rule 33 of the CGST Rules excludes such reimbursements from supply value.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-6">
                        <h3 class="faq-expanded__q">What was the Mohit Minerals Supreme Court ruling on ocean freight?</h3>
                        <div class="faq-expanded__a"><p>In Union of India vs Mohit Minerals (Civil Appeal 1390 of 2022, judgment dated 19 May 2022), the Supreme Court struck down the levy of GST on ocean freight under reverse charge mechanism for CIF imports. The Court held that since the ocean freight is already part of the assessable value on which IGST is paid at customs clearance under Section 5(1) proviso of the IGST Act, a separate RCM levy on the same freight amounts to double taxation. Importers can claim refund of past RCM ocean-freight payments within the 2-year window under Section 54(1).</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-7">
                        <h3 class="faq-expanded__q">Are pure-agent reimbursements taxable for travel agents?</h3>
