                <p>Freight forwarders, customs house agents, and logistics support providers fall under Heading 9967 at 18%. Air travel under Heading 9964 splits into economy 5% and business 12%. Rail passenger first AC / executive class attracts 5%. Ocean freight on CIF imports - earlier subject to RCM - was struck down by the Supreme Court in Mohit Minerals (May 2022). The place-of-supply rules under Sections 12(8), 12(9), 13(9), and 13(10) of the IGST Act determine whether each transaction is intra-state, inter-state, or zero-rated. Patron Accounting LLP runs full monthly travel and logistics GST compliance starting from INR 1,999 per month - covering scheme selection, Annexure V filings, pure-agent disclosures, and DRC-01C mismatch defence.</p>
