                    <p><strong>Key Terms for Healthcare Business Registration:</strong></p>
                    
<div class="key-term"><strong>Clinical Establishment:</strong> under Section 2(c) of Central Act 2010 - hospital, maternity home, nursing home, dispensary, clinic, sanatorium or institution offering services for diagnosis, treatment or care for illness, injury, deformity, abnormality or pregnancy; includes diagnostic facilities.</div>
<div class="key-term"><strong>Haryana Adoption Act 2018:</strong> Haryana Clinical Establishment (Registration and Regulation) Adoption Act 2018; adopts the Central Clinical Establishments Act 2010 in Haryana effective 4 April 2018.</div>
<div class="key-term"><strong>State Council for Clinical Establishments:</strong> state-level body under Section 4 of Central Act read with Haryana Adoption Act; operates registration framework.</div>
<div class="key-term"><strong>Provisional Registration:</strong> under Section 14 of Central Act - issued on application; valid for 12 months; convertible to Permanent Registration after compliance verification.</div>
<div class="key-term"><strong>Permanent Registration:</strong> under Section 15 of Central Act - issued after standards verification; valid for 5 years; renewable.</div>
<div class="key-term"><strong>Bio-Medical Waste:</strong> any waste generated during diagnosis, treatment or immunisation of human beings or animals; categorised in 4 colour-coded streams (yellow, red, white-translucent, blue) under Bio-Medical Waste Management Rules 2016.</div>
<div class="key-term"><strong>HSPCB:</strong> Haryana State Pollution Control Board - issues Bio-Medical Waste authorization to healthcare establishments under Rule 10 of BMW Rules 2016.</div>
<div class="key-term"><strong>Drug License:</strong> license under Drugs and Cosmetics Act 1940 and Rules 1945; issued by State Drugs Controller; categorised as Form 20/21 (retail) and Form 20B/21B (wholesale) for allopathic and restricted drugs.</div>
<div class="key-term"><strong>PC-PNDT Act:</strong> Pre-Conception and Pre-Natal Diagnostic Techniques (Prohibition of Sex Selection) Act 1994; mandatory registration for ultrasound, genetic diagnostic and pre-natal diagnostic facilities; renewed every 5 years.</div>
<div class="key-term"><strong>AERB:</strong> Atomic Energy Regulatory Board - approval required for X-ray, CT, MRI, nuclear medicine, radiation therapy under Atomic Energy (Radiation Protection) Rules 2004.</div>
<div class="key-term"><strong>NABH:</strong> National Accreditation Board for Hospitals and Healthcare Providers under Quality Council of India; VOLUNTARY accreditation; preferred by insurance panels and corporate empanelment.</div>
<div class="key-term"><strong>NMC:</strong> National Medical Commission under NMC Act 2019; replaced Medical Council of India in September 2020.</div>
<div class="key-term"><strong>Telemedicine Practice Guidelines 2020:</strong> framework for registered medical practitioners providing telemedicine; issued 25 March 2020 by MoHFW and MCI/NMC.</div>
<div class="key-term"><strong>Mental Healthcare Act 2017:</strong> separate framework for registration of mental health establishments under State Mental Health Authority.</div>


