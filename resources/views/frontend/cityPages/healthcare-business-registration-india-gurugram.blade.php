                <h2 class="section-title">What Is Healthcare Business Registration</h2>
                <div class="content-text what-is-definition">
                    
                    
<p><strong>Healthcare business registration in India is a composite licensing bundle - NOT a single registration - that allows a legal entity to lawfully operate a clinical, diagnostic, pharmacy, telemedicine, AYUSH, home healthcare or healthtech business under the layered framework of Central and State healthcare statutes, environmental rules, drug control, fire safety and tax laws.</strong></p>
<p>The first reality to surface is that healthcare is a STATE SUBJECT under Schedule VII List II Entry 6 of the Indian Constitution. The Central Clinical Establishments (Registration and Regulation) Act 2010 is a Central statute passed under Article 252 of the Constitution with the consent of certain states - it does NOT automatically apply across India. Each state must either ADOPT the Central Act through its own State Adoption Act (as Haryana did in 2018) or pass its own state healthcare licensing legislation (as Maharashtra, Karnataka, Tamil Nadu and Delhi have done). As of 2026, 19 States and Union Territories have adopted the Central Act including Haryana, while several large states operate under their own legislation. In Gurugram, the operative framework is the HARYANA CLINICAL ESTABLISHMENT (REGISTRATION AND REGULATION) ADOPTION ACT 2018 (adopted 22 March 2018, notified in the Haryana Gazette on 4 April 2018) read with subsequent state rules.</p>
<p>The second reality is the LICENSE BUNDLE - depending on the healthcare business type, the following may apply: (a) CLINICAL ESTABLISHMENT REGISTRATION from State Health Department for clinics, nursing homes, hospitals, diagnostic centres, pathology labs; (b) BIO-MEDICAL WASTE AUTHORIZATION from Haryana State Pollution Control Board (HSPCB) under Bio-Medical Waste Management Rules 2016 (issued under <a href="https://cpcb.nic.in" target="_blank" rel="noopener">Environment (Protection) Act 1986</a>); (c) DRUG LICENSE under <a href="https://cdsco.gov.in" target="_blank" rel="noopener">Drugs and Cosmetics Act 1940</a> - Form 20 (retail allopathic), Form 21 (retail restricted), Form 20B (wholesale allopathic), Form 21B (wholesale restricted) issued by State Drugs Controller; (d) FIRE NOC under Haryana Fire Services Act 2009 mandatory for facilities above prescribed size and in-patient capacity; (e) PC-PNDT REGISTRATION under Pre-Conception and Pre-Natal Diagnostic Techniques Act 1994 for ultrasound, genetic and pre-natal diagnostic services; (f) AERB APPROVAL under Atomic Energy (Radiation Protection) Rules 2004 for X-ray, CT scan, MRI (where ionising radiation involved), nuclear medicine, radiation therapy via <a href="https://www.aerb.gov.in" target="_blank" rel="noopener">AERB</a>; (g) FSSAI license for canteens and food services within the establishment; (h) TRADE LICENSE from Municipal Corporation; (i) SHOP AND ESTABLISHMENT registration under Haryana Shops and Commercial Establishments Act 1958; (j) GST REGISTRATION under CGST Act 2017; (k) STATE MEDICAL COUNCIL registration for individual doctors under NMC Act 2019.</p>
<p>The third reality is GST POSITIONING. Healthcare services are largely EXEMPT under Notification 12/2017-Central Tax (Rate) dated 28 June 2017 Entry 74 - services by clinical establishments, authorised medical practitioners and paramedics by way of healthcare services are exempt from GST. HOWEVER, two material exceptions exist: (1) ROOM RENT ABOVE INR 5,000 PER DAY in non-ICU rooms attracts 5 percent GST WITHOUT ITC since 18 July 2022 (per GST Council 47th meeting); ICU services continue to be exempt; (2) Services by senior doctors / consultants to hospitals (B2B) and services NOT classified as healthcare services (cosmetic surgery, hair transplant, certain non-essential procedures) may attract GST. Pharmacy and drugs are taxable under separate GST schedules.</p>
<p>Patron's role is to deliver the FULL BUNDLE in one engagement - entity setup recommendation, Clinical Establishment registration with Haryana State Health Department, Bio-Medical Waste authorization with HSPCB, Drug License if pharmacy is involved, Fire NOC, PC-PNDT if applicable, AERB if applicable, Trade License, Shop and Establishment, GST registration, post-registration GST positioning advisory (Entry 74 exemption plus INR 5,000 room rent treatment), Telemedicine Practice Guidelines 2020 readiness if telemedicine is involved, NABH advisory for future accreditation - starting INR 14,999 (Exl GST and Govt. Charges).</p>


