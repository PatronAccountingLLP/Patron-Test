                        <div class="faq-expanded__a"><p>Strictly speaking, an HUF exists by operation of Hindu personal law from the time of marriage of the Karta if there are lineal descendants - the deed is not what creates the HUF. However, a written HUF Deed on stamp paper is mandatory in practice for two reasons: the Income-tax Department asks for it during Form 49A processing for HUF PAN, and banks require a notarised deed to open an HUF account. Patron drafts the deed at intake on INR 100 stamp paper (varies by state under the relevant State Stamp Act).</p></div>
