                <h2 class="section-title">What Is HUF Formation and PAN Application</h2>
                <div class="content-text what-is-definition">
                    
                    
<p><strong>HUF (Hindu Undivided Family) is a separate 'person' under Section 2(31)(ii) of the Income Tax Act 1961 - a UNIQUE FEATURE of Indian tax law that allows a Hindu (including Sikh, Buddhist, Jain) family to be assessed as a separate entity from its individual members, with its own PAN, bank account, basic exemption limit, deductions and slab rates.</strong></p>
<p>HUF is NOT a creation of the Income Tax Act 1961 - it is a STATUS recognised under personal Hindu law (Mitakshara school in most of India; Dayabhaga in West Bengal and parts of Assam) read with the <a href="https://www.indiacode.nic.in" target="_blank" rel="noopener">Hindu Succession Act 1956</a> as amended by the Hindu Succession (Amendment) Act 2005. The Income Tax Act simply recognises HUF as a taxable person and provides for its separate assessment. This is critically important: HUF cannot be CREATED by mere paperwork. It requires either (a) an existing JOINT FAMILY status with ancestral property; or (b) introduction of property into HUF through methods that do NOT trigger Section 64(2) clubbing - typically gifts under will, gifts from non-coparceners, ancestral inheritance, or partition allocations.</p>
<p>The 2005 Amendment to Section 6 of the Hindu Succession Act 1956 (effective 9 September 2005) fundamentally reshaped HUF jurisprudence by giving DAUGHTERS equal coparcenary rights with sons. The Supreme Court in VINEETA SHARMA v RAKESH SHARMA (2020) 9 SCC 1 (3-judge bench: Mishra, Nazeer, Shah JJ; decided 11 August 2020) settled the law definitively - daughters acquire coparcenary rights by BIRTH with effect from 9 September 2005, and crucially the right does NOT depend on whether the father was alive on 9 September 2005. This overruled the earlier two-judge decision in Prakash v Phulavati (2015). The practical effect: in modern HUF formations, the coparcener list includes daughters by birth alongside sons; the Karta succession can include the eldest daughter; partition rights extend equally to all coparceners regardless of gender.</p>
<p>HUF formation and PAN application is the standard transactional service - executing the HUF Deed (also called HUF Declaration or HUF Creation Deed) identifying the Karta, coparceners, members and source of HUF capital; obtaining PAN through Form 49A under Category 'HUF' from <a href="https://www.incometax.gov.in" target="_blank" rel="noopener">Income Tax Department</a> via <a href="https://www.protean-tinpan.com" target="_blank" rel="noopener">NSDL Protean</a> or <a href="https://www.utiitsl.com" target="_blank" rel="noopener">UTIITSL</a>; opening HUF bank account in HUF name (operated by Karta); planning the first year ITR (Form ITR-2, ITR-3 or ITR-4 depending on income profile). The critical advisory layer is HONEST scoping that HUF tax planning works only with genuine HUF capital (ancestral, will-gift, non-coparcener gift) - not by transferring an individual's salary or personal investments to HUF, which Section 64(2) clubs back. Patron's role is to deliver the transactional formation PLUS honest planning scoping - starting INR 3,999 (Exl GST and Govt. Charges).</p>


