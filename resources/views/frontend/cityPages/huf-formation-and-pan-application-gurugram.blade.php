                        <div class="faq-expanded__a"><p>The 2005 amendment to the Hindu Succession Act 1956 made daughters coparceners on equal footing with sons by birth. Subsequent Supreme Court rulings (Vineeta Sharma v Rakesh Sharma, 2020) confirmed that daughters have full coparcenary rights including the right to be Karta when they are the senior-most coparcener. A wife is a member of her husband's HUF but not a coparcener; she cannot be Karta of that HUF, though she retains coparcenary rights in her natal HUF.</p></div>
