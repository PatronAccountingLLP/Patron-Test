                        <h3 class="faq-expanded__q">What is HUF and how does it save tax?</h3>
                        <div class="faq-expanded__a"><p>HUF (Hindu Undivided Family) is a separate taxable entity under Section 2(31) of the Income-tax Act 1961. A Hindu, Sikh, Jain or Buddhist family can form an HUF and treat it as a second tax assessee with its own PAN, basic exemption (INR 4 lakh under the new regime), and Chapter VIA deductions. Family income from ancestral property, gifts and HUF investments is taxed in the HUF's hands and not in any individual's. The structure typically saves INR 50,000 to INR 90,000 per year for a middle-income family with ancestral rental or business income routeable through the HUF.</p></div>
