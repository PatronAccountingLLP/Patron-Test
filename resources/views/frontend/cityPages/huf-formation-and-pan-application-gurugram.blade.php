<div class="key-term"><strong>Section 171(9) Partial Partition Bar:</strong> partial partition of HUF property NOT recognised for tax purposes since 1 January 1979; only TOTAL PARTITION recognised under Section 171.</div>
<div class="key-term"><strong>Section 47(i) Partition Exemption:</strong> tax-free distribution of capital assets on total partition of HUF; no capital gains tax.</div>
<div class="key-term"><strong>Section 56(2)(x) Gift Tax:</strong> gifts to HUF from non-relatives exceeding INR 50,000 in a financial year are taxable as income; gifts from relatives are exempt.</div>
<div class="key-term"><strong>Form 49A:</strong> PAN application form for entities including HUF; Karta signs as authorised signatory.</div>
<div class="key-term"><strong>Vineeta Sharma Case:</strong> Vineeta Sharma v Rakesh Sharma (2020) 9 SCC 1; Supreme Court 3-judge bench decision dated 11 August 2020 confirming daughters are coparceners by birth retroactively from 9 September 2005.</div>
<div class="key-term"><strong>Total Partition:</strong> under Section 171 of Income Tax Act 1961 read with personal Hindu law; permanent dissolution of HUF; capital assets distributed among coparceners tax-free under Section 47(i).</div>


