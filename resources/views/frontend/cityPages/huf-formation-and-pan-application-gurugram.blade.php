                        <div class="faq-expanded__a"><p>HUF is recognised only for families following Hindu, Sikh, Jain or Buddhist religions under Hindu personal law. Muslim, Christian, Parsi and Jewish families cannot form an HUF. Inter-religious families have HUF status based on the Hindu side - typically the Karta's religion. Patron Accounting screens religion eligibility at intake to avoid invalid PAN applications which the Income-tax Department rejects on category-mismatch grounds.</p></div>
