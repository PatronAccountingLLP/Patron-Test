                    <p><strong>Key Terms for HUF Formation and PAN Application:</strong></p>
                    
<div class="key-term"><strong>HUF (Hindu Undivided Family):</strong> a joint family of Hindus, Sikhs, Buddhists or Jains forming a separate 'person' under Section 2(31)(ii) of the Income Tax Act 1961.</div>
<div class="key-term"><strong>Mitakshara:</strong> dominant school of Hindu law followed in most of India; coparcenary by birth; HUF tax planning relevant primarily under Mitakshara.</div>
<div class="key-term"><strong>Dayabhaga:</strong> school of Hindu law in West Bengal and parts of Assam; succession only on death of father; different HUF dynamics.</div>
<div class="key-term"><strong>Karta:</strong> manager of the HUF; traditionally senior-most male member; post Sujata Sharma v Manu Gupta (Delhi HC 2016) and Vineeta Sharma 2020, daughter can be Karta where she is the eldest coparcener.</div>
<div class="key-term"><strong>Coparcener:</strong> person who acquires right in HUF property by BIRTH and can demand partition; pre-2005 only male descendants up to 3 degrees from common ancestor; post 2005 daughters included on same footing as sons.</div>
<div class="key-term"><strong>Member:</strong> non-coparcener relative of Karta (wife, mother, daughter-in-law) who has no partition right but is entitled to maintenance and share on partition.</div>
