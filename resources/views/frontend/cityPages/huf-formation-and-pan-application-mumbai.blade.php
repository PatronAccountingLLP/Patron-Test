                        <h3 class="faq-expanded__q">What is an HUF and who can form one?</h3>
                        <div class="faq-expanded__a"><p>A Hindu Undivided Family (HUF) is a joint family unit recognised under Hindu personal law and treated as a separate 'person' under Section 2(31) of the Income-tax Act, 1961. It consists of all persons lineally descended from a common ancestor plus their spouses and children. HUF eligibility is restricted to Hindus, Buddhists, Jains and Sikhs - Muslims, Christians and Parsis have separate succession regimes and cannot form an HUF. The HUF gets its own PAN, files its own ITR and has its own basic exemption slab.</p></div>
