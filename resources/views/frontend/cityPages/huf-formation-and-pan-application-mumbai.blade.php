<!-- SECTION 15A: LEGAL FRAMEWORK -->
<section class="content-section">
    <div class="content-container">
        <div class="text-content">
            <h2 class="section-title">Legal and Regulatory Framework</h2>
            <div class="content-text">
                
                
                <p><strong>Hindu Succession Act, 1956</strong> (as amended by the Hindu Succession (Amendment) Act, 2005 effective 9 September 2005) - the foundational personal law for joint Hindu family and coparcenary rights. The 2005 Amendment extended coparcenary rights to daughters with the same rights as sons by birth.</p>
                <p style="margin-top:16px;"><strong>Income-tax Act, 1961</strong> - administered by <a href="https://www.incometax.gov.in" target="_blank" rel="noopener">Income-tax Department of India</a>:</p>
                <ul>
                    <li>Section 2(31) - HUF treated as a separate "person" for tax purposes.</li>
                    <li>Section 56(2)(x) - gifts to HUF from defined "relatives" (parents, spouse, siblings, etc. of karta and coparceners) are exempt; gifts from non-relatives above INR 50,000 in a financial year are taxable as Income from Other Sources.</li>
                    <li>Section 64(1) - clubbing of income from transfers to spouse, minor child, son's wife.</li>
                    <li>Section 64(2) - clubbing of income where a member converts self-acquired property into HUF property without adequate consideration; income continues to be taxed in the member's hands.</li>
                    <li>Section 171 - HUF partition; only TOTAL partition recognised for tax purposes; partial partition not recognised since Finance Act 1980.</li>
                    <li>Section 80C, 80D, 80G - personal deductions available to HUF only under old regime; not available under new regime (default for FY 2026-27).</li>
                </ul>
                <p style="margin-top:16px;"><strong>FY 2026-27 tax slabs</strong> - HUF taxed at the same rates as individuals; basic exemption INR 2.5 lakh under old regime and INR 4 lakh under new regime.</p>
                <p style="margin-top:16px;"><strong>PPF Scheme</strong> - HUF cannot open NEW PPF accounts since 2011 amendment; existing HUF PPF accounts opened before May 2005 may continue to maturity.</p>
