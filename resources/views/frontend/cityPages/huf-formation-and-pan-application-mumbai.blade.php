                        <h3 class="faq-expanded__q">What is Section 64(2) clubbing and why does it matter for HUFs?</h3>
                        <div class="faq-expanded__a"><p>Section 64(2) of the Income-tax Act, 1961 is the most common DIY HUF failure mode. If a member converts self-acquired property into HUF property WITHOUT adequate consideration, income from that property continues to be taxed in the member's hands - NOT the HUF's. This defeats the purpose of the HUF. The safe ways to seed an HUF are - ancestral property (already joint), gifts from defined 'relatives' (parents, spouse, siblings of karta and coparceners) under Section 56(2)(x), or genuine joint capital from existing joint sources. Patron designs the seeding before the HUF Deed is signed.</p></div>
