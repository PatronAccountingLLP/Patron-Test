                <h2 class="section-title">What HUF Formation Actually Means</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p><strong>HUF Formation</strong> is the process of declaring a Hindu Undivided Family by drafting a HUF Deed (Declaration of HUF) signed by the karta (head of family - traditionally the eldest male, now can include the eldest female coparcener post 2005), applying for the HUF's own PAN via Form 49A, opening a separate bank account in the HUF's name, and beginning to operate the HUF as a separate tax entity that files its own income-tax return.</p>
                    <p>The HUF's legal foundation is Hindu personal law - it consists of persons lineally descended from a common ancestor plus their spouses and children.</p>
                    <p>The HUF is a JOINT family unit under Hindu law - not a contract, not a registration, but a legal personality that arises from the existence of a joint Hindu family.</p>
                    <p>The Income-tax Act gives the HUF separate tax personhood once it has its own PAN.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for HUF Formation and PAN:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>HUF:</strong> Hindu Undivided Family - a joint family unit recognised under Hindu personal law and treated as a separate person under Section 2(31) of the Income-tax Act, 1961.</li>
                        <li><strong>Karta:</strong> The head of the HUF - traditionally the eldest male, now can include the eldest female coparcener (post 2005 Amendment). Manages HUF affairs and signs on behalf of the HUF.</li>
                        <li><strong>Coparceners:</strong> Members with a birthright in joint Hindu family property. Sons and (post 2005 Amendment) daughters of the karta and other coparceners are all coparceners.</li>
                        <li><strong>Members:</strong> All persons part of the HUF - coparceners plus their spouses (e.g. wife of a son), children, daughters-in-law.</li>
                        <li><strong>HUF Deed:</strong> Declaration of HUF - written record signed by karta and coparceners stating the family's common ancestor, names of members, initial capital and intent to operate as an HUF.</li>
                        <li><strong>Form 49A:</strong> PAN application form for an HUF filed via Protean eGov (formerly NSDL) or UTIITSL portal.</li>
                        <li><strong>Section 64(2):</strong> Income-tax Act clubbing provision - self-acquired property converted to HUF without adequate consideration; income from that property continues to be taxed in the member hands.</li>
                        <li><strong>Section 56(2)(x):</strong> Income-tax Act provision - gifts from relatives (defined term) are exempt; gifts from non-relatives above INR 50,000 in a financial year are taxable.</li>
                        <li><strong>Section 171:</strong> Income-tax Act provision on HUF partition - only TOTAL partition is recognised for tax purposes; partial partition not recognised since Finance Act 1980.</li>
                        <li><strong>ITR-2 / ITR-3:</strong> Income-tax return forms for HUFs - ITR-2 if no business or profession income, ITR-3 if HUF runs a business or profession.</li>
