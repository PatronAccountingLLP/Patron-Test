                        <h3 class="faq-expanded__q">Are daughters coparceners in an HUF after the 2005 Amendment?</h3>
                        <div class="faq-expanded__a"><p>Yes. The Hindu Succession (Amendment) Act, 2005 effective 9 September 2005 extended coparcenary rights to daughters with the same rights as sons by birth in joint Hindu family property. Daughters can seek partition, dispose of their share by will, and inherit coparcenary property on the same terms as sons. This is retroactive - daughters alive on 9 September 2005 became coparceners by birth in pre-existing HUFs. Many older HUF Deeds drafted before 2005 need amendment to acknowledge daughters' rights.</p></div>
