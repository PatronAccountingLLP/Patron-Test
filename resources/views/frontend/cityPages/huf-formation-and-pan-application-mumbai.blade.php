                    <div class="faq-expanded__item" id="faq-8">
                        <h3 class="faq-expanded__q">How does HUF partition work under Section 171?</h3>
                        <div class="faq-expanded__a"><p>Section 171 of the Income-tax Act, 1961 governs HUF partition. Critically, only a TOTAL partition is recognised for tax purposes - the entire HUF property must be divided among all coparceners and the HUF must cease to exist as a tax entity. PARTIAL partition (dividing only some assets or excluding some members) is NOT recognised since Finance Act 1980 - post-partition income from such partial partition continues to be taxed in the HUF's hands. A total partition requires (a) physical division of all assets, (b) execution of a partition deed, and (c) an order under Section 171 from the Assessing Officer recognising the partition.</p></div>
