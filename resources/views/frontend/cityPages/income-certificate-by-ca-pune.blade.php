                <h2 class="section-title">What Is an Income Certificate by CA</h2>
                <div class="content-text what-is-definition">
                    
                    <p><strong>Definition:</strong> It is an official statement issued and signed by a practising Chartered Accountant confirming an individual&#39;s total income for a specified period, itemised by source, after reviewing the supporting financial records.</p>
<p>The CA prepares the certificate after examining records such as the <strong>ITR, computation of income, bank statements, salary slips, or business profit and loss</strong>, and states the total annual income and its sources - salary, business profits, profession, rent, and investments. It carries the CA&#39;s signature, seal, membership number, and a mandatory <strong>18-digit UDIN</strong> verifiable at the ICAI portal. Only a practising CA with a Certificate of Practice can issue it.</p>
<p>This is different from the <strong>government income certificate</strong> issued by a revenue authority (such as a tehsildar) for caste, scholarship, or reservation purposes. The CA income certificate is a professional financial attestation, often paired with a <a href="https://www.patronaccounting.com/net-worth-certificate">net worth certificate</a> for visa applications, and is supported by the individual&#39;s <a href="https://www.patronaccounting.com/accounting-services">ITR and accounting records</a>.</p>

