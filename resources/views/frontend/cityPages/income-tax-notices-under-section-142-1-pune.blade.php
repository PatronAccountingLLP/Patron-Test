                    <div class="faq-expanded__item" id="faq-3">
                        <h3 class="faq-expanded__q">How do I reply to a 142(1) notice?</h3>
                        <div class="faq-expanded__a"><p>Log in to incometax.gov.in. Navigate to Pending Actions > e-Proceedings. Select the notice. For 142(1)(i): file the pending return. For 142(1)(ii)/(iii): upload documents in PDF and enter written explanations. Submit within the 15-day deadline. Save the acknowledgment ID. If more time needed, file an extension request through the portal.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-4">
