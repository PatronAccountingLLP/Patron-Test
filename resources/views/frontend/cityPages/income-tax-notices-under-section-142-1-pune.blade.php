                        <div class="faq-expanded__a"><p>A preliminary inquiry notice from the Assessing Officer under the Income Tax Act. It can ask you to: (a) file your pending return, (b) produce specific books of accounts and documents, or (c) provide written information on specific points. It is not an accusation of tax evasion - it is a request for information to enable correct assessment. Issued through NeAC under the Faceless Assessment Scheme.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-2">
                        <h3 class="faq-expanded__q">Why did I receive a 142(1) notice?</h3>
                        <div class="faq-expanded__a"><p>Common Pune triggers: non-filing despite taxable transactions in AIS/SFT data, AIS vs ITR mismatch (capital gains, property, bank deposits not declared), TDS credit mismatch with 26AS, HRA claims without documentation, ESOP/RSU perquisite not matching Form 16/12BA, Section 44AD/44ADA presumptive income inconsistent with GST turnover, or high-value transactions flagged in SFT.</p></div>
