                    </div>
                    <div class="faq-expanded__item" id="faq-5">
                        <h3 class="faq-expanded__q">Can a 142(1) notice lead to scrutiny assessment?</h3>
                        <div class="faq-expanded__a"><p>Yes. If the AO is not satisfied with the response, the case can escalate to scrutiny assessment under Section 143(2) - a more intensive assessment involving detailed examination of all income, deductions, and transactions. A thorough, well-documented response at the 142(1) stage is the best way to prevent escalation.</p></div>
