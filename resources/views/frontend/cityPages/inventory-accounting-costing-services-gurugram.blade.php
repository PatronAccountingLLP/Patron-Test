            <div class="column-content">
                <h2 class="section-title">What Is Inventory Accounting and Costing?</h2>
                <div class="content-text what-is-definition">
                    <p>Boiled down, inventory accounting places a value on the goods you hold and keeps a trail of how they move, so both the closing inventory on the balance sheet and the cost of goods sold in the P&amp;L stand on firm ground. Costing runs alongside, working out exactly what a single unit costs to source or to build.</p>
                    <p>For an Udyog Vihar or Cyber City trader, the task centres on valuing bought-in goods on FIFO or weighted average and keeping COGS clear at SKU level. For a Manesar manufacturer it reaches into multi-level bills of materials, labour and overhead absorption, and a job, process, or standard costing choice to value WIP and finished goods.</p>
                    <p>It seldom stands alone: reconciliation matches counts to book stock, while ageing exposes the slow and dead lines. For the systems it sits inside, see <a href="/tally-accounting/gurugram">Tally Accounting in Gurugram</a> and <a href="/zoho-books-accounting/gurugram">Zoho Books Accounting in Gurugram</a>.</p>
                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Inventory Accounting in Gurugram:</strong></p>
                    <p><strong>FIFO:</strong> First-in, first-out, where your oldest buys are taken to sell first, so closing stock ends up priced at the newest rates.</p>
                    <p><strong>Weighted Average Cost:</strong> One pooled rate per unit, found by spreading the total cost of available goods over the total units in hand.</p>
                    <p><strong>Net Realisable Value:</strong> The likely sale price minus the cost still to finish and sell; AS 2 holds stock at cost or this, whichever is lower.</p>
                    <p><strong>Bill of Materials (BOM):</strong> A tiered recipe naming every component and quantity that builds into one finished unit.</p>
                    <p><strong>COGS:</strong> Cost of goods sold, derived as opening stock plus purchases or production, less the closing stock left on hand.</p>
