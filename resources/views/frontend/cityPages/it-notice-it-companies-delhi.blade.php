                <h2 class="section-title">What Is an Income Tax Notice for a Delhi IT Company?</h2>
                <div class="content-text what-is-definition">
                    
                    <p>An income tax notice for an IT or software company is any of the following statutory communications served on a Delhi-registered IT-sector PAN by the Pr CCIT Delhi Region structure or by the apex faceless administrations at Delhi: Section 142(1) (call for information), Section 143(2) (scrutiny pre-cursor), Section 143(3) or 144B (faceless assessment from NaFAC HQ or ReFAC Delhi), Section 92CA (reference to Transfer Pricing Officer Delhi), Section 147, 148, or 148A (reassessment), Section 156 (demand notice), or Section 271 (penalty proceedings).</p>
<p>Delhi IT-sector notices revolve around six recurring issue clusters: (a) Transfer Pricing under Chapter X Sections 92-92F handled by the dedicated Pr CIT IT-TP Delhi; (b) Section 10AA SEZ tax holiday disputes for Delhi-area SEZ units; (c) ESOP perquisite taxation under Section 17(2)(vi) at exercise plus Section 192 TDS; (d) Section 195 TDS on foreign software payments post Engineering Analysis Centre v CIT (2021) Supreme Court - Delhi HC is the source jurisprudence forum on Section 9(1)(vi) royalty; (e) Section 80-IAC DPIIT-recognised startup tax holiday eligibility; (f) Section 144B faceless assessment variation orders from ReFAC Delhi or directly from NaFAC HQ at Mayur Bhawan.</p>
<p>Key compliance pivot: Delhi IT-sector tax assessments are routinely picked up via CASS for substantial international transactions disclosed in Form 3CEB, Section 10AA SEZ deductions, large ESOP perquisite disclosures, or DPIIT startup status with 80-IAC claims. Given that NaFAC HQ is at Delhi, Delhi corporate cases may be assessed directly by the national faceless administration rather than only through Regional FACs.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for IT Notice Delhi:</strong></p>
                    <ul>
    <li><strong>Transfer Pricing (TP) - Chapter X, Sections 92 to 92F:</strong> Arm's length price regime for international transactions between associated enterprises. Mandatory for Indian IT subsidiaries of foreign parents, GCCs, captive software development centres. Delhi has the dedicated Pr CIT IT-TP plus the apex Pr CCIT (International Taxation) at Civic Centre Minto Road.</li>
    <li><strong>Associated Enterprise (Section 92A):</strong> Direct or indirect 26% voting power, common management, common board majority, or other prescribed relationships.</li>
    <li><strong>Six TP Methods (Rule 10B):</strong> CUP, RPM, CPM, PSM, TNMM (dominant for IT), and Other Method.</li>
    <li><strong>Section 92CA TPO Reference:</strong> Where the AO refers the international transaction to the Transfer Pricing Officer for ALP determination. TPO has 21-month window; binding on AO.</li>
    <li><strong>Section 10AA SEZ Deduction:</strong> 100% deduction for first 5 years, 50% for next 5 years, then 50% (subject to reserve) for additional 5 years for IT/ITeS units in approved SEZs. Sunset for new units 31 March 2020.</li>
    <li><strong>Section 80-IAC DPIIT Startup Tax Holiday:</strong> 100% deduction for any 3 consecutive years out of 10 from incorporation, for DPIIT-recognised startups with turnover up to Rs 100 crore.</li>
    <li><strong>ESOP Perquisite (Section 17(2)(vi)):</strong> Taxable as salary at exercise - FMV at allotment minus exercise price. Employer deducts TDS under Section 192.</li>
    <li><strong>Section 191A DPIIT Startup ESOP Deferral:</strong> Tax on ESOP perquisite deferred to earliest of 5 years from end of relevant Tax Year, date of sale of shares, or date of leaving employment.</li>
    <li><strong>Section 195 TDS plus Section 9(1)(vi) Royalty:</strong> Post Engineering Analysis Centre v CIT (2021) SC, off-the-shelf software for internal use is NOT royalty - hence no Section 195 TDS. Delhi HC was the source jurisprudence forum.</li>
    <li><strong>NaFAC (National Faceless Assessment Centre):</strong> Apex faceless assessment administration for all India - HQ at 6th Floor, Mayur Bhawan, Connaught Circus, New Delhi 110001.</li>
