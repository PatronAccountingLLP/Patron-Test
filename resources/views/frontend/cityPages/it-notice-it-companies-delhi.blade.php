    <li><strong>Pr CCIT (International Taxation) Delhi:</strong> The apex international tax administration at 3rd Floor, E-2 Block, Civic Centre, Minto Road, New Delhi 110002. Supervises CCIT (International Tax)-1, 2, 3 Delhi.</li>
