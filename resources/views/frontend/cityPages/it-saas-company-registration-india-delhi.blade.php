                        <h3 class="faq-expanded__q">Is GST applicable on export of SaaS or IT services from foreign clients?</h3>
                        <div class="faq-expanded__a"><p>Export of services is zero-rated under Section 16 of the IGST Act 2017 when all five conditions in Section 2(6) are met: (1) supplier located in India, (2) recipient located outside India, (3) place of supply outside India (per Section 13), (4) payment received in convertible foreign exchange, (5) supplier and recipient are not merely establishments of the same legal person. With a Letter of Undertaking (Form GST RFD-11) in place, the SaaS company invoices foreign clients at zero IGST and claims refund of unutilised input tax credit under Section 54 of the CGST Act 2017. Without LUT, 18% IGST is charged upfront and refund applied for - a 3 to 6 month cash-flow drag.</p></div>
