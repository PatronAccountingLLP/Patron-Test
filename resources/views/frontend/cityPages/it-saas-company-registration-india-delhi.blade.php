                        <div class="faq-expanded__a"><p>Pvt Ltd is the default for IT/SaaS startups with fundraising or ESOP plans. Only Pvt Ltd allows equity issuance to angels, VC and PE; only Pvt Ltd allows ESOP under Section 62 of the Companies Act 2013. Both Pvt Ltd and LLP are eligible for Section 80-IAC tax holiday. LLP works for IT consulting or service-only firms that will never raise external equity - lower compliance overhead (Form 8 and Form 11 annually; audit only if turnover above INR 40 lakh or contribution above INR 25 lakh) and partner capital flexibility. OPC is for solo founders pre-fundraising; conversion to Pvt Ltd is mandatory before the first equity round.</p></div>
