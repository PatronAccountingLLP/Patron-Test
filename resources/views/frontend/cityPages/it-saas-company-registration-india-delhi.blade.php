                        <h3 class="faq-expanded__q">What is LUT and when must it be filed?</h3>
                        <div class="faq-expanded__a"><p>LUT (Letter of Undertaking) is Form GST RFD-11 filed on the GST portal under the second proviso to Section 16(3) of the IGST Act 2017. It permits a registered exporter to supply zero-rated services without payment of IGST. Eligibility: not prosecuted for any offence involving tax amount above INR 2.5 crore. LUT must be filed before the first export invoice of the financial year and is valid for one financial year (1 April to 31 March). It must be re-filed every 1 April. ARN is issued instantly on the GST portal. Without LUT, foreign-client invoices attract 18% IGST upfront with refund claimed later under Section 54.</p></div>
