                <h2 class="section-title">What Is IT SaaS Company Registration</h2>
                <div class="content-text what-is-definition">
                    
                    <p>IT SaaS company registration in India is the combined process of incorporating a software or technology business under the Companies Act 2013 and layering on the sector-specific tax and benefit registrations - GSTIN with LUT for zero-rated export of services under Section 2(6) of the IGST Act 2017, DPIIT recognition through the National Single Window System (NSWS) and Section 80-IAC IMB certification for the 100% startup tax holiday. The bundle is what converts a generic Pvt Ltd into an export-ready, tax-optimised SaaS company.</p>

<p>The MCA SPICe+ web form handles incorporation, PAN, TAN and GSTIN in one filing. The LUT is filed annually on the GST portal. DPIIT recognition is free and online through NSWS at nsws.gov.in. Section 80-IAC IMB approval is a separate application to the Inter-Ministerial Board with Form 1 supporting documents.</p>

<p>Delhi is a major hub for IT and SaaS founders - proximity to the corporate decision-makers of Indian enterprises and PSUs that are early SaaS customers, the Startup India and Invest India headquarters, the Income-tax Department's central charge for foreign-tax credit and APA matters, and a deep AD Category-I Bank presence. Patron's Delhi office coordinates the full stack from one place.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for IT SaaS Company:</strong></p>
                    <p><strong>LUT (Letter of Undertaking):</strong> Form GST RFD-11 filed annually on the GST portal to invoice foreign clients without paying IGST upfront.</p>
<p><strong>Export of Services:</strong> Defined under Section 2(6) of the IGST Act 2017. Requires all 5 conditions: supplier in India, recipient outside India, place of supply outside India, payment in convertible foreign exchange and not an intermediary service.</p>
<p><strong>DPIIT Recognition:</strong> Recognition as a startup by the Department for Promotion of Industry and Internal Trade. Unlocks IPR rebates, self-certification and access to Section 80-IAC application.</p>
<p><strong>IMB Certificate:</strong> Inter-Ministerial Board certification under Section 80-IAC. Separate from DPIIT recognition and granted only to ~1.8% of DPIIT startups based on innovation evidence.</p>
<p><strong>SAC Codes:</strong> Service Accounting Codes 998313 (IT consulting), 998314 (IT design and development), 998315 (hosting), 998316 (network management), 998319 (other IT) under the GST regime.</p>
<p><strong>Intermediary (Section 13(8) IGST):</strong> If the service is treated as intermediary, place of supply shifts to India and 18% GST applies even on foreign-client invoices.</p>
<p><strong>Section 80-IAC Tax Holiday:</strong> 100% tax holiday on profits for any 3 consecutive years in the first 10, for Pvt Ltd or LLP incorporated 1 April 2016 to 31 March 2030, turnover up to INR 100 crore, IMB-certified.</p>

