                        <h3 class="faq-expanded__q">What is the registration process for an IT or SaaS company in India?</h3>
                        <div class="faq-expanded__a"><p>IT or SaaS company registration in India is a sequenced bundle. Step 1: pick the entity (Pvt Ltd is the default for fundraising-ready SaaS; LLP for service-only consulting; OPC for solo pre-fundraise). Step 2: file MCA SPICe+ with software-specific NIC codes 62 and 63 in the MoA objects clause - 7 to 15 working days for CIN with PAN, TAN and GSTIN. Step 3: obtain GSTIN under Section 22 (mandatory above turnover threshold) or Section 25(3) (voluntary). Step 4: file Form GST RFD-11 LUT before first export invoice. Step 5: open AD Category-I Bank current account. Step 6: apply for DPIIT recognition through NSWS at nsws.gov.in (free, 4 to 6 weeks). Step 7: apply for Section 80-IAC IMB Form 1 separately (2 to 4 months, ~1.8% approval rate).</p></div>
