                    <p><strong>Key Terms for IT SaaS Company Registration:</strong></p>
                    
<div class="key-term"><strong>SaaS (Software as a Service):</strong> cloud-hosted software delivered on subscription model; revenue typically recurring (monthly or annual).</div>
<div class="key-term"><strong>OIDAR (Online Information and Database Access or Retrieval):</strong> special category of services under Section 2(17) IGST Act 2017; covers SaaS, digital content, cloud services delivered electronically.</div>
<div class="key-term"><strong>STPI:</strong> Software Technology Parks of India scheme administered by Ministry of Electronics and Information Technology (MEITY); promotes software exports through bonded warehouse, simplified customs and operational benefits.</div>
<div class="key-term"><strong>SEZ:</strong> Special Economic Zone under SEZ Act 2005 and SEZ Rules 2006; tax holiday under Section 10AA of Income Tax Act 1961.</div>
<div class="key-term"><strong>Section 10AA:</strong> tiered tax holiday for SEZ units - first 5 years 100 percent of export profits exempt; next 5 years 50 percent exempt; next 5 years 50 percent subject to SEZ Re-Investment Reserve.</div>
<div class="key-term"><strong>DPIIT Recognition:</strong> Department for Promotion of Industry and Internal Trade recognition under G.S.R. 108(E) dated 4 February 2026; eligible startups within 10 years of incorporation; turnover less than INR 100 crore; innovative or scalable model.</div>
<div class="key-term"><strong>Section 80-IAC Tax Holiday:</strong> 100 percent profit-linked tax holiday for 3 consecutive years out of 10 from incorporation; requires SEPARATE IMB certificate beyond DPIIT Recognition.</div>
<div class="key-term"><strong>IMB (Inter-Ministerial Board):</strong> board that issues certificates for Section 80-IAC eligibility; more stringent scrutiny than DPIIT Recognition; about 3,700 of approximately 1.97 lakh DPIIT-recognised startups have IMB certificates as of 2026.</div>
<div class="key-term"><strong>Section 56(2)(viib) Angel Tax:</strong> previously taxed share premium above FMV from residents (and non-residents from 1 April 2023); ABOLISHED by Finance (No 2) Act 2024 effective 1 April 2025.</div>
<div class="key-term"><strong>ESOP (Employee Stock Option Plan):</strong> under Section 62(1)(b) of Companies Act 2013; perquisite taxation at exercise under Section 17(2)(vi) IT Act; deferment under Section 192(1C) for DPIIT-eligible startups.</div>
<div class="key-term"><strong>LUT (Letter of Undertaking):</strong> mechanism under Rule 96A of CGST Rules 2017 for exporting services without payment of IGST; renewed annually before 31 March.</div>
<div class="key-term"><strong>FIRC (Foreign Inward Remittance Certificate):</strong> issued by AD Bank confirming receipt of foreign currency; purpose code P0801 for software development services.</div>
<div class="key-term"><strong>SOFTEX Form:</strong> form filed under MEITY framework for software exports above prescribed threshold; documents the export and value.</div>
<div class="key-term"><strong>Software as Literary Work:</strong> Section 2(o) of Copyright Act 1957 - computer programme treated as literary work for copyright purposes; automatic protection on creation.</div>
<div class="key-term"><strong>Section 3(k) Patents Act:</strong> bars computer programmes per se, algorithms and business methods from patentability; only software combined with hardware demonstrating technical effect may be patentable.</div>
<div class="key-term"><strong>Delaware C-Corp Flip:</strong> common structure where Indian SaaS company sets up Delaware C-Corp as parent for US-based fundraise; Indian Pvt Ltd becomes wholly-owned subsidiary.</div>


