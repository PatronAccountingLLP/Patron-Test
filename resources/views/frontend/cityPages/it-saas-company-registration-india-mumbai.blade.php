                <h2 class="section-title">What IT and SaaS Company Registration Actually Means</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p><strong>IT and SaaS Company Registration</strong> is the bundled setup of (a) the legal entity - typically a Private Limited Company under the Companies Act, 2013, with cap-table-aware MoA and AoA and ESOP-ready clauses; (b) the tax registrations - GST (with both Indian-B2B and export-of-services tracks), Section 115BAA opt-in, DPIIT Startup Recognition for Section 80-IAC tax holiday eligibility, and Section 56(2)(viib) angel-tax-abolition awareness.</p>
                    <p>(c) The export framework - LUT under IGST Section 16, optional STPI registration for export-oriented IT/ITES units, IEC code for foreign client payments.</p>
                    <p>(d) The data and intermediary framework - DPDPA 2023 consent / notice / breach framework, IT Act 2000 intermediary compliance where applicable, OIDAR-aware GST for B2C cross-border digital services.</p>
                    <p>(e) The operational registrations - Maharashtra Shops Act, Professional Tax, MSME / Udyam, PF / ESI when team grows.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for IT and SaaS Company Registration:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>SAC 9983:</strong> Service Accounting Code for Information Technology (IT) and Information Technology enabled Services (ITES) - standard 18% GST rate.</li>
                        <li><strong>LUT:</strong> Letter of Undertaking under Section 16 of the IGST Act, 2017 - annual filing on the GST portal that allows export of services without payment of IGST.</li>
                        <li><strong>OIDAR:</strong> Online Information and Database Access Retrieval services - cross-border digital services where the recipient is in India and the supplier is outside India; specific GST treatment for B2C OIDAR.</li>
                        <li><strong>DPIIT Startup:</strong> Recognition under the Startup India Action Plan administered by Department for Promotion of Industry and Internal Trade (DPIIT); 10-year window post-incorporation; unlocks Section 80-IAC tax holiday and other startup benefits.</li>
                        <li><strong>Section 80-IAC:</strong> Income-tax Act provision allowing 100% tax deduction for any 3 consecutive years out of first 10 years post-incorporation for DPIIT-recognised eligible startups.</li>
                        <li><strong>Section 56(2)(viib) ("Angel Tax"):</strong> Pre Finance Act 2024 - excess over fair value on share issuance was taxed as Income from Other Sources. ABOLISHED from FY 2024-25 onwards for ALL investors.</li>
                        <li><strong>Section 115BAA:</strong> 22% concessional income-tax rate (plus surcharge and cess) for domestic companies that opt for the regime and forgo certain deductions; effective rate approximately 25.17%.</li>
                        <li><strong>STPI:</strong> Software Technology Parks of India - scheme under Ministry of Electronics and Information Technology (Meity) for export-oriented IT/ITES units; provides single-window FEMA and customs facilitation.</li>
                        <li><strong>ESOP:</strong> Employee Stock Option Plan under Section 62 of the Companies Act, 2013 - share-based employee benefits; cap-table planning critical from incorporation.</li>
                        <li><strong>SDF (Significant Data Fiduciary):</strong> Threshold designation under DPDPA, 2023 triggering enhanced obligations - DPO appointment, Data Protection Impact Assessment, audit; likely applies to most consumer SaaS and enterprise SaaS handling significant personal data.</li>
