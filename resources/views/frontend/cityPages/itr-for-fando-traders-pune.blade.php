<section class="content-section">
    <div class="content-container">
        <div class="text-content">
            <h2 class="section-title">Legal and Compliance Framework for F&amp;O Trader ITR</h2>
            <div class="content-text">
                
                <p><strong>Section 43(5), IT Act:</strong> F&amp;O trading = non-speculative business income under PGBP head. NOT speculative. NOT capital gains.</p>
<p><strong>ITR Form:</strong> ITR-3 for individuals and HUFs. ITR-4 only if opting for presumptive (44AD).</p>
<p><strong>Turnover:</strong> Absolute sum of profit and loss on each trade (per ICAI Guidance Note). Expired options premium included.</p>
<p><strong>Tax Audit (Section 44AB):</strong> Mandatory if turnover > Rs 1 crore (Rs 10 crore if 95%+ digital). Form 3CB-3CD. Due: 30 September. Penalty: 0.5% of turnover or Rs 1,50,000 (Section 271B).</p>
<p><strong>Presumptive (Section 44AD):</strong> Turnover up to Rs 3 crore. Minimum 6% (digital) or 8% (cash) profit. No books, no audit. File ITR-4.</p>
<p><strong>Loss Set-Off:</strong> Against house property, capital gains, other sources. NOT against salary. Carry forward 8 years against non-speculative business income.</p>
<p><strong>Advance Tax:</strong> Mandatory if expected tax > Rs 10,000. Four quarterly instalments. Interest: 234B (1% p.m.), 234C (1% p.m.).</p>
