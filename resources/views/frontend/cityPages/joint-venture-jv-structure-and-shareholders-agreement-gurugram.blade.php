                        <div class="faq-expanded__a"><p>SHA stamp duty is state-specific under the Indian Stamp Act 1899 and the relevant state Stamp Acts. Gurugram rates are typically INR 100 to INR 600 for non-monetary instruments; Maharashtra rates are higher (especially with monetary consideration); Karnataka has its own schedule. Under-stamping renders the SHA inadmissible as evidence in court proceedings under Section 35 of the Indian Stamp Act. The instrument must be stamped before or at the time of execution. Patron computes the correct stamp value before signing and procures e-stamp paper or equivalent.</p></div>
