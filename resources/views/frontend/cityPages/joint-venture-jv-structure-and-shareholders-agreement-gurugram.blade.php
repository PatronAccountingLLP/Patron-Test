                    <p><strong>Key Terms for Joint Venture and SHA:</strong></p>
                    
<div class="key-term"><strong>Joint Venture (JV):</strong> collaboration between two or more parties to undertake a specific business venture; can be Equity JV (separate entity) or Contractual JV (no separate entity).</div>
<div class="key-term"><strong>Equity JV:</strong> JV structured as a separate legal entity (typically Pvt Ltd or LLP) jointly owned by partners.</div>
<div class="key-term"><strong>Contractual JV:</strong> JV structured as a pure contractual arrangement without forming a new entity.</div>
<div class="key-term"><strong>Shareholders Agreement (SHA):</strong> private contract between shareholders governing rights and obligations; not filed with MCA.</div>
<div class="key-term"><strong>Articles of Association (AoA):</strong> public document filed with MCA; governs the company's internal rules; amendment under Section 14 Companies Act 2013 read with Section 5.</div>
<div class="key-term"><strong>Reserved Matters:</strong> matters in the SHA requiring affirmative voting or specific shareholder consent - typically change of business, capital structure changes, asset disposal above threshold, related party transactions, dividend declaration.</div>
<div class="key-term"><strong>ROFR (Right of First Refusal):</strong> existing shareholders have the right to match a third-party offer before the selling shareholder can complete the sale.</div>
<div class="key-term"><strong>ROFO (Right of First Offer):</strong> selling shareholder must first offer the shares to existing shareholders before approaching third parties.</div>
<div class="key-term"><strong>Tag-Along Right (TAR):</strong> minority protection right allowing minority to sell on same terms when majority sells; ensures minority is not stranded.</div>
<div class="key-term"><strong>Drag-Along Right (DAR):</strong> majority right to force minority to sell their shares in a complete exit; ensures acquirer can take 100 percent.</div>
<div class="key-term"><strong>Anti-Dilution Protection:</strong> mechanism to protect investor from value erosion on subsequent rounds at lower price; three variants - full ratchet (most protective), weighted average broad-based (most common), weighted average narrow-based.</div>
<div class="key-term"><strong>Liquidation Preference:</strong> preference share class right to receive specified amount before equity shareholders in liquidation event; participating or non-participating; 1x or 2x typical multiple.</div>
<div class="key-term"><strong>Deadlock:</strong> situation where JV partners cannot agree on a key matter and the JV cannot proceed; resolved via SHA-provided mechanism.</div>
<div class="key-term"><strong>Russian Roulette:</strong> deadlock resolution where one party offers a price and the other must either buy or sell at that price; deters frivolous deadlocks because the offering party must be prepared to sell at the same price.</div>
<div class="key-term"><strong>Texas Shoot-out:</strong> deadlock resolution where both parties submit sealed bids; highest bidder buys out the other at their bid price.</div>
<div class="key-term"><strong>Press Note 3 of 2020:</strong> DPIIT Press Note dated 17 April 2020 mandating government approval for FDI from land-border countries (China, Bangladesh, Pakistan, Bhutan, Nepal, Myanmar, Afghanistan) regardless of sector.</div>
<div class="key-term"><strong>Section 5 Competition Act 2002:</strong> combination thresholds (asset and turnover) for mandatory CCI pre-merger notification; current thresholds per Notification 27 March 2024.</div>


