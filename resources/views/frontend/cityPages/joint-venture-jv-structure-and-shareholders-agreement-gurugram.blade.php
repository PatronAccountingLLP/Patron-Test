                <h2 class="section-title">What Is JV Structure and Shareholders Agreement</h2>
                <div class="content-text what-is-definition">
                    
                    
<p><strong>A Joint Venture (JV) is a collaboration between two or more parties to undertake a specific business venture. The structure has two material decisions: (1) the VEHICLE - whether to form an Equity JV (separate legal entity jointly owned by partners) or a Contractual JV (pure contract without new entity); (2) the GOVERNANCE - the rights and obligations between JV partners codified through a Shareholders Agreement plus AoA amendment.</strong></p>
<p>EQUITY JV is the most common form - a new Private Limited Company or LLP is incorporated jointly by the JV partners; each partner contributes capital, holds shares or partnership interest in agreed proportion, and operates the JV through the corporate governance framework. The benefit: limited liability protection, independent legal personality, clean accounting, easier exit mechanisms. The trade-off: corporate compliance burden including ROC filings, statutory audit, board meetings; tax inefficiency in some cases due to double taxation (corporate income tax plus dividend distribution).</p>
<p>CONTRACTUAL JV is a pure agreement between parties without forming a new entity - each party retains its existing business but agrees to cooperate on a defined scope, share revenue or profit, allocate responsibilities. The benefit: no new entity to set up, simpler tax flow-through (each party reports its share), faster setup. The trade-off: no limited liability protection, each party fully exposed for JV liabilities, harder to scale, no separate brand or balance sheet, exit can be messier.</p>
<p>SHAREHOLDERS AGREEMENT (SHA) is the governance backbone of an Equity JV. It is a PRIVATE CONTRACT between the shareholders (and typically the company too) - NOT a public document filed with <a href="https://www.mca.gov.in" target="_blank" rel="noopener">MCA</a>. It governs the rights and obligations between shareholders inter se. Critically, an SHA on its own is enforceable only BETWEEN THE CONTRACTING SHAREHOLDERS. To make the provisions binding on the COMPANY itself and on future shareholders, the relevant provisions must be incorporated into the company's ARTICLES OF ASSOCIATION (AoA) via amendment under SECTION 14 read with Section 5 of the Companies Act 2013. This dual structure - SHA plus AoA amendment - is the standard Indian practice.</p>
<p>Key SHA provisions cover: (a) capital contribution and shareholding pattern; (b) reserved matters or affirmative voting items - matters requiring specific shareholder consent (typically change of business, capital structure changes, asset disposal above threshold, related party transactions, dividend declaration); (c) board composition and director nomination rights; (d) transfer restrictions including Right of First Refusal (ROFR) and Right of First Offer (ROFO) under Section 58(2) Companies Act 2013; (e) tag-along right (TAR) protecting minority and drag-along right (DAR) protecting majority; (f) anti-dilution protection (full ratchet, weighted average broad-based or narrow-based); (g) liquidation preference for preference share class under Section 43 Companies Act; (h) information rights including financial statements, business plan, board minutes; (i) non-compete and non-solicit during JV term and after exit; (j) confidentiality; (k) deadlock resolution mechanisms (Russian Roulette, Texas Shoot-out, mediation-then-arbitration, buy-sell with independent expert valuation); (l) exit mechanisms (IPO, trade sale, secondary sale, buyback, ROFR-triggered exit); (m) dispute resolution and arbitration framework under Arbitration and Conciliation Act 1996; (n) term and termination.</p>
<p>For foreign-partner JVs, FEMA (Non-Debt Instruments) Rules 2019 governs capital inflow with valuation under Para 4 Schedule I, Form FC-GPR within 30 days of share allotment via AD Bank on <a href="https://www.rbi.org.in" target="_blank" rel="noopener">RBI</a> FIRMS portal, and Press Note 3 of 2020 government approval check for land-border country partners via <a href="https://www.dpiit.gov.in" target="_blank" rel="noopener">DPIIT</a>. For large JVs, Competition Act 2002 Section 5 combination thresholds (per Notification 27 March 2024) trigger mandatory pre-merger filing with the <a href="https://www.cci.gov.in" target="_blank" rel="noopener">Competition Commission of India</a> in Form I or Form II with standstill obligation until clearance.</p>
<p>Patron's role spans the full JV structuring and SHA drafting engagement - JV vehicle recommendation, term sheet refinement, SHA drafting with full provision suite, AoA amendment drafting under Section 14, FEMA compliance roadmap if foreign partner, Competition Act Section 5 combination check, post-execution corporate actions (share allotment, Form PAS-3, Form MGT-14 for special resolutions, Form FC-GPR for foreign partner share allotment), starting INR 99,999 (Exl GST and Govt. Charges).</p>


