                    <div class="faq-expanded__item" id="faq-4">
                        <h3 class="faq-expanded__q">What types of joint venture structures are allowed in India?</h3>
                        <div class="faq-expanded__a"><p>Two structures dominate: Equity JV (an incorporated Pvt Ltd or LLP under the Companies Act 2013 or LLP Act 2008) with shareholders holding equity in proportion to their contribution; and Contractual JV (a private contract under the Indian Contract Act 1872 without a separate legal entity), typically used for time-bound projects, EPC consortia and technology collaborations. The choice is driven by liability appetite (equity JV gives limited liability), brand and exit horizon (equity JV is required for IPO or strategic sale), tax position (Section 79 carry-forward considerations on change in shareholding) and the regulatory route applicable to the partners.</p></div>
