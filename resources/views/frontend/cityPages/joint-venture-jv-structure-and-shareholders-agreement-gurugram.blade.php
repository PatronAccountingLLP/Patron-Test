                        <h3 class="faq-expanded__q">What are the essential clauses in an Indian SHA?</h3>
                        <div class="faq-expanded__a"><p>A standard Indian SHA covers: shareholding and capital structure, board composition with nominee director and observer rights, reserved matters (a 15 to 20 item list requiring minority consent), transfer restrictions (lock-in, ROFR, ROFO, prohibited transferees), anti-dilution (broad-based weighted average default), drag-along with fair-value floor, tag-along on pro-rata, pre-emptive rights, information rights (monthly MIS, quarterly financials, audited annuals), non-compete and non-solicit, confidentiality and IP, dividend policy, exit mechanisms (IPO, strategic sale, put/call, buy-back), dispute resolution (arbitration under the Arbitration and Conciliation Act 1996), reps and warranties, and termination and winding up.</p></div>
