                    <div class="faq-expanded__item" id="faq-1">
                        <h3 class="faq-expanded__q">What is the difference between a JV agreement and a shareholders agreement?</h3>
                        <div class="faq-expanded__a"><p>A Joint Venture Agreement (JVA) is the overarching contract that sets out the commercial bargain between the JV partners - business scope, capital contributions, profit-sharing, IP licensing, supply or services arrangements and JV-specific operational terms. A Shareholders Agreement (SHA) is the corporate-governance agreement among the shareholders of the JV company covering board composition, reserved matters, transfer restrictions, anti-dilution, drag-along, tag-along and exit. In equity JVs both are typically signed; in contractual JVs only the JVA exists since there is no incorporated entity.</p></div>
