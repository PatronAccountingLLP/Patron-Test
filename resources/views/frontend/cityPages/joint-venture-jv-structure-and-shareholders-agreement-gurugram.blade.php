                        <h3 class="faq-expanded__q">What are drag-along and tag-along rights?</h3>
                        <div class="faq-expanded__a"><p>Drag-along is the majority shareholder's right to force minority shareholders to sell their shares on the same terms when a qualifying third-party buyer acquires the company - prevents holdout in an exit transaction. Tag-along is the inverse - minority shareholder's right to join a majority sale on the same terms, preventing the minority from being stranded when promoters sell out. Drag-along is typically negotiated with a fair-value floor (last-round valuation or a multiple of invested capital) to prevent forced sales below market. Tag-along is usually on a pro-rata basis.</p></div>
