                    <div class="faq-expanded__item" id="faq-2">
                        <h3 class="faq-expanded__q">Is a shareholders agreement legally enforceable in India?</h3>
                        <div class="faq-expanded__a"><p>Yes. The Supreme Court in Vodafone International Holdings BV v Union of India (2012) 6 SCC 613 affirmed that SHA provisions are binding contracts enforceable between the parties under the Indian Contract Act 1872. However, Section 6 of the Companies Act 2013 provides that the Act prevails over the AoA which prevails over the SHA. Where the SHA and AoA conflict, the AoA prevails to the extent of conflict. Patron's entrenchment-first approach uses Section 5 of the Companies Act 2013 to lock critical SHA provisions into the AoA - they survive change-of-control, third-party transfers and partner death.</p></div>
