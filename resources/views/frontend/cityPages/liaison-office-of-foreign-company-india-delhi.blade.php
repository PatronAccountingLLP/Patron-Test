            <div class="column-content">
                <h2 class="section-title">What Is a Liaison Office of a Foreign Company</h2>
                <div class="content-text what-is-definition">
                    
                    <p>A Liaison Office of a foreign company in India is a non-commercial place of business established by a foreign company under Foreign Exchange Management (Establishment in India of a Branch Office, Liaison Office or Project Office or any other place of business) Regulations 2016 - Notification FEMA 22(R)/2016-RB. The LO acts purely as a representative office. It is funded entirely by inward remittance from the parent. It cannot earn income, undertake commercial or industrial activity, borrow or lend, or directly or indirectly acquire immovable property in India (lease up to 5 years is permitted).</p>

<p>The LO is approved by the RBI through an AD Category-I Bank using Form FNC. On approval the RBI allots a Unique Identification Number (UIN). The LO must obtain a Permanent Account Number from the Income-tax Department and register as a foreign company with the MCA under Section 380 of the Companies Act 2013 by filing Form FC-1. Annual filings include Form FC-3 with the MCA and an Annual Activity Certificate (AAC) certified by a Chartered Accountant.</p>

<p>Delhi is a common LO base for foreign companies wanting proximity to central ministries, regulators, embassy commercial sections and headquarters of large Indian corporates and PSUs. Patron's Delhi office coordinates AD Bank selection, RBI filings, MCA Form FC-1 registration and where applicable the Superintendent of Police intimation required by Delhi.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Liaison Office:</strong></p>
                    <p><strong>AD Category-I Bank:</strong> Authorised Dealer Bank (typically a scheduled commercial bank) that routes the LO application from the foreign parent to the RBI and handles all FEMA reporting.</p>
<p><strong>Form FNC:</strong> The application form for establishing a Branch, Liaison or Project Office in India - filed through the AD Bank to the RBI.</p>
<p><strong>UIN (Unique Identification Number):</strong> Allotted by the RBI after Form FNC approval. Used for all subsequent FEMA reporting.</p>
<p><strong>Annual Activity Certificate (AAC):</strong> Certified by a Chartered Accountant and filed by 31 March each year with the AD Bank and the Director-General of Income-tax (International Taxation), New Delhi. Confirms that the LO did only permitted activities during the year.</p>
<p><strong>Form FC-1 and FC-3:</strong> FC-1 is the first-time foreign-company registration with the MCA under Section 380 Companies Act 2013 (within 30 days of establishment). FC-3 is the annual return and financial statement filing under Section 381.</p>
<p><strong>Branch Office (BO) vs Project Office (PO) vs LO:</strong> BO can do commercial activity, taxed at 40%; PO is project-specific (typically EPC contracts); LO is non-commercial only.</p>
<p><strong>Regulation 4(b) FEMA 22(R):</strong> Lists the four permitted LO activities - representation, import/export promotion, technical/financial collaboration promotion and communication channel.</p>

