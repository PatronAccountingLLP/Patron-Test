            <h2 class="section-title">Legal and Compliance Framework</h2>
            <div class="content-text">
                
                <p><strong>Foreign Exchange Management Act, 1999:</strong> Parent statute. Sets the rules for foreign exchange transactions and foreign company presence in India.</p>
                <p><strong>Foreign Exchange Management (Establishment in India of a Branch Office, Liaison Office or Project Office or any other place of business) Regulations, 2016:</strong> Notification FEMA 22(R)/2016-RB dated 31 March 2016. Regulation 4(b) lists the four permitted LO activities. Regulation 5 covers procedure.</p>
                <p><strong>RBI FED Master Direction No 10/2015-16:</strong> Consolidated procedural directions for BO, LO and PO. Updated periodically; current version should be checked on RBI website.</p>
                <p><strong>Companies Act, 2013:</strong> Section 380 (registration of foreign companies via Form FC-1 within 30 days of establishment), Section 381 (annual filings via Form FC-3 and FC-4), Section 387 (penalties for non-compliance).</p>
                <p><strong>Income-tax Act, 1961:</strong> PAN under Section 139A; ITR filing under Section 139 (NIL return for LO with no Indian income); permanent establishment risk under Article 5 of relevant DTAA if activities drift beyond permitted scope.</p>
