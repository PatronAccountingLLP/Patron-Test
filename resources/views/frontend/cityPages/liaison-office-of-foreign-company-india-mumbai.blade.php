                        <h3 class="faq-expanded__q">What is a Liaison Office (LO) of a foreign company in India?</h3>
                        <div class="faq-expanded__a"><p>A Liaison Office (LO) is a place of business of a foreign company established in India to act as a communication channel between the parent abroad and Indian businesses. The LO is non-commercial - it cannot earn income in India, cannot enter contracts in its own name, and cannot undertake any trading or industrial activity. Permitted activities (per Schedule II of FEMA 22(R)/RB-2016) are limited to representing the parent, promoting export / import, promoting technical / financial collaborations, and acting as a communication channel. The LO is wholly funded by inward remittances from the parent abroad.</p></div>
