        <div class="two-column">
            <div class="column-content">
                <h2 class="section-title">What a Liaison Office Actually Is</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p>A <strong>Liaison Office (LO)</strong> is a "place of business" of a foreign company in India established to act as a communication channel - between the foreign parent abroad and Indian businesses, customers, suppliers, regulators, partners and the local market.</p>
                    <p>The LO does NOT have a separate legal personality from the foreign parent (it is treated as a branch of the parent for legal purposes) but it is treated as a "foreign company" under Section 2(42) and Section 380 of the Companies Act, 2013 and has its own PAN, TAN, FEMA reporting obligations and RoC registration.</p>
                    <p>The LO's funding flows ONE WAY - from the parent abroad into the LO's AD Cat-I bank account in Mumbai - and is spent on permitted expenses (rent, salaries, travel, communication, administration). The LO cannot bill anyone, cannot issue invoices in its own name, cannot enter contracts as a counterparty, and cannot represent the parent as a contracting party in commercial dealings.</p>
                    <p>It CAN attend meetings, share information, demonstrate products (without sale), gather market intelligence, build relationships and promote technical / financial collaborations. The taxability of the LO is therefore typically NIL (no income earned in India) but annual ITR filing is mandatory and certain reverse-charge GST / withholding tax obligations apply on outbound and inter-affiliate payments.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Liaison Office Setup:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>LO:</strong> Liaison Office - representative presence of a foreign company in India; non-commercial; communication channel only.</li>
                        <li><strong>BO:</strong> Branch Office - permits commercial activities including export / import, professional services, research and consultancy; can earn income; taxed at 40% plus surcharge / cess.</li>
                        <li><strong>PO:</strong> Project Office - established for executing a specific contract in India (typically construction, infrastructure, EPC); time-bound; project-specific.</li>
                        <li><strong>Form FNC:</strong> Foreign Non-Citizen application form - RBI prescribed format at Annex B of FED Master Direction 10/2015-16; used for LO, BO and PO applications.</li>
                        <li><strong>AD Cat-I bank:</strong> Authorised Dealer Category-I bank - bank licensed by RBI to deal in foreign exchange; routes Form FNC and ongoing FEMA filings; the LO bank account is opened with the AD Cat-I bank.</li>
                        <li><strong>UIN:</strong> Unique Identification Number - issued by RBI on approval of the LO; used in all subsequent regulatory filings.</li>
                        <li><strong>AAC:</strong> Annual Activity Certificate - CA-certified annual statement of activities of the LO filed with RBI through AD Cat-I bank on or before 30 September each year, confirming activities are within the permitted FEMA 22(R) Schedule II scope.</li>
                        <li><strong>Form FC-1:</strong> RoC filing under Section 380 of the Companies Act, 2013 - registration of the foreign company establishment in India within 30 days of setup.</li>
                        <li><strong>Form FC-2:</strong> RoC filing under Section 381 - intimation of any change in particulars of the foreign company / LO within 30 days.</li>
                        <li><strong>Letter of Comfort:</strong> Letter from the parent / group entity confirming financial backing for the LO; used when the applicant does not directly meet the USD 50,000 net worth or 3-year track record threshold.</li>
                        <li><strong>Special-scrutiny countries:</strong> Pakistan, China, Bangladesh, Sri Lanka, Afghanistan, Iran, Hong Kong, Macau - applicants from these countries face additional scrutiny under RBI approval route and may require MHA clearance.</li>
                    </ul>
                    

