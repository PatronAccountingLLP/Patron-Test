                <h2 class="section-title">What Is NBFC Registration and When It Applies</h2>
                <div class="content-text what-is-definition">
                    
                    
<p><strong>A Non-Banking Financial Company (NBFC) is defined under Section 45-I(f) of the Reserve Bank of India Act 1934 as a company registered under the Companies Act that is engaged in the business of loans and advances, acquisition of shares or stocks or bonds or debentures or securities issued by Government or local authority or other marketable securities of a like nature, leasing, hire-purchase, insurance business, chit business, but does NOT include any institution whose principal business is that of agriculture, industrial activity, sale or purchase or construction of immovable property.</strong></p>
<p>Under SECTION 45-IA of the RBI Act 1934, NO entity can commence or carry on the business of a non-banking financial institution WITHOUT (a) obtaining a Certificate of Registration (CoR) from the <a href="https://www.rbi.org.in" target="_blank" rel="noopener">Reserve Bank of India</a>; and (b) having a Net Owned Fund (NOF) of INR 10 crore (or such other amount as specified for specialised categories). Conducting NBFC business without registration is a violation attracting penalty under Section 58B of the RBI Act and Section 58C (offence by company).</p>
<p>WHEN DOES NBFC REGISTRATION APPLY - the Principal Business Criteria (PBC) test. RBI applies the 50-50 TEST per the RBI Press Release dated 8 April 1999 and reaffirmed in subsequent Master Directions: (a) FINANCIAL ASSETS must constitute MORE THAN 50 PERCENT of TOTAL ASSETS; AND (b) GROSS INCOME from financial assets must constitute MORE THAN 50 PERCENT of GROSS INCOME. BOTH tests must be met simultaneously for the company to qualify as an NBFC requiring registration. Companies sliding into NBFC status (e.g. an industrial company that increasingly lends to its supply chain) may not realise the crossing of PBC and continue operating without registration - this is a regulatory breach.</p>
<p>The MASTER DIRECTION - RBI (NBFC - Scale Based Regulation) Directions 2023 dated 19 October 2023 (updated 10 November 2023) is the operative framework superseding earlier RBI circulars. SCALE BASED REGULATION (SBR) effective 1 October 2022 classifies NBFCs into FOUR LAYERS based on size, activity, complexity and interconnectedness: (1) NBFC-BL (BASE LAYER) - non-deposit-taking NBFCs with asset size below INR 1000 crore, plus NBFC-P2P, NBFC-AA and Non-Operative Financial Holding Companies (NOFHC). (2) NBFC-ML (MIDDLE LAYER) - all deposit-taking NBFCs regardless of size, plus non-deposit-taking NBFCs with asset size INR 1000 crore plus, plus specialised entities (Standalone Primary Dealers, NBFC-IFC). (3) NBFC-UL (UPPER LAYER) - top 10 NBFCs by size plus specifically identified entities warranting enhanced regulatory requirements. (4) NBFC-TL (TOP LAYER) - usually empty by default unless RBI considers an UL NBFC has potential systemic risk requiring elevation. Regulatory burden increases proportionally as one moves from lower to higher layers.</p>
<p>CAPITAL REQUIREMENTS for new NBFC registration. From 1 October 2022, new NBFC-BL registrations require INR 10 CRORE NOF AB INITIO. Existing NBFCs (registered before 1 October 2022) have a glide path: INR 5 crore by 31 March 2025 and INR 10 crore by 31 March 2027. Specialised NBFCs have specific thresholds: (a) NBFC-P2P and NBFC-AA and NBFCs with no public funds and no customer interface continue at INR 2 crore (unchanged); (b) NBFC-MFI and NBFC-Factor at INR 5 crore; (c) NBFC-IFC (Infrastructure Finance Company) and IDF-NBFC (Infrastructure Debt Fund) at INR 300 crore; (d) Housing Finance Company (HFC) at INR 20 crore; (e) Mortgage Guarantee Company (MGC) at INR 100 crore. NOF MUST BE AVAILABLE AS LIQUID CAPITAL - not locked in fixed assets or investments in group companies beyond permissible limits.</p>
<p>The REGISTRATION PROCESS via COSMOS (Centralised Online Submission of Multiple Online Solutions) portal involves comprehensive scrutiny: (a) entity must be Pvt Ltd or Pub Ltd; LLP, partnership and proprietorship are INELIGIBLE; (b) MoA main objects must include financial activity as one of the main objects; (c) NOF must be infused at company level before application; (d) Form NBFC-1 with documentation; (e) PROMOTER FIT-AND-PROPER TEST - financial integrity, criminal record check, market integrity history, regulatory compliance history of promoter group, source of funds verification; (f) BUSINESS PLAN VIABILITY - 5-year financial projections, product portfolio, distribution strategy, risk management, IT systems; (g) SITE INSPECTION by RBI officers at registered office; (h) RBI clarifications and follow-up - multiple rounds typical; (i) Certificate of Registration (CoR) issued - timeline 6 to 12 months from application.</p>
<p>Patron's role spans the full end-to-end NBFC registration engagement - entity setup (Pvt Ltd with NBFC-optimised MoA and AoA via <a href="https://www.mca.gov.in" target="_blank" rel="noopener">MCA</a>), capital structuring with INR 10 crore NOF infusion plan with source-of-funds documentation, Promoter fit-and-proper preparation, 5-year business plan with financial projections and risk management framework, COSMOS application drafting, RBI follow-up through CoR issuance, and post-CoR compliance setup (capital adequacy, IRACP norms, statutory reserves, Section 45-IB and 45-IC compliance, COSMOS returns calendar). Starting INR 4,99,999 (Exl GST and Govt. Charges) - HIGHEST-TICKET service reflecting the depth of regulatory engagement.</p>


