                        <h3 class="faq-expanded__q">What is the minimum capital required for NBFC registration in India?</h3>
                        <div class="faq-expanded__a"><p>The minimum Net Owned Fund (NOF) required for NBFC registration under Section 45-IA of the RBI Act 1934 is Rs 10 crore, raised from Rs 2 crore with effect from October 2022 per RBI circular. NOF under Section 45-IA(7) is computed as paid-up equity capital plus free reserves per the latest audited balance sheet, minus accumulated losses, deferred revenue expenditure and intangibles, and minus investments in shares of subsidiaries, group companies and other NBFCs and certain inter-group loans above 10% of owned fund. The NOF must be from clean traceable sources.</p></div>
