                        <h3 class="faq-expanded__q">What is the 50/50 principal business test for NBFCs?</h3>
                        <div class="faq-expanded__a"><p>The 50/50 principal business test is the RBI criteria for classifying a company as an NBFC requiring registration under Section 45-IA. The company qualifies as an NBFC if (a) its financial assets constitute more than 50% of its total assets AND (b) its income from financial assets constitutes more than 50% of its gross income. Both conditions must be met. The test is applied annually based on audited financials. Patron pre-models this test against the projected balance sheet at incorporation to avoid downstream rejection.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-6">
                        <h3 class="faq-expanded__q">What is the difference between NBFC-D and NBFC-ND?</h3>
                        <div class="faq-expanded__a"><p>NBFC-D refers to Deposit-Taking NBFCs that accept public deposits, subject to stricter prudential norms including NBS-9 deposit returns, capital adequacy and exposure limits under the relevant RBI Master Direction. NBFC-ND refers to Non-Deposit-Taking NBFCs that do not accept public deposits and instead fund operations through equity, market borrowings, debentures and ECBs (within limits). Most modern NBFCs are NBFC-ND. The Scale-Based Regulation 2023 four-layer framework applies to both, with placement in Base, Middle, Upper or Top Layer based on size and activity.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-7">
                        <h3 class="faq-expanded__q">What is the Scale-Based Regulation (SBR) for NBFCs?</h3>
                        <div class="faq-expanded__a"><p>The RBI Master Direction - Non-Banking Financial Company - Scale Based Regulation 2023 (effective October 2022 with phased compliance) replaces the earlier systemically important vs non-systemically important classification with a four-layer prudential framework: Base Layer (NBFC-BL - smaller, less complex NBFCs), Middle Layer (NBFC-ML), Upper Layer (NBFC-UL - top 10 NBFCs by size and complexity) and Top Layer (NBFC-TL - residual reserve for systemic risk concerns). Each layer has progressively stricter prudential norms on capital, governance, exposure and disclosure.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-8">
                        <h3 class="faq-expanded__q">Can a foreign company invest in an Indian NBFC?</h3>
                        <div class="faq-expanded__a"><p>Yes, foreign direct investment in an Indian NBFC is permitted under the FEMA Non-Debt Instruments Rules 2019 - 100% FDI is allowed under the automatic route in 18 specified NBFC activities subject to minimum capitalisation norms. Press Note 3 of 2020 requires prior government approval where any beneficial owner is from a land-bordering country (China, Pakistan, Bangladesh, Bhutan, Nepal, Myanmar, Afghanistan). Form FC-GPR must be filed within 30 days of share allotment. The RBI fit-and-proper criteria for promoters apply to foreign investors as well.</p></div>
