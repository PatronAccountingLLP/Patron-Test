                        <h3 class="faq-expanded__q">What is Section 45-IA of the RBI Act, 1934?</h3>
                        <div class="faq-expanded__a"><p>Section 45-IA of the Reserve Bank of India Act 1934 mandates that no non-banking financial company shall commence or carry on the business of a non-banking financial institution without obtaining a Certificate of Registration (CoR) from the Reserve Bank of India and having a minimum Net Owned Fund as prescribed. Section 45-IA(7) defines the NOF computation. Operating without the CoR exposes the company to penalties under Chapter IIIB of the RBI Act. The CoR application is filed on the PRAVAAH portal.</p></div>
