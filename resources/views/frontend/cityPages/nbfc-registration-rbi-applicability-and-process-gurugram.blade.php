<div class="key-term"><strong>CAR (Capital Adequacy Ratio):</strong> 15 percent for NBFCs; Tier 1 capital minimum 10 percent of risk-weighted assets.</div>
<div class="key-term"><strong>Section 45-IB:</strong> 15 percent of net demand and time liabilities in approved securities (deposit-taking NBFCs).</div>
<div class="key-term"><strong>Section 45-IC:</strong> 20 percent of net profit transferred annually to Reserve Fund before dividend declaration.</div>
<div class="key-term"><strong>Digital Lending Guidelines:</strong> RBI guidelines dated 2 September 2022 governing digital lending by NBFCs and partnering Lending Service Providers (LSPs).</div>
<div class="key-term"><strong>FLDG (First Loss Default Guarantee):</strong> permitted between Regulated Entity and LSP with 5 percent cap per RBI Circular dated 8 June 2023.</div>


