            <div class="column-content">
                <h2 class="section-title">What NBFC Registration Actually Means</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p><strong>NBFC Registration</strong> is the process of obtaining a Certificate of Registration (CoR) from the Reserve Bank of India under Section 45-IA of the RBI Act, 1934 - the statutory authorisation to commence and carry on the business of a non-banking financial institution. Without a valid CoR, no entity can lawfully engage in financial activities such as lending, investment, accepting deposits, factoring, P2P lending, or account aggregation.</p>
                    <p>The applicant must be a company incorporated under the Companies Act, 2013 (a Pvt Ltd is the standard form); demonstrate the prescribed Net Owned Fund (NOF) at the application stage; satisfy the fit and proper criteria for promoters and directors; submit a credible business plan with financial projections; and comply with the SBR framework requirements relevant to its proposed layer.</p>
                    <p>The application is filed on the RBI COSMOS (Common Online Supervisory Monitoring and Operating System) portal, reviewed by the Department of Regulation (DoR), and typically completed in 6 to 12 months.</p>
                    <p>The CoR is the threshold; ongoing post-CoR compliance (CRAR maintenance, NPA classification, periodic returns, statutory audit per RBI criteria) is materially heavier than for a non-regulated business.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for NBFC Registration with RBI:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>NBFC:</strong> Non-Banking Financial Company - a company carrying on the business of financial institution under Section 45-I(c) of the RBI Act, 1934; registered under Section 45-IA.</li>
                        <li><strong>CoR:</strong> Certificate of Registration - issued by RBI under Section 45-IA after evaluating the application; the threshold authorisation to commence NBFC business.</li>
                        <li><strong>NOF:</strong> Net Owned Fund - paid-up equity capital + free reserves - investments in shares of subsidiaries / associates - book value of debentures / bonds / outstanding loans / FDs and book value of investment in shares of subsidiaries / associates exceeding 10 percent of owned funds.</li>
                        <li><strong>SBR:</strong> Scale-Based Regulation framework effective 1 October 2022; classifies NBFCs into 4 layers (BL, ML, UL, TL) with progressively stricter regulatory requirements.</li>
                        <li><strong>NBFC-ICC:</strong> Investment and Credit Company - the most common NBFC type; covers lending, investment in shares / debentures, hire purchase finance, leasing.</li>
                        <li><strong>NBFC-MFI:</strong> Micro Finance Institution - small-ticket loans to low-income borrowers; specific lending and pricing norms apply.</li>
                        <li><strong>NBFC-Factor:</strong> Factor - factoring business under the Factoring Regulation Act, 2011.</li>
                        <li><strong>NBFC-IFC:</strong> Infrastructure Finance Company - 75 percent of total assets deployed in infrastructure loans; NOF INR 300 crore.</li>
                        <li><strong>NBFC-P2P:</strong> Peer to Peer lending platform - online intermediation between lenders and borrowers; framework tightened 2024.</li>
                        <li><strong>NBFC-AA:</strong> Account Aggregator - consent-based financial data sharing; emerging high-growth category.</li>
                        <li><strong>CIC:</strong> Core Investment Company - holding company structure primarily holding investments in group companies.</li>
                        <li><strong>CRAR:</strong> Capital to Risk-weighted Assets Ratio - minimum 15 percent for NBFCs (10 percent Tier-I + 5 percent Tier-II); higher than banks.</li>
                        <li><strong>COSMOS:</strong> RBI Common Online Supervisory Monitoring and Operating System - the portal for NBFC application filings and ongoing regulatory returns.</li>
                    </ul>
                    

