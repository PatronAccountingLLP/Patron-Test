<section class="content-section">
    <div class="content-container">
        <div class="text-content">
            <h2 class="section-title">Legal and Compliance Framework (India)</h2>
            <div class="content-text">
                <p><strong>Governing statutes and provisions relevant to healthcare NGOs and charitable hospitals:</strong></p>
                <ul>
                    <li><strong>Section 10(23C)(via), Income-tax Act, 1961:</strong> Exemption for a hospital or medical institution existing solely for philanthropic purposes and not for profit, subject to approval by the Principal Commissioner or Commissioner.</li>
                    <li><strong>Section 10(23C)(iiiae):</strong> Exemption without separate approval where aggregate annual receipts do not exceed Rs 5 crore (raised from Rs 1 crore by the Finance Act, 2021 with effect from AY 2022-23).</li>
                    <li><strong>Section 10(23C)(iiiac):</strong> Exemption for a philanthropic hospital wholly or substantially (over 50 percent) financed by the Government.</li>
                    <li><strong>Sections 11, 12 and 12A / 12AB:</strong> The registration route to income-tax exemption; an entity cannot claim this and Section 10(23C) at the same time.</li>
                    <li><strong>Section 80G:</strong> Donor deductions (commonly 50 percent of the qualifying donation), applied via Form 10A / Form 10AB, with Form 10BD and Form 10BE for donation reporting.</li>
                    <li><strong>Finance Act, 2020 (effective 1 April 2021):</strong> 12A and 80G registrations are no longer perpetual; provisional registration then Form 10AB, renewed every five years.</li>
                    <li><strong>Income-tax (3rd Amendment) Rules, 2023:</strong> New Form 10B and Form 10BB audit reports effective from AY 2023-24; Form 10B where income exceeds Rs 5 crore, or foreign contribution is received, or income is applied outside India, else Form 10BB.</li>
                    <li><strong>Foreign Contribution (Regulation) Act, 2010:</strong> Registration or prior permission for foreign contribution; designated SBI New Delhi Main Branch account (IFSC SBIN0000691); FC-4 annual return by 31 December; sub-granting prohibited; administrative expenses capped at 20 percent.</li>
