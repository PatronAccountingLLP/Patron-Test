            <div class="column-content">
                <h2 class="section-title">How Charitable Hospital Tax Exemption Works in India</h2>
                <div class="content-text what-is-definition">
                    <p>A charitable hospital or medical-relief NGO gets its income-tax exemption from one of two regimes. Understanding the difference is the foundation of every engagement, because the route you choose determines the forms you file, the audit report you furnish and the way you monitor your annual receipts.</p>
                    <p><strong>Route 1 - Sections 11 and 12 via 12A/12AB registration:</strong> This is the flexible, standard route for most medical-relief NGOs and society or trust-run hospitals. The organisation registers under Section 12A / 12AB and, in parallel, obtains 80G approval so that its donors can claim deductions. This route pairs naturally with FCRA and CSR eligibility and is the most common structure Patron sets up.</p>
                    <p><strong>Route 2 - Section 10(23C)(via) approval:</strong> Section 10(23C)(via) exempts the income of any hospital or medical institution for the reception and treatment of persons suffering from illness, mental defectiveness, convalescence or requiring medical attention or rehabilitation, that exists solely for philanthropic purposes and not for profit, subject to approval by the Principal Commissioner or Commissioner of Income Tax. It is a hospital-specific approval often preferred by larger institutions. An organisation cannot claim exemption under both regimes at the same time.</p>
                    <p><strong>The Rs 5 crore small-hospital exemption - Section 10(23C)(iiiae):</strong> A philanthropic, not-for-profit hospital whose aggregate annual receipts do not exceed Rs 5 crore is exempt under Section 10(23C)(iiiae) without needing separate approval. The Finance Act, 2021 raised this limit from Rs 1 crore to Rs 5 crore with effect from Assessment Year 2022-23. Importantly, where the same person also runs an educational institution under Section 10(23C)(iiiad), the Rs 5 crore ceiling applies to the aggregate of receipts from both, not to each separately - so we monitor receipts each year to catch a crossing early.</p>
                    <p><strong>Government-financed hospitals - Section 10(23C)(iiiac):</strong> A hospital or medical institution that exists solely for philanthropic purposes and not for profit and is wholly or substantially financed by the Government (more than 50 percent of gross receipts by way of government grants) is exempt under Section 10(23C)(iiiac).</p>
                    <p><strong>Donor deductions under Section 80G:</strong> Section 80G approval enables donors to claim a deduction (commonly 50 percent of the qualifying donation) on contributions to the NGO; the NGO applies via Form 10A (provisional) and Form 10AB (regular). Under the regime effective from 1 April 2021 (Finance Act, 2020), 12A and 80G registrations are no longer perpetual - NGOs obtain provisional registration and must apply for regular registration in Form 10AB, with renewal required every five years.</p>
                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Healthcare NGO Compliance:</strong></p>
                    <ul style="margin-top:12px;">
                        <li><strong>Section 10(23C)(via):</strong> Hospital-specific income-tax exemption for a philanthropic, not-for-profit hospital, approved by the Principal Commissioner or Commissioner.</li>
                        <li><strong>Section 10(23C)(iiiae):</strong> Automatic exemption (no separate approval) for a not-for-profit hospital with aggregate annual receipts up to Rs 5 crore.</li>
                        <li><strong>Section 10(23C)(iiiac):</strong> Exemption for a philanthropic hospital wholly or substantially (over 50 percent) financed by the Government.</li>
                        <li><strong>Section 12A / 12AB:</strong> Registration route to claim exemption under Sections 11 and 12; alternative to Section 10(23C).</li>
                        <li><strong>Section 80G:</strong> Approval that lets donors claim a deduction (generally 50 percent) on donations; applied via Form 10A / Form 10AB.</li>
                        <li><strong>Form 10B / Form 10BB:</strong> The two audit report forms for charitable trusts and institutions (new forms effective AY 2023-24).</li>
                        <li><strong>FCRA 2010:</strong> The Foreign Contribution (Regulation) Act governing receipt of foreign medical grants; SBI New Delhi Main Branch designated account required.</li>
                        <li><strong>Form CSR-1 / Schedule VII:</strong> MCA registration enabling an NGO to act as a CSR implementing agency; promoting health care is an eligible Schedule VII activity.</li>
                    </ul>
