                <h2 class="section-title">The Registration and Compliance Stack for a Rural Development NGO</h2>
                <div class="content-text what-is-definition">
                    <p>A rural development NGO does not rely on a single registration. It stands on a stack of registrations, each unlocking a different funding source. Understanding how they fit together is the first step to a funded, compliant organisation.</p>
                    <p><strong>Layer 1 - Legal Entity:</strong> The NGO must first exist as a legal entity. There are three routes, all equally eligible for 12A, 80G, FCRA and CSR funding - a Section 8 company registered with the Ministry of Corporate Affairs (via a Form INC-12 licence and SPICe+ incorporation), a public charitable trust registered with the local Sub-Registrar under the Indian Trusts Act, or a society registered with the Registrar of Societies under the Societies Registration Act, 1860. The Section 8 company carries a central government licence and is frequently preferred by corporate CSR donors for the governance assurance it provides.</p>
                    <p><strong>Layer 2 - Tax Exemption (12A and 80G):</strong> Section 12A registration exempts the NGO's income from tax; Section 80G registration lets donors claim a deduction, generally 50 percent of the donated amount, capped at 10 percent of the donor's gross total income. Both are applied for using Form 10A. First-time applicants receive provisional registration valid for three years, and registrations are then valid for five years and must be renewed by filing Form 10AB before expiry.</p>
                    <p><strong>Layer 3 - CSR Eligibility (CSR-1):</strong> Since 1 April 2021, companies can route CSR funds only to entities that have filed Form CSR-1 with the MCA and obtained a CSR Registration Number as a registered implementing agency. To file CSR-1, the entity must be a trust, a society or a Section 8 company, and typically hold 12A and 80G registration. Rural development is expressly favoured - Schedule VII lists rural development projects at item (x), and environmental sustainability, agroforestry and conservation of natural resources at item (iv).</p>
                    <p><strong>Layer 4 - Foreign Funding (FCRA):</strong> To receive foreign contributions, an NGO must obtain FCRA registration from the Ministry of Home Affairs. Normal registration under Form FC-3A requires at least three years of existence and a minimum spend on core activities over the preceding three financial years; project-specific prior permission under Form FC-3B has no minimum-age requirement. FCRA rules require a designated FCRA bank account at the State Bank of India, New Delhi Main Branch, under Rule 17A of the Foreign Contribution (Regulation) Rules, 2011.</p>
                    <p><strong>Layer 5 - NGO Darpan and Grant Compliance:</strong> A Darpan Unique ID from the NITI Aayog NGO Darpan portal is a prerequisite for FCRA applications and for accessing central government grants, and is strongly preferred by corporate CSR donors. On top of registration sits the ongoing layer - grant-wise ledgers, statutory audit and utilisation certificates in prescribed GFR Form 12-A (or 12-C), signed by the head of finance and the head of the organisation and supported by audited accounts.</p>
                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Rural Development NGOs:</strong></p>
                    <ul style="margin-top:12px;">
                        <li><strong>Section 8 Company:</strong> Not-for-profit company licensed by the Ministry of Corporate Affairs under the Companies Act, 2013 via Form INC-12 and incorporated through SPICe+. Preferred by corporate CSR donors for governance assurance.</li>
                        <li><strong>Public Charitable Trust:</strong> Trust registered with the local Sub-Registrar under the Indian Trusts Act, 1882. A simple, lower-cost route equally eligible for 12A, 80G, FCRA and CSR.</li>
                        <li><strong>Society:</strong> Membership body registered with the Registrar of Societies under the Societies Registration Act, 1860.</li>
                        <li><strong>Section 12A / Form 10A:</strong> Registration that exempts an NGO's income from income tax; applied for via Form 10A, revalidated via Form 10AB.</li>
                        <li><strong>Section 80G:</strong> Registration allowing donors to claim a deduction, generally 50 percent of the donated amount, capped at 10 percent of gross total income.</li>
                        <li><strong>Form CSR-1:</strong> MCA filing that registers the NGO as a CSR implementing agency and yields a CSR Registration Number, mandatory since 1 April 2021.</li>
                        <li><strong>Schedule VII, Section 135:</strong> Companies Act, 2013 provisions listing rural development projects (item x) as a permitted CSR activity under the CSR framework.</li>
                        <li><strong>FCRA 2010 / Form FC-3A and FC-3B:</strong> Ministry of Home Affairs regime for foreign contributions - FC-3A for normal registration (3-year rule), FC-3B for project-specific prior permission (no minimum age).</li>
                        <li><strong>NGO Darpan:</strong> NITI Aayog portal issuing a Darpan Unique ID, a prerequisite for FCRA and central government grants; registration is free and online.</li>
                        <li><strong>Utilisation Certificate (GFR Form 12-A):</strong> Certificate confirming grant funds were used for the sanctioned purpose, signed by the head of finance and the head of the organisation and backed by audited accounts.</li>
                    </ul>
