                    <li><strong>Indian Trusts Act, 1882:</strong> Registration of public charitable trusts with the local Sub-Registrar.</li>
                    <li><strong>Societies Registration Act, 1860:</strong> Registration of societies with the Registrar of Societies.</li>
                    <li><strong>Companies Act, 2013 - Section 135 and Schedule VII:</strong> CSR framework; rural development projects at item (x); environmental sustainability, agroforestry and conservation of natural resources at item (iv).</li>
                    <li><strong>Form CSR-1 and CSR-2:</strong> MCA registration as a CSR implementing agency (mandatory since 1 April 2021) and CSR reporting.</li>
                    <li><strong>Section 12A, Income Tax Act:</strong> Income-tax exemption for the NGO's income, applied for via Form 10A.</li>
                    <li><strong>Section 80G, Income Tax Act:</strong> Donor deduction, generally 50 percent of the donated amount, capped at 10 percent of gross total income.</li>
                    <li><strong>Form 10A and Form 10AB:</strong> Application and five-year revalidation of 12A/80G; provisional registration for three years for first-time applicants.</li>
                    <li><strong>FCRA, 2010 and FCRR, 2011 (Rule 17A):</strong> Foreign-contribution regime administered by the Ministry of Home Affairs; designated FCRA account at SBI, New Delhi Main Branch.</li>
                    <li><strong>Form FC-3A and FC-3B:</strong> Normal FCRA registration (three-year rule) and project-specific prior permission (no minimum age).</li>
                    <li><strong>NGO Darpan (NITI Aayog):</strong> Darpan Unique ID - prerequisite for FCRA and central government grants.</li>
                    <li><strong>GFR Form 12-A (or 12-C):</strong> Prescribed grant utilisation certificate, signed by the head of finance and the head of the organisation and backed by audited accounts.</li>
