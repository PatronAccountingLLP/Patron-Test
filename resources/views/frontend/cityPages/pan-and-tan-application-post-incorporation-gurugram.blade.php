                <h2 class="section-title">What Is PAN and TAN Application Post Incorporation</h2>
                <div class="content-text what-is-definition">
                    
                    
<p><strong>PAN (Permanent Account Number) is the 10-character alphanumeric identifier issued by the <a href="https://www.incometax.gov.in" target="_blank" rel="noopener">Income Tax Department</a> under Section 139A of the Income Tax Act 1961 read with Rule 114 of the Income Tax Rules 1962. It is mandatory for all entities meeting prescribed income or transaction thresholds. PAN serves as the primary tax identifier for income tax returns, advance tax payments, TDS credit claims, GST registration linkage, bank account opening, property transactions, and many other regulatory and commercial purposes.</strong></p>
<p>PAN STRUCTURE is informative - the 10-character alphanumeric code has specific positional meaning. The FIFTH CHARACTER indicates the entity category: 'C' for Company, 'P' for Person (Individual), 'H' for HUF (Hindu Undivided Family), 'F' for Firm (partnership), 'A' for Association of Persons (AOP), 'T' for Trust, 'B' for Body of Individuals (BOI), 'L' for Local Authority, 'J' for Artificial Juridical Person, 'G' for Government. The fourth character indicates the holder's surname or entity name initial. The first three characters are alphabetic sequence; sixth character onwards is the unique sequential identifier.</p>
<p>TAN (Tax Deduction and Collection Account Number) is the 10-character alphanumeric identifier issued by the Income Tax Department under Section 203A of the Income Tax Act 1961 read with Rule 114A. It is mandatory for any person responsible for: (a) DEDUCTING TAX AT SOURCE (TDS) under Sections 192 (salary), 193 (interest on securities), 194 (dividend), 194A (interest other than securities), 194B (lottery winnings), 194C (contract payments), 194H (commission and brokerage), 194-I (rent), 194J (professional and technical fees), 194Q (purchase of goods above INR 50 lakh), 195 (payment to non-residents), and many other 194-series sections; (b) COLLECTING TAX AT SOURCE (TCS) under Section 206C (timber, scrap, motor vehicles above INR 10 lakh, foreign remittances etc.). Once allotted, TAN is used for the entity's LIFETIME and must be quoted in all TDS and TCS statements, challan payments and certificates.</p>
<p>THE MODERN REALITY - SPICe+ AGILE-PRO-S framework operational since February 2020 (<a href="https://www.mca.gov.in" target="_blank" rel="noopener">MCA Notification</a> dated 30 January 2020) integrates the Pvt Ltd or OPC incorporation transaction with auto-allocation of PAN, TAN, GSTIN (if opted), EPFO and ESIC registrations. The same SPICe+ Part A and Part B filing generates all these identifiers without separate applications. LLPs incorporated via FiLLiP also receive PAN automatically; TAN may need separate Form 49B application post-FiLLiP. This integration has made the standalone post-incorporation PAN-TAN application service LARGELY OBSOLETE for new Pvt Ltd and OPC entities. The service remains relevant only in specific GAP-FILL scenarios.</p>
<p>WHEN STANDALONE POST-INCORPORATION PAN-TAN APPLICATION IS NEEDED. (a) OLD PRE-SPICe+ COMPANIES - Pvt Ltd companies incorporated before February 2020 may have PAN but not TAN (TAN was a separate Form 49B application before SPICe+); when they cross income thresholds requiring TDS deduction, they need standalone TAN. (b) FOREIGN BRANCH OFFICE OR LIAISON OFFICE - foreign companies establishing Branch Office, Liaison Office or Project Office in India under FEMA Regulations 2016 require separate PAN application via Form 49AA; the foreign entity is the PAN holder with India presence classification. (c) HUF FORMATION - newly formed Hindu Undivided Family entities require PAN under Form 49A Category HUF. (d) PARTNERSHIP FIRMS - partnership firms (Indian Partnership Act 1932 registered firms or LLP Act 2008 LLPs that did not use FiLLiP integration) need separate PAN application via Form 49A. (e) TRUSTS - private trusts, public charitable trusts, religious trusts need PAN under Form 49A Category Trust. (f) SOCIETIES - registered societies under Societies Registration Act 1860 or state society Acts need PAN under Form 49A Category AOP or BOI as applicable. (g) LOST OR INCORRECT PAN - lost PAN card requires reprint application; incorrect PAN data (name spelling, date of birth, signatory) requires correction application. (h) ENTITIES THAT OPTED OUT OF AGILE-PRO-S - rare cases where the incorporation filer chose not to include AGILE-PRO-S at SPICe+ Part B; these entities need standalone post-incorporation PAN and TAN.</p>
<p>Patron's role for this transactional service is straightforward - eligibility check (which scenario applies), Form 49A or 49AA or 49B drafting, documentation collation, submission via <a href="https://www.protean-tinpan.com" target="_blank" rel="noopener">NSDL Protean</a> or <a href="https://www.utiitsl.com" target="_blank" rel="noopener">UTIITSL</a> portal, follow-up through allotment, and PAN card or TAN allotment letter delivery. Starting INR 1,499 (Exl GST and Govt. Charges) per single application engagement.</p>


