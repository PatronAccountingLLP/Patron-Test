            <div class="column-content">
                <h2 class="section-title">What a Project Office Actually Is</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p>A <strong>Project Office (PO)</strong> is a "place of business" of a foreign company established in India under FEMA 22(R)/RB-2016 specifically to execute a SPECIFIC CONTRACT or PROJECT awarded by an Indian counterparty.</p>
                    <p>The PO does NOT have a separate legal personality from the foreign parent (it is treated as a place of business of the parent); but it IS treated as a "foreign company" under Section 2(42) and Section 380 of the Companies Act, 2013, and has its own PAN, TAN, FEMA reporting obligations and RoC registration.</p>
                    <p>The PO can receive project-related receipts from the Indian counterparty under the project contract, in addition to inward remittances from the parent abroad; project receipts are taxable in India at 40 percent plus surcharge and cess as foreign company income.</p>
                    <p>The PO's scope is RIGIDLY LIMITED to the activities of the specific project for which it is established - no other commercial activity, no unrelated business, no income outside the project scope. On completion of the project, the PO MUST WIND UP; closure intimation through AD bank to RBI; remittance of residual funds (after settling all dues, taxes, contractor payments) to the parent abroad subject to RBI / AD bank approval.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Project Office:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>PO:</strong> Project Office - foreign company India presence for executing a specific project; project-specific and time-bound.</li>
                        <li><strong>General Permission route:</strong> PO can be set up under FEMA 22(R) Schedule III with NO specific RBI approval if any of 4 funding criteria are met; AD Cat-I bank processes Form FNC.</li>
                        <li><strong>Specific Approval route:</strong> Where none of the 4 general-permission criteria are met; Form FNC forwarded to RBI Central Office Mumbai / regional office; longer processing.</li>
                        <li><strong>LO:</strong> Liaison Office - non-commercial; communication channel only; cannot earn income.</li>
                        <li><strong>BO:</strong> Branch Office - permits broad commercial activities including export / import, professional services, research, consultancy; earns income.</li>
                        <li><strong>Form FNC:</strong> Foreign Non-Citizen application form - same form used for LO, BO and PO; at Annex B of FED Master Direction 10/2015-16.</li>
                        <li><strong>AD Cat-I bank:</strong> Authorised Dealer Category-I bank - bank licensed by RBI to deal in foreign exchange; processes Form FNC under general permission.</li>
                        <li><strong>UIN:</strong> Unique Identification Number - issued by RBI on PO approval; used in all subsequent filings.</li>
                        <li><strong>AAC:</strong> Annual Activity Certificate - CA-certified annual statement filed with RBI through AD bank by 30 September each year.</li>
                        <li><strong>Section 380 + Section 381:</strong> Companies Act 2013 - foreign company India registration; Form FC-1 (initial) + Form FC-2 (subsequent changes) at RoC.</li>
                        <li><strong>Section 195 withholding:</strong> Income-tax Act 1961 - withholding tax on payments to non-residents.</li>
                        <li><strong>Closure intimation:</strong> On project completion, PO must wind up; intimation via AD bank to RBI; residual fund repatriation subject to approval.</li>
                    </ul>
                    

