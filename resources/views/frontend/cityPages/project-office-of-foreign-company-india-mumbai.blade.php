                        <h3 class="faq-expanded__q">What is a Project Office (PO) of a foreign company in India?</h3>
                        <div class="faq-expanded__a"><p>A Project Office (PO) is a "place of business" of a foreign company established in India to execute a SPECIFIC CONTRACT or PROJECT awarded by an Indian counterparty. The PO is project-specific and time-bound to the contract duration. Unlike a Liaison Office (non-commercial, communication channel only) or a Branch Office (broad commercial activities), a PO is rigidly limited to the activities of the specific project for which it is established. Framework: FEMA 22(R)/RB-2016 + FED Master Direction 10/2015-16. Application via Form FNC through AD Category-I bank.</p></div>
