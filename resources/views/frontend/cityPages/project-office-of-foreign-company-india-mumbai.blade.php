                        <div class="faq-expanded__a"><p>A Liaison Office (LO) is NON-COMMERCIAL representative presence - no income, no contracts, communication only. A Branch Office (BO) is COMMERCIAL presence permitted to undertake export / import, professional services, research and consultancy; can earn income; taxed at 40 percent plus surcharge / cess. A Project Office (PO) is PROJECT-SPECIFIC - permitted to undertake only the activities of the specific project for which it is established; project income permitted and taxed at 40 percent plus surcharge / cess; PO winds up on project completion.</p></div>
