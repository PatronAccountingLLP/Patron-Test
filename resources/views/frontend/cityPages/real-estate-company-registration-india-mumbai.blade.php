                <h2 class="section-title">What Real Estate Company Registration Actually Means</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p><strong>Real Estate Company Registration</strong> in Mumbai is the bundled setup of (a) the legal entity - typically a Private Limited Company under the Companies Act, 2013, structured as a HOLDING COMPANY + PROJECT-SPECIFIC SPV COMPANIES for each development. This is the industry-standard structure - isolates project liability, enables project-by-project exits (sale of SPV or asset), and aligns with debt and equity financing structures used by builders and developers.</p>
                    <p>(b) the foundational tax registrations - PAN, TAN, GST with SAC 9954, Maharashtra Shops Act, Professional Tax;</p>
                    <p>(c) the real estate-specific regulatory registrations - MahaRERA project registration (mandatory above 500 sqm or 8 apartments under the Real Estate (Regulation and Development) Act, 2016), MahaRERA agent registration (mandatory for brokers and intermediaries), 70 percent escrow account setup, Quarterly Progress Report (QPR) framework;</p>
                    <p>(d) the Mumbai city-authority approvals - BMC building plan approval under DCR for Mumbai city projects, MMRDA for MMR projects, MHADA for housing schemes, SRA for slum redevelopment projects, TDR (Transferable Development Rights) coordination where applicable.</p>
                    <p>The Patron INR 14,999 starter covers the base + MahaRERA prep + GST; building plan and city-authority approvals are scoped per project given the project-specific complexity.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Real Estate Company Registration:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>RERA:</strong> Real Estate (Regulation and Development) Act, 2016 - central legislation; implemented by state regulators.</li>
                        <li><strong>MahaRERA:</strong> Maharashtra Real Estate Regulatory Authority - state regulator under RERA; office at Bandra-Kurla Complex, Mumbai.</li>
                        <li><strong>Project SPV:</strong> Special Purpose Vehicle - a separate Pvt Ltd company set up for each real estate project; isolates project liability; industry-standard structure.</li>
                        <li><strong>BMC DCR:</strong> BMC Development Control Regulations - the governing rules for building plans within BMC limits; significantly amended over time (DCR 33 series for redevelopment).</li>
                        <li><strong>MMRDA:</strong> Mumbai Metropolitan Region Development Authority - statutory authority for Mumbai Metropolitan Region (MMR) areas beyond BMC limits.</li>
                        <li><strong>MHADA:</strong> Maharashtra Housing and Area Development Authority - state housing authority; cluster redevelopment schemes; specific MHADA layouts.</li>
                        <li><strong>SRA:</strong> Slum Rehabilitation Authority - statutory body under the Maharashtra Slum Areas Act for slum redevelopment in Mumbai; specific incentive FSI framework.</li>
                        <li><strong>TDR:</strong> Transferable Development Rights - tradable development rights under Maharashtra DCR; active TDR market in Mumbai.</li>
                        <li><strong>70 percent escrow:</strong> RERA mandate - 70 percent of project collections from buyers must be deposited in a designated project bank account; only withdrawable against engineer / architect / CA certified project expenditure.</li>
                        <li><strong>QPR:</strong> Quarterly Progress Report - mandatory MahaRERA filing showing project progress and financial position.</li>
                        <li><strong>Section 80-IBA:</strong> Income-tax Act - 100 percent profit deduction on qualifying affordable housing project subject to size, price and other conditions.</li>
                        <li><strong>Section 50C:</strong> Income-tax Act - stamp duty value as deemed sale consideration where actual consideration is lower; relevant for property transactions.</li>
                        <li><strong>SAC 9954:</strong> GST Service Accounting Code for construction services.</li>
                        <li><strong>Occupancy Certificate (OC):</strong> BMC / MMRDA certificate confirming building is ready for occupation; GST on sale ceases post OC; LTCG holding period starts from OC for purchase context.</li>
                    </ul>
                    

