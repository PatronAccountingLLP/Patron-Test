        <div class="text-content">
            <h2 class="section-title">Who Receives a Section 143(2) Notice in Gurugram</h2>
            <div class="content-text">
                
                
<p>Any Gurugram-jurisdiction taxpayer - individual, HUF, firm, LLP or company - who has filed an ITR can receive a 143(2) notice if the return is flagged through CASS or selected under CBDT manual criteria. Common Gurugram triggers cluster around the city business mix:</p>

<ul>
    <li><strong>Cyber City IT / ITeS firms</strong> flagged for foreign tax credit claims under Section 90 DTAA, export-of-services receipts and Section 10AA SEZ exemption claims</li>
    <li><strong>Manesar manufacturers (IMT Manesar)</strong> on Section 32 depreciation, additional depreciation under Section 32(1)(iia), Section 35 R&amp;D deduction and Section 35AD specified-business deductions</li>
    <li><strong>Golf Course Road and Sohna real estate developers</strong> on JDA-derived capital gains, Section 80-IBA affordable housing deductions, transition input credit and project-wise turnover slicing</li>
    <li><strong>DLF Cyber Hub hospitality and retail</strong> on Section 32 plant / machinery depreciation, Section 17(5) blocked credit interaction with income tax depreciation and Section 35AD specified-business</li>
    <li><strong>Individual taxpayers in DLF Phase 1-5, Sushant Lok, Sector 56, South City and Nirvana Country</strong> on high-value property sales (LTCG and STCG), Section 54 / 54F / 54EC exemption claims and Schedule FA foreign asset disclosures</li>
    <li><strong>AIS mismatch flags</strong> on professional fees (Section 194J), interest (Section 194A), dividends, crypto / VDA receipts and F&amp;O income for Gurugram salaried and consultants</li>
</ul>

<p style="margin-top:16px;"><strong>Statutory time bar (proviso to Section 143(2)):</strong> The notice must be served within <strong>3 months from the end of the FY</strong> in which the ITR was filed. Example: ITR filed 31 July 2025 for AY 2025-26 - the FY of filing is FY 2025-26 ending 31 March 2026 - so the latest valid 143(2) date is <strong>30 June 2026</strong>. Notices beyond this are time-barred and quashable via writ before the Punjab and Haryana High Court.</p>

<p style="margin-top:12px;"><strong>Delhi HC 2024 jurisdictional ruling (relevant for Gurugram):</strong> In a 2024 decision, the Delhi High Court held that the power to issue 143(2) is not limited to the Assessing Officer or NaFAC officers alone - it extends to any prescribed income-tax authority under Rule 12E. Gurugram cases handled by Chandigarh-region authorities benefit from the same principle.</p>

<p style="margin-top:12px;"><strong>Pre-condition:</strong> 143(2) applies only if an ITR has been filed. For non-filers, Section 142(1) is issued first to compel filing; thereafter 143(2) can follow if the return is selected for scrutiny.</p>

