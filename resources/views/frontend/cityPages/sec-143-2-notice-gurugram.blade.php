                <h2 class="section-title">What Is a Section 143(2) Notice</h2>
                <div class="content-text what-is-definition">
                    
                    
<p>A <strong>Section 143(2) notice</strong> is a formal scrutiny assessment initiation notice issued under <strong>Section 143(2) of the Income Tax Act, 1961</strong> to a taxpayer who has filed an ITR, selecting that return for <strong>detailed examination</strong> by the Assessing Officer or NaFAC. It does not itself create a demand - it triggers the scrutiny assessment process that ends with an order under Section 143(3).</p>

<p>The notice can be issued <strong>only if an ITR has been filed</strong>. If no return has been filed, the department must first issue a notice under Section 142(1) requiring the taxpayer to file the return. The 143(2) notice must be served <strong>within 3 months from the end of the financial year</strong> in which the return was filed - a strict statutory bar under the proviso to Section 143(2).</p>

<p>Under Section 144B (replaced as Section 532 in the Income Tax Act 2025 effective 1 April 2026), the entire 143(2) scrutiny proceeding is conducted <strong>faceless via NaFAC</strong> with automated allocation to Assessment Units across the country. For Gurugram non-faceless cases, the notice issues from the <strong>Pr CIT(C) Gurugram office at Aayakar Bhawan, HSIIDC Building, Udyog Vihar Phase V</strong> or other jurisdictional charges within CCIT Panchkula and Pr CCIT NWR Chandigarh region.</p>

<p>The notice itself states the scrutiny type at issuance - <strong>limited scrutiny</strong> (specific issues only - AO cannot widen scope without PCIT approval), <strong>complete scrutiny</strong> (entire return examined across all heads of income) or <strong>manual scrutiny</strong> (CBDT criteria-based selection with annually-changing parameters). Identifying the scrutiny type correctly at intake determines the defence strategy.</p>


                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Section 143(2) Notice Gurugram:</strong></p>
                    
<ul>
    <li><strong>CASS (Computer-Assisted Scrutiny Selection):</strong> The risk-based algorithm that auto-flags ITRs for limited or complete scrutiny based on pre-set parameters - mismatches, high-value transactions, deduction anomalies.</li>
    <li><strong>Limited Scrutiny:</strong> Notice restricted to specific issues mentioned (e.g. property sale, foreign tax credit, deduction mismatch). AO cannot widen scope without higher (PCIT) approval.</li>
    <li><strong>Complete Scrutiny:</strong> Detailed examination of the entire return - income, deductions, exemptions, investments, supporting documents. Broader exposure surface.</li>
    <li><strong>Manual Scrutiny:</strong> Cases selected manually under CBDT-defined criteria (changes year-on-year) - typically high-risk industries, search-derived information or specific compliance triggers.</li>
    <li><strong>NaFAC:</strong> National Faceless Assessment Centre - headquartered in Delhi - the central hub allocating cases to Assessment Units (AU), Verification Units (VU), Technical Units (TU) and Review Units (RU) under Section 144B.</li>
    <li><strong>Pr CCIT NWR (North West Region):</strong> Over-arching CCIT for Gurugram region - headquartered at Chandigarh. Administrative authority for Haryana, Punjab and J&amp;K direct tax matters.</li>
    <li><strong>CCIT Panchkula:</strong> Haryana CCIT at Aayakar Bhawan, Sector-2, Panchkula - intermediate administrative oversight for Gurugram cases.</li>
    <li><strong>Pr CIT(C) Gurugram (Central Charge):</strong> Search and Central Charges for Gurugram - at Aayakar Bhawan, HSIIDC Building, Udyog Vihar Phase V.</li>
    <li><strong>e-Proceedings:</strong> The portal facility on incometax.gov.in (Worklist > E-Proceedings) where notices are served and responses uploaded - the only valid response channel under faceless assessment.</li>
    <li><strong>Section 143(3) Order:</strong> The final assessment order passed after considering the taxpayer responses to 143(2). This is the order that creates demand or determines refund.</li>
</ul>


