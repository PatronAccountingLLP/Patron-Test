<p><strong>Governing Act:</strong> Income Tax Act, 1961. Per Section 536(2)(c) of the Income Tax Act, 2025, the 1961 Act continues for assessment proceedings of AY 2026-27 and earlier even after 1 April 2026 when IT Act 2025 takes effect.</p>

<p><strong>Section 143(1):</strong> Summary processing intimation issued by CPC after ITR filing - not scrutiny.</p>

<p><strong>Section 143(2) (<a href="https://www.incometaxindia.gov.in" target="_blank" rel="noopener">Income Tax Department</a>):</strong> Scrutiny initiation notice - must be issued within <strong>3 months of end of FY of return filing</strong> (proviso to Section 143(2)).</p>

<p><strong>Section 143(3):</strong> Final scrutiny assessment order passed after considering taxpayer responses.</p>

<p><strong>Section 142(1):</strong> Notice to file return (where none filed) or to produce documents - precedes 143(2) in non-filer cases.</p>

<p><strong>Section 144:</strong> Best judgement assessment - where taxpayer fails to comply with notices including 143(2). Default adverse route.</p>

<p><strong>Section 144B (<a href="https://www.incometax.gov.in" target="_blank" rel="noopener">e-Filing Portal</a>):</strong> Faceless assessment procedure - NaFAC, AU, VU, TU, RU framework. Becomes Section 532 under IT Act 2025 from 1 April 2026.</p>

<p><strong>Section 144B(6):</strong> Show Cause Notice procedure before adverse 143(3) order - 15-day reply window with right to personal hearing via video conferencing or physical at Aayakar Bhawan HSIIDC for jurisdictional cases.</p>

<p><strong>Section 153:</strong> Time limit for completion of assessment - 12 months from end of AY (extendable to 18 months).</p>

<p><strong>Section 271(1)(b):</strong> Penalty of Rs 10,000 for non-compliance with Section 142(1) or 143(2).</p>

<p><strong>Section 246A:</strong> Appeal to Commissioner (Appeals) against 143(3) assessment order - within 30 days with 20 percent pre-deposit.</p>

<p><strong>Section 253:</strong> Appeal to Income Tax Appellate Tribunal Chandigarh bench (for Haryana matters) - within 60 days of CIT(A) order.</p>

<p><strong>Section 276CC:</strong> Prosecution for wilful failure to furnish return - 3 months to 7 years imprisonment.</p>

<p><strong>Rule 12E (Income Tax Rules):</strong> Specifies prescribed income-tax authorities that may issue 143(2) - extended beyond AO and NaFAC per Delhi HC 2024 ruling.</p>

<p><strong>CBDT Instruction 5/2016:</strong> Limited scrutiny scope - AU cannot widen scope without PCIT approval.</p>

<p><strong>Faceless Assessment Scheme 2019:</strong> Notified by CBDT in September 2019 - operationalised through NaFAC headquartered in Delhi.</p>

<p><strong>Key Case Law:</strong> <em>Delhi High Court 2024</em> - Power to issue 143(2) extends to other prescribed authorities under Rule 12E. <em>Punjab and Haryana HC</em> - parallel jurisprudence on faceless scrutiny natural-justice and time-bar grounds (relevant for Pr CCIT NWR Chandigarh region cases).</p>

