                <h2 class="section-title">What Is a Section 143(2) Notice</h2>
                <div class="content-text what-is-definition">
                    
                    
<p>A <strong>Section 143(2) notice</strong> is the scrutiny selection notice issued by the Assessing Officer under <strong>Section 143(2) of the Income Tax Act, 1961</strong> when the return filed under Section 139 is selected for detailed Section 143(3) assessment. It is NOT itself an inquiry, does NOT seek information and does NOT demand any tax - it is purely a notification that the case has been selected for scrutiny.</p>

<p>Selection is typically driven by <strong>CASS (Computer-Assisted Scrutiny Selection)</strong> - the CBDT risk-parameter algorithm that selects cases based on high refund, high deduction, AIS / TIS data mismatch, sector-specific flags and previous-year scrutiny outcomes. A smaller proportion originate from compulsory scrutiny categories (Section 132 search cases by Mumbai Central Charges, Section 133A survey, CBDT instruction-based selections).</p>

<p>Section 143(2) must be served <strong>within 3 months from the end of the financial year</strong> in which the return is furnished (Finance Act 2021 amendment effective 1 April 2021; previously 6 months). For an ITR furnished any time during FY 2024-25 (1 April 2024 to 31 March 2025), the Section 143(2) notice must be served by <strong>30 June 2025</strong>. This 3-month time limit is <strong>jurisdictional</strong> - a Section 143(2) served beyond the limit is invalid and the entire assessment fails. <strong>Bombay High Court has consistently struck down time-barred Section 143(2) notices in writ proceedings</strong> under Article 226 at its Principal Seat at Mumbai.</p>

<p>Under <strong>Section 144B (replaced as Section 271 in the Income Tax Act 2025 effective 1 April 2026)</strong>, the entire scrutiny proceeding is conducted faceless - NaFAC Delhi allocates the case to a Faceless Assessing Unit (FAU) which may be located anywhere in India. Mumbai assessees do not interact with a Mumbai-based AO for ordinary geographical cases. Communication happens entirely through the e-filing portal and registered e-mail / SMS.</p>


