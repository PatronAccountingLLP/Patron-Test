<p><strong>Section 147 IT Act 1961 (<a href="https://www.incometaxindia.gov.in" target="_blank" rel="noopener">Income Tax Department</a>):</strong> Charging section for income escaping assessment.</p>

<p><strong>Section 148 IT Act 1961 (<a href="https://www.incometax.gov.in" target="_blank" rel="noopener">e-Filing Portal</a>):</strong> Procedural section for reassessment notice.</p>

<p><strong>Section 148A IT Act 1961:</strong> Mandatory pre-notice procedure introduced by Finance Act 2021. Four sub-stages - 148A(a) inquiry, 148A(b) Show Cause Notice with 7-30 day reply, 148A(c) hearing opportunity, 148A(d) reasoned order.</p>

<p><strong>Section 149 IT Act 1961 (w.e.f. 1 Sep 2024 per Finance (No.2) Act 2024):</strong> Time limits - <strong>3 years 3 months</strong> from AY-end if escaped income less than Rs 50 lakh; <strong>5 years 3 months</strong> if Rs 50 lakh or more. Jurisdictional limit.</p>

<p><strong>Section 151 IT Act 1961:</strong> Approval authority - PCIT / CIT within 3 years from AY-end; PCCIT / CCIT beyond 3 years.</p>

<p><strong>Section 151A IT Act 1961:</strong> Faceless allocation scheme for Section 148 notices - <strong>CBDT Notification 18/2022 dated 29 March 2022</strong> makes faceless mandatory. P&amp;H HC has held this overrides JAO concurrent jurisdiction.</p>

<p><strong>Section 153 IT Act 1961:</strong> Reassessment order time-limit - 12 months from end of FY in which Section 148 notice was served.</p>

<p><strong>Section 234(3) IT Act 1961:</strong> Interest at 1 percent per month for delay in filing return after Section 148 notice.</p>

<p><strong>Section 246A IT Act 1961:</strong> CIT(A) appeal jurisdiction for reassessment orders; 30-day window from order; 20 percent pre-deposit.</p>

<p><strong>Section 253 IT Act 1961:</strong> ITAT second-appeal jurisdiction; 60 days from CIT(A) order; <a href="https://www.itat.gov.in" target="_blank" rel="noopener">ITAT Chandigarh Bench</a> handles Gurugram appeals (NOT ITAT Delhi).</p>

<p><strong>Section 270A IT Act 1961:</strong> Penalty for under-reporting (50 percent of tax) or mis-reporting (200 percent of tax) of income arising from reassessment.</p>

<p><strong>Section 271(1)(b) IT Act 1961:</strong> Penalty of Rs 10,000 for failure to comply with Section 148 / 142(1) notice.</p>

