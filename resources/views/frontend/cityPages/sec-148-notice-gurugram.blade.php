            <div class="column-content">
                <h2 class="section-title">What Is a Section 148 Notice</h2>
                <div class="content-text what-is-definition">
                    
                    
<p>A <strong>Section 148 notice</strong> is a formal reassessment notice issued under <strong>Section 148 of the Income Tax Act, 1961</strong> requiring a taxpayer to file a return for a prior assessment year because the Assessing Officer has information that income chargeable to tax has escaped assessment. It is the procedural vehicle that triggers reassessment proceedings under <strong>Section 147</strong> (the charging section for escaped income).</p>

<p>The notice can only be issued <strong>after the mandatory Section 148A procedure</strong> introduced by Finance Act 2021. Section 148A has four sub-stages: <strong>148A(a)</strong> - preliminary inquiry by AO; <strong>148A(b)</strong> - Show Cause Notice to assessee with 7 to 30 day reply window; <strong>148A(c)</strong> - opportunity of being heard; <strong>148A(d)</strong> - reasoned order by AO deciding whether to issue the Section 148 notice. The Supreme Court in <strong>Union of India v. Rajeev Bansal [2024] 469 ITR 46 (SC)</strong> (decided 3 October 2024) confirmed that non-compliance with Section 148A voids the entire reassessment.</p>

<p>Critically for Gurugram, the <strong>Punjab and Haryana High Court has gone further than other High Courts</strong>. In a series of rulings from <strong>Jatinder Singh Bhangu (19 July 2024) onwards</strong>, the P&amp;H HC has held that <strong>ALL Section 148 notices issued by Jurisdictional Assessing Officers post 29 March 2022 are void ab initio</strong> - because Section 151A read with CBDT Notification 18/2022 mandates faceless allocation from the notice stage itself. This is a uniquely pro-taxpayer position not yet adopted by Delhi HC, which took a contrary concurrent-jurisdiction view in T.K.S. Builders (currently pending Supreme Court resolution).</p>

<p>The <strong>time limit under Section 149</strong> (effective 1 September 2024 per Finance (No.2) Act 2024) is: <strong>3 years 3 months</strong> from end of AY if escaped income is less than Rs 50 lakh, and <strong>5 years 3 months</strong> if Rs 50 lakh or more. Section 149 is a jurisdictional limit - notices beyond these windows are void. P&amp;H HC has been quashing time-barred notices including those issued on 29 July 2022 for AY 2015-16.</p>


                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Section 148 Notice Gurugram:</strong></p>
                    
<ul>
    <li><strong>Section 148A Show Cause Notice (SCN):</strong> Mandatory pre-notice letter under Section 148A(b) sharing the information against you and giving 7 to 30 days to reply. Your strongest defence window.</li>
    <li><strong>Section 148A(d) Order:</strong> Reasoned order passed by the AO after considering your SCN reply, deciding whether to issue the Section 148 notice.</li>
    <li><strong>Section 151 Approval Authority:</strong> Within 3 years from AY-end, PCIT or CIT must approve; beyond 3 years, PCCIT or CCIT must approve.</li>
    <li><strong>Section 151A Faceless Allocation:</strong> Empowers Central Government to specify faceless scheme for issuing Section 148 notices. <strong>CBDT Notification 18/2022 (29 March 2022)</strong> makes faceless allocation mandatory. P&amp;H HC has held this overrides JAO concurrent jurisdiction.</li>
    <li><strong>Jatinder Singh Bhangu Doctrine:</strong> P&amp;H HC ruling (19 July 2024, Neutral Citation 2024:PHHC:090942-DB) by Chief Justice Sheel Nagu and Justice Jagmohan Bansal - Section 148 notices by JAO post 29 March 2022 are void; faceless allocation is mandatory from notice stage. Affirmed in Jasjit Singh (29 July 2024) and Karambir Singh Nalwa (28 November 2025).</li>
    <li><strong>Section 149 Time Limit (post 1 Sep 2024):</strong> 3 years 3 months from AY-end if escaped income less than Rs 50 lakh; 5 years 3 months if Rs 50 lakh or more.</li>
    <li><strong>GKN Driveshafts Objections:</strong> SC ruling [2003] 259 ITR 19 granting every taxpayer the right to demand recorded reasons, file written objections and receive a speaking order before reassessment proceeds.</li>
    <li><strong>Rajeev Bansal Concession:</strong> Per para 19(f) of SC judgment [2024] 469 ITR 46, all notices issued on or after 1 April 2021 for AY 2015-16 must be dropped.</li>
