            <div class="content-text">
                
                
<p>Any <strong>Delhi-registered GSTIN holder</strong> can receive a Section 73 SCN if the proper officer finds tax shortfall, erroneous refund or wrongly availed ITC for FY 2017-18 to FY 2023-24. Common Delhi triggers include <strong>GSTR-1 vs GSTR-3B mismatch</strong> flagged by analytics, <strong>GSTR-2A / 2B vs ITC ledger differences</strong>, inverted duty refund verification by the <strong>CGST Delhi Audit Commissionerates</strong> and reconciliation findings post-GSTR-9 filing.</p>

<p><strong>Statutory threshold (Section 73(11)):</strong> The tax amount in dispute must exceed <strong>Rs 1,000 for the financial year</strong>. Below this, no SCN is issued. Above this, the SCN is mandatory in non-fraud cases unless the taxpayer voluntarily pays under DRC-03.</p>

<p><strong>Statutory deadline (Section 73(10)):</strong> The adjudicating order must be passed within <strong>3 years from the due date of the relevant GSTR-9</strong>. Working backward, the SCN deadline under Section 73(2) is <strong>3 months earlier - effectively 2 years 9 months</strong> from GSTR-9 due date. Notices beyond this are time-barred and challengeable via writ in the Delhi High Court.</p>

<p><strong>Recent Delhi case law:</strong> In <em>Delhi Soccer P. Ltd. v. Union of India</em> (W.P. (C) 7804/2024, decided 28 May 2024), the Delhi High Court set aside a Section 73 demand order because the taxpayer was denied a fair opportunity to reply. The court confirmed that natural justice is non-negotiable - even if non-response was due to mistaken belief, ex parte orders are bad in law. This is the leading Delhi precedent for procedural defence.</p>

