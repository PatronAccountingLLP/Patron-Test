<p><strong>Section 50:</strong> Interest at 18 percent per annum on tax not paid or short paid.</p>

<p><strong>Section 75(2):</strong> If Section 74 SCN is set aside on fraud grounds, it is deemed a Section 73 SCN automatically.</p>

