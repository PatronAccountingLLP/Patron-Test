<p><strong>Governing Act:</strong> Central Goods and Services Tax Act, 2017 (Sections 73, 50, 75, 107, 116, 169) and Delhi Goods and Services Tax Act, 2017 (parallel provisions for SGST notices).</p>

<p><strong>Section 73(1) (<a href="https://taxinformation.cbic.gov.in" target="_blank" rel="noopener">CBIC Portal</a>):</strong> Empowers proper officer to issue SCN for non-fraud tax shortfall, erroneous refund or wrongly availed ITC.</p>

