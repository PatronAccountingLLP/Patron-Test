                <h2 class="section-title">What Is a Section 73 GST Notice</h2>
                <div class="content-text what-is-definition">
                    
                    
<p>A <strong>Section 73 GST notice</strong> is a show cause notice issued under <strong>Section 73(1) of the CGST Act, 2017</strong> (and the parallel provision under the Delhi GST Act, 2017) where the proper officer believes that tax has not been paid, has been short-paid, has been erroneously refunded, or input tax credit has been wrongly availed or utilised - for any reason <strong>other than</strong> fraud, wilful misstatement, or suppression of facts.</p>

<p>The SCN is served in <strong>Form GST DRC-01</strong> with the demand summary, typically preceded by a pre-SCN intimation in <strong>Form GST DRC-01A</strong>. The taxpayer must reply within 30 days through <strong>Form GST DRC-06</strong> on the GST portal. If tax and interest are paid within the 30-day window under <strong>Section 73(8)</strong>, the entire penalty is waived - the single biggest lever in Section 73 defence.</p>

<p>Per <strong>Section 73(12)</strong> inserted by the Finance (No.2) Act 2024, Section 73 applies only to demands pertaining to <strong>FY 2017-18 through FY 2023-24</strong>. From FY 2024-25 onwards, the unified <strong>Section 74A</strong> replaces both Sections 73 and 74. Delhi assessees with open historical SCNs continue to be governed by Section 73 procedure - and that is the entire universe this page addresses.</p>

<p>Delhi adjudication is split between the Central Tax (CGST) zone and the State Tax (SGST) zone. The Central zone covers four <strong>CGST Delhi Commissionerates</strong> - North, South, East and West - notified under <strong>Notification 2/2017-Central Tax dated 19 June 2017</strong>. The State zone is the <strong>Delhi Department of Trade and Taxes</strong> at Vyapar Bhawan, IP Estate, New Delhi. Identifying your issuing authority on the SCN header determines where DRC-06 is filed and where personal hearings happen.</p>


                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Section 73 GST Notice Delhi:</strong></p>
                    
<ul>
    <li><strong>DRC-01 SCN:</strong> Formal show cause notice on Form GST DRC-01 specifying tax, interest and penalty demanded under Section 73(1). Served by CGST or Delhi SGST proper officer with DIN.</li>
    <li><strong>DRC-01A Pre-Intimation:</strong> Optional pre-SCN intimation under Rule 142(1A) giving the taxpayer a chance to pay or contest before formal SCN. Most matters close here.</li>
    <li><strong>DRC-03 Voluntary Payment:</strong> Form to pay tax and interest voluntarily before SCN under Section 73(5) or within 30 days under Section 73(8) - results in NIL penalty.</li>
    <li><strong>DRC-06 Reply:</strong> Mandatory reply form filed on gst.gov.in within 30 days of SCN. Attach evidence, reconciliations and legal arguments.</li>
    <li><strong>DRC-07 Order:</strong> Summary of order issued by the adjudicating authority after considering DRC-06 reply and personal hearing under Section 116.</li>
    <li><strong>Section 73(8) - 30-Day Window:</strong> If tax and interest are paid within 30 days of SCN, the entire penalty under Section 73(9) is waived. This is the single biggest lever in Section 73.</li>
    <li><strong>Section 75(2) Deemed Section 73:</strong> If a Section 74 (fraud) SCN is set aside on the ground that fraud is not proved, it is deemed to be a Section 73 SCN automatically.</li>
    <li><strong>Section 128A:</strong> Conditional waiver of interest and penalty for FY 2017-18, 2018-19 and 2019-20 demands - the legacy GST clean-slate opportunity inserted on 53rd GST Council recommendation.</li>
</ul>


