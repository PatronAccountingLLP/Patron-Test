                <h2 class="section-title">What Is a Section 73 GST Notice</h2>
                <div class="content-text what-is-definition">
                    
                    
<p>A <strong>Section 73 GST notice</strong> is a non-fraud demand show cause notice (SCN) issued under <strong>Section 73 of the CGST Act, 2017 read with Rule 142 of the CGST Rules, 2017</strong> (and parallel provisions of the Haryana GST Act, 2017) when the proper officer believes that tax has been short paid, not paid, erroneously refunded, or ITC has been wrongly availed - <strong>without fraud, wilful misstatement or suppression of facts</strong>.</p>

<p>The lifecycle typically begins with <strong>Form DRC-01A</strong> (pre-SCN intimation) under Rule 142(1A), giving you a chance to pay voluntarily under Section 73(5) with <strong>zero penalty</strong>. If unresolved, the formal <strong>DRC-01 SCN</strong> issues with a 30-day reply window in <strong>Form DRC-06</strong>. Payment within 30 days of SCN under <strong>Section 73(8) still attracts NIL penalty</strong>. Adjudication ends with <strong>DRC-07 - the final demand order</strong> creating tax, interest and penalty liability.</p>

<p>From FY 2024-25 onwards, Section 73 and Section 74 have been unified into <strong>Section 74A</strong> - a single demand section regardless of fraud / non-fraud classification, with a uniform 42-month order time limit. For FY 2017-18 to 2023-24, the existing Section 73 / 74 framework continues to apply, and <strong>Section 128A</strong> offers a waiver of interest and penalty for non-fraud demands relating to FY 2017-18, 2018-19 and 2019-20 if tax is paid in full by 31 March 2025 (or extended date per CBIC).</p>

<p>Gurugram adjudication is split between the Central Tax (CGST) zone and the State Tax (SGST) zone. The Central zone is the <strong>CGST Gurugram Commissionerate</strong> at Mudit Square (Plot 24, Sector 32) under the larger <strong>CGST Panchkula Zone</strong>. The State zone is the <strong>Haryana SGST Department</strong> operating through <strong>DETC Gurugram East</strong> (Sector 34 Infocity-1 near Honda Chowk) and <strong>DETC Gurugram West</strong> (Sector 32). Identifying your issuing authority on the SCN header determines where DRC-06 is filed and where personal hearings happen.</p>


                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Section 73 GST Notice Gurugram:</strong></p>
                    
<ul>
    <li><strong>DRC-01A (Pre-SCN Intimation):</strong> Issued under Rule 142(1A) before the formal SCN. Pay tax and interest under Section 73(5) for NIL penalty - the cheapest possible exit.</li>
    <li><strong>DRC-01 (SCN):</strong> Formal show cause notice under Section 73(1). 30-day reply window. Triggers demand adjudication.</li>
    <li><strong>DRC-06 (Reply):</strong> Taxpayer written reply to the SCN with annexures, GSTR reconciliations and case law citations - filed on the GST portal.</li>
    <li><strong>DRC-07 (Final Order):</strong> The demand order under Section 73(9) creating tax, interest and penalty liability. Appealable before Appellate Authority within 3 months under Section 107.</li>
    <li><strong>Section 73(5) Voluntary Payment:</strong> Pay tax plus interest before SCN is issued - zero penalty. Lowest-cost exit available.</li>
    <li><strong>Section 73(8) 30-Day NIL Penalty:</strong> Pay tax plus interest within 30 days of DRC-01 SCN - zero penalty under Section 73(8). The single biggest cost-saving lever.</li>
    <li><strong>Section 73(9) Penalty:</strong> 10 percent of tax demand or Rs 10,000 (whichever higher) if you contest and lose at adjudication.</li>
    <li><strong>Section 128A Waiver:</strong> Interest and penalty waived for non-fraud demands relating to FY 2017-18, 2018-19, 2019-20 if tax is paid in full by 31 March 2025. Once-only relief.</li>
    <li><strong>Section 74A:</strong> Unified demand section from FY 2024-25 onwards - replaces separate Section 73 (non-fraud) and Section 74 (fraud) with a single 42-month order time limit.</li>
    <li><strong>CGST Panchkula Zone:</strong> The over-arching CGST zone covering Haryana including Gurugram Commissionerate at Mudit Square.</li>
</ul>


