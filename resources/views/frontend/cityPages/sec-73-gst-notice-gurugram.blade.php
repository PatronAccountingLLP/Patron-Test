            <h2 class="section-title">Who Receives a Section 73 Notice in Gurugram</h2>
            <div class="content-text">
                
                
<p>Any Gurugram-registered GSTIN holder can receive a Section 73 notice when the proper officer identifies <strong>genuine errors without fraud</strong> in GST filings. Common Gurugram triggers cluster around the city business mix:</p>

<ul>
    <li><strong>Cyber City IT / ITeS exporters</strong> flagged for refund-versus-export turnover mismatches under Rule 89 - typical FY 17-18 to FY 20-21 disputes from CGST Gurugram Audit Commissionerate</li>
    <li><strong>Manesar manufacturing belt assessees (IMT Manesar)</strong> with ITC anomalies on capital goods or RCM under Section 9(3) / 9(4) - common in auto components and electronics manufacturing</li>
    <li><strong>Real estate developers around Golf Course Road, Sohna and Dwarka Expressway</strong> on construction-stage GST under Notification 11/2017 (5 / 1 percent rates), Joint Development Agreement RCM and TDR / FSI reverse charge</li>
    <li><strong>Hospitality groups in DLF Cyber Hub and MG Road</strong> on Section 17(5) blocked credits, restaurant ITC restrictions and outdoor catering classification</li>
    <li><strong>Logistics operators near IMT Manesar and Dharuhera</strong> on e-way bill value mismatches and inter-state warehouse transfer disclosures</li>
</ul>

<p><strong>Time bar (Section 73(10)):</strong> The SCN must be issued within <strong>2 years 9 months</strong> from the due date of the annual return (GSTR-9) for the relevant FY. The final order must be passed within <strong>3 years</strong> of the GSTR-9 due date. Notices beyond this are time-barred and challengeable via writ petition before the Punjab and Haryana High Court under Article 226.</p>

<p><strong>Section 128A waiver eligibility:</strong> Non-fraud demands for FY 2017-18, 2018-19 and 2019-20 are eligible for the once-only interest and penalty waiver under Section 128A if the tax is paid in full by <strong>31 March 2025</strong> (or extended date per CBIC notification). Gurugram first wave of FY 17-18 and 18-19 Section 73 SCNs are squarely within this window.</p>

<p><strong>FY 2024-25 onwards:</strong> Section 74A applies - unified demand section replacing Section 73 and Section 74 with a single 42-month order time limit regardless of fraud or non-fraud classification.</p>

