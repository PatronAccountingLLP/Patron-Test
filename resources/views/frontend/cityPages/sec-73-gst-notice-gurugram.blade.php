            <h2 class="section-title">Legal and Compliance Framework Governing Section 73 in Gurugram</h2>
            <div class="content-text">
                
                
<p><strong>Governing Act:</strong> Central Goods and Services Tax Act, 2017 (Sections 73, 50, 75, 107, 116, 169) and Haryana Goods and Services Tax Act, 2017 (parallel provisions for SGST notices).</p>

<p><strong>Section 73(1) (<a href="https://taxinformation.cbic.gov.in" target="_blank" rel="noopener">CBIC Portal</a>):</strong> Empowers proper officer to issue SCN for non-fraud tax shortfall, erroneous refund or wrongly availed ITC.</p>

<p><strong>Section 73(2):</strong> SCN must be issued at least 3 months before the time limit for adjudicating order under Section 73(10) - effectively 2 years 9 months from GSTR-9 due date.</p>

<p><strong>Section 73(5):</strong> Voluntary pre-SCN payment via DRC-03 - NIL penalty.</p>

<p><strong>Section 73(8):</strong> Payment within 30 days of SCN - NIL penalty. The single biggest lever in Section 73 defence.</p>

<p><strong>Section 73(9):</strong> Penalty on adjudication - 10 percent of tax or Rs 10,000, whichever is higher.</p>

<p><strong>Section 73(10):</strong> Order must be passed within 3 years from the due date of GSTR-9 for the relevant FY.</p>

<p><strong>Section 73(11) - 73(12):</strong> Penalty modifications for self-assessed tax plus interest on collected-but-not-paid tax. Section 73 applies up to FY 2023-24 only.</p>

<p><strong>Section 50:</strong> Interest at 18 percent per annum on tax short-paid; 24 percent per annum on excess ITC retained.</p>

<p><strong>Section 74A:</strong> Unified provision replacing Sections 73 and 74 from FY 2024-25 onwards with a 42-month order time limit.</p>

<p><strong>Section 128A:</strong> Conditional waiver of interest and penalty for FY 2017-18, 2018-19 and 2019-20 demands where full tax is paid by the notified date - inserted on 53rd GST Council recommendation.</p>

<p><strong>Section 107:</strong> Appeal to Appellate Authority within 3 months with 10 percent pre-deposit (Form GST APL-01) - max Rs 25 crore CGST + Rs 25 crore SGST pre-deposit cap.</p>

<p><strong>Section 112:</strong> Second appeal to GSTAT Allahabad bench (which has jurisdiction over Haryana matters) - Form GST APL-05.</p>

<p><strong>Section 116:</strong> Authorised representative provisions - CA or advocate authority letter for personal hearing at CGST Mudit Square or Haryana SGST DETC.</p>

<p><strong>Rule 142 (CGST Rules):</strong> Procedural framework for DRC-01A intimation, DRC-01 SCN, DRC-06 reply and DRC-07 order. Rule 142(1A) specifically covers the pre-SCN intimation route.</p>

<p><strong>Key CBIC Circulars and Notifications (<a href="https://cgstpanchkulazone.gov.in" target="_blank" rel="noopener">CGST Panchkula Zone</a>):</strong></p>
<ul>
    <li><strong>Circular 183/15/2022-GST:</strong> ITC mismatch defences for FY 2017-18 and FY 2018-19 (supplier-side defaults)</li>
    <li><strong>Notification 09/2023-CT:</strong> FY 2017-18 / 18-19 / 19-20 SCN deadline extensions</li>
    <li><strong>Notification 56/2023-CT:</strong> Further extensions on Section 73 SCN timelines</li>
    <li><strong>53rd GST Council recommendation:</strong> Section 128A conditional waiver scheme for FY 17-18, 18-19, 19-20</li>
    <li><strong>Notification 11/2017-CT(R) as amended:</strong> Real estate sector 5 / 1 percent rate (relevant for Gurugram developers)</li>
</ul>

<p><strong>Key Case Law:</strong> Punjab and Haryana High Court has heard multiple Section 73 challenges since 2022 - particularly on time-bar defects under Section 73(2) and natural justice violations. GST Council Circular 183/15/2022 provides defence framework for FY 2017-18 and FY 2018-19 ITC mismatch cases.</p>

