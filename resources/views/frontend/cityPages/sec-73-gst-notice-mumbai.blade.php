                <h2 class="section-title">What Is a Section 73 GST Notice</h2>
                <div class="content-text what-is-definition">
                    
                    
<p><strong>Section 73 of the CGST Act 2017</strong> is the non-fraud show-cause notice provision. It applies where tax has not been paid, has been short paid, has been erroneously refunded, or ITC has been wrongly availed or utilised - <strong>WITHOUT</strong> any fraud, wilful misstatement or suppression of facts on the part of the assessee.</p>

<p>Section 73 is distinguished from Section 74 (which applies where fraud, wilful misstatement or suppression is present) by the level of culpability alleged. Section 73 carries a milder penalty regime - <strong>NIL penalty</strong> if voluntary payment is made under <strong>Section 73(5) before SCN</strong> or under <strong>Section 73(8) within 30 days of SCN</strong>; 10 percent of tax or Rs 10,000 whichever higher under Section 73(9) for adjudicated cases. CBIC Circular 31/05/2018-GST sets the Section 73 vs 74 distinguishing criteria.</p>

<p>The time limit for SCN under <strong>Section 73(2)</strong> is 2 years 9 months from the annual-return due date for the FY in question; the order must be passed within 3 years under <strong>Section 73(10)</strong> - subject to <strong>Section 168A extensions</strong> notified for FYs 2017-18 to 2020-21 via Notifications 09/2023-CT and 56/2023-CT.</p>

<p>For Mumbai-registered businesses, Section 73 SCNs come from one of Mumbai 7 CGST Commissionerates or the Maharashtra State Tax Department Mumbai Circle offices under the Maharashtra GST Act 2017 mirror. The DRC-01 to DRC-07 lifecycle (DRC-01A intimation, DRC-01 SCN, DRC-03 voluntary payment, DRC-06 reply, DRC-07 final order) is identical across CGST and Maharashtra SGST under cross-empowerment.</p>


                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Section 73 GST Notice Mumbai:</strong></p>
                    
<ul>
    <li><strong>Section 73(1):</strong> Empowers proper officer to issue SCN for tax not paid, short paid, erroneously refunded or ITC wrongly availed in non-fraud cases - specifies tax, interest under Section 50, and proposed penalty under Section 73(9).</li>
    <li><strong>Section 73(2):</strong> SCN time limit - must be issued at least 3 months before Section 73(10) limit; effectively 2 years 9 months from annual-return due date.</li>
    <li><strong>Section 73(5):</strong> Voluntary payment BEFORE SCN service in Form DRC-03 with tax + interest under Section 50; result: <strong>NIL penalty</strong> - the most valuable Section 73 escape hatch.</li>
    <li><strong>Section 73(8):</strong> Post-SCN voluntary payment within 30 days in Form DRC-03 with tax + interest; result: <strong>NIL penalty</strong> + all proceedings deemed concluded.</li>
    <li><strong>Section 73(9):</strong> Adjudication penalty - 10 percent of tax OR Rs 10,000 whichever higher.</li>
    <li><strong>Section 73(10):</strong> Order time limit - 3 years from annual-return due date.</li>
    <li><strong>Section 168A:</strong> Empowers government to extend statutory time limits - Notifications 09/2023-CT and 56/2023-CT have extended Section 73(2) / 73(10) limits for FYs 2017-18 to 2020-21.</li>
    <li><strong>GSTAT Western Regional Bench Mumbai:</strong> Co-located GSTAT bench - covers Maharashtra (including Pune), Goa, Daman and Diu, Dadra and Nagar Haveli. Mumbai assessees enjoy a co-location advantage that Pune and Gurugram assessees lack.</li>
    <li><strong>Bombay High Court Principal Seat:</strong> Writ jurisdiction under Article 226 for Section 73 time-bar, jurisdictional infirmity, natural justice violations - also co-located in Mumbai.</li>
</ul>


