            <h2 class="section-title">Legal and Compliance Framework Governing Section 73 in Mumbai</h2>
            <div class="content-text">
                
                
<p><strong>Governing Act:</strong> Central Goods and Services Tax Act, 2017 (Sections 73, 50, 75, 107, 112, 116, 168A, 169) and Maharashtra Goods and Services Tax Act, 2017 (parallel provisions for SGST notices from Maharashtra State Tax Mazgaon HQ Circle officers).</p>

<p><strong>Section 73(1) (<a href="https://taxinformation.cbic.gov.in" target="_blank" rel="noopener">CBIC Portal</a>):</strong> Empowers proper officer to issue SCN for non-fraud tax shortfall, erroneous refund or wrongly availed ITC - specifies tax, interest under Section 50 and proposed penalty under Section 73(9).</p>

<p><strong>Section 73(2):</strong> SCN must be issued at least 3 months before the time limit for adjudicating order under Section 73(10) - effectively 2 years 9 months from GSTR-9 due date.</p>

<p><strong>Section 73(5):</strong> Voluntary pre-SCN payment via DRC-03 with tax + Section 50 interest - <strong>NIL penalty</strong> - the most valuable Section 73 escape hatch.</p>

<p><strong>Section 73(6):</strong> Proper officer barred from serving SCN for amounts paid voluntarily under Section 73(5).</p>

<p><strong>Section 73(7):</strong> If Section 73(5) payment falls short, SCN may issue for balance only - not for the paid portion.</p>

<p><strong>Section 73(8):</strong> Payment within 30 days of SCN service in Form DRC-03 - <strong>NIL penalty + all proceedings deemed concluded</strong>. The single biggest lever in Section 73 defence.</p>

<p><strong>Section 73(9):</strong> Adjudication penalty - 10 percent of tax OR Rs 10,000 whichever higher.</p>

<p><strong>Section 73(10):</strong> Order time limit - 3 years from GSTR-9 due date.</p>

<p><strong>Section 73(11):</strong> Section 73(9) penalty NOT leviable on voluntary deposit under Section 73(5) / 73(8).</p>

<p><strong>Section 50:</strong> Interest at 18 percent per annum on tax not paid or short paid; 24 percent per annum on excess ITC reversal.</p>

<p><strong>Section 168A:</strong> Empowers government to extend statutory time limits via notification. Notifications 09/2023-CT and 56/2023-CT have extended Section 73(2) / 73(10) limits for FYs 2017-18 to 2020-21.</p>

<p><strong>Section 107:</strong> Appeal to Mumbai Appellate Authority within 3 months with 10 percent pre-deposit (Form GST APL-01) - max Rs 25 crore CGST + Rs 25 crore SGST cap.</p>

<p><strong>Section 112:</strong> Second appeal to <a href="https://gstcouncil.gov.in" target="_blank" rel="noopener">GSTAT Western Regional Bench Mumbai</a> - covers Maharashtra, Goa, Daman and Diu, Dadra and Nagar Haveli - co-located with Mumbai CGST.</p>

<p><strong>Section 116:</strong> Authorised representative provisions - CA or advocate authority letter for personal hearing.</p>

<p><strong>CBIC Circular 31/05/2018-GST:</strong> Distinguishes Section 73 (non-fraud) from Section 74 (fraud, wilful misstatement, suppression).</p>

<p><strong>Notifications 09/2023-CT and 56/2023-CT:</strong> Section 168A extensions of Section 73(10) order limit for FYs 2017-18 to 2020-21 due to COVID-19 backlog.</p>

<p><strong>Maharashtra GST Act 2017:</strong> State mirror of CGST Act applicable to state-component Section 73 SCNs from Maharashtra State Tax Mumbai Circle officers at Mazgaon HQ.</p>

