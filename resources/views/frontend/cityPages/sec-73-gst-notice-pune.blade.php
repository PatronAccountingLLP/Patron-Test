                <h2 class="section-title">What Is a Section 73 GST Notice</h2>
                <div class="content-text what-is-definition">
                    
                    
<p><strong>Section 73 of the CGST Act 2017</strong> is the non-fraud show-cause notice provision. It applies where tax has not been paid, has been short paid, has been erroneously refunded, or ITC has been wrongly availed or utilised - <strong>WITHOUT</strong> any fraud, wilful misstatement or suppression of facts on the part of the assessee.</p>

<p>Section 73 is distinguished from Section 74 (which applies where fraud, wilful misstatement or suppression is present) by the level of culpability alleged. Section 73 carries a milder penalty regime - <strong>NIL penalty</strong> if voluntary payment is made under <strong>Section 73(5) before SCN</strong> or under <strong>Section 73(8) within 30 days of SCN</strong>; 10 percent of tax or Rs 10,000 whichever higher under Section 73(9) for adjudicated cases. CBIC Circular 31/05/2018-GST sets the Section 73 vs 74 distinguishing criteria.</p>

<p>The most important practical feature of Section 73 is the <strong>NIL-penalty escape hatch</strong>. Under Section 73(5), if the assessee pays tax + interest under Section 50 BEFORE the SCN is served and intimates the proper officer in Form DRC-03, NO penalty is payable. Under Section 73(8), if the assessee pays tax + interest within 30 days of SCN service, again NO penalty is payable AND all proceedings are deemed concluded. Only Section 73(9) adjudication triggers the 10 percent or Rs 10,000 penalty.</p>

<p>The time limit for SCN under <strong>Section 73(2)</strong> is 2 years 9 months from the annual-return due date; the order must be passed within 3 years under <strong>Section 73(10)</strong> - subject to <strong>Section 168A extensions</strong> notified for FYs 2017-18 to 2020-21 via Notifications 09/2023-CT and 56/2023-CT. For Pune-registered businesses, Section 73 SCNs come from Pune-I CGST or Pune-II CGST or the Maharashtra State Tax Pune Circle office.</p>


                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Section 73 GST Notice Pune:</strong></p>
                    
<ul>
    <li><strong>Section 73(1):</strong> Empowers proper officer to issue SCN for tax not paid, short paid, erroneously refunded or ITC wrongly availed in non-fraud cases.</li>
    <li><strong>Section 73(2):</strong> SCN time limit - 2 years 9 months from annual-return due date.</li>
    <li><strong>Section 73(5):</strong> Voluntary payment BEFORE SCN in Form DRC-03 - <strong>NIL penalty</strong>.</li>
    <li><strong>Section 73(6):</strong> Proper officer barred from serving SCN for amounts paid voluntarily under Section 73(5).</li>
    <li><strong>Section 73(7):</strong> If Section 73(5) payment falls short, SCN may issue for balance only.</li>
    <li><strong>Section 73(8):</strong> Post-SCN payment within 30 days in DRC-03 - <strong>NIL penalty</strong> + proceedings deemed concluded.</li>
    <li><strong>Section 73(9):</strong> Adjudication penalty - 10 percent of tax OR Rs 10,000 whichever higher.</li>
    <li><strong>Section 73(10):</strong> Order time limit - 3 years from annual-return due date.</li>
    <li><strong>Section 168A:</strong> Government power to extend statutory time limits - Notifications 09/2023-CT and 56/2023-CT extended Section 73(2) / 73(10) limits for FYs 2017-18 to 2020-21.</li>
    <li><strong>GSTAT Western Bench Mumbai:</strong> Section 112 second appeal forum for Maharashtra including Pune - Pune assessees travel approximately 150 km.</li>
