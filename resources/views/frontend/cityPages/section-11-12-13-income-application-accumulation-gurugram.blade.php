                        <h3 class="faq-expanded__q">How much of a trust's income can be accumulated indefinitely?</h3>
                        <div class="faq-expanded__a"><p>Up to 15% of income can be set apart or accumulated indefinitely without any condition or filing. Only the accumulation of income beyond this 15% is subject to Section 11(2) conditions and the five-year time limit filed through Form 10.</p></div>
