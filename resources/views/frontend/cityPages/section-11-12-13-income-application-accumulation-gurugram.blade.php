    <div class="content-container">
        <div class="text-content">
            <h2 class="section-title">Who Needs to Comply with Sections 11, 12 and 13</h2>
            <div class="content-text">
                <p>These provisions apply to every organisation that claims exemption on the strength of charitable or religious registration. If your entity relies on Section 11/12 exemption, Section 13 discipline is not optional - it is the price of keeping that exemption.</p>
                <ul>
                    <li><strong>Charitable Trusts Registered under Section 12A/12AB:</strong> Any public charitable trust that has obtained registration must meet the 85% application rule each year and file Form 9A or Form 10 where full application is not achieved.</li>
                    <li><strong>Religious Trusts and Endowments:</strong> Religious trusts enjoy the same Section 11 framework and must apply income to religious objects in India, with the same accumulation options.</li>
                    <li><strong>NGOs and Societies:</strong> Registered societies pursuing charitable objects and holding 12A/12AB registration fall squarely within Sections 11, 12 and 13.</li>
                    <li><strong>Section 8 Companies:</strong> A <a href="/section8-company-registration">Section 8 company</a> registered for charitable purposes and holding 12A/12AB registration must satisfy the application, accumulation, and specified-person conditions like any other exempt entity.</li>
                    <li><strong>Trusts Receiving Large Voluntary Contributions:</strong> Where donations are substantial, correct classification of corpus versus general donations under Section 12 becomes critical to the 85% computation.</li>
                    <li><strong>Trusts with Founder, Trustee, or Substantial-Contributor Dealings:</strong> Any trust that transacts with its insiders - loans, property use, salaries, purchases - must test every transaction against Section 13(3) to avoid forfeiting exemption.</li>
                    <li><strong>Trusts Undergoing Audit under Form 10B or Form 10BB:</strong> Where total income before exemption exceeds Rs 5 crore, or foreign contribution is received, or income is applied outside India, audit in Form 10B is required; other trusts audit in Form 10BB. Specified-person details are mandatory disclosures in both.</li>
                </ul>
                <p><strong>Why This Matters:</strong> Exemption is assessed year by year. A trust can be fully compliant for a decade and still lose exemption in a single year through a shortfall in application, a late Form 9A/Form 10, or an inadvertent benefit to a specified person. Ongoing discipline - not one-time registration - is what protects the trust.</p>
