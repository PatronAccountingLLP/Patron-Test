<section class="content-section">
    <div class="content-container">
        <div class="text-content">
            <h2 class="section-title">Legal Framework (India)</h2>
            <div class="content-text">
                <p><strong>Governing Provisions:</strong> The framework rests on Sections 11, 12 and 13 of the Income Tax Act 1961, read with the prescribed forms and CBDT circulars.</p>
                <ul>
                    <li><strong>Section 11 Income Tax Act 1961:</strong> Application of income to objects in India; 85% application rule; 15% standard accumulation under Section 11(1); deemed application; accumulation under Section 11(2); investment modes under Section 11(5).</li>
                    <li><strong>Section 12 Income Tax Act 1961:</strong> Deems voluntary contributions (other than directed corpus donations) to be income subject to the Section 11 conditions.</li>
                    <li><strong>Section 13 Income Tax Act 1961:</strong> Circumstances denying exemption - Section 13(1)(c) benefit to specified persons; Section 13(1)(d) investments outside Section 11(5).</li>
                    <li><strong>Section 13(3) Income Tax Act 1961:</strong> Defines specified persons - author/founder, trustees and managers, and substantial contributors.</li>
                    <li><strong>Section 164(2) Income Tax Act 1961:</strong> Taxation of the relevant income at the maximum marginal rate where exemption is denied for a Section 13 violation.</li>
                    <li><strong>Section 139(1) Income Tax Act 1961:</strong> Due date for furnishing the return, which governs the deadline for Form 9A and Form 10.</li>
                    <li><strong>Form 9A:</strong> Electronic form to exercise the deemed-application option.</li>
                    <li><strong>Form 10:</strong> Electronic form to accumulate income under Section 11(2).</li>
                    <li><strong>Form 10B / Form 10BB:</strong> Audit reports for trusts, with mandatory specified-person disclosures.</li>
                    <li><strong>CBDT Circular 6/2023 (24 May 2023):</strong> Accumulation or deemed application not denied where Form 9A or Form 10 is furnished on or before the Section 139(1) due date.</li>
                    <li><strong>Finance Act 2025 (effective 1 April 2025):</strong> Revised the substantial-contributor threshold to more than Rs 1 lakh in the year or more than Rs 10 lakh in aggregate, and removed relatives and concerns of substantial contributors from the specified-person definition.</li>
                </ul>
                <p style="margin-top:16px;font-size:13px;color:var(--text-muted);font-style:italic;">This page is for general information and does not constitute legal or tax advice. Statutory positions may change; always confirm the current law and your specific facts with a qualified professional before acting.</p>
