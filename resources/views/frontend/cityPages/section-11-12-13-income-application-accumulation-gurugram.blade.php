                    <div class="faq-expanded__item" id="faq-5">
                        <h3 class="faq-expanded__q">What kind of benefit to a specified person is prohibited?</h3>
                        <div class="faq-expanded__a"><p>Section 13(1)(c) prohibits any part of the trust's income or property being used or applied, directly or indirectly, for the benefit of a specified person, for example unsecured loans, rent-free or below-market use of property, excessive salaries, or purchases at inflated prices. Section 13(1)(d) restricts investments outside the Section 11(5) modes.</p></div>
