                        <div class="faq-expanded__a"><p>Where exemption is denied for a Section 13 violation, the income involved is taxed at the maximum marginal rate under Section 164(2). Recent amendments limit the denial to the portion of income actually applied or invested in violation, rather than the entire income of the trust.</p></div>
