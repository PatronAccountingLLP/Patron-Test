            <div class="column-content">
                <h2 class="section-title">The Section 11, 12 &amp; 13 Framework Explained</h2>
                <div class="content-text what-is-definition">
                    <p>Understanding how the three sections interlock is the foundation of trust tax compliance. Each section addresses a distinct question - how much to spend, what counts as income, and what conduct forfeits exemption.</p>
                    <p><strong>Section 11 - Application and Accumulation of Income:</strong> Section 11 requires a charitable or religious trust to apply at least 85% of its income to its objects in India during the previous year. Up to 15% of income may be accumulated or set apart without any condition under Section 11(1), and this standard accumulation can be retained indefinitely with no separate form or investment requirement. Where the trust cannot apply the required 85% because income was not received in the year, or for any other reason, it may exercise the "deemed application" option through <strong>Form 9A</strong>. Where the trust wishes to accumulate income beyond the free 15% for a future purpose, Section 11(2) permits accumulation for up to five years through <strong>Form 10</strong>, with the funds invested in the modes specified under Section 11(5).</p>
                    <p><strong>Section 12 - Voluntary Contributions as Income:</strong> Section 12 deems voluntary contributions received by a trust - other than <strong>corpus donations</strong> received with a specific written direction that they form part of the corpus - to be income derived from property held under trust. Such contributions must then satisfy the same Section 11 application and accumulation conditions. This is why donation classification matters: a corpus donation is treated differently from a general donation that enters the 85% computation.</p>
                    <p><strong>Section 13 - Conditions That Forfeit Exemption:</strong> Section 13 sets out circumstances in which the exemptions under Sections 11 and 12 are denied. The most important is Section 13(1)(c), which denies exemption where any part of the trust's income or property is used or applied, directly or indirectly, for the benefit of <strong>specified persons</strong> defined in Section 13(3). Section 13(1)(d) restricts investments held outside the Section 11(5) modes. Where exemption is forfeited, the relevant income is taxed at the <strong>maximum marginal rate under Section 164(2)</strong>.</p>
                    <p><strong>The Compliance Chain:</strong> The three sections operate as a chain - Section 12 defines what income is, Section 11 dictates how that income must be applied or accumulated, and Section 13 polices the conduct of the trust and its insiders. A failure at any link can cost the trust its exemption for the year, which is why <a href="/form-10b-audit-report-section-12a-trust">Form 10B audit reporting</a> requires mandatory disclosure of specified-person transactions.</p>
                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms:</strong></p>
                    <ul style="margin-top:12px;">
                        <li><strong>85% Application Rule (Section 11(1)):</strong> At least 85% of income must be applied to charitable or religious objects in India during the year.</li>
                        <li><strong>15% Standard Accumulation:</strong> Up to 15% of income may be set apart indefinitely with no condition, form, or investment requirement.</li>
                        <li><strong>Form 9A - Deemed Application:</strong> Option to treat income as applied where it was not received during the year or could not be applied for another reason; filed electronically on or before the Section 139(1) due date.</li>
                        <li><strong>Form 10 - Accumulation (Section 11(2)):</strong> Accumulation of income beyond 15% for a stated purpose for a maximum of five years, invested under Section 11(5).</li>
                        <li><strong>Section 11(5) Modes of Investment:</strong> The prescribed forms and modes in which accumulated or set-apart funds must be invested or deposited.</li>
                        <li><strong>Specified Persons (Section 13(3)):</strong> Author/founder, trustees and managers, and substantial contributors whose benefit from the trust is restricted.</li>
                        <li><strong>Maximum Marginal Rate (Section 164(2)):</strong> The rate at which income is taxed when exemption is denied for a Section 13 violation.</li>
                        <li><strong>Corpus Donation:</strong> A voluntary contribution received with a specific written direction that it forms part of the corpus - not counted as income under Section 12.</li>
                    </ul>
                </div>
            </div>
