                        <h3 class="faq-expanded__q">For how long can income be accumulated under Section 11(2)?</h3>
                        <div class="faq-expanded__a"><p>Income accumulated under Section 11(2) via Form 10 can be held for a maximum of five years for the stated charitable or religious purpose. The period during which the income cannot be applied due to a court or authority order is excluded from this five-year period.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-3">
                        <h3 class="faq-expanded__q">What is the difference between Form 9A and Form 10?</h3>
                        <div class="faq-expanded__a"><p>Form 9A is for "deemed application" where an income shortfall is due to non-receipt of income or other reasons; the income must generally be applied in the year of receipt or the immediately following year. Form 10 is for accumulating income for a specific future purpose for up to five years and requires investment under Section 11(5).</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-4">
                        <h3 class="faq-expanded__q">Who are the specified persons under Section 13(3)?</h3>
                        <div class="faq-expanded__a"><p>They include the author or founder of the trust, trustees and managers, and substantial contributors. Following the Finance Act 2025 amendment effective 1 April 2025, a substantial contributor is a person whose contribution exceeds Rs 1 lakh during the year or Rs 10 lakh in aggregate, and relatives and concerns of such contributors are no longer treated as specified persons.</p></div>
