<!-- SECTION 15A: LEGAL FRAMEWORK -->
<section class="content-section">
    <div class="content-container">
        <div class="text-content">
            <h2 class="section-title">Legal and Compliance Framework for Section 8 Companies</h2>
            <div class="content-text">
                
                <p><strong>Companies Act, 2013 - Section 8:</strong> Permits registration of companies for promoting charitable objects. CRC issues licence allowing the company to omit 'Limited'. All profits applied towards objects. No dividend distribution.</p>
                <p><strong>Companies (Incorporation) Rules, 2014 - Rule 19:</strong> Prescribes procedure for licence application including INC-12 (now in SPICe+), INC-13 (MOA format), INC-14 (professional declaration) and INC-15 (subscriber declaration). Name must include Foundation/Association/Council terms.</p>
                <p><strong>Section 8(7) - Revocation of Licence:</strong> Central Government can revoke licence if company contravenes conditions, acts fraudulently or against objects. Officers face imprisonment up to 3 years and fine up to Rs 25 lakh.</p>
