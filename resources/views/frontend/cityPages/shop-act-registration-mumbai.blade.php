                        <li><strong>Form F (0-9 emp):</strong> Self-declaration. No verification. Intimation Certificate. LIFETIME validity. No renewal. Even ZERO employees need this.</li>
                        <li><strong>24-Hour Economy:</strong> S.27 permits ALL establishments (except liquor) to operate 24 hours. No separate permission. Mumbai's night economy enabled by law.</li>
                        <li><strong>Marathi Name Board:</strong> S.22 mandates Marathi in Devnagari as primary script. English/Hindi secondary. BMC inspectors check compliance.</li>
