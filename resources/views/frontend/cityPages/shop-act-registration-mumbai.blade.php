                <div class="content-text what-is-definition">
                    
                    <p>Shop Act registration - called Gumasta license in Maharashtra - is the mandatory registration of every establishment under the Maharashtra Shops and Establishments Act 2017 with MCGM/BMC, enabling the business to legally operate from a commercial space.</p>
                    <p>The 2017 Act replaced the 1948 Act (effective 19 December 2017): two tracks (Form A/F), lifetime validity for <10, online filing, Facilitator replaces Inspector, 24-hour operations permitted. Old 1948 Act Gumasta is no longer valid.</p>
                    <p>'Establishment' = every commercial premises: from a BKC MNC office to a Bandra cafe to a Powai startup. Filed on <a href="https://portal.mcgm.gov.in" target="_blank" rel="noopener">MCGM portal</a>. Learn more at <a href="/shop-act-registration">our national Shop Act practice</a>.</p>

