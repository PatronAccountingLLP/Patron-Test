            <h2 class="section-title">Legal and Compliance Framework for TAN in Delhi</h2>
            <div class="content-text">
                
                <p><strong>Section 203A - Mandatory TAN:</strong> Every person deducting TDS or collecting TCS must apply for TAN allotment. TAN must be quoted in all TDS/TCS returns, payment challans, certificates (Form 16/16A/27D), statements of financial transactions and other prescribed documents.</p>
<p><strong>Section 272BB - Penalty:</strong> Failure to apply for TAN or failure to quote TAN (or quoting incorrect TAN) in prescribed documents attracts a penalty of Rs 10,000. This is a flat penalty - not proportional to the TDS amount.</p>
<p><strong>Form 49B:</strong> Application form for allotment of TAN. Filed online via Protean (<a href="https://www.incometax.gov.in/" target="_blank" rel="noopener">incometaxindia.gov.in</a>) or offline at TIN Facilitation Centres. No supporting documents required. Fee Rs 77. Companies incorporated via SPICe+ get TAN through Form INC-32 during incorporation.</p>
<p><strong>Section 234E - Late Filing Penalty:</strong> Late filing of quarterly TDS return attracts a fee of Rs 200 per day until the return is filed, subject to a maximum of the TDS amount. This makes timely TDS return filing critical for Delhi businesses with large TDS deductions.</p>
<p><strong>Section 201(1A) - Interest on Late TDS Payment:</strong> TDS deducted but not paid to the government attracts interest at 1.5% per month (or part thereof) from the date of deduction to the date of actual payment. TDS not deducted when required attracts interest at 1% per month.</p>
<p><strong>TDS Return Forms:</strong> Form 24Q (salary TDS - Section 192), Form 26Q (non-salary TDS to residents), Form 27Q (TDS on payments to non-residents), Form 27EQ (TCS). Returns filed quarterly on TRACES.</p>
<p><strong>TRACES:</strong> Online portal (tdscpc.gov.in) managed by CPC-TDS, Ghaziabad. Used for TDS return filing, correction statements, Form 16/16A download, challan status, OLTAS verification and PAN verification. TAN registration on TRACES is mandatory post-allotment.</p>
<p><strong>Sections 194-IA/194-IB/194M - PAN Instead of TAN:</strong> Individuals deducting TDS on property purchase (194-IA), rent (194-IB) or payments to contractors/professionals (194M) can use PAN instead of TAN. These are specific exceptions for individual deductors below certain thresholds.</p>

