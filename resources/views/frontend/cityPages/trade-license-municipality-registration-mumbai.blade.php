                <h2 class="section-title">What a BMC Trade Licence Actually Is</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p>A <strong>BMC Trade Licence</strong> is a permit issued by the Brihanmumbai Municipal Corporation under Section 313 of the BMC Act, 1888 authorising the holder to carry on a specific trade or business within BMC limits.</p>
                    <p>Section 394 of the BMC Act prohibits the carrying on of certain trades, processes or operations in factories or premises without such a licence. The BMC has notified a list of trades and processes (under Schedule M, Schedule N and subsequent BMC general body resolutions) that require this licence - generally trades involving (a) PUBLIC HEALTH (food preparation, healthcare, water supply); (b) PUBLIC SAFETY (hazardous materials, fire risk premises, gaming); (c) SANITATION (waste-generating businesses, animal-related trades); (d) NUISANCE potential (entertainment venues, manufacturing).</p>
                    <p>The Trade Licence is fundamentally DIFFERENT from the Shop and Establishment Act registration (Gumasta). The Gumasta is a state-level registration under the Maharashtra Shops Act 2017 applicable to ALL commercial establishments and is more about labour and employment conditions; the BMC Trade Licence is a municipal-level activity-based permit and is about the right to carry on the specific trade in the BMC jurisdiction.</p>
                    <p>Most Mumbai businesses in food, healthcare, personal care, entertainment or manufacturing categories need BOTH - Gumasta + BMC Trade Licence. Pure professional services offices (CA / CS / law firms / consulting), pure IT / software offices, and pure non-regulated retail typically do NOT need BMC Trade Licence (though they still need Gumasta and other registrations).</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for BMC Trade License:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>BMC Trade Licence:</strong> Activity-based municipal permit issued by BMC under BMC Act 1888 for specific notified trades within BMC limits.</li>
                        <li><strong>Municipality Registration:</strong> Generic term for municipal authority registration - in Mumbai it is the BMC Trade Licence; in other cities it is the equivalent municipal corporation framework (PMC, KDMC, NMMC, TMC, Vasai-Virar etc.).</li>
                        <li><strong>BMC Act 1888:</strong> Mumbai Municipal Corporation Act, 1888 - the foundational statute for BMC and Mumbai municipal administration.</li>
                        <li><strong>Section 313:</strong> BMC Act - empowers BMC to issue licences for trades and occupations.</li>
                        <li><strong>Section 394:</strong> BMC Act - prohibits carrying on certain trades, processes or operations in factories or premises without licence.</li>
                        <li><strong>Schedule M / N:</strong> BMC Act schedules listing trades and processes requiring licence.</li>
                        <li><strong>Ward officer:</strong> BMC official at the ward level (one of 24 wards) who handles Trade Licence applications, inspections, renewals for the ward.</li>
                        <li><strong>BMC Citizen Services Portal:</strong> portal.mcgm.gov.in - BMC online portal hosting Trade Licence application module among other services.</li>
                        <li><strong>Trade Licence vs Gumasta:</strong> Trade Licence is MUNICIPAL (BMC) and ACTIVITY-BASED; Gumasta (Shop Act) is STATE (Maharashtra) and applies to ALL commercial establishments.</li>
