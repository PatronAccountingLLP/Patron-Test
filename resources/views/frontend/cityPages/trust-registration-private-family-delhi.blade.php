                        <h3 class="faq-expanded__q">What is a private family trust under the Indian Trusts Act 1882?</h3>
                        <div class="faq-expanded__a"><p>A private family trust is a legal arrangement under the Indian Trusts Act 1882, where a settlor (the property owner) transfers property to one or more trustees to hold and manage for the benefit of specified family beneficiaries. Section 3 of the Act defines a trust as an obligation annexed to the ownership of property, and arising out of a confidence reposed in and accepted by the owner, or declared and accepted by him, for the benefit of another, or of another and the owner. Section 6 prescribes that the trust is created by the settlor settling property in favour of trustees for the beneficiaries.</p></div>
