                        <h3 class="faq-expanded__q">Who are settlor, trustee, and beneficiary?</h3>
                        <div class="faq-expanded__a"><p>Settlor (also called author or trustor) is the person who creates the trust by transferring property. Trustee is the person who accepts the property and undertakes to manage it for the beneficiaries per the trust deed; trustees are the legal owners of the trust property and act in a fiduciary capacity. Beneficiary is the person or class of persons for whose benefit the trust is created - typically family members in a private family trust. Under Indian law, the same person can be settlor, trustee and beneficiary simultaneously, though combining all three raises Section 61 (revocable trust) and other clubbing issues.</p></div>
