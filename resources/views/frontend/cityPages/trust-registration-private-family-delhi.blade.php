        <div class="text-content">
            <h2 class="section-title">Legal and Compliance Framework</h2>
            <div class="content-text">
                
                <p><strong>Indian Trusts Act, 1882:</strong> Section 3 definition of trust; Section 6 creation by settlor settling property for trustees and beneficiaries; Section 7 settlor capacity; Section 8 subject matter; Section 9 beneficiary capacity; Section 10 transfer to trustee; Sections 11-30 trustee duties and powers (including investment under Section 20); Section 60 beneficiary right to information; Section 73 vacancy and substitution of trustees; Sections 77-79 extinction of trust.</p>
                <p><strong>Income-tax Act, 1961:</strong> Section 161 - representative assessee taxation (specific trusts at beneficiary slab rates); Section 164 - tax at Maximum Marginal Rate where shares are indeterminate (discretionary trusts); Section 61 - revocable trust income clubbed with settlor; Section 56(2)(x) - angel tax on gifts to trust by non-relatives above INR 50,000; Section 47 - exemptions on transfer to trust in specific cases (charitable trusts); Section 2(31)(iv) - trust as person under the Act.</p>
                <p><strong>Registration Act, 1908:</strong> Section 17 - non-testamentary instruments creating, declaring, assigning, limiting or extinguishing any right, title or interest in immovable property of the value of INR 100 and upwards must be registered. A trust deed transferring immovable property to trustees falls under Section 17.</p>
                <p><strong>Indian Stamp Act, 1899 and state Stamp Acts:</strong> State-wise stamp duty on trust deeds. Article 64 of Schedule IA covers settlement and trust deed. Maharashtra typically charges 5% of trust corpus for movable property settlements; Karnataka, Tamil Nadu and Delhi each have their own schedules. Patron checks the state-specific Article 64 (or equivalent) before drafting.</p>
                <p><strong>Transfer of Property Act, 1882:</strong> Section 5 transfer of property; Section 53 fraudulent transfer (trust set up to defeat creditors can be challenged); rule against perpetuities (applies to private trusts - typical 100-year limit).</p>
