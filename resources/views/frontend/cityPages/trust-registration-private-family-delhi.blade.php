                <h2 class="section-title">What Is a Private Family Trust</h2>
                <div class="content-text what-is-definition">
                    
                    <p>A Private Family Trust is a legal arrangement under the Indian Trusts Act 1882, in which a property owner (called the settlor or author of the trust) transfers identified property to one or more trustees to hold and manage for the benefit of specified family beneficiaries. Section 3 of the Indian Trusts Act defines a trust as an obligation annexed to the ownership of property, and arising out of a confidence reposed in and accepted by the owner, or declared and accepted by him, for the benefit of another, or of another and the owner. Section 6 of the Act prescribes the creation mechanics - the settlor settles the property in favour of trustees for beneficiaries.</p>

<p>Unlike a public trust (which serves the general public and is typically charitable - governed by state-specific legislation like the Maharashtra Public Trusts Act 1950), a private family trust serves identifiable beneficiaries - usually family members, dependants or relatives. The trust is the most flexible Indian succession-planning vehicle: it survives the settlor's death, holds family assets across generations, manages distribution to beneficiaries (including minors and persons with disabilities), and avoids the probate delay and family disputes that often follow intestate succession or contested wills.</p>

<p>Delhi-based families have a particular advantage on stamp-duty optimisation - Delhi's Article 64 Schedule IA rates on trust deeds are moderate compared to Maharashtra's 5% on corpus settlements. Patron Accounting's Delhi office handles the full structuring, deed drafting, Sub-Registrar registration and tax positioning for private family trusts in the NCR.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Private Family Trust:</strong></p>
                    <p><strong>Settlor (Author or Trustor):</strong> The person who creates the trust by transferring property and declaring confidence in the trustee. Per Section 7 of the Indian Trusts Act, must be of contractual capacity.</p>
<p><strong>Trustee:</strong> The person who accepts the confidence and undertakes to hold and manage the trust property for the beneficiaries per the trust deed. Per Section 10 of the Act, must have contractual capacity. Can be a family member, lawyer, CA or a corporate trustee. Sections 11-12 prescribe trustee duties.</p>
<p><strong>Beneficiary:</strong> The person or persons for whose benefit the trust is created. Can be specified individuals (named in the deed) or a class (e.g. all my lineal descendants).</p>
<p><strong>Trust Property (Trust Corpus):</strong> The property settled by the settlor on the trustee. Can be movable (cash, securities) or immovable (land, building). Forms the base on which the trust operates.</p>
<p><strong>Trust Deed:</strong> The instrument that creates the trust. Lists settlor, trustees, beneficiaries, trust property, objectives, trustee powers and duties, distribution mechanism, vesting, revocation rights, perpetuity period and dissolution. Must be executed on non-judicial stamp paper of the appropriate value per state Stamp Act.</p>
<p><strong>Specific (Determinate) Trust:</strong> A trust where beneficiaries are individually identifiable and their respective shares are specified. Income tax under Section 161 Income-tax Act 1961 - tax in hands of trustee as representative assessee at the beneficiary's applicable slab rate.</p>
<p><strong>Discretionary Trust:</strong> A trust where either the beneficiaries are not individually identifiable, OR their shares are not specified. Income tax under Section 164 - tax at Maximum Marginal Rate (30% plus surcharge and cess) in trustee's hands.</p>
<p><strong>Revocable vs Irrevocable Trust:</strong> Revocable - settlor retains the right to revoke; income clubs back with the settlor under Section 61 Income-tax Act 1961. Irrevocable - settlor relinquishes revocation rights; cleanest tax-positioning option but settlor loses control.</p>

