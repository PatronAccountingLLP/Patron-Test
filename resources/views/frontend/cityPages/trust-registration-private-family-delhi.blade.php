                    <div class="faq-expanded__item" id="faq-3">
                        <h3 class="faq-expanded__q">Is registration of a private family trust mandatory?</h3>
                        <div class="faq-expanded__a"><p>Registration is mandatory under Section 17 of the Registration Act 1908 where the trust property includes immovable property of value INR 100 or more. For trusts with only movable property (cash, securities, gold), registration is optional but strongly recommended for legal certainty, evidentiary value, and ease of dealing with banks and securities depositories. Most banks require a registered trust deed before opening a trust bank account. Patron recommends registering all private family trusts irrespective of asset class.</p></div>
