                    <div class="faq-expanded__item" id="faq-4">
                        <h3 class="faq-expanded__q">What is the difference between specific (determinate) and discretionary trust?</h3>
                        <div class="faq-expanded__a"><p>In a specific (also called determinate) trust, the beneficiaries are individually identifiable from the deed and their respective shares in the income or corpus are specified. Income is taxed under Section 161 of the Income-tax Act 1961 - in the hands of the trustee as representative assessee, at the slab rate applicable to the individual beneficiary. In a discretionary trust, either the beneficiaries are not individually identifiable, or their shares are not specified - trustees have discretion. Income is taxed under Section 164 at the Maximum Marginal Rate (currently 30% plus surcharge and cess).</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-5">
                        <h3 class="faq-expanded__q">How is a private family trust taxed in India?</h3>
                        <div class="faq-expanded__a"><p>Specific trust under Section 161 - the trustee files ITR-5 as representative assessee and the income is taxed at the rate applicable to each beneficiary on their specified share. Discretionary trust under Section 164 - the trustee files ITR-5 and the income is taxed at Maximum Marginal Rate (currently 30% plus surcharge and cess). If the trust is revocable, Section 61 clubs the entire income back with the settlor. If the trust runs a business, the business income is taxed at MMR in the trustee's hands regardless of trust type. Capital gains, house property income, and other heads follow their normal rules with trustee as the representative assessee.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-6">
                        <h3 class="faq-expanded__q">How much stamp duty is payable on a trust deed?</h3>
                        <div class="faq-expanded__a"><p>Stamp duty on a private family trust deed varies by state under Article 64 of Schedule IA of the Indian Stamp Act 1899 (or the corresponding article of the state Stamp Act). Maharashtra typically charges 5% of the trust corpus for movable-property settlements and higher for immovable property. Karnataka charges considerably lower rates. Delhi, Tamil Nadu and other states have their own schedules. For a INR 10 crore corpus trust in Maharashtra, stamp duty alone can be INR 50 lakh. Patron does the state-wise optimisation before recommending the trust jurisdiction of registration.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-7">
                        <h3 class="faq-expanded__q">Can the settlor also be a trustee or beneficiary?</h3>
                        <div class="faq-expanded__a"><p>Yes, the Indian Trusts Act 1882 permits the settlor to be a trustee, and also to be a beneficiary (or one of the beneficiaries). However, where the settlor retains substantial benefit or control - in particular, where the trust is revocable - Section 61 of the Income-tax Act 1961 clubs the trust income back with the settlor, defeating the tax-planning intent. The cleanest structures distinguish settlor, trustees and beneficiaries as distinct persons, with the settlor retaining only limited oversight rights (such as appointing or removing trustees, or being a protector with veto rights on specific decisions).</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-8">
                        <h3 class="faq-expanded__q">What is the difference between revocable and irrevocable trust?</h3>
                        <div class="faq-expanded__a"><p>In a revocable trust, the settlor retains the right to revoke (cancel) the trust and reclaim the trust property at any time during their lifetime. Income is clubbed back with the settlor under Section 61 of the Income-tax Act 1961 - tax benefits are limited. In an irrevocable trust, the settlor relinquishes the right to revoke - the trust property is no longer the settlor's; income is taxed in the trust (per Section 161 or 164) and does not club back. Irrevocable trusts are the cleaner tax-planning option but require the settlor to give up control - typically structured with trustee-appointment / removal rights and a protector role for the settlor to retain oversight without triggering revocability.</p></div>
