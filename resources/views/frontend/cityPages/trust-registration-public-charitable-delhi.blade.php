            <h2 class="section-title">Legal and Compliance Framework</h2>
            <div class="content-text">
                
                <p><strong>Maharashtra Public Trusts Act, 1950 (formerly Bombay Public Trusts Act 1950):</strong> Applies to Maharashtra and Gujarat. Section 2(13) public trust definition; Section 18 duty to register; Sections 19-22 Charity Commissioner inquiry; Section 28 register entry; Section 36 trustee accounts; Section 37 audit; Section 41 budget filings; Section 70 penalties. Delhi clients do not register here.</p>
                <p><strong>Indian Trusts Act, 1882:</strong> Section 1 provides that the Act does not apply to public or charitable trusts - but the general principles (trustee duties, beneficiary rights, fiduciary duty framework) inform public charitable trust law where no dedicated state Act exists (Delhi, UP, Karnataka, Tamil Nadu and similar).</p>
