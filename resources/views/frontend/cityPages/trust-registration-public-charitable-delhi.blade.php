                <h2 class="section-title">What Is a Public Charitable Trust</h2>
                <div class="content-text what-is-definition">
                    
                    <p>A Public Charitable Trust is an express or constructive trust created for a public religious or charitable purpose. Section 2(13) of the Maharashtra Public Trusts Act 1950 defines a public trust as an express or constructive trust for either a public religious or charitable purpose. The Income-tax Act 1961 in Section 2(15) defines charitable purpose to include relief of the poor, education, yoga, medical relief, preservation of environment, preservation of monuments, and the advancement of any other object of general public utility.</p>

<p>In Maharashtra and Gujarat, public charitable trusts are governed by the Maharashtra Public Trusts Act 1950 (formerly Bombay Public Trusts Act 1950) - the Charity Commissioner is the regulatory authority, and Sections 18-22 prescribe the registration mechanics. In Delhi and other states (UP, Karnataka, Tamil Nadu, West Bengal and similar) where no dedicated Public Trusts Act exists, public charitable trusts are created under general trust law (Indian Trusts Act 1882 framework adapted) and registered with the Sub-Registrar of Assurances.</p>

<p>Across all states, the Income-tax Act Section 12AB registration is required for tax exemption on the trust's income, and Section 80G registration is required for donors to claim deduction. Patron Accounting's Delhi office handles the full registration stack for charitable trusts based in Delhi NCR - trust deed, Sub-Registrar registration, PAN, Section 12AB, 80G, FCRA and annual ITR-7.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Public Charitable Trust:</strong></p>
                    <p><strong>Public Trust (Section 2(13) MPTA 1950):</strong> An express or constructive trust for either a public religious or charitable purpose. Distinguished from private trust by the public-purpose requirement.</p>
<p><strong>Charitable Purpose (Section 2(15) Income-tax Act 1961):</strong> Relief of the poor; education; yoga; medical relief; preservation of environment; preservation of monuments, places or objects of artistic or historic interest; advancement of any other object of general public utility (subject to commercial-receipt threshold).</p>
<p><strong>Settlor / Author of Trust:</strong> Person who creates the public charitable trust by transferring property and declaring confidence in the trustees.</p>
<p><strong>Trustees:</strong> Persons (typically 2 to 10) who accept the property and undertake to manage it for the public charitable purpose. Trustee composition matters - benefit to specified persons or relatives triggers Section 13(1)(c) clawback of exemption.</p>
<p><strong>Charity Commissioner:</strong> Statutory authority under Maharashtra Public Trusts Act 1950 with powers of supervision, regulation and control of public trusts in Maharashtra and Gujarat. Receives applications, conducts inquiries, registers trusts and approves trustee actions.</p>
<p><strong>Section 12AB Registration:</strong> Mandatory registration under the post-2021 regime for tax exemption under Section 11. Replaces the legacy Section 12A or 12AA. Granted by CIT(Exemptions); 5-year provisional registration followed by revalidation.</p>
<p><strong>Section 80G Registration:</strong> Mandatory registration for donors to claim deduction on donations. 50% or 100% deduction available per institutional category. Granted by CIT(Exemptions); 5-year cycle.</p>
<p><strong>FCRA Registration:</strong> Foreign Contribution (Regulation) Act 2010 registration mandatory for receiving foreign donations. Granted by Ministry of Home Affairs; 5-year cycle.</p>
<p><strong>Section 13(1)(c) ITA Trap:</strong> If any income or property of the trust is applied or used for the benefit of any specified person (settlor, trustee, or relative), the exemption under Section 11 is denied. The most common error in private-family-disguised-as-charitable setups.</p>

