<section class="content-section" id="what-section">
    <div class="content-container">
        <div class="two-column">
            <div class="column-content">
                <h2 class="section-title">What Is a Public Charitable Trust</h2>
                <div class="content-text what-is-definition">
                    
                    <p><strong>A public charitable trust is a non-profit vehicle created by one or more settlors who transfer property to trustees to be held and applied for a charitable purpose as defined in Section 2(15) of the Income-tax Act - relief of the poor, education, yoga, medical relief, preservation of environment, preservation of monuments or places or objects of artistic or historic interest, or the advancement of any other object of general public utility.</strong></p>
                    <p>The trust's beneficiaries are a section of the public - not named family members. Once registered under Section 12AB of the Income-tax Act, the trust's income from property held for charitable purposes is exempt under Section 11 to the extent the income is applied for charitable purposes (subject to the 85 percent application rule).</p>
                    <p>With separate Section 80G recognition, donors to the trust can claim deduction on contributions. The trust route co-exists with two alternatives - Society under the Societies Registration Act, 1860 and Section 8 Company under the Companies Act, 2013 - which target the same tax outcomes but through different corporate forms.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Public Charitable Trust Registration:</strong></p>
                    <p><strong>Charitable Purpose (Section 2(15)):</strong> Includes relief of the poor, education, yoga, medical relief, preservation of environment and monuments, and the advancement of any other object of general public utility - subject to the commercial-activity proviso in the same section.</p>
                    <p><strong>Section 11 Exemption:</strong> Exempts income from property held under trust for charitable or religious purposes from total income, to the extent applied for those purposes; up to 15 percent may be accumulated under Section 11(1)(a).</p>
                    <p><strong>Section 12AB Registration:</strong> The current registration regime under the Income-tax Act effective from 1 April 2021 - replaces the earlier Section 12AA regime; provisional registration for 3 years, then regular registration for 5 years on application via Form 10AB.</p>
                    <p><strong>Section 80G Recognition:</strong> A separate Income Tax Department recognition that lets donors claim 50 percent or 100 percent deduction on contributions; requires the trust to satisfy the five-condition test under Section 80G(5).</p>
                    <p><strong>85 Percent Application Rule:</strong> Section 11(1)(a) - the trust must apply at least 85 percent of its income during the year for charitable or religious purposes; the balance up to 15 percent may be accumulated under specified conditions.</p>
                    <p><strong>Form 10A:</strong> The application form for fresh provisional registration under Section 12AB and Section 80G - to be filed within 1 month from the date the trust seeks registration.</p>
                    <p><strong>Form 10AB:</strong> The application form for renewal of registration - to be filed at least 6 months before expiry, OR within 6 months of the start of activities, whichever is earlier.</p>

