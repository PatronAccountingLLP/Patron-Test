                        <div class="faq-expanded__a"><p>Section 11(1)(a) of the Income-tax Act requires a charitable trust to apply at least 85 percent of its income from property held under trust toward its charitable purpose in the year of receipt. The balance up to 15 percent can be accumulated under Section 11(2) by filing Form 9A or Form 10 declaring the purpose and time-frame for accumulation. Failure to apply 85 percent or to validly accumulate the balance results in the unapplied portion being included in taxable income, defeating the Section 11 exemption.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-6">
                        <h3 class="faq-expanded__q">How is Section 80G deduction different for donors - 50 percent or 100 percent?</h3>
                        <div class="faq-expanded__a"><p>Section 80G provides donor deduction at two rates depending on the category of the donee institution under Schedule I to the Act. The 100 percent deduction (with or without qualifying limit) covers specified national funds (for example PM CARES, National Defence Fund, PM National Relief Fund) and notified institutions. The 50 percent deduction (with or without qualifying limit) is the standard rate for most charitable trusts that have obtained 80G recognition. The qualifying-limit constraint caps eligible donations at 10 percent of the donor's adjusted gross total income.</p></div>
