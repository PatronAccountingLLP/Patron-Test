                        <div class="faq-expanded__a"><p>Yes - listed companies and prescribed corporates can only route Section 135 CSR funds to implementing agencies that are registered under CSR-1 on the MCA portal. CSR-1 registration presupposes 12AB registration (or Section 80G or 10(23C) recognition). Without CSR-1, the donor corporate cannot validly account for the CSR spend under Rule 4 of the Companies (CSR Policy) Rules, 2014. Patron files CSR-1 in parallel with the Form 10AB conversion to keep the CSR donor pipeline live.</p></div>
