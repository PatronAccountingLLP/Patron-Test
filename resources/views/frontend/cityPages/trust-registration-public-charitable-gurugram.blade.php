            <h2 class="section-title">Who Should Register a Public Charitable Trust</h2>
            <div class="content-text">
                
                <p>The public charitable trust route is suitable for the following situations in Gurugram:</p>
                <ul>
                    <li>A family that wants to convert annual giving into a structured philanthropy with a deed, trustees, and a charitable-purpose lock - distinct from a private family trust</li>
                    <li>A founder of an education, health, or relief initiative that needs Section 11 income exemption and Section 80G donor deduction to attract contributions</li>
                    <li>A CSR implementing agency under Section 135 of the Companies Act, 2013 - listed corporates require CSR-1 registration on the MCA portal for the implementing agency, which presupposes 12AB registration</li>
                    <li>A religious organisation (temple board, gurudwara committee, church trust) where the trust form sits more naturally than the society or Section 8 form</li>
                    <li>An education endowment or scholarship fund attached to an existing institution</li>
                    <li>An environment, animal welfare, or arts and culture initiative seeking institutional grants</li>
                </ul>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Statutory anchor:</strong> Registration is the prerequisite for the tax exemption - without Section 12AB registration there is no Section 11 exemption, and without Section 80G recognition donors cannot claim deduction. Form 10A must be filed within 1 month from the date the trust seeks registration. Section 13 of the Income-tax Act blocks the exemption if the trust's income or property is applied for the benefit of the settlor, trustees, or their relatives - the trust must serve the public, not a specific family or community.</p>
                </div>
                <div class="highlight-box" style="margin-top:16px;background:var(--orange-lighter);border-left:4px solid var(--orange);">
                    <p><strong>Critical Distinction (Public Charitable Trust vs Private Family Trust):</strong> A PRIVATE FAMILY TRUST under the Indian Trusts Act, 1882 is created for named family beneficiaries and is INELIGIBLE for Section 12A exemption. A PUBLIC CHARITABLE TRUST (this page) is created for a section of the public for charitable purposes and IS eligible for Section 12AB and 80G. The two routes use different deeds, different tax regimes (Sections 161-164 vs Sections 11-13), and different objectives.</p>
