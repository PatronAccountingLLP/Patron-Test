                <h2 class="section-title">What a Public Charitable Trust Actually Is</h2>
                <div class="content-text what-is-definition">
                    
                    
                    <p>A <strong>Public Charitable Trust</strong> is a trust established for a CHARITABLE OR RELIGIOUS PURPOSE for the benefit of the GENERAL PUBLIC (or a section of the public determined by an impersonal criterion like geography, profession, age, disease). Section 2(13) of the Maharashtra Public Trusts Act, 1950 defines a "public trust" as an express or constructive trust for either a public religious or charitable purpose or both.</p>
                    <p>This is fundamentally different from a PRIVATE TRUST (where beneficiaries are identified individuals or a family) - private trusts are governed by the Indian Trusts Act, 1882; public trusts in Maharashtra are governed by the Maharashtra Public Trusts Act 1950 with mandatory registration before the Charity Commissioner.</p>
                    <p>The "charitable purposes" recognised under Section 2(23) of the Income-tax Act 2025 (unchanged from Section 2(15) of the Income-tax Act 1961) include - relief of the poor, education, yoga, medical relief, preservation of environment, preservation of monuments and places or objects of artistic or historic interest, and the advancement of any other object of general public utility.</p>
                    <p>The dual registration framework comprises - (a) STATE-LEVEL Charity Commissioner registration under Section 18 Maharashtra Public Trusts Act 1950 - mandatory before commencing operations; (b) CENTRAL-LEVEL tax registration under Section 332 of the Income-tax Act 2025 (the RNPO framework that replaced Sections 12A / 12AA / 12AB / 10(23C) / 80G effective 1 April 2026) - mandatory for income-tax exemption and donor deductibility.</p>
                    

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Public Charitable Trust:</strong></p>
                    
                    <ul style="margin-top:8px;">
                        <li><strong>Public Trust:</strong> Trust for the benefit of the general public; Section 2(13) Maharashtra Public Trusts Act 1950.</li>
                        <li><strong>Charity Commissioner:</strong> Maharashtra state authority supervising public trusts; head office Lalbaug Mumbai; regional offices across Maharashtra.</li>
                        <li><strong>Section 18 (MPT Act 1950):</strong> Mandatory registration of every public trust with Charity Commissioner via Schedule II application.</li>
                        <li><strong>Section 35 (MPT Act 1950):</strong> Annual budget (Schedule IX A) and accounts (Schedule IX C) filing with Charity Commissioner.</li>
                        <li><strong>RNPO:</strong> Registered Non-Profit Organisation - new unified term under Income-tax Act 2025 Chapter XVII-B; replaces "charitable trust / institution" under old Act.</li>
                        <li><strong>Section 332 (IT Act 2025):</strong> Registration of RNPO - replaces Sections 12A / 12AA / 12AB / 10(23C) of Income-tax Act 1961.</li>
                        <li><strong>Section 354 (IT Act 2025):</strong> Donor approval - replaces Section 80G of Income-tax Act 1961.</li>
                        <li><strong>Section 355(g) (IT Act 2025):</strong> RNPO definition - any person with valid uncancelled registration under "specified provision".</li>
                        <li><strong>Section 355(m) read with Section 536(2):</strong> Transition - existing 12A / 12AB / 10(23C) registrations automatically continue as RNPO from 1 April 2026.</li>
                        <li><strong>Section 2(23) Charitable Purpose:</strong> Relief of poor, education, yoga, medical relief, preservation of environment, preservation of monuments, advancement of object of general public utility.</li>
                        <li><strong>85 Percent Application Rule:</strong> Section 336 IT Act 2025 - RNPO must apply at least 85 percent of regular income for charitable purposes annually.</li>
                        <li><strong>Form 10A:</strong> Provisional RNPO registration application (3-year validity).</li>
                        <li><strong>Form 10AB:</strong> Regular RNPO registration / renewal application (5-year; 10-year for small trusts).</li>
                        <li><strong>Form 10BD:</strong> Annual statement of donations received by RNPO (with donor PANs).</li>
                        <li><strong>Form 10BE:</strong> Donor certificate - issued by RNPO; required for donor to claim Section 354 deduction.</li>
                        <li><strong>FCRA Registration:</strong> Foreign Contribution Regulation Act 2010 - mandatory for trusts receiving foreign donations; SBI Sansad Marg Branch designated account.</li>
                        <li><strong>CSR-1:</strong> Form CSR-1 registration with MCA; mandatory to receive Section 135 Companies Act 2013 CSR funds.</li>
