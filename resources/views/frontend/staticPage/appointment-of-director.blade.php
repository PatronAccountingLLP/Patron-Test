            <div class="column-content">
                <h2 class="section-title">What is Appointment of Director?</h2>
                <div class="content-text what-is-definition">
                    
                    <p><strong>Appointment of Director</strong> is the formal legal process by which a company adds a new individual to its Board of Directors under the Companies Act 2013, governed by Section 152, requiring DIN, consent, board resolution, and MCA filing.</p>
<p>Directors are defined under Section 2(34) as individuals appointed to the board of a company. They function as the governing brain of the company, responsible for strategic direction, statutory compliance, and fiduciary duties to shareholders.</p>
<p><strong>Filing portal:</strong> <a href="https://www.mca.gov.in" target="_blank" rel="noopener">MCA V3 Portal (mca.gov.in)</a></p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Appointment of Director:</strong></p>
                    <p><strong>DIN (Director Identification Number)</strong> - Unique 8-digit number allotted by MCA under Section 154. Mandatory for all directors.</p>
<p><strong>DSC (Digital Signature Certificate)</strong> - Class 3 DSC required for signing MCA forms electronically.</p>
<p><strong>DIR-12</strong> - MCA e-form for reporting appointment, resignation, or change in designation within 30 days of the event.</p>
<p><strong>DIR-2</strong> - Consent letter in which the proposed director confirms willingness to act as director. Mandatory attachment with DIR-12.</p>
<p><strong>DIR-8</strong> - Declaration of non-disqualification under Section 164, signed by proposed director.</p>

