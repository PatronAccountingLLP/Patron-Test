                <h2 class="section-title">What Is an ASMT-10 Scrutiny Notice?</h2>
                <div class="content-text what-is-definition">
                    
                    <p>ASMT-10 is a scrutiny notice issued under Section 61 of the Central Goods and Services Tax Act 2017 read with Rule 99 of the CGST Rules 2017, when the proper officer identifies discrepancies in the returns filed by a registered taxpayer. It is NOT a demand notice for recovery of tax, interest, or penalty - it is a formal inquiry asking the taxpayer to explain the discrepancies noticed during scrutiny.</p>
                    <p>The notice typically includes an annexure detailing the exact mismatch parameters, the period in question, and the quantified amount of tax the officer believes is at issue. The taxpayer must reply in Form ASMT-11 within 30 days of receipt, either explaining the discrepancy ground-by-ground with reconciliation evidence, or accepting it and paying the differential tax with interest via DRC-03.</p>
                    <p>If the officer is satisfied with the explanation, a closure order is issued in Form ASMT-12 under Section 61(2) and no further action is taken. If the explanation is not satisfactory, the officer may escalate the matter to Section 65 audit, Section 67 inspection, Section 73/74/74A Show Cause Notice, or Section 63 best judgment assessment. The Madras High Court has held that ASMT-10 is a mandatory pre-condition for a DRC-01 SCN under Section 73 or 74 - if the SCN is issued on grounds completely different from the ASMT-10, the entire proceedings are vitiated.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for ASMT-10 Notice:</strong></p>
                    <ul>
                        <li><strong>Section 61 CGST Act:</strong> Empowers the proper officer to scrutinize returns and related particulars, communicate discrepancies, and seek explanation. Section 61(2) provides that if the explanation is acceptable, no further action is taken; Section 61(3) permits escalation to audit, inspection, or demand proceedings.</li>
                        <li><strong>Rule 99 CGST Rules:</strong> Procedural rule prescribing the scrutiny notice in Form GST ASMT-10, the reply in Form GST ASMT-11, and the closure or escalation route.</li>
                        <li><strong>Form GST ASMT-10:</strong> Scrutiny notice communicating discrepancies in GSTR-1, GSTR-3B, GSTR-2A/2B, GSTR-9, GSTR-9C, e-way bills, e-invoices, or refunds. Specifies the period, parameter, and quantified amount.</li>
                        <li><strong>Form GST ASMT-11:</strong> Reply by the registered person within 30 days of service. Filed electronically on the GST portal under Services, User Services, View Additional Notices and Orders.</li>
                        <li><strong>Form GST ASMT-12:</strong> Closure order issued under Section 61(2) when the officer is satisfied with the reply - formal disposal of the scrutiny.</li>
                        <li><strong>Form GST DRC-03:</strong> Voluntary payment intimation used when the taxpayer accepts the discrepancy and pays differential tax plus interest under Section 50.</li>
                        <li><strong>Section 63 Best Judgment Assessment:</strong> If ASMT-11 is not filed within the window, the officer can determine tax based on available data without taxpayer input, then issue a Section 73 or 74 SCN.</li>
                        <li><strong>Madras HC Mandatory Pre-Condition:</strong> ASMT-10 is a mandatory pre-condition for a DRC-01 SCN; if the SCN raises grounds completely different from the ASMT-10, the entire proceedings (including the DRC-07 order) are vitiated.</li>
