                    <div class="faq-expanded__item" id="faq-1">
                        <h3 class="faq-expanded__q">What is an ASMT-10 notice in GST?</h3>
                        <div class="faq-expanded__a"><p>An ASMT-10 notice is a scrutiny notice issued under Section 61 of the CGST Act 2017 read with Rule 99 of the CGST Rules when the proper officer identifies discrepancies in the GST returns filed by a registered taxpayer. It is NOT a demand notice for recovery of tax, interest, or penalty - it is a formal inquiry asking the taxpayer to explain the discrepancies noticed during scrutiny. The notice includes an annexure detailing the exact mismatch parameters, the period in question, and the quantified amount of tax the officer believes is at issue. Common triggers include ITC mismatch between GSTR-3B and GSTR-2B, tax payment mismatch between GSTR-1 and GSTR-3B, e-way bill values exceeding GSTR-1 outward supplies, and ineligible Section 17(5) blocked credit claims.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-2">
                        <h3 class="faq-expanded__q">What is the deadline to reply to an ASMT-10 notice?</h3>
                        <div class="faq-expanded__a"><p>The taxpayer must file Form ASMT-11 within 30 days of receipt of the ASMT-10 notice under Rule 99(2) of the CGST Rules. The 30-day clock starts from the date of service of the notice as reflected on the GST portal. Extension can be requested in the same ASMT-11 form (no separate form is prescribed). If no reply is filed within the prescribed window, the proper officer can proceed to best judgment assessment under Section 63 of the CGST Act, followed by a Section 73 or Section 74 Show Cause Notice. The Day-1 response discipline is critical to preserving all defence options.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-3">
                        <h3 class="faq-expanded__q">What happens if I do not reply to an ASMT-10 notice?</h3>
                        <div class="faq-expanded__a"><p>If no ASMT-11 reply is filed within the 30-day window, the proper officer can escalate the matter on several tracks. The most common is Section 63 best judgment assessment - the officer determines tax based on available data without taxpayer input, then issues a Section 73 or Section 74 SCN. Other escalation routes include Section 65 departmental audit, Section 66 special audit, and Section 67 inspection and seizure (in fraud-suspicion cases). Penalty exposure at the SCN stage is 10 percent under Section 73 (non-fraud) or 100 percent under Section 74 (fraud) - significantly higher than the zero penalty if resolved at ASMT-10 stage.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-4">
                        <h3 class="faq-expanded__q">What is Form ASMT-12?</h3>
                        <div class="faq-expanded__a"><p>Form ASMT-12 is the closure order issued by the proper officer under Section 61(2) of the CGST Act when satisfied with the taxpayer ASMT-11 reply. It constitutes the formal disposal of the scrutiny - no further action is taken on the discrepancies flagged in the ASMT-10. ASMT-12 is the desired outcome of any ASMT-10 engagement. If the officer is not satisfied with the explanation, instead of ASMT-12 the matter escalates to one of the demand-side or audit-side provisions under Section 61(3).</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-5">
                        <h3 class="faq-expanded__q">Can I pay tax voluntarily on an ASMT-10 discrepancy?</h3>
                        <div class="faq-expanded__a"><p>Yes. If the discrepancy flagged in the ASMT-10 is genuine and the taxpayer accepts the differential tax liability, voluntary payment can be made via Form DRC-03 under Rule 142(2) of the CGST Rules. The payment includes tax plus interest under Section 50 at 18 percent per annum from the original due date. The DRC-04 acknowledgement is then attached to the ASMT-11 reply to demonstrate acceptance. Voluntary payment at the ASMT-10 stage attracts ZERO penalty - a significant advantage over paying after a Section 73 SCN (10 percent penalty) or Section 74 SCN (100 percent penalty).</p></div>
