            <h2 class="section-title">Legal and Regulatory Framework for Bank Reconciliation</h2>
            <div class="content-text">
                
                <p>What bank reconciliation protects is the most basic claim any set of books makes - that the cash it reports actually exists - and Section 128's true-and-fair requirement rests on it. A lender reading the balance sheet, or an auditor testing it, starts at the bank line, so a ledger that disagrees with the statement fails at the first check.</p>
<p>The framework treats the reconciliation as the bridge between an external record and the books. Every statement line is matched to a ledger entry, and the gaps have names: uncleared payments, gateway lags, card settlements in transit. That is why the <a href="/glossary/bank-clearing-account">Bank Clearing Account</a> exists to hold timing differences and why <a href="/glossary/outstanding-checks-timing-differences">Outstanding Checks / Timing Differences</a> are tracked rather than written off. Bank reconciliation services keep the cash position honest against the provisions below.</p>
<ul class="al-list"><li><span class="al-t"><strong>Section 128, Companies Act 2013</strong></span><span class="al-d">The books are kept true and fair, and the reported bank balance must agree with the statement it claims to reflect - the basis of <a href="/glossary/bank-reconciliation">Bank Reconciliation</a>.</span></li><li><span class="al-t"><strong>Section 128 and Section 44AA, Income-tax Act 1961</strong></span><span class="al-d">The double-entry obligation applies to every entity, so the cash and card ledgers of companies and non-corporates alike are reconciled the same way.</span></li><li><span class="al-t"><strong>Section 44AA, Income-tax Act 1961</strong></span><span class="al-d">A non-corporate business maintains books once the limits are crossed, and an accurate bank position is the foundation of those books.</span></li><li><span class="al-t"><strong>Section 44AB, Income-tax Act 1961</strong></span><span class="al-d">Where a tax audit applies, unreconciled bank differences are exactly what the auditor is required to probe, so they are cleared first.</span></li><li><span class="al-t"><strong>Rule 3(1), Companies (Accounts) Rules 2014</strong></span><span class="al-d">The audit trail stays enabled, so a reclassified receipt or a corrected charge is logged. A long-neglected account is first rebuilt through <a href="/backlog-bookkeeping-catch-up-services">catch-up bookkeeping</a>.</span></li></ul>
