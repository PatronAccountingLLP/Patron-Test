                    <div class="faq-expanded__item" id="faq-1">
                        <h3 class="faq-expanded__q">Who must include a CARO 2020 Annexure in the audit report?</h3>
                        <div class="faq-expanded__a"><p>Every Indian company and foreign company (as defined under Section 2(42) Companies Act 2013) must include a CARO 2020 Annexure, except - banking companies (under Banking Regulation Act 1949), insurance companies (under Insurance Act 1938), Section 8 companies, One Person Companies, small companies (under Section 2(85) - updated to Rs 10 crore paid-up + Rs 100 crore turnover via G.S.R. 880(E) dated 1 December 2025), and private limited companies meeting all 4 strict conditions (not subsidiary/holding of public co; paid-up + reserves not over Rs 1 crore; bank/FI borrowings not over Rs 1 crore at any time; revenue not over Rs 10 crore).</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-2">
                        <h3 class="faq-expanded__q">What is the 4-condition exemption for Private Limited Companies?</h3>
                        <div class="faq-expanded__a"><p>Under CARO 2020 Para 1(vi), a private limited company is exempt from CARO if it meets ALL four conditions simultaneously - (1) it is not a subsidiary or holding company of a public company; (2) paid-up capital plus reserves and surplus as on the Balance Sheet date does not exceed Rs 1 crore; (3) total borrowings from any bank or financial institution do not exceed Rs 1 crore at any point during the financial year; (4) total revenue under Schedule III (including discontinuing operations) does not exceed Rs 10 crore during the FY. Failing any one condition makes CARO applicable.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-3">
                        <h3 class="faq-expanded__q">Does CARO 2020 apply to LLPs?</h3>
                        <div class="faq-expanded__a"><p>No. CARO 2020 applies only to companies registered under the Companies Act 2013 (and foreign companies under Section 2(42)). Limited Liability Partnerships (LLPs) are registered under the LLP Act 2008 and are not 'companies' under the Companies Act framework. LLP audits conducted under Rule 24(8) of the LLP Rules 2009 do not include a CARO Annexure. This is a clean structural distinction; do not extend CARO clause logic to LLP audits.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-4">
                        <h3 class="faq-expanded__q">What is new in CARO 2020 compared to CARO 2016?</h3>
                        <div class="faq-expanded__a"><p>CARO 2020 expanded from 16 clauses (CARO 2016) to 21 clauses with several new requirements - benami transactions disclosure (1(e)), revaluation of PPE (1(d)), working capital limits from banks (2(b)), wilful defaulter declaration (9(b)), end-use of term loans (9(c)-(f)), undisclosed income in IT search (8), whistleblower complaints (11(c)), resignation of statutory auditors (18), going concern material uncertainty (19), and CSR unspent transfer (20). Reporting is more granular and entity-specific; boilerplate clauses no longer pass NFRA scrutiny.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-5">
                        <h3 class="faq-expanded__q">How is CARO 2020 connected to Section 143(12) fraud reporting?</h3>
                        <div class="faq-expanded__a"><p>CARO Clause 11 reports on three sub-clauses - (a) whether the auditor has noticed any fraud by or on the company during the year; (b) whether the auditor has filed a Form ADT-4 with the Central Government for fraud above Rs 1 crore under Section 143(12) read with Rule 13 of Companies (Audit and Auditors) Rules 2014; (c) whether whistleblower complaints were received and considered by the auditor. Clause 11(b) is a direct cross-reference to Section 143(12) - the auditor's statutory duty to report material fraud to the Government within 60 days of knowledge.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-6">
                        <h3 class="faq-expanded__q">What happens if CARO 2020 clauses are qualified or adverse?</h3>
                        <div class="faq-expanded__a"><p>A qualified or adverse CARO clause does not automatically qualify the audit opinion under SA 700 - they are reported separately. However, qualifications can trigger - NFRA financial reporting quality review attention; SEBI scrutiny for listed companies; lender concerns affecting renewal of facilities; investor and acquirer diligence findings; ICAI Peer Review attention; Section 147 penalty exposure where the qualification reveals non-compliance with Companies Act provisions. Recurring CARO qualifications indicate poor compliance hygiene and reduce audit quality scores.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-7">
                        <h3 class="faq-expanded__q">Does CARO 2020 apply to consolidated financial statements (CFS)?</h3>
                        <div class="faq-expanded__a"><p>Per CARO 2020 Para 2, the Order does NOT apply to the audit of consolidated financial statements EXCEPT Clause 21. Clause 21 requires the principal auditor of the CFS to disclose qualifications or adverse remarks made in the CARO Annexures of subsidiary auditors' reports. So at the CFS level, only Clause 21 reporting applies; the other 20 clauses are only at standalone entity level. This was a clarificatory change vs CARO 2016.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-8">
                        <h3 class="faq-expanded__q">How has Small Company definition update affected CARO applicability?</h3>
                        <div class="faq-expanded__a"><p>MCA Notification G.S.R. 880(E) dated 1 December 2025 updated the Small Company definition under Section 2(85) - paid-up capital up to Rs 10 crore AND turnover up to Rs 100 crore (previously Rs 4 crore + Rs 40 crore). Small Companies are exempt from CARO 2020 under Para 1(v). The expanded threshold means many more Pvt Ltds become Small Companies and exit CARO from FY 2025-26 audit cycle onwards. However, a Pvt Ltd that is a holding or subsidiary of a public company cannot qualify as Small Company even if below the thresholds - and may still need to test the Pvt Ltd 4-condition exemption.</p></div>
