<section class="content-section" id="who-section">
    <div class="content-container">
        <div class="text-content">
            <h2 class="section-title">Who Needs CARO 2020 Advisory? 6 Exemption Routes Explained</h2>
            <div class="content-text">
                
                <p>CARO 2020 applies to every company - Indian and foreign (Section 2(42)) - except via one of 6 enumerated exemption routes in Para 1 of the Order. Auditors must apply the exemption test before assuming CARO does not apply; documentation of the exemption basis is itself an audit working paper.</p>

<h3 style="margin-top:24px;">Exemption Route 1 - Banking Company</h3>
<p>A banking company as defined in Section 5(c) of the Banking Regulation Act 1949 - banks face Banking Regulation Act audit framework instead of CARO.</p>

<h3 style="margin-top:16px;">Exemption Route 2 - Insurance Company</h3>
<p>An insurance company as defined in the Insurance Act 1938 - regulated by IRDAI with sector-specific audit framework.</p>

<h3 style="margin-top:16px;">Exemption Route 3 - Section 8 Company</h3>
<p>A company licensed to operate under Section 8 of the Companies Act 2013 (non-profit company with charitable objects). The exemption recognises the non-commercial nature; however such companies still face Section 8 specific audit framework and FCRA audit if applicable.</p>

<h3 style="margin-top:16px;">Exemption Route 4 - One Person Company (OPC)</h3>
<p>An OPC as defined in Section 2(62) of the Companies Act 2013. Threshold for OPC is paid-up capital under Rs 50 lakh and turnover under Rs 2 crore; above these the OPC must convert to private/public company. OPCs continue to require statutory audit but without CARO Annexure.</p>

<h3 style="margin-top:16px;">Exemption Route 5 - Small Company (Updated December 2025)</h3>
<p>A small company as defined in Section 2(85) Companies Act 2013. The definition was significantly liberalised by <strong>G.S.R. 880(E) dated 1 December 2025</strong> - paid-up capital up to <strong>Rs 10 crore AND turnover up to Rs 100 crore</strong> (previously Rs 4 crore + Rs 40 crore). Both conditions must be met. This expansion brings many more companies into the Small Company bracket and out of CARO 2020 from FY 2025-26 audit cycle onwards. Holding/subsidiary of public co cannot qualify as Small Company even if below thresholds.</p>

<h3 style="margin-top:16px;">Exemption Route 6 - Pvt Ltd 4-Condition Test</h3>
<p>A private limited company that meets ALL four conditions:</p>
<ul>
    <li>Not a subsidiary or holding company of a public company</li>
    <li>Paid-up capital plus reserves and surplus not more than <strong>Rs 1 crore</strong> as on the Balance Sheet date</li>
    <li>Total borrowings from any bank or financial institution not more than <strong>Rs 1 crore</strong> at any point of time during the financial year</li>
    <li>Total revenue (as disclosed under Schedule III including revenue from discontinuing operations) not exceeding <strong>Rs 10 crore</strong> during the financial year</li>
</ul>
<p>Note this is stricter than Small Company route 5; many Pvt Ltds qualify under route 5 (since December 2025) before needing route 6.</p>

<div class="highlight-box" style="margin-top:24px;">
<p><strong>LLPs are NOT covered by CARO 2020</strong> because LLPs are not companies - they are registered under the LLP Act 2008, not the Companies Act 2013. LLP audits under Rule 24(8) of LLP Rules 2009 do not include a CARO Annexure. This is a clean structural distinction; do not extend CARO clause logic to LLP audits.</p>
</div>

