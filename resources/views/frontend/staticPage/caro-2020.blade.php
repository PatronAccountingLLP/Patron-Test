                <h2 class="section-title">What Is CARO 2020?</h2>
                <div class="content-text what-is-definition">
                    
                    <p>The <strong>Companies (Auditor's Report) Order 2020 (CARO 2020)</strong> is a regulatory order issued by the Ministry of Corporate Affairs under Section 143(11) of the Companies Act 2013, notified vide <strong>G.S.R. 130(E) dated 25 February 2020</strong>. It prescribes 21 specific matters on which the statutory auditor of a company must report by way of an Annexure to the audit report under Section 143(3).</p>
<p>CARO 2020 applies to every Indian company (incorporated under the Companies Act 2013) and every foreign company (under Section 2(42)) EXCEPT through one of <strong>6 enumerated exemption routes</strong> in Para 1 of the Order - (1) banking companies under Banking Regulation Act 1949, (2) insurance companies under Insurance Act 1938, (3) Section 8 companies, (4) One Person Companies (OPC) under Section 2(62), (5) small companies under Section 2(85), and (6) private limited companies meeting a strict 4-condition test under Para 1(vi).</p>
<p>The most significant recent development is <strong>G.S.R. 880(E) dated 1 December 2025</strong> which updated the Small Company definition under Section 2(85) - paid-up capital up to Rs 10 crore AND turnover up to Rs 100 crore (previously Rs 4 crore + Rs 40 crore). This expansion brings many more Pvt Ltds into the Small Company bracket and out of CARO from FY 2025-26 audit cycle onwards.</p>
<p>The CARO Annexure forms part of the statutory audit report under SA 700 and is signed with the same UDIN. CARO findings are public-domain documents filed with ROC as part of Form AOC-4 within 30 days of AGM, and are reviewed in lender diligence, investor due diligence, regulator inspections (including NFRA for PIEs), and tax assessments. Recurring CARO qualifications attract NFRA financial reporting quality review attention and ICAI Peer Review scrutiny.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for CARO 2020:</strong></p>
                    <p><strong>Section 143(11) Companies Act 2013:</strong> Statutory authority empowering the Central Government, in consultation with NFRA, to direct auditors to report on specified matters - CARO is the order issued under this section.</p>
<p><strong>G.S.R. 130(E) dated 25 February 2020:</strong> MCA notification number for CARO 2020 - the foundational text.</p>
<p><strong>G.S.R. 880(E) dated 1 December 2025:</strong> MCA notification updating Small Company definition under Section 2(85) to Rs 10 crore paid-up + Rs 100 crore turnover - expanding CARO Small Co exemption route.</p>
<p><strong>Pvt Ltd 4-Condition Test (Para 1(vi)):</strong> All four required - not subsidiary/holding of public co; paid-up + reserves up to Rs 1 crore; bank/FI borrowings up to Rs 1 crore at any time; revenue up to Rs 10 crore.</p>
<p><strong>Wilful Defaulter (Clause 9(b)):</strong> As declared by any bank, financial institution, or other lender per RBI Master Direction on Wilful Defaulters; auditor must report whether the company is declared a wilful defaulter.</p>
<p><strong>Benami Transactions (Clause 1(e)):</strong> Whether any proceedings have been initiated or are pending against the company for holding benami property under the Benami Transactions (Prohibition) Act 1988 (as amended).</p>
<p><strong>Form ADT-4 (Clause 11(b)):</strong> The form prescribed under Rule 13 of Companies (Audit and Auditors) Rules 2014 for auditor to report fraud above Rs 1 crore to the Central Government under Section 143(12).</p>
<p><strong>Material Uncertainty - Going Concern (Clause 19):</strong> Whether material uncertainty exists on the date of audit report indicating that the company is unable to meet its liabilities as and when they fall due within 1 year from the BS date.</p>
<p><strong>CSR Unspent (Clause 20):</strong> Transfer of unspent CSR amount per Section 135(5) and 135(6) of Companies Act 2013 - to Schedule VII Fund within 6 months for non-ongoing projects; to special account within 30 days for ongoing projects.</p>
<p><strong>CFS Applicability (CARO Para 2):</strong> Only Clause 21 of CARO applies to the audit of consolidated financial statements (qualifications/adverse remarks in subsidiary CARO reports to be reproduced); other clauses do not apply at CFS level.</p>

