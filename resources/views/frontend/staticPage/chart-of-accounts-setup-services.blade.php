<p>That is why the design is a compliance exercise, not a naming one. The heads have to carry the input-tax split, the TDS nature of a payment and the Schedule III grouping all at once, which is why a <a href="/glossary/segmented-chart-of-accounts">Segmented Chart of Accounts</a> and a clean <a href="/glossary/parent-child-ledger-hierarchy">Parent-Child Ledger Hierarchy</a> matter more than tidy labels. Chart of accounts setup builds that structure to satisfy the provisions below from the first entry.</p>
