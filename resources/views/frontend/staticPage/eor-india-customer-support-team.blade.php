<h3 style="color:var(--blue);margin-top:16px;">Code on Wages 2019</h3>
<p>Effective 21 November 2025. Defines minimum wage, timely payment of wages by the 7th of succeeding month under Section 17. Overtime at 2x wages for work beyond 48 hours per week or 9 hours per day.</p>
<h3 style="color:var(--blue);margin-top:16px;">Code on Social Security 2020</h3>
