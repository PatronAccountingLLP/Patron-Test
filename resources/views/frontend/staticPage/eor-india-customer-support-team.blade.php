<h3 style="color:var(--blue);margin-top:16px;">EPF and MP Act 1952</h3>
<p>Employer contributes 12 percent of Basic + DA to PF on wage ceiling Rs 15,000. Late deposit attracts 12 percent annual interest under Section 7Q plus damages under Section 14B. Reference: <a href="https://www.epfindia.gov.in" target="_blank" rel="noopener">EPFO official portal</a>.</p>
<h3 style="color:var(--blue);margin-top:16px;">ESI Act 1948</h3>
<p>Employer contributes 3.25 percent and employee 0.75 percent on gross wages up to Rs 21,000 per month. Critical health cover for night-shift agents. Penalty up to 6 months imprisonment under Section 85. Reference: <a href="https://www.esic.gov.in" target="_blank" rel="noopener">ESIC official portal</a>.</p>
