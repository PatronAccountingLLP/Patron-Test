<p>Effective 21 November 2025. Caps daily working hours at 8 (exclusive of breaks) and total spread-over at 12 hours. Section 43 governs employment of women, including night shift permission with consent and safety conditions. Subsumes Factories Act 1948 and most state Shops and Establishments Acts on working hours. Reference: <a href="https://labour.gov.in" target="_blank" rel="noopener">Ministry of Labour and Employment</a>.</p>
