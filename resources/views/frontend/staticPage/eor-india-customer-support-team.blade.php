                <h2 class="section-title">What Is an EOR for India Customer Support Teams</h2>
                <div class="content-text what-is-definition">
                    
                    <p>An Employer of Record (EOR) for India customer support teams is a locally registered Indian entity that legally employs your voice, chat, and email agents under the Code on Wages 2019, the Code on Social Security 2020, and the OSH Code 2020, while you direct their work day to day.</p>
<p>The EOR signs the India-compliant employment contract, files monthly Provident Fund returns under the EPF and MP Act 1952, deposits Employees State Insurance under the ESI Act 1948, deducts and remits TDS under Section 192 of the Income Tax Act 1961, accrues gratuity at 4.81 percent under the Payment of Gratuity Act 1972, and obtains state-specific night shift exemptions under the applicable Shops and Establishments Act. The model removes Permanent Establishment exposure for the foreign parent company because the EOR (not the parent) is the employer in India.</p>
<p>Foreign hirers retain authorship of scripts, set SLAs, run QA, and operate the CRM. The EOR carries every Indian statutory liability, from minimum wage adherence under state-notified schedules to PoSH Internal Complaints Committee setup to Karnataka Form R or Maharashtra Rule 2018 consent collection for night shifts.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for EOR Customer Support:</strong></p>
                    <ul>
  <li><strong>EOR (Employer of Record):</strong> Third-party Indian entity that becomes the legal employer for payroll, PF, ESI, TDS, and Shops Act compliance while client controls work.</li>
  <li><strong>BPO (Business Process Outsourcing):</strong> Vendor model where the BPO company manages agents, processes, and outcomes. Client buys output (calls answered, tickets resolved), not headcount.</li>
  <li><strong>Captive Centre:</strong> Wholly-owned Indian subsidiary running its own customer support team. Requires entity setup, FDI compliance, transfer pricing.</li>
  <li><strong>ITeS (IT Enabled Services):</strong> Statutory category under most state Shops Acts that grants exemption from Section 66 night shift bar. Customer support typically qualifies.</li>
  <li><strong>Spread-over:</strong> Total elapsed time from start to end of a shift including breaks. OSH Code Section 25 caps spread-over at 12 hours regardless of actual work hours.</li>
  <li><strong>PoSH Act 2013:</strong> Sexual Harassment of Women at Workplace (Prevention, Prohibition and Redressal) Act 2013. Mandatory ICC for any establishment with 10+ employees.</li>
</ul>

