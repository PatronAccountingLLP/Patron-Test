            <h2 class="section-title">Legal and Regulatory Framework for ESOP Accounting</h2>
            <div class="content-text">
                
                <p>ESOP numbers are kept to Ind AS 102, and that standard is made binding through the Companies Act's requirement that the financial statements be true and fair. The standard treats an option granted to an employee as a cost of their service, so a scheme that pays nothing in cash still leaves a charge in the profit and loss.</p>
<p>The framework then splits into two clocks that rarely align: the accounting charge spread over the vesting period and the tax event that lands only on exercise. That is why <a href="/glossary/accounting/vesting-period-graded-amortization">Vesting Period Graded Amortization</a> governs the books while a separate perquisite arises for the employee, and why the choice against an <a href="/glossary/accounting/esop-intrinsic-value-method">ESOP Intrinsic Value Method</a> matters for what gets disclosed. ESOP accounting keeps both clocks and the disclosures aligned against the provisions below.</p>
<ul class="al-list"><li><span class="al-t"><strong>Ind AS 102, Share-based Payment</strong></span><span class="al-d">The fair value of options at grant is charged to profit and loss over the vesting period, with graded vesting recognised tranche by tranche - the heart of <a href="/glossary/accounting/ind-as-102-share-based-payment">Ind AS 102 Share-based Payment</a>. Companies on AS instead follow ICAI's Guidance Note on Accounting for Share-based Payments (revised 2020).</span></li><li><span class="al-t"><strong>Section 17(2)(vi) with Rule 3(8), Income-tax Act 1961</strong></span><span class="al-d">The gap between fair market value on exercise and the exercise price is a perquisite in the employee's hands, with TDS deducted under Section 192.</span></li><li><span class="al-t"><strong>Section 192(1C)/191(2), Income-tax Act 1961</strong></span><span class="al-d">An eligible startup may defer the perquisite TDS to the earliest of five years, sale, or exit, easing the employee's cash position.</span></li><li><span class="al-t"><strong>Schedule III with the Guidance Note on Share-based Payments</strong></span><span class="al-d">The outstanding options, weighted exercise price and the year's charge are disclosed in the notes to accounts.</span></li><li><span class="al-t"><strong>Companies (Accounting Standards) Rules 2021 and Companies (Indian Accounting Standards) Rules 2015</strong></span><span class="al-d">The standard that applies follows the company's framework, with Ind AS 102 binding once the company is on Ind AS.</span></li><li><span class="al-t"><strong>Section 128, Companies Act 2013</strong></span><span class="al-d">The books carrying the charge stay on accrual and double entry, retained for eight years. A neglected scheme is first rebuilt through <a href="/backlog-bookkeeping-catch-up-services">catch-up bookkeeping</a>.</span></li></ul>
