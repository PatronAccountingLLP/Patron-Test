    <div class="content-container">
        <div class="text-content">
            <h2 class="section-title">Who Needs to File Form 10B?</h2>
            <div class="content-text">
                <p>An audit report is mandatory for any Section 12A/12AB trust or institution whose total income - computed before applying the exemptions of Sections 11 and 12 - exceeds the basic exemption limit. Among those trusts, Form 10B (rather than Form 10BB) is required in any of the following situations:</p>
                <ul>
                    <li><strong>Total income above Rs 5 crore:</strong> If the total income of the trust or institution, computed without giving effect to Sections 11 and 12 (or the relevant clauses of Section 10(23C)), exceeds Rs 5 crore during the previous year, Form 10B is mandatory.</li>
                    <li><strong>Foreign contribution received:</strong> If the trust or institution received any foreign contribution during the previous year, Form 10B is mandatory irrespective of income level. This aligns your income tax reporting with your FCRA compliance obligations.</li>
                    <li><strong>Income applied outside India:</strong> If the trust or institution applied any part of its income outside India during the previous year, Form 10B is mandatory irrespective of income level.</li>
                    <li><strong>Section 10(23C) approved institutions:</strong> Universities, educational institutions, hospitals and other institutions approved under Section 10(23C) that cross the same thresholds also fall within the Form 10B requirement.</li>
                    <li><strong>Large charitable and religious trusts:</strong> Public charitable trusts, religious trusts and societies registered under Section 12A/12AB with substantial receipts or grants.</li>
                    <li><strong>NGOs with cross-border activity:</strong> Non-profit organisations engaged in overseas relief, research collaborations or grants that involve applying income outside India.</li>
                </ul>
                <p style="margin-top:16px;">Any eligible trust or institution that does not fall within these three Form 10B triggers files its audit report in the simpler <a href="/form-10bb-audit-report-section-1023c-institution">Form 10BB</a> instead. If you are unsure which applies, Patron's CA team reviews your accounts and activity profile and confirms the correct form before filing.</p>
