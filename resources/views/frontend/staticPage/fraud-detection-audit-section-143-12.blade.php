            <h2 class="section-title">Legal and Compliance Framework</h2>
            <div class="content-text">
                
                <p><strong>Section 143(12) Companies Act 2013 (PRIMARY ANCHOR) -</strong> If an auditor, in the course of performance of duties, has reason to believe an offence involving fraud is being or has been committed against the company by officers or employees, the auditor shall immediately report the matter to the Central Government within prescribed time and manner. <strong>Section 143(13)</strong> - good faith protection (no duty regarded as contravened by reason of good-faith reporting). <strong>Section 143(14)</strong> - applies to cost auditors and Section 138 internal auditors. <strong>Section 143(15)</strong> - penalty for failure to report (Rs 1 lakh to Rs 25 lakh listed; Rs 1 lakh to Rs 5 lakh unlisted). MCA21: <a href="https://www.mca.gov.in" target="_blank" rel="noopener">mca.gov.in</a>.</p>
<p><strong>Rule 13 Companies (Audit and Auditors) Rules 2014 -</strong> Procedure for reporting fraud under Section 143(12) - Rs 1 crore threshold; Form ADT-4 format; 60-day filing window; Audit Committee/Board for below Rs 1 crore with 45-day response.</p>
<p><strong>Section 134(5)(c) -</strong> Director Responsibility Statement on fraud prevention. <strong>Section 138</strong> - internal audit applicability (listed; unlisted public Rs 50 cr+ paid-up or Rs 200 cr+ turnover; private Rs 200 cr+ turnover or Rs 100 cr+ loans). <strong>Section 177 / 177(9) / 177(10)</strong> - Audit Committee; Vigil Mechanism mandatory for listed and prescribed classes; disclosure in Board Report. <strong>Rule 7 Companies (Meetings of Board) Rules 2014</strong> - prescribed classes for mandatory vigil mechanism.</p>
<p><strong>Section 447 -</strong> Punishment for fraud - imprisonment minimum 6 months extending to 10 years (minimum 3 years where public interest); fine 1 to 3 times the amount; non-bailable for amounts &gt;= Rs 10 lakh or 1% of turnover (whichever lower). <strong>Sections 448/449</strong> - false statement/false evidence. <strong>Sections 211/212</strong> - SFIO establishment and investigation.</p>
<p><strong>ICAI Standards -</strong> SA 240 (PRIMARY - auditor's fraud responsibility; two categories; fraud triangle); SA 250 (laws and regulations); SA 260 (Revised - TCWG communication); SA 315 (risk assessment); SA 330 (response to risks); SA 700 (opinion). ICAI Guidance Note on Reporting on Fraud (Revised 2016). ICAI: <a href="https://www.icai.org" target="_blank" rel="noopener">icai.org</a>.</p>
<p><strong>NFRA Circular 26 June 2023 -</strong> Auditor must file ADT-4 even where not first to identify fraud; resignation does not absolve responsibility; conservative threshold interpretation. NFRA: <a href="https://www.nfra.gov.in" target="_blank" rel="noopener">nfra.gov.in</a>.</p>
<p><strong>CARO 2020 Clause 3(xi) -</strong> Three-part reporting - (a) nature and amounts of any fraud BY and ON the company noticed or reported during the year; (b) whether ADT-4 has been filed under Section 143(12); (c) whether the auditor has considered whistleblower complaints received during the year. <strong>PMLA 2002</strong> - Section 447 fraud is a scheduled offence. <strong>SEBI LODR Regulation 30</strong> - listed entity material event disclosure (24-hour notification for material fraud).</p>

