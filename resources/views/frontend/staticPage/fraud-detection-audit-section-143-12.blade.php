            <h2 class="section-title">When Does Section 143(12) Reporting Apply</h2>
            <div class="content-text">
                
                <p>Section 143(12) reporting applies in two distinct scenarios.</p>
<h3 style="margin-top:20px;">Scenario A: Fraud Identified During Statutory Audit</h3>
<p>The statutory auditor, during the course of performing audit duties under Section 143, comes across facts and circumstances that lead to 'reason to believe' that an offence involving fraud is being or has been committed against the company by officers or employees. Common triggers - unexplained ledger entries; missing supporting documentation; vendor/customer confirmations that don't reconcile; ageing anomalies; bank reconciliation discrepancies; payroll ghost employees; expense reimbursement irregularities; cash transactions exceeding thresholds; non-arm's-length related-party transactions.</p>
<h3 style="margin-top:20px;">Scenario B: Whistleblower Complaint or Management Disclosure</h3>
<p>The company has received a whistleblower complaint through the Section 177(9) Vigil Mechanism, or management has disclosed fraud to the auditor. Per NFRA Circular 26 June 2023, the reporting obligation applies EVEN WHERE the auditor is not the first to identify the fraud - if reasonable auditor judgment supports 'reason to believe' that fraud occurred, ADT-4 filing is required.</p>
