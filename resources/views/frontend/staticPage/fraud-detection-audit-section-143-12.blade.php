                <h2 class="section-title">What Is Fraud Detection Audit Under Section 143(12)</h2>
                <div class="content-text what-is-definition">
                    
                    <p>Fraud detection audit under Section 143(12) of the Companies Act, 2013 refers to the statutory auditor's mandatory responsibility - during the performance of audit duties - to report to the Central Government any fraud or suspected fraud against the company by officers or employees. The provision uses the 'reason to believe' threshold - the auditor need not have proven the fraud, but must have reason to believe an offence involving fraud is being or has been committed. Reporting is mandatory regardless of the general audit materiality threshold, with the Rs 1 crore threshold under Rule 13 only determining the recipient (Central Government via Form ADT-4 above Rs 1 crore; Audit Committee/Board below).</p>
<p>Under ICAI Standard on Auditing SA 240, fraud is an intentional act by one or more individuals among management, those charged with governance, employees, or third parties, involving the use of deception to obtain an unjust or illegal advantage. SA 240 distinguishes two categories - (a) fraudulent financial reporting; (b) misappropriation of assets. Section 143(12) targets the second category primarily, though the language is sometimes interpreted to also cover fraudulent financial reporting where it injures the company itself.</p>
<p>The auditor's responsibility under SA 240 begins with risk assessment under SA 315 - the auditor identifies and assesses risks of material misstatement due to fraud at financial-statement level and assertion level, and maintains professional skepticism throughout the audit. Specific procedures include inquiries of management, engagement-team discussion, consideration of fraud risk factors (the 'fraud triangle' - incentive or pressure, opportunity, rationalisation), analytical procedures, journal entry testing, and assessment of management override of controls.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Fraud Detection Audit:</strong></p>
                    <p><strong>Section 143(12) -</strong> Auditor's mandatory reporting to Central Government on fraud against company by officers/employees, where the auditor has 'reason to believe' an offence involving fraud is being or has been committed during the course of audit.</p>
<p><strong>'Reason to Believe' Threshold -</strong> Lower than 'knowledge'; the auditor need not have proven fraud; sufficient if facts and circumstances would lead a reasonable auditor to believe an offence involving fraud is being or has been committed.</p>
<p><strong>Rule 13 of Companies (Audit and Auditors) Rules 2014 -</strong> Procedure for reporting fraud under Section 143(12); prescribes the Rs 1 crore threshold and Form ADT-4 format.</p>
<p><strong>Form ADT-4 -</strong> Format prescribed under Rule 13 for reporting fraud to Central Government; filed by the auditor within 60 days of knowledge.</p>
<p><strong>SA 240 -</strong> ICAI Standard on Auditing - The Auditor's Responsibilities Relating to Fraud; professional skepticism + risk assessment + specific fraud procedures.</p>
<p><strong>Fraud Triangle -</strong> Three risk factors that together indicate higher fraud risk - (1) Incentive or Pressure; (2) Opportunity; (3) Rationalisation.</p>
<p><strong>CARO 2020 Clause 3(xi) -</strong> Three-part fraud reporting - (a) nature and amounts of any fraud by/on the company; (b) whether ADT-4 has been filed under Section 143(12); (c) whether whistleblower complaints received by the company have been considered.</p>
<p><strong>Section 177(9) Vigil Mechanism -</strong> Mandatory for listed and prescribed companies - vigil mechanism for directors and employees; direct access to Chair of Audit Committee; protection against victimisation.</p>
<p><strong>Section 447 Punishment for Fraud -</strong> Imprisonment 6 months to 10 years (minimum 3 years where public interest involved) + fine 1x to 3x amount; non-bailable for amounts &gt;= Rs 10 lakh or 1% of turnover; scheduled offence under PMLA 2002.</p>
<p><strong>Forensic Audit -</strong> Specific investigative engagement distinct from statutory audit - broader scope (multi-year, transaction-level, evidence-preservation), used when fraud is suspected or confirmed; follows specific forensic engagement standards.</p>

