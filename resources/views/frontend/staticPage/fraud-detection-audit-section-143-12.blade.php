                    <div class="faq-expanded__item" id="faq-1">
                        <h3 class="faq-expanded__q">What is Section 143(12) fraud reporting?</h3>
                        <div class="faq-expanded__a"><p>Section 143(12) of the Companies Act, 2013 requires the statutory auditor - during the course of performing audit duties - to report to the Central Government any fraud against the company by officers or employees where the auditor has 'reason to believe' the fraud is being or has been committed. The reporting threshold is 'reason to believe' - lower than 'knowledge' or 'certainty'. The auditor reports via Form ADT-4 for frauds of Rs 1 crore or more under Rule 13; smaller frauds are reported to the Audit Committee or Board. Section 143(13) provides good faith protection; Section 143(15) provides penalty for failure to report.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-2">
                        <h3 class="faq-expanded__q">When is the auditor required to report fraud to Central Government?</h3>
                        <div class="faq-expanded__a"><p>The auditor is required to report fraud to Central Government via Form ADT-4 when (1) the auditor has 'reason to believe' fraud has been or is being committed against the company by officers or employees during audit performance; (2) the fraud amount is Rs 1 crore or more (aggregated for connected transactions per Rule 13); (3) the 60-day filing window from the auditor's knowledge has not yet expired. Below Rs 1 crore, the auditor reports to Audit Committee or Board with a 45-day response window; if action is inadequate, the auditor escalates to Central Government regardless of amount.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-3">
                        <h3 class="faq-expanded__q">What is the Rs 1 crore threshold under Section 143(12)?</h3>
                        <div class="faq-expanded__a"><p>Rule 13(1) of the Companies (Audit and Auditors) Rules, 2014 prescribes the Rs 1 crore threshold - frauds of Rs 1 crore or more (aggregated for connected transactions forming a single scheme) are reported to the Central Government via Form ADT-4; frauds below Rs 1 crore are reported to the Audit Committee or Board with a 45-day response window. Aggregation applies to connected transactions (same perpetrator, same method, same period); independent frauds by different perpetrators are treated separately. Patron's approach is to aggregate conservatively toward Central Government reporting where the question is ambiguous.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-4">
                        <h3 class="faq-expanded__q">What is Form ADT-4 and Rule 13 procedure?</h3>
                        <div class="faq-expanded__a"><p>Form ADT-4 is the format prescribed under Rule 13 for reporting fraud to the Central Government under Section 143(12). The form includes a detailed description of the fraud, amount involved, parties involved, nature of fraud, period of occurrence, supporting evidence, management's stated response, and the auditor's recommendations. Rule 13 procedure - the auditor must file ADT-4 within 60 days of knowledge of the fraud; pre-filing communication with the Audit Committee or Board is recommended under SA 260; filing is via the MCA portal; the auditor retains a copy; ADT-4 is confidential and not public record like AOC-4.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-5">
                        <h3 class="faq-expanded__q">What does CARO 3(xi) require on fraud reporting?</h3>
                        <div class="faq-expanded__a"><p>CARO 2020 Clause 3(xi) requires three-part substantive reporting in the audit report Annexure A - (a) nature and amounts of any fraud BY the company AND ON the company noticed or reported during the year (separate disclosure, with specific amounts and nature of perpetrator); (b) whether a report under Section 143(12) has been filed by the auditors in Form ADT-4 (affirmative statement with status and date if filed); (c) whether the auditor has considered whistleblower complaints received by the company during the year. All three parts must be reported even if there is no fraud or no whistleblower complaints.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-6">
                        <h3 class="faq-expanded__q">How should companies handle whistleblower complaints?</h3>
                        <div class="faq-expanded__a"><p>For listed and prescribed companies, Section 177(9) Vigil Mechanism is mandatory - direct access to the Audit Committee Chair, protection against victimisation, and an investigation protocol. Patron's protocol - (1) complaint receipt with a direct access channel; (2) initial assessment within 7 days for subject matter and credibility; (3) investigation initiation within 30 days with statutory auditor notification under SA 240 + Section 143(12); (4) investigation typically 30 to 90 days with documentation and chain of custody; (5) resolution with remedial/disciplinary/recovery action and regulator notification if criminal; (6) statutory auditor consideration under CARO 3(xi)(c). The protocol applies even to voluntary vigil mechanisms.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-7">
                        <h3 class="faq-expanded__q">What is the difference between statutory audit and forensic audit?</h3>
                        <div class="faq-expanded__a"><p>Statutory audit is the annual audit appointment under Section 139 with the objective of expressing an opinion on financial statements; fraud detection is performed under SA 240 with reasonable assurance and sample-based testing; output is the audit opinion and CARO 3(xi) reporting plus ADT-4 filing where the Rs 1 crore threshold is met. Forensic audit is a SEPARATE engagement to investigate specific allegations - multi-year scope (typical 3 to 7 years), transaction-level deep-dive, chain of custody per the Indian Evidence Act; output is a forensic report with findings, damages quantification, perpetrator identification, and an expert witness statement. Statutory audit is mandatory annually; forensic audit is engaged when specific investigation is needed.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-8">
                        <h3 class="faq-expanded__q">What are the consequences of fraud under Section 447?</h3>
                        <div class="faq-expanded__a"><p>Section 447 of the Companies Act, 2013 provides punishment for fraud - imprisonment for a minimum of 6 months (minimum 3 years where public interest is involved) and a maximum of 10 years; fine of 1 to 3 times the amount involved. The offence is non-bailable for amounts of Rs 10 lakh or more or 1 percent of the company's turnover (whichever is lower). Fraud under Section 447 is also a SCHEDULED OFFENCE under the Prevention of Money-Laundering Act, 2002 - creating additional money-laundering implications including attachment of properties and parallel ED investigation. Section 448 punishes false statements; Section 449 punishes false evidence.</p></div>
