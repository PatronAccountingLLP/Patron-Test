                <div class="faq-expanded__list">
                    <div class="faq-expanded__item" id="faq-1">
                        <h3 class="faq-expanded__q">What is an advance ruling under GST?</h3>
                        <div class="faq-expanded__a"><p>An advance ruling is a written, binding decision issued by the State Authority for Advance Ruling (AAR) under Section 95(a) of the CGST Act, 2017, on questions relating to classification, notification applicability, time and value of supply, ITC admissibility, tax liability, registration, or whether an activity is a supply. The applicant seeks it before undertaking the proposed transaction.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-2">
                        <h3 class="faq-expanded__q">What are the 7 questions on which an advance ruling can be sought?</h3>
                        <div class="faq-expanded__a"><p>Section 97(2) CGST Act lists seven categories: (a) classification of goods or services, (b) applicability of a notification, (c) determination of time and value of supply, (d) admissibility of input tax credit, (e) determination of tax liability, (f) whether the applicant is required to be registered, and (g) whether any activity amounts to or results in a supply.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-3">
                        <h3 class="faq-expanded__q">What is the fee for filing an advance ruling application?</h3>
                        <div class="faq-expanded__a"><p>Under Rule 104 of the CGST Rules, the application fee is INR 5,000 under CGST and INR 5,000 under SGST, totalling INR 10,000. For OIDAR taxpayers, the fee is paid under the IGST head. The fee is deposited online through the common portal at the time of filing Form GST ARA-01.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-4">
                        <h3 class="faq-expanded__q">How long does the AAR take to give a ruling?</h3>
                        <div class="faq-expanded__a"><p>Section 98(6) of the CGST Act requires the AAR to pronounce its ruling within 90 days of receipt of the application. The 90-day period excludes time taken for furnishing additional information requested by the AAR. In practice, including preliminary and final hearings, the total elapsed time is typically 3 to 5 months from filing.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-5">
                        <h3 class="faq-expanded__q">Can I appeal an adverse AAR ruling?</h3>
                        <div class="faq-expanded__a"><p>Yes. Under Section 100 of the CGST Act, the applicant or the jurisdictional officer may appeal to the Appellate Authority for Advance Ruling (AAAR) within 30 days of communication of the ruling (extendable by another 30 days for sufficient cause). The applicant files Form GST ARA-02 with INR 20,000 fee (CGST + SGST); the officer files Form GST ARA-03 with no fee.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-6">
                        <h3 class="faq-expanded__q">Is the advance ruling binding on other taxpayers?</h3>
                        <div class="faq-expanded__a"><p>No. Under Section 103 of the CGST Act, an advance ruling binds only the applicant who sought it and the jurisdictional officer concerned with that applicant. It is not a precedent for other taxpayers, although rulings are frequently cited persuasively. The binding effect continues until there is a change in law, facts, or circumstances under Section 103(2).</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-7">
                        <h3 class="faq-expanded__q">When is an advance ruling declared void?</h3>
                        <div class="faq-expanded__a"><p>Under Section 104 of the CGST Act, the AAR or AAAR may declare an advance ruling void ab initio if it finds that the ruling was obtained by the applicant through fraud, suppression of material facts, or misrepresentation. Before such declaration, the applicant is given an opportunity of being heard, and all provisions of the Act apply as if no advance ruling had been pronounced.</p></div>
                    </div>
