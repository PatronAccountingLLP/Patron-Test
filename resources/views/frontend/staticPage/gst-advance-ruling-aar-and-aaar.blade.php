        <div class="two-column">
            <div class="column-content">
                <h2 class="section-title">What Is a GST Advance Ruling?</h2>
                <div class="content-text what-is-definition">
                    
                    <p>A GST Advance Ruling is a written, binding decision issued by the State Authority for Advance Ruling (AAR) under Section 95(a) of the CGST Act, 2017, on any of the seven questions listed in Section 97(2). The ruling is sought before the proposed transaction is undertaken and locks the tax treatment for both the applicant and the jurisdictional officer under Section 103.</p>
                    <p>If aggrieved by the AAR ruling, the applicant or the jurisdictional officer may appeal to the State Appellate Authority for Advance Ruling (AAAR) under Section 100 within 30 days. The AAAR pronounces its order within 90 days under Section 101(2).</p>
                    <p>The AAAR ruling is final under Indian GST law - no further statutory appeal lies. Constitutional remedy via writ jurisdiction to the High Court (Article 226) or the Supreme Court remains available.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for GST Advance Ruling:</strong></p>
                    <ul>
                        <li><strong>AAR:</strong> Authority for Advance Ruling - State-level body constituted under Section 96 CGST Act; pronounces original rulings under Section 98(4).</li>
                        <li><strong>AAAR:</strong> Appellate Authority for Advance Ruling - State-level appellate body under Section 99 CGST Act; hears appeals from the AAR under Section 100.</li>
                        <li><strong>Applicant:</strong> Defined in Section 95(c) - any person registered or desirous of obtaining registration under the Act.</li>
                        <li><strong>Section 97(2) Questions:</strong> Seven categories on which the AAR can pronounce - (a) classification, (b) notification applicability, (c) time and value of supply, (d) ITC admissibility, (e) tax liability, (f) registration, (g) whether an activity is supply.</li>
                        <li><strong>Form GST ARA-01:</strong> Application form for advance ruling filed on the common portal with the INR 10,000 fee.</li>
                        <li><strong>Form GST ARA-02:</strong> Appeal form for the AAAR filed by the applicant with the INR 20,000 fee; Form ARA-03 is used by the jurisdictional officer with no fee.</li>
