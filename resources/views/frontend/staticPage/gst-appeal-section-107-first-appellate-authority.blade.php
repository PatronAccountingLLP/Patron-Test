            <div class="column-content">
                <h2 class="section-title">What Is a Section 107 GST Appeal?</h2>
                <div class="content-text what-is-definition">
                    
                    <p>A Section 107 GST appeal is the first formal challenge by an aggrieved taxpayer (or the department under Section 108 revisional authority) against any order passed by an adjudicating authority under the CGST Act 2017 - filed before the appellate authority at the level of Commissioner (Appeals) for state matters or Joint Commissioner (Appeals) for central matters. The appeal is filed electronically in Form GST APL-01 on the GST portal within 3 months from the date of communication of the order being appealed against.</p>
                    <p>The appellate authority has discretion to condone an additional 1 month delay for sufficient cause shown, but cannot condone delays beyond 4 months - the only remedy after that is a writ petition under Article 226 of the Constitution before the jurisdictional High Court. The appeal admission is subject to mandatory pre-deposit of 10 percent of disputed tax under Section 107(6), subject to a maximum cap of Rs 20 crore per CGST and Rs 20 crore SGST, plus 100 percent of the admitted portion of demand.</p>
                    <p>Once admitted via final acknowledgement in Form APL-02, the appellate authority schedules personal hearings (maximum 3 adjournments under Section 107(8)) and disposes the appeal within the 1-year aspirational timeline of Section 107(13). The final order is issued in Form APL-04 - the authority can confirm, modify, annul, or remand. On filing plus pre-deposit, recovery of the disputed balance is automatically stayed. The onward route from an adverse APL-04 is the GSTAT under Section 112 with an additional 10 percent pre-deposit.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Section 107 GST Appeal:</strong></p>
                    <ul>
                        <li><strong>Section 107 CGST Act:</strong> Governs the first appellate stage - an aggrieved taxpayer files before the Commissioner (Appeals) or Joint Commissioner (Appeals) within 3 months of order communication.</li>
                        <li><strong>First Appellate Authority (FAA):</strong> The officer hearing Section 107 appeals - Commissioner (Appeals) for state GST or Joint Commissioner (Appeals) for central GST. Distinct from the GSTAT, which is the second appellate tribunal under Section 112.</li>
                        <li><strong>Form APL-01:</strong> The electronic appeal filing form - statement of facts, grounds of appeal, disputed amounts, pre-deposit details, and supporting documents, filed via DSC or EVC.</li>
                        <li><strong>Form APL-02:</strong> The final acknowledgement confirming admission of the appeal - triggers the 1-year aspirational disposal clock under Section 107(13).</li>
                        <li><strong>Form APL-03:</strong> The departmental appeal by the Commissioner under Section 107(2), filed within 6 months of the original order.</li>
                        <li><strong>Form APL-04:</strong> The final order under Section 107(11) - confirmed, modified, annulled, or remanded - which triggers the 3-month window for an onward GSTAT appeal.</li>
                        <li><strong>Form APL-01W / APL-03W:</strong> Withdrawal forms introduced via CGST Circular 250/07/2025-GST dated 24 June 2025; after APL-02, withdrawal requires appellate authority approval.</li>
                        <li><strong>10% Pre-Deposit (Section 107(6)):</strong> Mandatory pre-condition for admission - 10 percent of disputed tax, capped at Rs 20 crore per CGST and Rs 20 crore SGST. Editable in APL-01 from April 2026 where DRC-03 payments already exist.</li>
                        <li><strong>Admitted Portion:</strong> 100 percent of any demand portion the taxpayer does not contest must be paid before filing - separate from the 10 percent pre-deposit on the disputed portion.</li>
                        <li><strong>Electronic Cash Ledger:</strong> The preferred method for paying pre-deposit per the Jyoti Construction case, avoiding technical rejection that can arise with Credit Ledger usage.</li>
                        <li><strong>Section 107(8) Adjournments:</strong> Maximum 3 adjournments per party per appeal.</li>
                        <li><strong>Section 107(11) Powers of FAA:</strong> Confirm, modify, annul, or remand. The FAA can directly downgrade Section 74 to Section 73 without remand.</li>
                        <li><strong>Section 107(13) Disposal Timeline:</strong> 1-year aspirational disposal period from the date of filing - a guideline, not a strict deadline.</li>
                        <li><strong>Recovery Stay on Filing:</strong> Automatic stay of recovery on the disputed amount upon filing plus pre-deposit; flagged non-recoverable on the portal until disposal.</li>
                        <li><strong>Section 75(2) Downgrade Defence:</strong> If Section 74 fraud cannot be established, the demand is treated as raised under Section 73 with a 10 percent penalty cap. Section 107 is the LAST stage where this downgrade applies WITHOUT remand.</li>
                        <li><strong>GSTAT (Section 112):</strong> The second appellate tribunal - hears appeals against APL-04 with an additional 10 percent pre-deposit (total 20 percent across stages).</li>
                        <li><strong>Article 226 Writ Remedy:</strong> The constitutional remedy before the High Court for appeals filed beyond 4 months, where the statutory authority cannot condone.</li>
                        <li><strong>Section 128A Amnesty:</strong> Conditional waiver of interest and penalty for Section 73 demands for FY 2017-18 to FY 2019-20, subject to conditions.</li>
