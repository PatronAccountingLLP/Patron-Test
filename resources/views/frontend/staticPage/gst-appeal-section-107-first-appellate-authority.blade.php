                        <h3 class="faq-expanded__q">What is a Section 107 GST appeal?</h3>
                        <div class="faq-expanded__a"><p>A Section 107 GST appeal is the first formal challenge by an aggrieved taxpayer against any order passed by an adjudicating authority under the CGST Act 2017 - typically a DRC-07 demand order under Section 73, 74, or 74A, a refund rejection order, a registration cancellation order, or other appealable orders. The appeal is filed electronically in Form APL-01 before the appellate authority (Commissioner or Joint Commissioner Appeals) within 3 months of order communication. The appeal admission is subject to mandatory 10 percent pre-deposit of disputed tax under Section 107(6), capped at Rs 20 crore per CGST and Rs 20 crore SGST. The final order is issued in Form APL-04 within the 1-year aspirational disposal timeline under Section 107(13).</p></div>
