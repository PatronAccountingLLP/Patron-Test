<section class="content-section">
    <div class="content-container">
        <div class="text-content">
            <h2 class="section-title">Legal and Compliance Framework</h2>
            <div class="content-text">
                
                <h3>Section 16 of IGST Act 2017</h3>
                <p>Defines zero-rated supply (exports + SEZ). Section 16(3) provides two refund routes for handicraft exports: LUT/bond without IGST payment with refund of accumulated ITC under Rule 89(4), or IGST-paid with refund of IGST under Rule 96.</p>
                <h3>Sections 22 and 23 of CGST Act 2017</h3>
                <p>Section 22 - persons liable for registration above Rs 20 lakh aggregate turnover (Rs 10 lakh in Special Category States). Section 23 - persons not liable for registration including categories exempted under Section 23(2), which is the basis for Notification 3/2018-IT handicraft exemption.</p>
                <h3>Section 54 of CGST Act 2017</h3>
                <p>Section 54(1) - 2-year limitation from relevant date. Section 54(3)(ii) - substantive right to refund of unutilised ITC where input GST rate exceeds output GST rate. Section 54(6) - provisional 90 percent refund for zero-rated supplies. Section 54(7) - final refund within 60 days. Section 56 - 6 percent interest if delayed beyond 60 days; 9 percent for appellate orders.</p>
                <h3>Notification 21/2018-Central Tax (Rate) (26 July 2018)</h3>
                <p>Defines 'handicraft goods' through a list of 33 to 39 HSN categories where products are made by craftsmen predominantly by hand even though some machinery may also be used. Most handicraft HSN attract a 5 percent or 12 percent output GST rate. The 'predominantly by hand' test is the operational dividing line - disputes typically arise where mechanical aids (lathes, looms, grinding machines) are extensive.</p>
                <h3>Notification 3/2018-Integrated Tax (22 October 2018) - Supersedes 8/2017-IT</h3>
                <p>Supersedes Notification 8/2017-Integrated Tax dated 14 September 2017. Exempts inter-State handicraft suppliers from compulsory GST registration under Section 24(i) CGST Act so long as the all-India aggregate turnover does not exceed Rs 20 lakh (Rs 10 lakh in Special Category States other than Jammu and Kashmir). PAN and e-way bill compliance under Rule 138 remain mandatory.</p>
                <h3>Notification 56/2018-Central Tax (23 October 2018) - Supersedes 32/2017-CT</h3>
                <p>Supersedes Notification 32/2017-Central Tax dated 15 September 2017. Provides parallel CGST track exemption for casual taxable handicraft suppliers. Together with Notification 3/2018-IT, this is the operational framework for handicraft registration exemption.</p>
                <h3>Rule 89(4) of CGST Rules 2017</h3>
                <p>Refund formula for accumulated ITC on zero-rated supply: Refund Amount = (Turnover of zero-rated supply of goods plus services) x Net ITC / Adjusted Total Turnover. Capital goods ITC is excluded from Net ITC.</p>
                <h3>Rule 89(5) of CGST Rules 2017 (Amended by Notification 14/2022-CT)</h3>
                <p>Refund formula for inverted duty structure. Notification 14/2022-Central Tax dated 5 July 2022 amended the formula to: Maximum Refund = (Turnover of inverted rated supply x Net ITC / Adjusted Total Turnover) minus (Tax payable on inverted rated supply x Net ITC / ITC availed on inputs and input services). Applicable prospectively per CBIC Circular 181/13/2022-GST. Net ITC limited to input goods only - Supreme Court in Union of India v VKC Footsteps India Pvt Ltd (2021) upheld this restriction.</p>
                <h3>Rule 96 and Rule 96A of CGST Rules 2017</h3>
                <p>Rule 96 - IGST-paid auto-refund route. Shipping bill is deemed application; ICES validates against GSTN; PFMS disburses. Rule 96A - LUT mechanism in Form GST RFD-11, valid for one financial year. Subject to Notification 37/2017-CT prosecution eligibility test.</p>
                <h3>Notification 37/2017-Central Tax (4 October 2017)</h3>
                <p>LUT framework for export under bond or letter of undertaking. Eligibility: no prosecution for tax evasion above Rs 2.5 crore under CGST Act, IGST Act, or existing law.</p>
                <h3>EPCH and RoDTEP Framework</h3>
