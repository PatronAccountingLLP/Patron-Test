            <div class="column-content">
                <h2 class="section-title">What Is GST Refund Under Inverted Duty Structure?</h2>
                <div class="content-text what-is-definition">
                    
                    <p>Inverted Duty Structure (IDS) refund is a refund of accumulated unutilised Input Tax Credit allowed under <strong>Section 54(3)(ii) of the CGST Act 2017</strong> to a registered manufacturer or supplier where the rate of GST on inputs is higher than the rate of GST on outward supplies, other than nil-rated or fully exempt supplies.</p>
                    <p>Section 54(3) of the CGST Act 2017 permits refund of unutilised ITC only in two cases: zero-rated supplies (exports and SEZ) under clause (i), and inverted duty structure under clause (ii). Refund is not allowed where the output is nil-rated or fully exempt, or where the goods are notified as ineligible by the Government on the recommendation of the GST Council under proviso to Section 54(3).</p>
                    <p><strong>Rule 89(5) of the CGST Rules 2017</strong> prescribes the formula for the maximum refund amount, restricted to ITC on input goods only - input services and capital goods are excluded per the Supreme Court ruling in Union of India versus VKC Footsteps India Pvt. Ltd. (2021) 9 TMI 626.</p>
                    <p><strong>Rule 89(5) Refund Formula:</strong></p>
                    <div class="highlight-box" style="margin-top:8px;margin-bottom:16px;background:var(--orange-lighter);border-left:4px solid var(--orange);padding:14px 16px;border-radius:6px;">
                        <p style="margin:0;font-size:14px;color:var(--text-dark);line-height:1.7;"><strong>Maximum Refund = {(Turnover of Inverted Rated Supply x Net ITC) / Adjusted Total Turnover} MINUS {Tax Payable on Inverted Rated Supply x (Net ITC / ITC availed on inputs and input services)}</strong></p>
                    </div>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for IDS Refund:</strong></p>
                    <ul style="margin-bottom:0;">
                        <li><strong>Inverted Duty Structure (IDS):</strong> GST rate on inputs higher than GST rate on output supplies, leading to ITC accumulation.</li>
                        <li><strong>Net ITC (Rule 89(5)):</strong> ITC availed on inputs (goods) only during the relevant period - excludes input services and capital goods.</li>
                        <li><strong>Adjusted Total Turnover (Rule 89(4)):</strong> Total turnover in a state excluding exempt supplies (other than zero-rated) and turnover of services not zero-rated.</li>
                        <li><strong>Turnover of Inverted Rated Supply:</strong> Value of outward supplies on which the input GST rate exceeded the output GST rate during the refund period.</li>
                        <li><strong>Tax Payable on Inverted Rated Supply:</strong> Output GST paid on the inverted rated supplies during the period - subtracted in the formula.</li>
                        <li><strong>Provisional Refund (Rule 91(2)):</strong> Up to 90 percent of refund claim sanctioned without detailed scrutiny under CGST Instruction 6/2025, applicable to IDS from 01 October 2025.</li>
                        <li><strong>Relevant Date (Section 54):</strong> The due date of furnishing GSTR-3B for the period to which the refund relates - the 2-year limitation runs from this date.</li>
                        <li><strong>VKC Footsteps SC (2021):</strong> Supreme Court ruling upholding Rule 89(5) restriction of Net ITC to input goods only.</li>
                        <li><strong>Safari Retreats SC (03.10.2024):</strong> Functionality test under Section 17(5)(d) - unlocked input service ITC for builders remains outside Rule 89(5) IDS refund pool.</li>
                        <li><strong>CGST Instruction 6/2025:</strong> Extends 90 percent provisional refund to IDS claims filed on or after 01.10.2025.</li>
                    </ul>

