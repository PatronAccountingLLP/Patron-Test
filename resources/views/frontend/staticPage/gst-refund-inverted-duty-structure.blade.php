                        <div class="faq-expanded__a"><p>Inverted duty structure (IDS) under GST is a situation where the rate of GST on inputs (raw materials, packaging) is higher than the rate of GST on outward supplies (finished goods), causing accumulation of unutilised Input Tax Credit. Section 54(3)(ii) of the CGST Act 2017 permits refund of this accumulated ITC subject to Rule 89(5) of the CGST Rules 2017 and the exclusion list under proviso to Section 54(3).</p></div>
