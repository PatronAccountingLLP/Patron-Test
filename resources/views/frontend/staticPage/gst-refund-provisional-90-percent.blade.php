                <div class="content-text what-is-definition">
                    
                    <p>90 percent provisional refund is the GST mechanism by which a registered taxpayer entitled to refund receives 90 percent of the claimed refund amount within 7 days of the RFD-02 acknowledgement, before the proper officer completes full scrutiny. The remaining 10 percent is sanctioned in Form GST RFD-06 within 60 days of RFD-02 under Section 54(7). The objective is working capital relief - particularly for exporters and inverted duty manufacturers whose refund cycles otherwise stretch to 60-90 days.</p>
                    <p><strong>Section 54(6) of the CGST Act 2017</strong> codifies the facility for zero-rated supplies. The 56th GST Council recommended amending Section 54(6) to include inverted duty structure - in the interim, CGST Instruction 6/2025 directs field formations to apply the 90 percent provisional facility to IDS refunds.</p>
