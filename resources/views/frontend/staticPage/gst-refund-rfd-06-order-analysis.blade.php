            <div class="content-text">
                
                <p><strong>Rule 92 of CGST Rules 2017:</strong> Where, upon examination of the application, the proper officer is satisfied that a refund is due and payable to the applicant, he shall make an order in Form GST RFD-06 sanctioning the amount of refund to which the applicant is entitled. Where the proper officer is satisfied that the amount refundable is not payable to the applicant, he shall issue a notice in Form GST RFD-08 to the applicant, requiring him to furnish a reply in Form GST RFD-09 within a period of fifteen days of the receipt of such notice and after considering the reply, make an order in Form GST RFD-06.</p>
                <p><strong>Section 107(1) of CGST Act 2017:</strong> Any person aggrieved by any decision or order passed under this Act by an adjudicating authority may appeal to such Appellate Authority as may be prescribed within three months from the date on which the said decision or order is communicated to such person.</p>
                <p><strong>Section 107(4) of CGST Act 2017:</strong> The Appellate Authority may, if he is satisfied that the appellant was prevented by sufficient cause from presenting the appeal within the aforesaid period of three months or six months, as the case may be, allow it to be presented within a further period of one month.</p>
                <p><strong>Section 107(6) of CGST Act 2017:</strong> No appeal shall be filed under sub-section (1), unless the appellant has paid (a) in full, such part of the amount of tax, interest, fine, fee and penalty arising from the impugned order, as is admitted by him; and (b) a sum equal to ten per cent of the remaining amount of tax in dispute arising from the said order, subject to a maximum of twenty-five crore rupees, in relation to which the appeal has been filed.</p>
                <p><strong>K Line India Pvt Ltd v Union of India (Bombay HC, 2026-VIL-381-BOM, dated 15.04.2026):</strong> The Bombay High Court held that an RFD-06 refund rejection order passed without prior issuance of deficiency memo and without affording opportunity of hearing stands in direct violation of Rule 92 of the CGST Rules 2017 and consequently lacks legal validity. Such an order is not merely irregular - it is void ab initio and non-est in the eyes of law. The Department's subsequent conduct of entertaining a fresh refund application and issuing a deficiency memo unmistakably indicates that the proceedings had not attained finality. In such circumstances, the rigid application of limitation cannot be invoked to defeat substantive rights, particularly where the very foundation of the earlier action is legally unsustainable. The judgment reaffirms that procedural safeguards under GST are not dispensable, and any departure renders the order non-est. The Court invoked Article 226 jurisdiction to step beyond the narrow confines of statutory limitation.</p>
                <p><strong>Section 112(1) of CGST Act 2017 - Second Appeal Before GSTAT:</strong> Any person aggrieved by an order passed against him under Section 107 may appeal to the Appellate Tribunal against such order within three months from the date on which the order sought to be appealed against is communicated to the person preferring the appeal.</p>
                <p><strong>Section 112(8) of CGST Act 2017 - Additional Pre-Deposit:</strong> No appeal shall be filed under sub-section (1) or sub-section (3) unless the appellant has paid (a) in full, the admitted portion; and (b) a sum equal to ten per cent of the remaining amount of tax in dispute, in addition to the amount paid under sub-section (6) of section 107.</p>
