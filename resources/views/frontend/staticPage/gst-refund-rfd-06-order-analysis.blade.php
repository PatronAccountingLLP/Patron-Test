                <p>Form GST RFD-06 is the final adjudication on a refund application. It is issued under Rule 92 of the CGST Rules 2017 by the proper officer within the 60-day window prescribed under Section 54(7) of the CGST Act 2017. The order can sanction the refund in full, sanction in part with rejection of the balance, sanction with adjustment against demand under Form RFD-07A, sanction with withholding under Form RFD-07B, or reject in full. Where rejection in part or in full is proposed, the law mandates a prior Show Cause Notice in Form RFD-08; the applicant has 15 days to file reply in Form RFD-09 with documents, arguments, and request for hearing. Patron's settled discipline is to treat the RFD-09 stage as the substantive opportunity to prevent rejection.</p>
