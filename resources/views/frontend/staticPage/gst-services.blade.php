 // Measure a fixed/sticky site header ONCE and expose its height as
 // --site-header-h. CSS position:sticky then handles all the actual
 // pinning natively (reliable on mobile - no scroll-math in JS).
 function measureHeader() {
 var h = 0, nodes = document.body.getElementsByTagName('*');
 for (var i = 0; i < nodes.length; i++) {
 var el = nodes[i];
 if (el === wrap || (wrap && wrap.contains(el))) continue;
 var cs = window.getComputedStyle(el);
 if ((cs.position === 'fixed' || cs.position === 'sticky') &&
 (parseFloat(cs.top) || 0) === 0) {
 var r = el.getBoundingClientRect();
 if (r.top <= 1 && r.height > 0 && r.height < 200 &&
 r.width >= window.innerWidth * 0.6 && r.bottom > h) {
 h = r.bottom;
 }
 }
 }
 document.documentElement.style.setProperty('--site-header-h', h + 'px');
 }

