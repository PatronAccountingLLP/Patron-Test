            <p>The <strong>National Appellate Authority for Advance Ruling (NAAR)</strong> is a long-awaited mechanism to resolve one of the most persistent challenges under GST - conflicting advance rulings pronounced by different state-level Appellate Authorities for Advance Ruling (AAAR). Introduced by <strong>Section 101A CGST Act 2017</strong> (effective 01-01-2020 via Finance Act 2019), the NAAR was never separately constituted as an independent body.</p>

