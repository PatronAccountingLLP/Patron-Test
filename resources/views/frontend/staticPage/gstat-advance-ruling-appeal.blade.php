    <div class="content-container">
        <div class="text-content">
                <h2 class="section-title">NAAR Advance Ruling Appeal - Overview</h2>
            <div class="highlight-box" style="background:var(--orange-lighter);border-left:4px solid var(--orange);padding:20px 24px;border-radius:var(--radius-md);margin-bottom:24px;">
                <p style="margin:0;font-size:15px;line-height:1.7;"><strong>TL;DR:</strong> The <strong>National Appellate Authority for Advance Ruling (NAAR)</strong> under <strong>Section 101A CGST Act 2017</strong> resolves conflicting advance rulings from different state AAAR bodies. From <strong>April 2026</strong>, the <strong>GSTAT Principal Bench at New Delhi</strong> serves as NAAR. Appeals are filed in <strong>Form GST ARA-03</strong> within <strong>30 days</strong> of the AAAR order. Patron Accounting advises multi-state businesses on NAAR strategy.</p>
            </div>

            <div class="table-responsive-wrapper" style="margin-bottom:24px;">
                <table>
                    <thead><tr><th>Parameter</th><th>Details</th></tr></thead>
