                <h2 class="section-title">Who Should File a NAAR Appeal at GSTAT?</h2>
                <p>A <strong>NAAR appeal</strong> is relevant for any person who has received conflicting advance rulings from the AAAR in two or more states under <strong>Section 101B CGST Act 2017</strong>. This typically affects:</p>

                <ul>
                    <li><strong>Multi-state businesses (distinct persons under Section 25):</strong> A company registered in Maharashtra and Karnataka may receive contradictory classification rulings from the respective state AAARs - one classifying a supply as goods at 18% and the other as services at 12%.</li>
                    <li><strong>Pan-India service providers:</strong> IT companies, logistics firms, and consultancies operating across multiple states may face divergent rulings on place of supply, intermediary classification, or exemption eligibility.</li>
                    <li><strong>Manufacturers with multi-state operations:</strong> Classification disputes (HSN code), ITC eligibility, and valuation methodology rulings that differ across states.</li>
                </ul>

                <p>The appeal must be filed within <strong>30 days</strong> of the AAAR order in <strong>Form GST ARA-03</strong> under <strong>Section 101B CGST Act</strong>. The AAAR may condone delay up to an additional 30 days on sufficient cause.</p>
