            <div class="column-content">
                <h2 class="section-title">What Is the National Appellate Authority for Advance Ruling (NAAR)?</h2>
                <div class="highlight-box" style="background:var(--blue-lighter);border-radius:var(--radius-md);padding:20px 24px;margin-bottom:20px;">
                    <p style="margin:0;"><strong>Definition:</strong> The National Appellate Authority for Advance Ruling (NAAR) is the national-level appellate body constituted under <strong>Section 101A of the Central Goods and Services Tax Act 2017</strong> to hear appeals under <strong>Section 101B</strong> against conflicting advance rulings pronounced by Appellate Authorities for Advance Ruling (AAAR) in two or more states or union territories.</p>
                </div>

                <p>The NAAR was introduced by the <strong>Finance Act 2019</strong> (effective 01-01-2020) to address the problem of divergent interpretations across states - where the same transaction or classification question received different rulings from AAR and AAAR bodies in different states. Under <strong>Section 101B</strong>, an appeal lies to the NAAR only where conflicting advance rulings have been pronounced by the Appellate Authorities of two or more states or union territories.</p>

                <p><strong>NAAR vs GSTAT Appellate Jurisdiction:</strong> The <strong>NAAR under Section 101A</strong> is fundamentally different from the <strong>GSTAT appellate jurisdiction under Section 112</strong> - NAAR hears appeals only against conflicting AAAR rulings from multiple states (a narrow, policy-level jurisdiction), while GSTAT hears appeals against adverse orders of the first appellate authority on all GST matters including demand, penalty, refund, and ITC disputes (a broad, fact-finding jurisdiction).</p>

                <p><strong>Dual Function of the Principal Bench:</strong> From April 2026, the GSTAT Principal Bench at New Delhi will serve dual functions - as the Principal Bench of the Appellate Tribunal for place of supply disputes and matters of national importance under Section 109(7), and as the National Appellate Authority for Advance Ruling under Section 101A for conflicting advance rulings.</p>

                <div class="highlight-box" style="background:#f0fdf4;border-radius:var(--radius-md);padding:16px 20px;margin-top:20px;">
