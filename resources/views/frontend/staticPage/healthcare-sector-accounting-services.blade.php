                        <h3 class="faq-expanded__q">Is GST applicable on healthcare services in India?</h3>
                        <div class="faq-expanded__a"><p>Most core healthcare services are exempt from GST under Notification No. 12/2017-Central Tax (Rate) dated 28 June 2017. Services provided by clinical establishments (hospitals, nursing homes, clinics), authorised medical practitioners, and paramedics involving diagnosis, treatment, or care for illness, injury, pregnancy, or deformity in any recognised system of medicine are exempt (SAC 9993). Exceptions include: hospital room rent exceeding Rs 5,000 per day for non-ICU rooms (5% GST, no ITC), cosmetic surgeries unrelated to medical restoration (18% GST), and food supplied to non-admitted visitors (taxable).</p></div>
