                        <h3 class="faq-expanded__q">What is TDS on payment to consultant doctors under Section 194J?</h3>
                        <div class="faq-expanded__a"><p>Under Section 194J of the Income Tax Act, 1961, TDS at 10% must be deducted on professional fees paid to consultant or retainer doctors where the aggregate payment exceeds Rs 50,000 in a financial year and there is no employer-employee relationship. Multiple ITAT judgments including ITAT Delhi (ITA No. 554/Del/2016 - Escorts Heart Institute) and ITAT Mumbai have confirmed that payments to consultant/retainer doctors without supervision, fixed hours, or employment benefits attract Section 194J, not Section 192 (salary). Misclassification results in 'assessee in default' proceedings under Section 201(1).</p></div>
