                        <h3 class="faq-expanded__q">Can a hospital claim Input Tax Credit on GST paid for medical equipment?</h3>
                        <div class="faq-expanded__a"><p>Generally, no. Since core healthcare services are exempt from GST under Notification No. 12/2017-CT(Rate), hospitals providing predominantly exempt services cannot claim ITC on inputs per Section 17(2) of the CGST Act, 2017. If a hospital also provides taxable services (room rent above Rs 5,000/day, cosmetic surgery, parking), it must use the apportionment formula under Rule 42 and 43 of the CGST Rules to claim proportionate ITC on the taxable portion only.</p></div>
