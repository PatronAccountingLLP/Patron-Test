                    <p>Unlike general accounting, healthcare accounting deals with GST exemptions on core clinical services (SAC 9993), an inverted duty structure where hospitals bear input GST without ITC recovery, complex TDS classification between Section 192 (salary) and Section 194J (professional fees) for consultant doctors, and Section 80D advisory for health insurance deductions.</p>
