                        <div class="faq-expanded__a"><p>Eight most common rejection or extended-clarification reasons - (1) Incomplete ISP credentials (training, exam, F and P, KYC for any ISP); (2) Deficient infrastructure (photographs unclear, lease in personal name, no operational readiness); (3) Net worth gaps (bank balance below Rs 10 lakh, certificate older than 3 months, loan-funded portion); (4) Area-of-operation ambiguity (state without district, inconsistent across documents); (5) Principal Officer Fit and Proper issues (regulatory history, undisclosed conviction, qualification verification); (6) MoA Object Clause mismatch; (7) Insurer consent letter issues (expired, misaligned with business plan, wrong signatory); (8) 3-year business plan unrealistic. Patron's pre-filing audit prevents each.</p></div>
