                        <div class="faq-expanded__a"><p>The IRDAI IMF registration process for existing Pvt Ltd or LLP entities involves - (1) Verifying entity eligibility under IMF Regulation 2.2; (2) Reviewing and if needed amending the MoA Object Clause via Special Resolution plus Form MGT-14; (3) Confirming Rs 10 lakh net worth (Rs 5 lakh single-district) via Patron CA Net Worth Certificate; (4) Coordinating Principal Officer 50-hour training and IRDAI certification examination; (5) Onboarding minimum 2 Insurance Sales Persons with 25-hour training and certification; (6) Compiling 25+ documents across 7 categories; (7) Filing online application on IRDAI portal with Rs 5,000 application fee; (8) Handling clarification queries from Authority; (9) Receiving 3-year IMF Registration Certificate on Rs 10,000 registration fee payment.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-2">
                        <h3 class="faq-expanded__q">What documents are required for IRDAI IMF application?</h3>
                        <div class="faq-expanded__a"><p>25+ documents across 7 categories - (1) Entity Documents: MoA, AoA / LLP Agreement, Certificate of Incorporation, PAN, shareholding pattern, MGT-14 acknowledgement; (2) Financial: CA Net Worth Certificate, bank statement, audited financials for 2-3 years, 3-year business plan; (3) Principal Officer: qualifications, training and exam certificates, Fit and Proper declaration, KYC, photographs, resume; (4) ISPs: training and exam certificates, F and P certification, KYC for each ISP; (5) Infrastructure: office lease, photographs, organisation chart; (6) Tie-up: insurer consent letter (at least one); (7) Authority: application form, Board Resolution, Code of Conduct Undertaking, statutory declarations.</p></div>
