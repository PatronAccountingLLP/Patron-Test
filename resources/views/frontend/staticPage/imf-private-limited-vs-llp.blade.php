                        <li><strong>Section 167B Income Tax Act 1961:</strong> LLP taxed at 30 percent flat rate on total income, plus 12 percent surcharge if total income exceeds Rs 1 crore, plus 4 percent cess. Effective approximately 31.2 percent below Rs 1 crore; 34.94 percent above Rs 1 crore. No concessional 22 percent regime available.</li>
