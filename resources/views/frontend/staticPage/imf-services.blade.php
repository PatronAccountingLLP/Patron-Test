            <div class="sec-tag"><span class="tag-num">01</span>Overview</div>
            <h2 class="sec-h2">What is an Insurance Marketing Firm (IMF), and who needs it?</h2>
            <div class="quick-answer-box">
                <h3>Quick Answers</h3>
                <ul>
                    <li>An Insurance Marketing Firm (IMF) is an IRDAI-registered entity that distributes insurance (up to 2 life + 2 general + 2 health insurers) plus other financial products within its registered district or districts.</li>
                    <li>Eligible entities are a Company, LLP, or Co-operative Society whose name contains "Insurance Marketing Firm" or "IMF", with minimum net worth of INR 10 lakh (INR 5 lakh for a single aspirational district).</li>
                    <li>You need a qualified Principal Officer (50-hour IRDAI training plus the IMF exam) and at least one Insurance Sales Person (ISP) per office.</li>
                    <li>Registration is filed on imf.irda.gov.in with a INR 5,000 fee paid from a State Bank of India account in the IMF's name; validity is now perpetual (since 5 February 2026).</li>
                    <li>It is ideal for insurance agents, mutual fund distributors, financial advisors, Registered Investment Advisers and bank retirees who want to run an independent, multi-insurer distribution business.</li>
                </ul>
            </div>
            <p>An <strong>Insurance Marketing Firm (IMF)</strong> is a distribution licence created under the <strong>IRDAI (Registration of Insurance Marketing Firm) Regulations, 2015</strong>. It lets one entity solicit and service insurance from more than one insurer while operating independently &mdash; with its own Insurance Sales Persons (ISPs) &mdash; and cross-sell other approved financial products. In effect, it sits between a single-tied agent and a full broker: broader than agency, simpler than broking.</p>
            <p>This page is the <strong>IMF hub</strong>. Rather than sell a single package, it routes you to the exact engagement you need &mdash; whether that is a fresh end-to-end setup, IRDAI registration for an entity you already own, certification of your Principal Officer and ISPs, or an annual compliance retainer. Below, the <strong>Setup &amp; Lifecycle</strong> section covers the build, and the <strong>By Segment</strong> section maps the right path for agents, advisors, mutual fund distributors, brokers and bank professionals.</p>
            <p><strong>Who typically needs an IMF?</strong> Insurance agents and POSPs who have outgrown a single insurer; AMFI-registered mutual fund distributors and SEBI Registered Investment Advisers who want to add insurance; multi-line brokers weighing a simpler structure; and serving or retired bank and NBFC staff who want an independent, multi-product distribution business in their own district.</p>
