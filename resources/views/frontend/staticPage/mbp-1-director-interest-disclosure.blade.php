                        <h3 class="faq-expanded__q">What is Form MBP-1?</h3>
                        <div class="faq-expanded__a"><p>Form MBP-1 is the written notice through which every director discloses interest or concern in other companies, bodies corporate, firms or associations of individuals, including shareholding, to the Board, under Section 184(1) of the Companies Act, 2013. It helps the company identify and manage potential conflicts of interest.</p></div>
