                        <h3 class="faq-expanded__q">When must MBP-1 be given?</h3>
                        <div class="faq-expanded__a"><p>A director must give MBP-1 at the first board meeting in which the person participates as a director, at the first board meeting of every financial year, and at the first board meeting held after any change in the interest already disclosed. It is a recurring annual disclosure that also updates whenever a director’s interests change.</p></div>
                    </div>
