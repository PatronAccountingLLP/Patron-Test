                <div class="content-text what-is-definition">
                    
                    <p>Form MBP-1 is the written notice through which every director discloses interest or concern in other companies, bodies corporate, firms or associations of individuals, including shareholding, to the Board, under Section 184(1) of the Companies Act, 2013.</p><p>It is given at the first board meeting in which a person participates as a director, at the first board meeting of every financial year, and at the first board meeting after any change. The company records it in the board minutes and the register of contracts.</p>

