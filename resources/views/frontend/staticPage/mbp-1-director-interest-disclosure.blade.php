                        <h3 class="faq-expanded__q">Is MBP-1 filed with the ROC?</h3>
                        <div class="faq-expanded__a"><p>No. MBP-1 is given by the director to the company and the Board, taken on record in the board minutes and the register of contracts, and preserved in the company’s safe custody. It is not an e-form filed directly with the Registrar of Companies, which is one reason it is so often overlooked in routine compliance.</p></div>
