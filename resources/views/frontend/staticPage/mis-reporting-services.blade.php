                <p>Complexity in MIS report preparation depends on the entities being consolidated, the depth of cost-centre splits you want, each additional revenue line tracked and whether budgets exist to measure against. Commentary and the variance walk are included. Rebuilding books that were never closed, and any filing through the government GST portal, sit outside this scope.</p>
