            <h2 class="section-title">Legal and Regulatory Framework for MIS Reporting</h2>
            <div class="content-text">
                
                <p>An MIS pack is only as reliable as the ledger beneath it, and Section 128 requires that ledger to be true and fair on accrual and double entry. A management report built on unclosed books is a forecast dressed up as a fact.</p>
<p>So the framework matters even though the report itself is unregulated. The figures a board reads - margin, working capital, cash - have to reconcile to the statutory books beneath them, measured under the same standards that will later be audited. That is why <a href="/glossary/gross-profit">Gross Profit</a>, <a href="/glossary/ebitda">EBITDA</a> and <a href="/glossary/working-capital">Working Capital</a> in a monthly MIS report are drawn from the ledger, not estimated alongside it. The provisions below are what the underlying numbers answer to.</p>
<ul class="al-list"><li><span class="al-t"><strong>Section 128, Companies Act 2013</strong></span><span class="al-d">The books the MIS draws on are kept true and fair on accrual and double entry, so a management figure ties back to a statutory one.</span></li><li><span class="al-t"><strong>Companies (Accounting Standards) Rules 2021 and Companies (Indian Accounting Standards) Rules 2015</strong></span><span class="al-d">The same measurement standards apply to the MIS numbers as to the audited accounts, so the two never diverge - the basis of a reliable <a href="/glossary/net-profit">Net Profit</a> line.</span></li><li><span class="al-t"><strong>Section 128 and Section 44AA, Income-tax Act 1961</strong></span><span class="al-d">The double-entry obligation behind the report applies whether the entity is a company or a non-corporate, so the pack is built on real books.</span></li><li><span class="al-t"><strong>Section 44AA, Income-tax Act 1961</strong></span><span class="al-d">A non-corporate business maintains its books once the limits are crossed, giving the MIS a compliant source to summarise.</span></li><li><span class="al-t"><strong>Rule 3(1), Companies (Accounts) Rules 2014</strong></span><span class="al-d">The audit trail stays enabled, so any adjustment that changes an MIS figure is itself traceable. A months-behind ledger is closed first via <a href="/backlog-bookkeeping-catch-up-services">catch-up bookkeeping</a>.</span></li></ul><p style="margin-top:20px;padding-top:16px;border-top:1px solid var(--gray-200);font-size:13px;color:var(--text-muted);"><strong>Official sources:</strong> <a href="https://www.mca.gov.in" target="_blank" rel="noopener">Ministry of Corporate Affairs</a> &middot; <a href="https://www.incometax.gov.in" target="_blank" rel="noopener">Income Tax Department</a> &middot; <a href="https://www.gst.gov.in" target="_blank" rel="noopener">GST Portal</a> &middot; <a href="https://www.startupindia.gov.in" target="_blank" rel="noopener">Startup In