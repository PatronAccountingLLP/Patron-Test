                <div class="content-text what-is-definition">
                    
                    <p>A company net worth certificate is a document prepared and certified by a practising Chartered Accountant that states a company's net worth, its total assets minus total liabilities, as on a specific date, usually the latest audited financial year-end.</p>
                    <p>For companies, net worth is not computed at market value as it is for individuals. It follows Section 2(57) of the Companies Act 2013 and is drawn directly from the audited balance sheet. The certificate is issued on CA letterhead with a signature, seal, and a unique UDIN that any institution can verify online.</p>

