            <h2 class="section-title">How Company Net Worth Is Defined</h2>
            <div class="content-text">
                
                <p>Under Section 2(57) of the Companies Act 2013, a company's net worth is the aggregate of paid-up share capital, securities premium, and reserves created out of profits, minus accumulated losses, deferred expenditure, and miscellaneous expenditure not written off. Revaluation reserves and write-back of depreciation are excluded.</p>
                <p>Because this is a book-value, audited-financials computation, it differs fundamentally from an individual's market-value net worth. The certificate is issued by a practising Chartered Accountant under ICAI professional standards, including SA 200, SA 500, and SA 505, and carries a UDIN that any bank, tender authority, or regulator can verify at the ICAI portal, making forgery effectively impossible. It is read alongside your <a href="/statutory-audit">statutory audit</a> financials.</p>
                <p>For the statutory definition, refer to <a href="https://www.mca.gov.in" target="_blank" rel="noopener">the Ministry of Corporate Affairs (mca.gov.in)</a>, which administers the Companies Act 2013. The net worth certificate restates the audited figures into a single CA-certified statement of corporate net worth.</p>

