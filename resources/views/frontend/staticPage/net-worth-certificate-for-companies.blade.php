                        <li><strong>Shareholders' funds:</strong> paid-up share capital plus reserves and surplus.</li>
                        <li><strong>UDIN:</strong> the unique document identification number that authenticates the certificate.</li>
                        <li><strong>Reference date:</strong> the date of computation, aligned to the latest audited year-end.</li>
