                <h2 class="section-title">What Is a Net Worth Certificate for Directors Fit and Proper</h2>
                <div class="content-text what-is-definition">
                    
                    <p><strong>Definition:</strong> A net worth certificate for directors under fit and proper rules is a Chartered Accountant-signed statement of a director (or KMP) total assets minus total liabilities as on a specific date, attached to the regulator-prescribed declaration form to evidence the financial integrity and solvency criterion.</p>
                    <p>The certificate is issued on CA letterhead with signature, ICAI membership number, firm registration number and UDIN, and is filed with the Nomination and Remuneration Committee of the regulated entity, which in turn relies on it during the due diligence required by SEBI, RBI or IRDAI. Unlike a visa or home loan net worth certificate, this is a B2B compliance document with regulator-specific format obligations.</p>
                    <p>SEBI Schedule II requires evidence of financial solvency and net worth; RBI Annex-1 prescribes declaration in Annexure II format with annual refresh as of 31 March; IRDAI Master Circular requires all directors and KMP to be fit and proper across multiple dimensions including financial integrity. The certificate covers immovable assets, movable assets and verified liabilities for the individual director, not the regulated entity itself. UDIN is verified live on the ICAI portal at <a href="https://udin.icai.org" target="_blank" rel="noopener">udin.icai.org</a> by Nomination Committees and regulators.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Net Worth Certificate for Directors (Fit and Proper):</strong></p>
                    <ul>
                        <li><strong>Schedule II SEBI (Intermediaries) Regulations 2008:</strong> Four-part fit and proper test - integrity and reputation, absence of convictions and restraint orders, competence including financial solvency and net worth, absence of wilful defaulter status.</li>
                        <li><strong>RBI Annex-1 (NBFC Master Direction):</strong> Originally RBI June 2004 directive under Section 10A Banking Regulation Act 1949, extended to NBFC directors. Prescribes Annexure II declaration and deed of covenant in Annex-3.</li>
                        <li><strong>IRDAI Master Circular on Corporate Governance for Insurers 2024:</strong> Dated 22 May 2024. The Sabka Bima Sabki Raksha (Amendment of Insurance Laws) Act 2025 (Presidential assent 20 December 2025) tightened the framework from 2026.</li>
                        <li><strong>KMP (Key Managerial Personnel):</strong> Managing Director, Whole-Time Director, CEO, CFO, Company Secretary and other roles notified by the regulator.</li>
                        <li><strong>Nomination Committee:</strong> Board-level committee under Section 178 of the Companies Act 2013. Under RBI Annex-1 the committee evaluates the fit and proper declaration.</li>
                        <li><strong>UDIN:</strong> 18-digit Unique Document Identification Number, mandatory on all CA certificates since 1 July 2019. Nomination committees and regulators verify it live on the ICAI portal.</li>
