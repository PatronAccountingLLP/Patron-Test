                <div class="content-text what-is-definition">
                    
                    <p><strong>Definition:</strong> A net worth certificate for students is a Chartered Accountant-signed financial document certifying the total assets minus total liabilities of the student and the sponsoring parent or guardian as on a specific date, presented in both INR and the destination country currency.</p>
                    <p>The certificate is issued on CA letterhead with signature, ICAI membership number, firm registration number and UDIN, and is recognised by foreign embassies and university financial certification offices. It covers immovable assets (property, land), movable assets (bank balances, fixed deposits, mutual funds, demat holdings, gold, vehicles) and verified liabilities (home loan, education loan, personal loan).</p>
                    <p>Universities use it to satisfy their financial documentation requirements (I-20 in the USA, CAS in the UK, Letter of Acceptance in Canada, eCoE in Australia). Visa offices use it as supporting evidence alongside bank statements, GICs, blocked accounts or education loan sanction letters. UKVI caseworkers and US Embassy officers verify the 18-digit UDIN on the ICAI portal at <a href="https://udin.icai.org" target="_blank" rel="noopener">udin.icai.org</a> in real time.</p>

