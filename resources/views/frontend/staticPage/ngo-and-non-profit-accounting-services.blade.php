            <div class="content-text">
                
                <p>What the law protects in a not-for-profit is the donor's and the exchequer's shared interest that money given for a purpose was actually spent on it, and Sections 11 and 12 of the Income-tax Act are where that protection is written. Exemption is not automatic; it is earned by applying income to the stated objects and evidencing it.</p>
