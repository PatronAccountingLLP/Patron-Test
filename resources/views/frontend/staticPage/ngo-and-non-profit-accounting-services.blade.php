<section class="content-section" id="what-section">
    <div class="content-container">
        <div class="two-column">
            <div class="column-content">
                <h2 class="section-title">What Are NGO Accounting Services?</h2>
                <div class="content-text what-is-definition">
                    
                    <p>A grant is the unit this work is organised around, with every rupee tagged, from the day it is received, to the fund that paid for it. NGO accounting services keep restricted, unrestricted and corpus balances apart across the whole year. Coding on that basis means a donor can be shown exactly how a contribution was applied, without a reconstruction at audit time.</p>
<p>Fund-wise ledgers are maintained, donor and utilisation schedules prepared, and registrations under Section 12A and 80G kept current, so exemption is not lost. Corpus and restricted gifts are held to the donor&#x27;s condition rather than released to general use. Where foreign contribution is received, it operates the designated bank account the law requires and files the related returns from it. Trust accounting services of this kind also watch the rule that most income be applied to the objects each year. This engagement stays with the books and the fund reports; commercial income-tax filings sit elsewhere.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for NGO and Non-Profit Accounting:</strong></p>
                    <div class="key-terms"><ul class="al-list"><li><span class="al-t"><a href="/glossary/fcra-designated-bank-account">FCRA Designated Bank Account</a></span><span class="al-d">The single account through which all foreign contributions must first be received.</span></li><li><span class="al-t"><a href="/glossary/section-12a-80g-annual-upkeep">Section 12A / 80G Annual Upkeep</a></span><span class="al-d">Ongoing compliance that keeps a charity&#x27;s tax exemption and donor deductions valid.</span></li><li><span class="al-t"><a href="/glossary/restricted-corpus-donations">Restricted Corpus Donations</a></span><span class="al-d">Gifts a donor requires be held permanently rather than spent.</span></li><li><span class="al-t"><a href="/glossary/fund-based-accounting">Fund-Based Accounting</a></span><span class="al-d">Keeping separate books for each fund so its money is tracked apart.</span></li><li><span class="al-t"><a href="/glossary/section-10-23c-exemption-rules">Section 10(23C) Exemption Rules</a></span><span class="al-d">Conditions under which certain funds and institutions enjoy income-tax exemption.</span></li><li><span class="al-t"><a href="/glossary/85-income-application-rule">85% Income Application Rule</a></span><span class="al-d">Requirement that a charity apply most of its income to its objects yearly.</span></li></ul></div>

