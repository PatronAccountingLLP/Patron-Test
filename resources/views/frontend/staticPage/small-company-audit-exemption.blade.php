                <h2 class="section-title">What Is the Small Company Audit Exemption Really?</h2>
                <div class="content-text what-is-definition">
                    
                    <p>The phrase "small company audit exemption" is a misnomer - no Indian statute exempts small companies from statutory audit. What Section 2(85) of the Companies Act, 2013 actually unlocks is a package of three reporting and procedural exemptions: CARO 2020 reporting, ICFR reporting under Section 143(3)(i), and the mandatory auditor rotation rule under Section 139(2).</p>
                <p>A small company is defined under Section 2(85) of the Companies Act, 2013, read with Rule 2(1)(t) of the Companies (Specification of Definition Details) Rules, 2014, as substituted by <a href="https://www.mca.gov.in/bin/dms/getdocument?mds=CViJxHWXV1QAr0wxTM5lCA%3D%3D" target="_blank" rel="noopener">G.S.R. 880(E) dated 1 December 2025</a>. To qualify, a private company must satisfy BOTH conditions: paid-up share capital must not exceed Rs 10 crore AND turnover as per the profit and loss account of the immediately preceding financial year must not exceed Rs 100 crore. Both tests are conjunctive, not alternative.</p>
                <p>Critically, four types of companies cannot qualify as small even within these thresholds: public companies, holding or subsidiary companies, Section 8 (non-profit) companies, and companies governed by any special Act. This proviso to Section 2(85) is non-negotiable.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Small Company Audit Exemption:</strong></p>
                    <ul style="margin-top:12px;">
                    <li><strong>Statutory audit:</strong> the mandatory annual audit of financial statements by an independent CA under Section 139 of the Companies Act, 2013. Applies to every company in India regardless of size.</li>
                    <li><strong>Tax audit:</strong> a separate audit under Section 44AB of the Income Tax Act, 1961, triggered by turnover thresholds (Rs 1 crore or Rs 10 crore for digital businesses). Has no connection with Section 2(85).</li>
                    <li><strong>CARO 2020 reporting:</strong> the Companies (Auditor's Report) Order, 2020 - 21 additional clauses the auditor must report on within the main audit report. Small companies are exempt under Para 1(2)(iv).</li>
                    <li><strong>ICFR reporting:</strong> the auditor's separate opinion on internal financial controls over financial reporting under Section 143(3)(i). Switches off for small companies via MCA Notification G.S.R. 583(E) dated 13 June 2017.</li>
                    <li><strong>Auditor rotation:</strong> the mandatory change of auditor every 5 years (individual) or 10 years (firm) under Section 139(2). Does not apply to small companies because Rule 5 of Companies (Audit and Auditors) Rules, 2014 limits scope to listed and prescribed classes.</li>
                </ul>

