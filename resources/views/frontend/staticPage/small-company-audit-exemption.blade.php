                </table>
                <p style="font-style:italic;font-size:14px;color:var(--text-muted);">Source: Companies (Specification of Definition Details) Amendment Rules, 2025 notified via G.S.R. 880(E) dated 1 December 2025, in force from the date of publication in the Official Gazette.</p>

                <h3 style="margin-top:32px;">The Four Exclusions (proviso to Section 2(85))</h3>
                <p>Even if both threshold tests pass, a company CANNOT be a small company if it is:</p>
                <ul>
                    <li>A public company</li>
                    <li>A holding company or a subsidiary company (irrespective of who the holding or subsidiary is)</li>
                    <li>A company registered under Section 8 of the Companies Act, 2013 (non-profit)</li>
                    <li>A company or body corporate governed by any special Act (banking companies, insurance companies, NBFCs governed by RBI Act etc.)</li>
                </ul>
                <p><strong>Worked example.</strong> A private company has paid-up capital of Rs 6 crore and turnover of Rs 85 crore for FY 2024-25. It is not a subsidiary, not a Section 8 company, not governed by any special Act. From FY 2025-26 onwards it qualifies as a small company. The audit fee should drop, CARO reporting falls away, the auditor does not need to be rotated when the five-year term ends, and the annual return moves from Form MGT-7 to the abridged Form MGT-7A. The statutory audit itself continues every year.</p>

