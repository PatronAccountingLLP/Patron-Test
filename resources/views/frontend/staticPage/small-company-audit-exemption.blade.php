                        <div class="faq-expanded__a"><p>Effective 1 December 2025, MCA Notification G.S.R. 880(E) revised the small company definition under Section 2(85). The new thresholds are paid-up share capital up to Rs 10 crore AND turnover up to Rs 100 crore in the immediately preceding financial year. Both tests must be met simultaneously. Crossing either limit removes small company status from the following financial year onwards.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-3">
                        <h3 class="faq-expanded__q">Is CARO 2020 applicable to a small company?</h3>
                        <div class="faq-expanded__a"><p>No. Para 1(2)(iv) of the Companies (Auditor's Report) Order, 2020 excludes small companies as defined in Section 2(85) from the scope of CARO 2020. The exemption is absolute and does not depend on borrowings, reserves or revenue. The auditor's report on a small company will simply state that CARO 2020 is not applicable - the 21 CARO clauses do not need to be reported on.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-4">
                        <h3 class="faq-expanded__q">Does ICFR reporting apply to a small company under Section 143(3)(i)?</h3>
                        <div class="faq-expanded__a"><p>No, subject to one condition. MCA Notification G.S.R. 583(E) dated 13 June 2017 exempts OPCs, small companies and qualifying private companies from the auditor's reporting requirement on internal financial controls under Section 143(3)(i). The condition: the company must NOT have defaulted in filing financial statements (Section 137) or the annual return (Section 92). One missed AOC-4 deadline disqualifies you from the ICFR exemption.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-5">
                        <h3 class="faq-expanded__q">Does auditor rotation under Section 139(2) apply to a small company?</h3>
                        <div class="faq-expanded__a"><p>No. Rule 5 of the Companies (Audit and Auditors) Rules, 2014 limits mandatory auditor rotation to listed companies, unlisted public companies with paid-up share capital of Rs 10 crore or more, private companies with paid-up share capital of Rs 50 crore or more, and companies with public borrowings of Rs 50 crore or more. Small companies fall outside this perimeter and can retain the same auditor indefinitely subject to AGM ratification.</p></div>
