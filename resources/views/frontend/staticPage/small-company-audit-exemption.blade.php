            <h2 class="section-title">Who Qualifies as a Small Company Under Section 2(85)</h2>
            <div class="content-text">
                
                <p>A private company qualifies as a small company for any financial year if, on the immediately preceding year-end, BOTH of these conditions are satisfied AND none of the four exclusions apply.</p>
                <table class="table-comparison" style="margin:24px 0;">
                    <thead><tr><th>Test</th><th>Threshold (pre-1 Dec 2025)</th><th>Threshold (from 1 Dec 2025)</th></tr></thead>
                    <tbody>
                        <tr><td>Paid-up share capital</td><td>Up to Rs 4 crore</td><td><strong>Up to Rs 10 crore</strong></td></tr>
                        <tr><td>Turnover (preceding FY)</td><td>Up to Rs 40 crore</td><td><strong>Up to Rs 100 crore</strong></td></tr>
