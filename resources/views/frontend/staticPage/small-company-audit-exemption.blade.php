                        <h3 class="faq-expanded__q">Is statutory audit mandatory for a small company under the Companies Act, 2013?</h3>
                        <div class="faq-expanded__a"><p>Yes. Statutory audit under Section 139 is mandatory for every company registered under the Companies Act, 2013, irrespective of size, turnover or profit. There is no minimum turnover threshold for audit exemption. The Section 2(85) small company status removes CARO reporting, ICFR reporting and auditor rotation - it does NOT remove the audit itself. The auditor still examines and signs the financial statements every year.</p></div>
