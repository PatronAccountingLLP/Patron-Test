<h3 style="margin-top:32px;">Section 249 Restrictions: The Three-Month Look-Back</h3>
<p>Under Section 249, a company is INELIGIBLE for strike off under Section 248(2) where ANY of the following occurred in the 3 months immediately preceding STK-2 filing:</p>
<ul>
    <li>Company name change (Form INC-24)</li>
    <li>Registered office shifted from one state to another (Form INC-23)</li>
    <li>Property or rights of the company disposed of, other than in ordinary course of business</li>
    <li>Activity carried on outside the objects clause of the Memorandum of Association</li>
    <li>Application made to Tribunal for compromise / arrangement under Section 230</li>
    <li>Company under liquidation proceedings</li>
</ul>
