                        <h3 class="faq-expanded__q">What is the procedure to strike off a private limited company?</h3>
                        <div class="faq-expanded__a"><p>The voluntary strike off procedure under Section 248(2) of the Companies Act, 2013 has nine steps: (1) eligibility screening including Section 249 three-month look-back, (2) clearing any pending AOC-4 / MGT-7 / ITR filings, (3) board resolution authorising strike off, (4) EGM and special resolution with 75% paid-up capital consent, (5) MGT-14 filing within 30 days, (6) STK-3 / STK-4 / STK-8 document preparation, (7) STK-2 filing to C-PACE with Rs 10,000 fee, (8) STK-6 public notice and 30-day objection window, (9) STK-7 final dissolution notice in Official Gazette. Total duration 3-6 months via C-PACE since May 2023.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-2">
                        <h3 class="faq-expanded__q">When is a company eligible for strike off under Section 248(2)?</h3>
                        <div class="faq-expanded__a"><p>A company is eligible if it has not commenced business within 1 year of incorporation OR has not carried on business for the 2 immediately preceding financial years AND has not applied for dormant status under Section 455. All liabilities must be settled to nil, all annual MCA filings (AOC-4, MGT-7) and Income Tax returns must be up-to-date, and no Section 249 restrictions can have triggered in the 3 months preceding STK-2 filing. Special resolution by 75% paid-up capital consent at EGM is mandatory.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-3">
                        <h3 class="faq-expanded__q">When should a company NOT apply for strike off?</h3>
                        <div class="faq-expanded__a"><p>Avoid strike off when: (a) operations are planned to resume within 1-2 years - use dormant company status under Section 455 instead; (b) pending tax assessment, scrutiny, or appeal - struck-off company cannot defend; (c) pending litigation; (d) outstanding bank loan or registered charges; (e) unpaid statutory dues (TDS, EPF, ESI); (f) company has assets to distribute - assets vest with Central Government under Section 250(2) if struck off; (g) Section 249 restrictions in the 3-month look-back triggered.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-4">
                        <h3 class="faq-expanded__q">What documents are required for STK-2 filing?</h3>
                        <div class="faq-expanded__a"><p>Mandatory documents include: board resolution authorising strike off; special resolution at EGM with 75% paid-up capital consent; MGT-14 SRN; STK-3 indemnity bond from each director on Rs 100 stamp paper, notarized; STK-4 affidavit from each director on Rs 50-100 stamp paper, notarized; STK-8 statement of accounts CA-certified, dated max 30 days before STK-2 filing, showing NIL assets and NIL liabilities; bank account closure letter; PAN and Aadhaar of each director; latest AOC-4 and MGT-7 SRNs; Income Tax Returns for the last 2 assessment years; regulatory NOCs where applicable.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-5">
                        <h3 class="faq-expanded__q">How long does the strike off process take?</h3>
                        <div class="faq-expanded__a"><p>Since May 2023 when the Centre for Processing Accelerated Corporate Exit (C-PACE) became the sole processing authority, strike-off applications complete in 3-6 months end-to-end - down from 6-12 months under the older system. Typical breakdown: 1-4 weeks for preparation and resolutions, 1-2 weeks for STK-2 filing to C-PACE, 4-6 weeks for C-PACE acknowledgement and STK-6 publication, 30 days for objection window, 4-8 weeks for STK-7 Gazette dissolution publication. Companies with pending filings to clear may take longer.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-6">
                        <h3 class="faq-expanded__q">What is the difference between suo-moto strike off and voluntary strike off?</h3>
                        <div class="faq-expanded__a"><p>Suo-moto strike off under Section 248(1) is initiated by the Registrar of Companies based on reasonable cause - typically failure to commence business within 1 year or non-operation for 2 financial years. The Registrar issues notice in Form STK-1, and the company has 30 days to respond. Voluntary strike off under Section 248(2) is initiated by the company itself with board approval and 75% special resolution. Suo-moto strike off can trigger director disqualification under Section 164(2) for 5 years where non-filing of MGT-7 was the cause; voluntary strike off does not.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-7">
                        <h3 class="faq-expanded__q">Can a struck-off company be restored?</h3>
                        <div class="faq-expanded__a"><p>Yes - under Section 252 of the Companies Act, 2013, any person aggrieved by the strike-off order can apply to the National Company Law Tribunal (NCLT) for restoration within 3 years from the date of the strike-off order. The applicant must demonstrate either that the company was actually operational at the time of strike off, or that restoration is just and equitable. Restoration costs typically Rs 50,000 to Rs 2 lakh including NCLT fees, arrears of annual filings, penalty fees, and legal representation. Patron handles restoration as a separate engagement.</p></div>
                    </div>
                    <div class="faq-expanded__item" id="faq-8">
                        <h3 class="faq-expanded__q">What happens to director liability after strike off?</h3>
                        <div class="faq-expanded__a"><p>Under Section 250 of the Companies Act, 2013, the company is dissolved upon strike off but director and officer personal liability for the company's past acts and obligations continues. Specifically, directors remain liable for unpaid taxes, statutory dues, contractual obligations, or any frauds / misrepresentations under Sections 447 / 448 - for 8 years after dissolution. This is why a clean STK-8 statement of accounts and proper STK-3 / STK-4 documentation matters: it forms the directors' defence record for any subsequent scrutiny. Patron archives the complete filing pack for the full 8-year liability window.</p></div>
