<p>Voluntary strike off under Section 248(2) is the right route when the company meets ALL of the following:</p>
<ul>
    <li>Genuine wind-down decision - no plans to resume operations</li>
    <li>No operating activities for the last 2 financial years</li>
    <li>All liabilities settled to nil - creditors, employees, statutory dues, tax</li>
    <li>Bank account closed (or empty and ready to close)</li>
    <li>No pending litigation against the company or by the company</li>
    <li>No pending tax or regulatory matters (Income Tax, GST, ESI, PF, Customs)</li>
    <li>All annual MCA filings (AOC-4, MGT-7) up-to-date - mandatory per Rule 4 second proviso</li>
    <li>All Income Tax returns filed - C-PACE checks this</li>
    <li>Special resolution passed at EGM with 75% paid-up capital consent</li>
</ul>

