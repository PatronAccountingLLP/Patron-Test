                <h2 class="section-title">What Is Strike Off Under Section 248?</h2>
                <div class="content-text what-is-definition">
                    
                    <p>Strike off is the statutory process by which the Registrar of Companies removes a company's name from the official Register of Companies, formally dissolving the company under Sections 248 to 252 of the Companies Act, 2013 read with the Companies (Removal of Names of Companies from the Register of Companies) Rules, 2016. Once struck off, the company ceases to exist as a legal entity - it cannot carry on business, sign contracts, or operate bank accounts. <strong>Director personal liabilities for past dues and obligations remain.</strong></p>
<p>Two routes exist. Under Section 248(1), the Registrar initiates strike off <strong>suo-moto</strong> where a company has failed to commence business within 1 year of incorporation, or has not carried on business for 2 immediately preceding financial years and has not applied for dormant status under Section 455. Under Section 248(2), the <strong>company initiates voluntary strike off</strong> by special resolution (75% paid-up capital consent) - typically the preferred path for genuine wind-downs because it preserves the directors' record. This service page covers the voluntary route under Section 248(2).</p>

