<h3 style="margin-top:24px;">Key Sections and Authorities</h3>
<ul>
    <li>Section 248(1) - suo-moto strike off by Registrar for failure to commence within 1 year or non-operation for 2 FYs</li>
    <li>Section 248(2) - voluntary strike off by company via special resolution (75% paid-up capital consent)</li>
    <li>Section 248(5) - dissolution effective from STK-7 Gazette publication date</li>
    <li>Section 249 - restrictions on Section 248(2) applications (3-month look-back)</li>
    <li>Section 250 - effect of strike off: company dissolved; assets vest with Central Government under Section 250(2); director liability for past acts remains</li>
    <li>Section 252 - restoration by NCLT within 3 years from strike-off date</li>
    <li>Section 164(2) - director disqualification for 5 years on suo-moto strike-off triggered by 3 consecutive years of non-filing MGT-7</li>
    <li><strong>Centre for Processing Accelerated Corporate Exit (C-PACE)</strong> - operational since May 2023 as sole authority processing all STK-2 applications</li>
</ul>

<h3 style="margin-top:24px;">Penalty and Liability Schedule</h3>
<ul>
    <li>STK-2 government fee: Rs 10,000 (paid at filing under Rule 4)</li>
    <li>Stamp duty on STK-3 indemnity bond: Rs 100 per director (state variations apply)</li>
    <li>Stamp duty on STK-4 affidavit: Rs 50 to Rs 100 per director (state variations apply)</li>
    <li>Director disqualification under Section 164(2): 5 years where suo-moto strike off triggered by 3 consecutive years of non-filing</li>
    <li>Director personal liability under Section 250: continues for 8 years for past acts and obligations even after company dissolution</li>
    <li>Section 449 - punishment for false evidence in STK-4 affidavit: imprisonment up to 7 years plus fine</li>
