                <div class="content-text what-is-definition">
                    
                    <p>The MSME turnover certificate is a document prepared and certified by a practising Chartered Accountant that states an enterprise's annual turnover for a financial year, drawn from its audited financials and GST records, for the purpose of MSME classification under the Udyam framework.</p>
                    <p>Since the 2020 reform, MSME classification uses a composite criterion of investment and turnover. The Udyam portal automatically classifies an enterprise each year from its GST and income tax data, but there are situations, manual filings, a year not yet reflected, a discrepancy to resolve, or a benefit that needs substantiating, where an independent CA-certified turnover figure is required. That is exactly what this certificate provides.</p>

