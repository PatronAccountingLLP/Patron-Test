<section class="content-section">
    <div class="content-container">
        <div class="text-content">
            <h2 class="section-title">How MSME Classification and Reclassification Work</h2>
            <div class="content-text">
                
                <p>MSME classification rests on a composite of investment in plant, machinery, or equipment and annual turnover. Since the 2020 reform, turnover sits alongside investment, and both must be within a category's limits; if either is exceeded, the enterprise moves to the next category. The Udyam portal recalculates the category every financial year, drawing investment from income tax data and turnover from GST returns, both linked to the enterprise's PAN, so classification is dynamic rather than fixed at registration.</p>
                <p>This is why turnover matters on a recurring basis. As a business grows, its turnover can move it from micro to small or small to medium, changing the benefits and obligations that apply, including priority lending, tender preference for micro and small enterprises, and protections such as the 45-day payment rule. Where the turnover needs to be evidenced independently, for a scheme, a tender, a lender, or to resolve a classification question, our CA-certified certificate establishes the figure, reconciled to your audited books and GST returns.</p>
                <p>The limits were revised by S.O. 1364(E), effective 1 April 2025, with existing Udyam registrations auto-benefiting without re-registration; for the classification framework and limits, refer to the <a href="https://udyamregistration.gov.in" target="_blank" rel="noopener">Udyam Registration portal (udyamregistration.gov.in)</a>. Udyam auto-classifies an enterprise each year from GST returns (turnover) and ITR (investment) linked to PAN; the CA turnover certificate is independent turnover evidence used where classification, reclassification, or a downstream benefit needs the figure substantiated.</p>

