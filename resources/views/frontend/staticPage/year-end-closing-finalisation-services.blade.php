<section class="content-section" id="what-section">
    <div class="content-container">
        <div class="two-column">
            <div class="column-content">
                <h2 class="section-title">What Is Year-End Closing and Finalisation?</h2>
                <div class="content-text what-is-definition">
                    
                    <p>A financial year is the unit this work is built around: twelve months of entries that must be brought to a single, defensible set of statements before the books can close. Finalisation of accounts is the exercise of taking a year&#x27;s ledger from its rough closing position to statements that withstand scrutiny, every balance carrying a schedule that explains it.</p>
<p>It is the bridge between routine bookkeeping and the signed financials. Balances are examined rather than accepted, sub-ledgers are agreed to the general ledger, and adjustments for accruals, prepayments and depreciation are brought in so each figure sits in the right period. Balance sheet finalisation is the visible output, though the working papers beneath it carry the weight. A ledger that merely balances is not the same as one that is right, and this is where the two are made to meet. Done properly, finalisation of accounts settles the differences a year accumulates instead of rolling them into the next year&#x27;s opening balances.</p>

                </div>
                <div class="highlight-box" style="margin-top:20px;">
                    <p><strong>Key Terms for Year End Closing Finalisation:</strong></p>
                    <div class="key-terms"><ul class="al-list"><li><span class="al-t"><a href="/glossary/closing-journal-entries">Closing Journal Entries</a></span><span class="al-d">Final entries that transfer income and expense balances to close the accounting year.</span></li><li><span class="al-t"><a href="/glossary/accrued-liabilities">Accrued Liabilities</a></span><span class="al-d">Expenses incurred but not yet billed, recognised so the period carries its costs.</span></li><li><span class="al-t"><a href="/glossary/prepaid-expense-amortization">Prepaid Expense Amortization</a></span><span class="al-d">Spreading a payment made in advance across the periods that actually benefit from it.</span></li><li><span class="al-t"><a href="/glossary/accrual-accounting">Accrual Accounting</a></span><span class="al-d">Recording income and costs when earned or incurred, not when cash moves.</span></li><li><span class="al-t"><a href="/glossary/depreciation">Depreciation</a></span><span class="al-d">Charging the cost of an asset across the years it is used.</span></li><li><span class="al-t"><a href="/glossary/trial-balance">Trial Balance</a></span><span class="al-d">A list of every ledger balance, proving debits equal credits before finalising.</span></li><li><span class="al-t"><a href="/glossary/journal-entry">Journal Entry</a></span><span class="al-d">A dated record posting equal debits and credits to named ledger accounts.</span></li><li><span class="al-t"><a href="/glossary/prior-period-adjustments">Prior-Period Adjustments</a></span><span class="al-d">Corrections to earlier years&#x27; figures, made in the current accounts with disclosure.</span></li></ul></div>

