            <div class="content-text">
                
                <p>What year-end closing protects is the single true-and-fair set of accounts that everyone downstream relies on - the lender, the auditor, the ROC and the assessing officer - and Section 129 of the Companies Act is where the demand for those statements is written. Everything the finalisation produces has to survive all four readers.</p>
<p>So the close is governed by more than good practice. The books are cut off, adjusted and drawn into the Schedule III format, then adopted and filed on prescribed forms. That is why <a href="/glossary/closing-journal-entries">Closing Journal Entries</a>, <a href="/glossary/accrued-liabilities">Accrued Liabilities</a> and <a href="/glossary/prepaid-expense-amortization">Prepaid Expense Amortization</a> are statutory acts of measurement, not tidying. Finalisation of accounts brings the year to a defensible close against the provisions below.</p>
<ul class="al-list"><li><span class="al-t"><strong>Section 129 with Schedule III, Companies Act 2013</strong></span><span class="al-d">The year-end balance sheet and profit and loss are drawn in the Schedule III format and adopted at the AGM within the Companies Act timeline.</span></li><li><span class="al-t"><strong>Sections 137 and 92, Companies Act 2013</strong></span><span class="al-d">The adopted accounts are filed with the ROC in AOC-4, and the annual return in MGT-7, once the year is closed.</span></li><li><span class="al-t"><strong>Section 44AB, Income-tax Act 1961</strong></span><span class="al-d">Where the turnover thresholds are met, the finalised accounts feed the tax audit report in Form 3CA/3CB with 3CD.</span></li><li><span class="al-t"><strong>Companies (Accounting Standards) Rules 2021 and Companies (Indian Accounting Standards) Rules 2015</strong></span><span class="al-d">The closing entries are measured under the applicable AS or Ind AS, on the <a href="/glossary/accrual-accounting">Accrual Accounting</a> basis the standards require.</span></li><li><span class="al-t"><strong>Section 128, Companies Act 2013</strong></span><span class="al-d">The books being closed must already be true and fair, and are retained for eight years after the year ends. A backlog is cleared first via <a href="/backlog-bookkeeping-catch-up-services">catch-up bookkeeping</a>.</span></li></ul>
